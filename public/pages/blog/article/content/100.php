<section id="pros">
  <h2>Плюсы текстильной обуви</h2>
  <p>Почему миллионы людей выбирают текстильную обувь:</p>
  <ul>
    <li><strong>Лёгкость.</strong> Текстильная обувь в 2–3 раза легче кожаной. Это снижает нагрузку на ноги при ходьбе и особенно заметно в кроссовках для бега и фитнеса.</li>
    <li><strong>Воздухопроницаемость.</strong> Ткань и сетка пропускают воздух значительно лучше кожи. Ноги меньше потеют — это главное преимущество в тёплую погоду.</li>
    <li><strong>Комфорт.</strong> Текстиль мягче кожи, не требует разноски, не натирает. Многие модели имеют эластичные вставки, которые подстраиваются под форму стопы.</li>
    <li><strong>Цена.</strong> Текстильная обувь в среднем в 1.5–2 раза дешевле кожаной при сопоставимом уровне комфорта.</li>
    <li><strong>Разнообразие дизайнов.</strong> На ткани легче печатать принты, делать вышивку и комбинировать цвета. Текстильная обувь часто ярче и оригинальнее кожаной.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Лучший выбор для лета</h4>
    <p>Для жаркого климата и активного отдыха текстильная обувь — оптимальный выбор. Она легче, дышит и не так нагревается на солнце, как кожа или синтетика.</p>
  </div>
</section>

<section id="cons">
  <h2>Минусы текстильной обуви</h2>
  <p>У текстильной обуви есть и серьёзные недостатки:</p>
  <div data-type="compare">
    <article data-variant="good">
      <h4>Достоинства</h4>
      <ul>
        <li>Лёгкая и комфортная</li>
        <li>Хорошо вентилируется</li>
        <li>Доступная цена</li>
        <li>Мягкая, не натирает</li>
        <li>Большой выбор дизайнов</li>
      </ul>
    </article>
    <article data-variant="bad">
      <h4>Недостатки</h4>
      <ul>
        <li>Быстро пачкается</li>
        <li>Впитывает влагу как губка</li>
        <li>Сложнее чистится (пятна въедаются)</li>
        <li>Меньший срок службы (1–3 года)</li>
        <li>Не держит форму при намокании</li>
      </ul>
    </article>
  </div>
  <p>Главная проблема текстильной обуви — её «маркость». Белые текстильные кроссовки пачкаются с первой прогулки, а глубокую чистку выдерживают не все модели. Кроме того, текстиль впитывает запахи и быстрее изнашивается на сгибах.</p>
</section>

<section id="materials">
  <h2>Виды текстильных материалов</h2>
  <p>Не весь текстиль одинаков. Вот основные типы, используемые в обуви:</p>
  <ul>
    <li><strong>Хлопковый канвас.</strong> Классика — из него шьют кеды Converse и Vans. Прочный, дышащий, но медленно сохнет и боится воды. Требует водоотталкивающей пропитки.</li>
    <li><strong>Полиэстер (сетка).</strong> Используется в спортивных кроссовках (Nike Flyknit, Adidas Primeknit). Лёгкий, быстро сохнет, эластичный. Но менее прочный, чем канвас.</li>
    <li><strong>Нейлон (Cordura).</strong> Очень прочный синтетический материал. Используется в трекинговой обуви и военных ботинках. Износостойкий, водоотталкивающий, но менее дышащий.</li>
    <li><strong>Смесовые ткани.</strong> Комбинация хлопка с синтетикой (полиэстер, эластан). Оптимальный вариант: дышит, как хлопок, но эластичнее и прочнее.</li>
    <li><strong>Джинсовая ткань.</strong> Прочная, стильно выглядит, но жёсткая и долго сохнет. Используется реже.</li>
  </ul>
</section>

<section id="care">
  <h2>Как ухаживать за текстильной обувью</h2>
  <p>Текстильная обувь пачкается быстро, но и чистить её проще, чем кожу или замшу. Вот основные правила:</p>
  <ol>
    <li><strong>Не откладывайте чистку.</strong> Свежие пятна с текстиля удаляются легче. Засохшая грязь въедается в волокна.</li>
    <li><strong>Используйте пену для чистки.</strong> Специальная пена для текстиля наносится на загрязнение, вспенивается щёткой и удаляется салфеткой. Без воды!</li>
    <li><strong>Можете стирать шнурки отдельно.</strong> Шнурки из текстиля стираются легко, но сушить их лучше при комнатной температуре.</li>
    <li><strong>Сушите при комнатной температуре.</strong> Текстиль боится батарей — ткань садится и деформируется. Набейте кроссовки бумагой для сохранения формы.</li>
    <li><strong>Водоотталкивающий спрей обязателен.</strong> Без пропитки текстиль впитывает грязь и воду мгновенно. Обрабатывайте раз в 2 недели.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>О машинной стирке</h4>
    <p>Текстильные кроссовки можно стирать в машинке, но с осторожностью. Используйте мешок для стирки, деликатный режим (30 °C), отключите отжим. Никогда не стирайте в машинке обувь с мембраной, кожаными вставками или сложной конструкцией подошвы.</p>
  </div>
</section>

