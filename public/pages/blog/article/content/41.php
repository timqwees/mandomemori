<section id="why">
  <h2>Зачем менять стельки</h2>
  <p>Многие носят обувь годами с заводскими стельками, даже не задумываясь об их замене. А зря — изношенные стельки создают сразу несколько проблем:</p>
  <ul>
    <li><strong>Потеря амортизации.</strong> Спрессованная стелька перестаёт смягчать удар при ходьбе. Нагрузка на суставы и позвоночник возрастает на 20–30%.</li>
    <li><strong>Неприятный запах.</strong> Стелька впитывает пот, бактерии и запахи. Через 6–8 месяцев носки даже при регулярном уходе стелька становится источником запаха.</li>
    <li><strong>Скольжение ноги.</strong> Изнашиваясь, стелька теряет сцепление с носком. Нога начинает скользить вперёд, создавая дискомфорт и натирая пальцы.</li>
    <li><strong>Потеря тепла.</strong> У зимних стелек износ приводит к потере теплоизолирующих свойств. Войлок или шерсть спрессовываются и перестают удерживать тепло.</li>
    <li><strong>Ухудшение посадки.</strong> Если обувь стала свободнее — возможно, стелька потеряла толщину и объём. Замена может вернуть комфортную посадку.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Как часто менять стельки</h4>
    <p>Рекомендуемая периодичность: для ежедневной носки — каждые 3–4 месяца, для сезонной — раз в полгода, для спортивной обуви — каждые 2–3 месяца интенсивных тренировок.</p>
  </div>
</section>

<section id="types">
  <h2>Виды стелек по материалу</h2>
  <p>Материал стельки определяет её свойства: амортизацию, воздухопроницаемость, теплоизоляцию и долговечность.</p>
  <ul>
    <li><strong>Кожаные стельки.</strong> Натуральная кожа — лучший выбор для классической обуви. Кожа дышит, впитывает влагу и принимает форму стопы. Минус — высокая цена и необходимость ухода.</li>
    <li><strong>ЭВА (этиленвинилацетат).</strong> Лёгкий вспененный материал с отличной амортизацией. Используется в спортивных стельках. Быстро теряет упругость.</li>
    <li><strong>Полиуретан.</strong> Прочный, упругий, долго держит форму. Подходит для повседневной обуви. Не пропускает воздух.</li>
    <li><strong>Войлок и шерсть.</strong> Тёплые, натуральные, хорошо впитывают влагу. Идеальны для зимней обуви. Минус — быстро спрессовываются.</li>
    <li><strong>Гелевые стельки.</strong> Содержат гелевые вставки в зонах повышенной нагрузки. Максимальная амортизация, но не дышат.</li>
    <li><strong>Латекс.</strong> Натуральный материал с антибактериальными свойствами. Упругий, долговечный, подходит для аллергиков.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Комбинированные стельки — лучший выбор для большинства случаев. Например, кожа + ЭВА: кожа впитывает влагу, а ЭВА обеспечивает амортизацию. Такие стельки служат дольше и комфортнее однослойных.</p>
  </div>
</section>

