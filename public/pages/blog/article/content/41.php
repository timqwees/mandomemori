<section id="size">
  <h2>Как подобрать размер и форму</h2>
  <p>Правильный размер стельки критически важен для комфорта. Слишком большая стелька будет сминаться, слишком маленькая — не обеспечит полную поддержку.</p>
  <h3>Как измерить правильно</h3>
  <ol>
    <li>Достаньте старую стельку из обуви — это идеальный шаблон для подбора.</li>
    <li>Измерьте длину и ширину стельки.</li>
    <li>Новая стелька должна совпадать по длине или быть на 1–2 мм меньше (для кожаных стелек допускается больше — они подрезаются под размер).</li>
    <li>Учитывайте полноту: если у вас широкая стопа, выбирайте анатомические стельки с углублением под пятку.</li>
    <li>Проверьте высоту свода: ортопедические стельки имеют поддержку продольного и поперечного свода.</li>
  </ol>
  <h3>Подрезка стелек</h3>
  <p>Большинство универсальных стелек имеют разметку для подрезки до нужного размера. Используйте острые ножницы и обводите стельку карандашом по контуру старой. Важно: не подрезайте анатомические элементы, только внешний контур.</p>
  <div data-type="box" data-color="dark">
    <h4>Важно</h4>
    <p>Не ставьте толстые стельки в обувь, которая и так сидит плотно. Подъём стопы увеличится, и вы будете испытывать дискомфорт при ходьбе. Максимальная толщина стельки для стандартной обуви — 5 мм.</p>
  </div>
</section>

<section id="ortho">
  <h2>Ортопедические стельки — нужны ли они вам</h2>
  <p>Ортопедические стельки — не просто модный аксессуар, а медицинское изделие. Они показаны при:</p>
  <ul>
    <li>Плоскостопии (продольном или поперечном).</li>
    <li>Болях в стопах, коленях, спине при ходьбе.</li>
    <li>Пяточной шпоре.</li>
    <li>Разной длине ног.</li>
    <li>Диабетической стопе.</li>
  </ul>
  <p>Ортопедические стельки делятся на три категории:</p>
  <ul>
    <li><strong>Профилактические.</strong> Мягкие, с лёгкой поддержкой свода. Подходят для здоровых ног при длительном стоянии или ходьбе.</li>
    <li><strong>Лечебные.</strong> Жёсткие, с выраженным супинатором. Назначаются врачом-ортопедом после диагностики.</li>
    <li><strong>Индивидуальные.</strong> Изготавливаются по слепку стопы на заказ. Обеспечивают максимально точную поддержку.</li>
