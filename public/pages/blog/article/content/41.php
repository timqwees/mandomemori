<section id="care">
  <h2>Уход за стельками</h2>
  <p>Чтобы стельки служили дольше и сохраняли гигиеничность, соблюдайте простые правила:</p>
  <ul>
    <li><strong>Извлекайте стельки после каждой носки.</strong> Обувь и стельки должны проветриваться отдельно.</li>
    <li><strong>Сушите при комнатной температуре.</strong> Не на батарее и не феном — высокая температура деформирует материалы.</li>
    <li><strong>Чистите мыльным раствором.</strong> Раз в месяц протирайте стельки влажной губкой с мылом. Не замачивайте.</li>
    <li><strong>Используйте антибактериальный спрей.</strong> Обработка раз в неделю предотвращает появление бактерий и грибка.</li>
    <li><strong>Меняйте вовремя.</strong> Даже при идеальном уходе стельки нужно менять каждые 4–6 месяцев.</li>
