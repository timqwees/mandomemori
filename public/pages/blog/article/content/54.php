<section id="replacement">
  <h2>Полная замена подошвы кроссовок</h2>
  <p>Полная замена подошвы — самый сложный и дорогой метод реставрации. Технически возможна не для всех кроссовок.</p>
  <h3>Когда замена возможна</h3>
  <ul>
    <li>Кроссовки с рантовым креплением подошвы (редко встречается в современной обуви).</li>
    <li>Обувь, где подошва приклеена к верху без сложной литой межподошвы.</li>
    <li>Дорогие кроссовки ручной сборки, где замена подошвы предусмотрена конструкцией.</li>
  </ul>
  <h3>Когда замена невозможна</h3>
  <ul>
    <li>Кроссовки с литой подошвой (95% современных моделей Nike, Adidas, Asics).</li>
    <li>Обувь с интегрированной межподошвой (пена + резина — единое целое).</li>
    <li>Кроссовки с карбоновой пластиной внутри подошвы.</li>
    <li>Беговые кроссовки со сложной амортизацией (Air, Boost, Gel).</li>
  </ul>
  <p>Для большинства современных кроссовок полная замена подошвы невозможна из-за конструктивных особенностей. В таких случаях оптимальное решение — профилактика.</p>
  <div data-type="box" data-color="dark">
    <h4>Сравнение</h4>
    <p>Замена подошвы на классических туфлях (15000–20000 ₽) — реальная услуга. Замена подошвы на кроссовках — чаще всего миф. Не верьте мастерам, которые обещают «полностью заменить подошву» на Nike Air Force 1 или Adidas Ultraboost.</p>
