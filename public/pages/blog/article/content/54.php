<section id="types">
  <h2>Типы подошв кроссовок и их износ</h2>
  <p>Подошвы кроссовок делятся на несколько типов, и каждый требует своего подхода к реставрации:</p>
  <ul>
    <li><strong>Резиновая подошва (цельная).</strong> Классический тип — резина распределена по всей нижней поверхности. Основной износ: стирание протектора на пятке и носке. Ремонт: наращивание резиной или установка профилактики.</li>
    <li><strong>Двусоставная подошва (межподошва + резина).</strong> Верхний слой — пенный материал (EVA, полиуретан), нижний — резина. Износ: резина стирается до пены. Ремонт: замена резинового слоя или установка накладки.</li>
    <li><strong>Литая подошва.</strong> Единая конструкция из полиуретана или ЭВА. Износ: стирание по всей площади, потеря формы. Ремонт: только полная замена, но это сложно.</li>
    <li><strong>Подошва с карбоновой пластиной.</strong> В гоночных кроссовках. Износ: резина стирается до карбона. Ремонт: возможен только резиновой накладкой, карбон не восстанавливается.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Оцените износ</h4>
    <p>Измерьте остаточную толщину подошвы в самой стёртой точке. Если осталось менее 2 мм — реставрация необходима. Если подошва протёрта до межподошвы (пены) — нужна срочная реставрация, иначе вода разрушит пену.</p>
  </div>
</section>

<section id="building">
  <h2>Наращивание изношенных участков подошвы</h2>
  <p>Наращивание — метод, при котором на изношенный участок подошвы наносится слой композитного материала, восстанавливающий первоначальную толщину.</p>
  <h3>Материалы для наращивания</h3>
  <ul>
    <li><strong>Жидкая резина.</strong> Полиуретановый состав, который застывает в эластичную резину. Наносится кистью или шпателем. Подходит для локальных участков.</li>
    <li><strong>Резиновая заплатка.</strong> Вырезается из листовой резины нужной толщины и приклеивается на полиуретановый клей. Более грубый, но надёжный метод.</li>
    <li><strong>Специальные ремонтные составы (Shoe Goo, Бока-Сапожок).</strong> Густые полимерные пасты, которые заполняют изношенные участки. Застывают в эластичную массу, похожую на резину.</li>
  </ul>
  <h3>Процесс наращивания</h3>
  <ol>
    <li>Зачистите и обезжирьте изношенный участок.</li>
    <li>Нанесите состав или приклейте заплатку.</li>
    <li>Сформируйте нужную форму и толщину.</li>
    <li>Оставьте на 24 часа для полной полимеризации.</li>
    <li>Отшлифуйте лишний материал, придайте форму протектора.</li>
  </ol>
  <p>Наращивание — временное решение (2–4 месяца). Для долгосрочного результата лучше установить профилактику.</p>
  <div data-type="box" data-color="green">
    <h4>Когда наращивание эффективно</h4>
    <p>Если износ локальный (только пятка или только носок) и не превышает 4 мм — наращивание даст хороший результат. При равномерном износе по всей подошве лучше выбрать установку профилактики.</p>
  </div>
</section>

<section id="patch">
  <h2>Установка заплаток на подошву кроссовок</h2>
  <p>Когда подошва протёрта до дыры, простая заплатка может спасти кроссовки. Заплатка ставится с внутренней или внешней стороны, в зависимости от типа повреждения.</p>
  <h3>Внутренняя заплатка</h3>
  <p>Если подошва протёрта, но не сквозная, заплатка ставится с внутренней стороны (под стельку). Она укрепляет истончённый участок, предотвращая его дальнейшее разрушение.</p>
  <h3>Внешняя заплатка (накладка)</h3>
  <p>При сквозной дыре заплатка ставится снаружи. Используется резина толщиной 3–5 мм, которая приклеивается и обрезается по контуру. После установки край шлифуется.</p>
  <div data-type="box" data-color="red">
    <h4>Недостаток заплаток</h4>
    <p>Заплатка — заметна, особенно на цветной подошве. Если эстетика важна, лучше рассмотреть профессиональную замену подошвы или установку профилактики.</p>
  </div>
</section>

<section id="soleprotection">
  <h2>Профилактика для кроссовок</h2>
  <p>Профилактика (накладка на всю подошву) — лучший способ реставрации и защиты подошвы кроссовок. Она полностью закрывает изношенную поверхность и защищает её от дальнейшего износа.</p>
  <h3>Особенности профилактики для кроссовок</h3>
  <ul>
    <li><strong>Толщина.</strong> Для кроссовок используется тонкий полиуретан 2–3 мм — он незаметен и не утяжеляет обувь.</li>
    <li><strong>Материал.</strong> Полиуретан или термопластичная резина. Для беговых кроссовок — лёгкий полиуретан, для повседневных — резина.</li>
    <li><strong>Цвет.</strong> Подбирается под цвет подошвы. Доступны чёрный, белый, серый, коричневый и прозрачный.</li>
    <li><strong>Протектор.</strong> Профилактика может быть гладкой или с мелким протектором для лучшего сцепления.</li>
  </ul>
  <h3>Процесс установки профилактики на кроссовки</h3>
  <ol>
    <li>Подошва зачищается от грязи и старой резины.</li>
    <li>Профилактика подбирается по размеру и обрезается с запасом.</li>
    <li>Материал разогревается для лучшего прилегания.</li>
    <li>Фиксируется на полиуретановый клей под прессом.</li>
    <li>Излишки обрезаются по контуру, край шлифуется.</li>
  </ol>
