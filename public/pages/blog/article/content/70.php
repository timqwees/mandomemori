<section id="phenomenon">
  <h2>Феномен лимитированных кроссовок</h2>
  <p>Почему люди платят 50 000–500 000 ₽ за пару кроссовок, которые в магазине стоили 10 000–20 000 ₽? Дело в психологии и экономике:</p>
  <ul>
    <li><strong>Дефицит.</strong> Лимитированные тиражи создают искусственный дефицит. Когда кроссовок выпущено 5000 пар, а желающих — 500 000, цена взлетает. Это закон спроса и предложения.</li>
    <li><strong>Статус.</strong> Редкие кроссовки — символ статуса. Обладание лимиткой показывает, что вы «в теме», имеете связи и ресурсы для её приобретения.</li>
    <li><strong>Коллекционирование.</strong> Как и марки, монеты или карточки, кроссовки стали объектом коллекционирования. Есть рейтинги, аукционы, сообщества коллекционеров.</li>
    <li><strong>Ресейл-рынок.</strong> Перепродажа лимиток — бизнес. Люди покупают кроссовки не для носки, а для перепродажи по более высокой цене. Это разогревает рынок ещё больше.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Исторический контекст</h4>
    <p>Феномен начался в 2010-х с коллабораций Nike и Off-White (Virgil Abloh) и Yeezy (Kanye West). В 2026 году рынок лимиток — это эволюционировавшая система с аукционами, ресейл-платформами и профессиональными перекупами.</p>
  </div>
</section>

<section id="market">
  <h2>Рынок лимиток 2026: ключевые тренды</h2>
  <p>Что происходит на рынке редких кроссовок в этом году:</p>
  <ul>
    <li><strong>Цены на ретро растут.</strong> Air Jordan 1 Chicago 1985 года продаются за 5–10 млн ₽ на аукционах. Ретро-модели Nike и Adidas 90-х дорожают на 20–30% в год.</li>
    <li><strong>Коллаборации — локомотив.</strong> Каждая коллаборация Nike x Tiffany, Adidas x Gucci, Salomon x MM6 создаёт ажиотаж и перепродаётся в 2–3 раза дороже розницы.</li>
    <li><strong>Падение интереса к Yeezy.</strong> После разрыва Adidas с Kanye West интерес к Yeezy снизился. Цены на ресейле упали на 30–50% от пика 2022 года.</li>
    <li><strong>Рост ресейл-платформ.</strong> StockX, GOAT, Grailed и российские аналоги (Kufar, Vinted специальные разделы) делают рынок прозрачнее, но цены — не ниже.</li>
    <li><strong>«Женские» модели дорожают.</strong> Коллаборации, ориентированные на женскую аудиторию, в 2026 году перепродаются дороже мужских из-за меньшего тиража.</li>
  </ul>
</section>

<section id="invest">
  <h2>Можно ли заработать на лимитках</h2>
  <p>Короткий ответ: редко. Длинный: рынок лимиток больше похож на азартные игры, чем на надёжную инвестицию.</p>
  <p>Успешные кейсы (купить за 10 000 ₽, продать за 100 000 ₽) становятся вирусными и создают иллюзию лёгких денег. Но реальность такова:</p>
  <ul>
    <li>70% лимиток не растут в цене или растут на 10–20% (это не покрывает комиссию ресейл-платформы).</li>
    <li>Чтобы заработать, нужно предсказывать тренды и участвовать в дропах (зачастую через ботов или связи).</li>
    <li>Риски: брак, подделки, изменение трендов, снижение спроса.</li>
    <li>Надёжнее инвестировать в классику (Air Jordan 1 Chicago, Nike Dunk low "Panda") — но и доходность там ниже.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Предостережение</h4>
    <p>Не вкладывайте деньги, которые не готовы потерять. Ресейл — это не банковский депозит. Если вы покупаете кроссовки для перепродажи, рассматривайте это как хобби с возможным, но не гарантированным доходом.</p>
  </div>
</section>

<section id="fakes">
  <h2>Как не купить подделку</h2>
  <p>Рынок лимиток наводнён фейками. Качество подделок 2026 года — пугающе высокое. Как защитить себя:</p>
  <ul>
    <li><strong>Покупайте только у проверенных продавцов.</strong> StockX, GOAT, официальные магазины. На вторичном рынке (Avito, Vinted) — проверяйте рейтинг и историю продавца.</li>
    <li><strong>Изучите детали.</strong> В интернете есть гайды по аутентификации для каждой популярной модели. Сравнивайте швы, форму «птички» Nike, упаковку, бирки.</li>
    <li><strong>Проверяйте бирку.</strong> На внутренней бирке должны быть: страна производства, дата, артикул. Шрифт, расположение, качество печати — важные признаки.</li>
    <li><strong>Используйте аутентификацию.</strong> На ресейл-платформах услуга входит в стоимость. При покупке «с рук» — закажите проверку в специализированном сервисе (стоит 500–1500 ₽).</li>
    <li><strong>Запах.</strong> У подделок часто резкий химический запах клея и краски. Оригинальные кроссовки пахнут резиной и кожей.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Если цена слишком низкая — это почти гарантированно подделка. Лимитка не может стоить в 2–3 раза дешевле рынка просто «потому что продавец срочно продаёт».</p>
  </div>
</section>

