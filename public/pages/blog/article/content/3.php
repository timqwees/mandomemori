<section id="method5">
  <h2>Способ 5: Заморозка (спорный метод)</h2>
  <p>Метод заморозки основан на том, что бактерии погибают при температуре ниже −20 °C. Теоретически это работает, но на практике есть важные ограничения, о которых редко говорят.</p>
  <h3>Как замораживать</h3>
  <ol>
    <li>Поместите кроссовки в герметичный зип-пакет — это защитит их от запахов в морозилке.</li>
    <li>Удалите из кроссовок максимальное количество воздуха.</li>
    <li>Положите в морозильную камеру на 8–12 часов.</li>
    <li>Достаньте и дайте оттаять при комнатной температуре (не используйте нагрев!).</li>
    <li>Просушите полностью перед следующим использованием.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Когда заморозка НЕ работает</h4>
    <p>Заморозка убивает бактерии, но не удаляет продукты их жизнедеятельности — именно они создают запах. Кроме того, многие бактерии образуют споры, которые выживают даже при −30 °C. Метод эффективен только в комбинации с чисткой.</p>
  </div>
  <p><strong>Важное предупреждение:</strong> заморозка противопоказана для кроссовок с гелевыми вставками (Nike Air, ASICS GEL) — низкие температуры разрушают структуру геля. Также не стоит замораживать обувь из кожи и замши — вода, кристаллизуясь, повреждает волокна.</p>
</section>

