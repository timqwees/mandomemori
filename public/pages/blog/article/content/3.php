<section id="cause">
  <h2>Почему возникает запах</h2>
  <p>Вопреки распространённому мнению, пот сам по себе не имеет резкого запаха. Неприятный аромат — результат жизнедеятельности бактерий, которые питаются органическими веществами в поте и выделяют летучие соединения.</p>
  <ul>
    <li><strong>Бактерии.</strong> На стопе человека обитает более 100 видов бактерий. В тёплой и влажной среде кроссовок они размножаются каждые 20–30 минут.</li>
    <li><strong>Влага.</strong> Повышенное потоотделение в жаркую погоду создаёт идеальную среду для бактерий. Особенно если обувь из синтетических материалов, которые не пропускают воздух.</li>
    <li><strong>Материал стелек.</strong> Дешёвые стельки из поролона и EVA впитывают пот и не отдают влагу — внутри кроссовка образуется «парник».</li>
    <li><strong>Отсутствие вентиляции.</strong> Ношение кроссовок без перерыва, особенно в жару, не даёт обуви просохнуть между использованиями.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Факт</h4>
    <p>Одна пара кроссовок за 8 часов носки накапливает до 50 мл пота. Этого достаточно, чтобы внутри образовалась колония из миллиардов бактерий.</p>
  </div>
</section>

<section id="method1">
  <h2>Способ 1: Пищевая сода на ночь</h2>
  <p>Самый простой, дешёвый и доступный способ. Сода — природный абсорбент, который впитывает влагу и нейтрализует кислые запахи. Работает как временное решение и как элемент регулярного ухода.</p>
  <h3>Как применять</h3>
  <ol>
    <li>Достаньте стельки из кроссовок (если они съёмные).</li>
    <li>Насыпьте внутрь каждого кроссовка по 2–3 столовых ложки соды.</li>
    <li>Равномерно распределите соду по всей внутренней поверхности.</li>
    <li>Оставьте на 8–12 часов (на всю ночь).</li>
    <li>Утром тщательно вытряхните соду и пропылесосьте внутреннюю полость.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Для усиления эффекта добавьте в соду 5–7 капель эфирного масла чайного дерева — оно обладает антибактериальными свойствами и оставляет лёгкий свежий аромат.</p>
  </div>
  <p><strong>Минусы:</strong> метод не уничтожает бактерии, а только маскирует запах. Требует регулярного повторения (раз в 2–3 дня при активной носке).</p>
</section>

