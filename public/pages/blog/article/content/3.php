<section id="method4">
  <h2>Способ 4: Стирка стелек</h2>
  <p>Стельки — главный источник запаха в кроссовках. Они контактируют с подошвой стопы, впитывают пот и создают идеальную среду для бактерий. Замена или стирка стелек — самый эффективный способ кардинально решить проблему запаха.</p>
  <h3>Как стирать стельки</h3>
  <ol>
    <li>Извлеките стельки из кроссовок. Проверьте, не приклеены ли они — некоторые бренды фиксируют стельки на заводе.</li>
    <li>Замочите в тёплой воде с мыльным раствором или специальным средством для спортивной обуви на 15–20 минут.</li>
    <li>Потрите мягкой щёткой, уделяя внимание области под пальцами и пяткой.</li>
    <li>Тщательно прополощите в чистой воде.</li>
    <li>Промокните полотенцем и оставьте сушиться при комнатной температуре. Не сушите на батарее!</li>
  </ol>
