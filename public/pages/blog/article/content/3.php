  <ul>
    <li><strong>Носите носки.</strong> Даже летом. Выбирайте тонкие хлопковые или бамбуковые носки — они впитывают пот и защищают обувь.</li>
    <li><strong>Сушите кроссовки после каждой носки.</strong> Используйте электрические сушилки для обуви вентиляторного типа — они продувают внутреннюю полость и не перегревают материалы.</li>
    <li><strong>Используйте антиперспиранты для ног.</strong> Специальные средства с хлоридом алюминия снижают потоотделение на 40–60%.</li>
