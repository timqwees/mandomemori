<section id="method3">
  <h2>Способ 3: Кедровые колодки и угольные мешочки</h2>
  <p>Кедровые колодки — классика ухода за обувью, которая переживает второе рождение. Кедровая древесина обладает природными антисептическими свойствами, впитывает влагу и придаёт обуви приятный древесный аромат.</p>
  <p>Угольные мешочки — японская технология, основанная на активированном угле бамбука. Один грамм такого угля может адсорбировать до 100 мг летучих соединений и избыточной влаги.</p>
  <h3>Как использовать</h3>
  <ul>
    <li>Вставляйте кедровые колодки сразу после носки, пока обувь тёплая — пар быстрее впитывается в дерево.</li>
    <li>Угольные мешочки помещайте внутрь на ночь в каждый кроссовок.</li>
    <li>Раз в месяц просушивайте колодки на солнце или прогревайте в духовке при 60 °C (не выше, чтобы не повредить дерево).</li>
    <li>Угольные мешочки меняйте раз в 3–4 месяца.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Экспертное мнение MANDO MEMORI</h4>
