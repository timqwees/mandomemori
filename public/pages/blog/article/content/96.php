<section id="durability">
  <h2>Долговечность и уход</h2>
  <p>Долговечность зависит не только от материала, но и от ухода:</p>
  <ul>
    <li><strong>Натуральная кожа.</strong> При регулярном уходе (чистка, крем, пропитка) служит 10+ лет. Без ухода — трескается за 1–2 года. Восстанавливается профессиональной реставрацией.</li>
    <li><strong>Экокожа.</strong> Не требует сложного ухода — достаточно протирать влажной салфеткой. Но при повреждении верхнего слоя (царапины, порезы) восстановить её практически невозможно. Обычно экокожа начинает шелушиться на сгибах через 2–3 года.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Если покупаете обувь из экокожи, выбирайте модели с минимальным количеством швов и без острых углов — в этих местах экокожа трескается в первую очередь.</p>
  </div>
</section>

<section id="choice">
  <h2>Что выбрать в 2026 году</h2>
  <p>Ответ зависит от ваших приоритетов:</p>
  <ul>
    <li><strong>Для ежедневной носки.</strong> Натуральная кожа — если готовы ухаживать. Окупается долговечностью.</li>
    <li><strong>Для редкого выхода.</strong> Экокожа — если обувь носится 1–2 раза в месяц. Срок службы не критичен.</li>
    <li><strong>Для спорта.</strong> Ни то, ни другое. Кроссовки для бега и фитнеса лучше выбирать из современных синтетических материалов (сетка, Primeknit, Flyknit).</li>
    <li><strong>Для дождливой погоды.</strong> Экокожа практичнее — она меньше боится воды. Но в экокожаной обуви быстрее потеют ноги.</li>
    <li><strong>Для аллергиков.</strong> Экокожа гипоаллергенна — хороший выбор, если натуральная кожа вызывает раздражение.</li>
