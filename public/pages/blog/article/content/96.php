<section id="leather">
  <h2>Что такое натуральная кожа</h2>
  <p>Натуральная кожа — это обработанная шкура животных. Качество зависит от типа кожи, выделки и производителя, но базовые характеристики неизменны:</p>
  <ul>
    <li><strong>Дышит.</strong> Кожа пропускает воздух, благодаря чему ноги меньше потеют. Это её главное преимущество перед большинством заменителей.</li>
    <li><strong>Принимает форму ноги.</strong> Со временем кожаная обувь подстраивается под анатомию владельца, становясь максимально комфортной.</li>
    <li><strong>Долговечна.</strong> При правильном уходе кожаная обувь служит 5–10 лет и более. Кожа устойчива к разрывам и истиранию.</li>
    <li><strong>Ремонтопригодна.</strong> Царапины, потёртости и даже трещины на коже можно отреставрировать. Кожаную подошву можно заменить.</li>
    <li><strong>Требует ухода.</strong> Кожу нужно чистить, питать кремами и защищать от воды. Без ухода она сохнет и трескается.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Виды натуральной кожи</h4>
    <p>Гладкая, зернистая (Saffiano, Pebble), нубук (шлифованная кожа с ворсом), велюр (с более длинным ворсом), лаковая — каждый тип требует своего подхода к уходу.</p>
  </div>
</section>

<section id="eco">
  <h2>Что такое экокожа</h2>
  <p>Экокожа (искусственная кожа на полиуретановой основе) — современный заменитель, который по тактильным ощущениям близок к натуральной коже. В отличие от дерматина и ПВХ-заменителей, экокожа дышит за счёт микропористой структуры полиуретана.</p>
  <ul>
    <li><strong>Этичность.</strong> Не используется животное сырьё. Это главный аргумент сторонников веганского образа жизни.</li>
    <li><strong>Цена.</strong> Экокожа в 2–3 раза дешевле качественной натуральной кожи при сопоставимом внешнем виде.</li>
    <li><strong>Лёгкость.</strong> Экокожа легче натуральной, что делает обувь более комфортной для длительного ношения.</li>
    <li><strong>Устойчивость к влаге.</strong> Экокожа не впитывает воду так активно, как натуральная, и требует меньше сложного ухода.</li>
    <li><strong>Меньший срок службы.</strong> Экокожа живёт 2–4 года, после чего начинает шелушиться и трескаться. Её сложно реставрировать.</li>
  </ul>
</section>

