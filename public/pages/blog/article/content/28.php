<section id="prevent">
  <h2>Профилактика: как не допустить запах</h2>
  <p>Предотвратить запах проще, чем бороться с ним. Пять простых правил:</p>
  <ul>
    <li><strong>Сушите обувь после каждой носки.</strong> Не носите одну пару два дня подряд. Обувь должна полностью высыхать 24 часа.</li>
    <li><strong>Используйте антибактериальные спреи.</strong> Обрабатывайте внутреннюю поверхность после каждой носки.</li>
    <li><strong>Меняйте стельки каждые 3 месяца.</strong> Стельки — рассадник бактерий. Дешёвые стельки можно менять чаще, дорогие анатомические — раз в 6 месяцев.</li>
    <li><strong>Носите качественные носки.</strong> Хлопок, бамбук, шерсть мериноса — никакой синтетики.</li>
    <li><strong>Обувь по сезону.</strong> В жаркую погоду носите открытую или дышащую обувь. Кроссовки из кожзама в жару гарантируют запах.</li>
  </ul>
</section>

<section id="insoles">
  <h2>Стельки: выбор и замена</h2>
  <p>Стельки — ключевой элемент в борьбе с запахом. От их состояния зависит 50% успеха.</p>
  <ul>
    <li><strong>Угольные стельки.</strong> Абсорбируют влагу и запахи. Срок службы — 3–4 месяца.</li>
    <li><strong>Стельки с активированным углём + алоэ.</strong> Не только впитывают запах, но и ухаживают за кожей стопы.</li>
    <li><strong>Серебряные стельки.</strong> Ионы серебра подавляют размножение бактерий. Дороже, но служат до года.</li>
    <li><strong>Анатомические стельки.</strong> Помогают при гипергидрозе (чрезмерной потливости) за счёт правильного распределения нагрузки.</li>
  </ul>
