<section id="mistakes">
  <h2>Чего нельзя делать</h2>
  <ul>
    <li><strong>Маскировать запах духами или одеколоном.</strong> Спирт не убивает бактерии, а смесь запаха пота и парфюма даёт ещё более неприятный аромат.</li>
    <li><strong>Сушить на батарее.</strong> Высокая температура создаёт идеальную среду для бактерий и портит материал.</li>
    <li><strong>Носить влажную обувь.</strong> Влажная среда — рай для бактерий. Дождитесь полного высыхания.</li>
    <li><strong>Использовать кондиционер для белья.</strong> Он забивает поры и ухудшает вентиляцию. Для стирки стелек используйте только мыло или соду.</li>
  </ul>
</section>

