<section id="ziptypes">
  <h2>Виды молний для обуви</h2>
  <p>В обуви используются разные типы застёжек-молний. Понимание конструкции поможет выбрать правильный вариант при замене:</p>
  <ul>
    <li><strong>Спиральные молнии (витые).</strong> Самые распространённые в обуви. Гибкие, пластичные, подходят для сапог и ботинок с изгибами. Звенья выполнены из нейлона или полиэстера.</li>
    <li><strong>Тракторные молнии.</strong> Крупные звенья, высокая прочность. Используются в зимних ботинках, треккинговой обуви и рабочих сапогах. Выдерживают агрессивные условия.</li>
    <li><strong>Металлические молнии.</strong> Латунь или никель. Ставятся на дорогую обувь как элемент дизайна. Самые надёжные, но и самые дорогие.</li>
    <li><strong>Потайные молнии.</strong> Используются в женских сапогах для эстетики. Менее прочные, требуют аккуратного обращения.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Как определить тип молнии</h4>
    <p>Посмотрите на зубцы: если они закрученные и мягкие — это спиральная молния. Если зубцы отдельные и крупные — тракторная. Металлическая — всегда холодная на ощупь и блестит характерным металлическим блеском.</p>
  </div>
</section>

<section id="problems">
  <h2>Типичные поломки молний</h2>
  <p>Молния может выйти из строя по разным причинам. Вот самые частые проблемы:</p>
  <ul>
    <li><strong>Расходятся зубцы.</strong> Самая частая проблема. Возникает из-за износа бегунка (слайдера) или деформации зубцов. Часто лечится заменой бегунка.</li>
    <li><strong>Заедает.</strong> Молния идёт туго или не двигается. Причина: грязь, песок, деформация звеньев, износ смазки.</li>
    <li><strong>Слетел бегунок.</strong> Язычок оторвался или бегунок соскочил с направляющих. Чаще всего необходима замена бегунка.</li>
    <li><strong>Порвана ткань молнии.</strong> Текстильная основа молнии разорвалась. Требуется полная замена молнии.</li>
    <li><strong>Сломаны зубцы.</strong> Один или несколько зубцов отсутствуют. Если повреждение небольшое — можно нарастить зубец, если обширное — замена молнии.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Не пытайтесь починить сами</h4>
    <p>Самостоятельный ремонт молнии плоскогубцами или подручными средствами часто усугубляет проблему. Согнутые зубцы, повреждённый бегунок — после таких «ремонтов» замена молнии будет стоить дороже.</p>
  </div>
</section>

<section id="replace">
  <h2>Процесс замены молнии на сапогах</h2>
  <p>Замена молнии на высокой обуви — трудоёмкая работа, которая требует опыта и специального оборудования. Вот как проходит процесс в мастерской:</p>
  <ol>
    <li><strong>Осмотр.</strong> Мастер оценивает длину и тип старой молнии, подбирает подходящий аналог. Важно совпадение цвета, профиля звеньев и длины.</li>
    <li><strong>Распарывание.</strong> Старая молния аккуратно удаляется. В зависимости от конструкции может потребоваться частичное распарывание подкладки.</li>
    <li><strong>Подготовка новой молнии.</strong> Новая молния укорачивается до нужной длины. Верхний и нижний стопоры переустанавливаются.</li>
    <li><strong>Вмётывание.</strong> Новая молния вмётывается в сапог на специальной обувной машине. Используются усиленные нитки.</li>
    <li><strong>Фиксация.</strong> После вмётывания мастер проверяет работу молнии, при необходимости регулирует натяжение.</li>
    <li><strong>Восстановление подкладки.</strong> Если подкладка распарывалась — она аккуратно зашивается.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Как долго ждать</h4>
    <p>Замена молнии на сапогах занимает от 1 до 3 дней в зависимости от загруженности мастерской и сложности модели. В срочном порядке некоторые мастерские выполняют замену за 2–4 часа.</p>
  </div>
</section>

