<section id="leather">
  <h2>Особенности итальянской кожи Common Projects</h2>
  <p>Common Projects используют итальянскую кожу высшего качества (телячья кожа, хромового дубления) от известных кожевенных мануфактур. У неё есть важные особенности:</p>
  <ul>
    <li><strong>Без защитного покрытия.</strong> Кожа «дышит», но впитывает грязь и влагу быстрее, чем кожа с защитным слоем (как у Nike AF1).</li>
    <li><strong>Тонкая и мягкая.</strong> Кожа Common Projects тоньше и мягче, чем у большинства кроссовок. Она эластичная, но легко царапается.</li>
    <li><strong>Белый краситель — уязвим.</strong> Белый цвет достигается пигментацией кожи, а не покраской поверх слоя. Это значит, что при сильном трении краситель может стираться.</li>
    <li><strong>Эластичность.</strong> Кожа Common Projects разнашивается по ноге, принимая её форму. Это плюс, но при неправильной сушке форма теряется.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Поймите философию</h4>
    <p>Common Projects — про красивую патину времени. Мелкие царапины и потёртости — часть характера этих кроссовок. Не стремитесь к стерильной белизне — лёгкий налёт носки делает их аутентичнее.</p>
  </div>
</section>

<section id="daily">
  <h2>Ежедневный уход за Common Projects</h2>
  <p>Минимализм в дизайне требует минимализма в уходе. Вот что нужно делать регулярно:</p>
  <ol>
    <li><strong>Протирайте после каждой носки.</strong> Мягкой сухой или слегка влажной микрофиброй удалите пыль и лёгкую грязь. Не трите сильно — только мягкие промокающие движения.</li>
    <li><strong>Убирайте подошву.</strong> Белая резиновая подошва Common Projects желтеет. Протирайте её после каждой носки влажной салфеткой. Для профилактики пожелтения — раз в месяц обрабатывайте смесью соды и уксуса.</li>
    <li><strong>Используйте распорки.</strong> После носки вставляйте деревянные или пластиковые распорки. Они сохраняют форму и впитывают влагу.</li>
    <li><strong>Проветривайте.</strong> После дня в кроссовках дайте им «подышать» 30–60 минут перед тем, как убрать в коробку.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Средство для ежедневного ухода</h4>
    <p>Используйте универсальную салфетку для ухода за кожи. Common Projects официально рекомендует салфетки Apple Brand или Collonil Clean & Care — они деликатно очищают и питают кожу за одно протирание.</p>
  </div>
</section>

<section id="deep">
  <h2>Глубокая чистка Common Projects</h2>
  <p>Раз в 2–3 недели Common Projects нуждаются в более основательной чистке. Вот пошаговая инструкция, безопасная для итальянской кожи:</p>
  <ol>
    <li><strong>Подготовка.</strong> Выньте шнурки и стельки. Шнурки — замочите в тёплой мыльной воде на 15–20 минут. Стельки — протрите антибактериальной салфеткой.</li>
    <li><strong>Удаление пыли.</strong> Сухой мягкой щёткой пройдитесь по всей поверхности кроссовка, включая швы.</li>
    <li><strong>Чистка кожи.</strong> Используйте специальный лосьон для гладкой кожи (Saphir, Collonil). Нанесите на ватный диск, аккуратно протрите кроссовок. Не используйте мыльную воду — она сушит кожу.</li>
    <li><strong>Чистка подошвы.</strong> Щёткой средней жёсткости с мыльной водой. Для белой подошвы — добавьте немного перекиси водорода.</li>
    <li><strong>Сушка.</strong> Набейте кроссовки бумажными полотенцами. Сушите при комнатной температуре 24 часа. Меняйте бумагу через 4–6 часов.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Чего нельзя делать</h4>
    <p>Не стирайте в машинке — кожа Common Projects не переживёт машинную стирку. Не замачивайте — вода размягчает кожу и деформирует её. Не используйте отбеливатель.</p>
  </div>
</section>

<section id="stains">
  <h2>Удаление пятен с Common Projects</h2>
  <p>Белая кожа Achilles Prime собирает пятна быстро. Вот как удалять разные типы загрязнений:</p>
  <ul>
    <li><strong>Грязь и пыль.</strong> Достаточно влажной микрофибры. Если засохла — аккуратно счистите мягкой щёткой.</li>
    <li><strong>Чёрные полосы (от резины, подошвы).</strong> Используйте очиститель для белой гладкой кожи. Нанесите на ватный диск, аккуратно потрите полосу. Не трите сильно — стирается краска.</li>
    <li><strong>Масляные пятна.</strong> Присыпьте тальком или кукурузным крахмалом, оставьте на 2–4 часа. Тальк впитает масло. Стряхните, протрите влажной тканью.</li>
    <li><strong>Красное вино, кофе.</strong> Промокните пятно белой тканью (не трите!). Нанесите очиститель для кожи. Действуйте быстро — застарелые пятна от вина с кожи удалить почти невозможно.</li>
    <li><strong>Кровь.</strong> Только холодная вода! Горячая вода «заваривает» белок крови в коже. Промокните ватным диском с холодной водой.</li>
  </ul>
</section>

<section id="whites">
  <h2>Как сохранить белизну Common Projects</h2>
  <p>Белизна — главная ценность Common Projects. Вот что помогает её сохранить:</p>
  <ul>
    <li><strong>Водоотталкивающая пропитка.</strong> Нанесите пропитку для гладкой кожи перед первой ноской. Это создаёт невидимый барьер, который отталкивает грязь и воду. Обновляйте раз в 2–3 недели.</li>
    <li><strong>Носите в сухую погоду.</strong> Common Projects — не для дождя и слякоти. Влажная грязь въедается в итальянскую кожу быстрее.</li>
    <li><strong>Не носите с тёмными джинсами.</strong> Неокрашенные джинсы линяют, и синяя краска въедается в белую кожу почти необратимо. Закатывайте штанины или выбирайте светлые джинсы.</li>
    <li><strong>Белый крем-восстановитель.</strong> Раз в месяц используйте белый крем для кожи (например, Saphir Teint Français Blanc). Он питает кожу и маскирует мелкие потёртости.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Важно</h4>
    <p>Если белая кожа Common Projects пожелтела — не пытайтесь отбелить её перекисью. Химия может испортить краситель. Только крем-восстановитель или профессиональная реставрация в MANDO MEMORI.</p>
  </div>
</section>

<section id="gold">
  <h2>Уход за золотым номером на заднике</h2>
  <p>Золотой номер (каждая пара Common Projects имеет уникальный номер из 10 цифр) — это не наклейка, а тиснение золотой фольгой. Со временем он стирается. Это нормально и даже придаёт паре характер.</p>
  <p>Если вы хотите сохранить номер: не трите задник щёткой, не используйте абразивы, избегайте контакта со спиртом. При чистке задника — используйте только мягкую влажную ткань, без давления.</p>
  <p>Восстановить стёртый номер можно в ателье — мастера наносят новое тиснение. В MANDO MEMORI мы можем восстановить золотой номер ваших Common Projects.</p>
