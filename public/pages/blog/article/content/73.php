<section id="materials">
  <h2>Особенности материалов Asics</h2>
  <p>Asics использует широкий спектр материалов, и их сочетание в одной модели — обычное дело. Вот основные материалы, с которыми вы столкнётесь:</p>
  <ul>
    <li><strong>Сетка (mesh).</strong> Основной материал верха у беговых моделей. Лёгкая, дышащая, но грязь въедается быстро. Чистится пенными очистителями.</li>
    <li><strong>Замша и искусственная замша.</strong> На ретро-моделях (Gel-Lyte III, Gel-NYC). Требует сухой чистки.</li>
    <li><strong>Кожаные вставки.</strong> В моделях Gel-NYC и некоторых кроссовках для города. Чистятся влажной тканью.</li>
    <li><strong>Полиуретановые усилители.</strong> Жёсткие вставки в области пятки для поддержки. Чистятся как обычная резина.</li>
    <li><strong>Гелевые капсулы Gel.</strong> Видимые вставки в подошве — визитная карточка Asics. Требуют осторожного обращения.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Asics использует термоклеевые соединения в кроссовках. Высокая температура (сушка на батарее, фен) разрушает клей — подошва может отойти. Сушите только при комнатной температуре.</p>
  </div>
</section>

<section id="cleaning">
  <h2>Чистка верха Asics</h2>
  <p>Пошаговая инструкция для беговых и повседневных моделей:</p>
  <ol>
    <li><strong>Подготовка.</strong> Выньте шнурки и стельки. Шнурки постирайте отдельно (в мешочке, если машинка). Стельки — замочите в мыльном растворе на 15 минут.</li>
    <li><strong>Сухая чистка.</strong> Щёткой средней жёсткости удалите сухую грязь с сетки. Не трите сильно — сетка может растянуться.</li>
    <li><strong>Влажная чистка.</strong> Смешайте тёплую воду с небольшим количеством мягкого мыла. Нанесите пену на мягкую щётку (не на кроссовок!) и аккуратно чистите сетку круговыми движениями.</li>
    <li><strong>Смывание.</strong> Влажной микрофиброй удалите остатки мыла. Не мочите кроссовки полностью — только локально.</li>
    <li><strong>Сушка.</strong> Набейте кроссовки бумажными полотенцами, поставьте сушиться при комнатной температуре. Меняйте бумагу каждые 4–6 часов.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Для стойких загрязнений</h4>
    <p>Если мыльный раствор не справляется — используйте специализированный пенный очиститель для спортивной обуви (Collonil, Tarrago, Nikwax). Нанесите пену, подождите 2–3 минуты, удалите влажной тканью.</p>
  </div>
</section>

<section id="gel">
  <h2>Уход за гелевой амортизацией Gel</h2>
  <p>Видимые гелевые вставки — отличительная черта Asics. С ними нужно обращаться аккуратно:</p>
  <ul>
    <li><strong>Не царапайте.</strong> Гелевые капсулы закрыты тонким слоем полиуретана. Не используйте жёсткие щётки в зоне Gel.</li>
    <li><strong>Не нагревайте.</strong> Гелевые капсулы чувствительны к высоким температурам. Сушка на батарее или феном может повредить гель.</li>
    <li><strong>Чистка.</strong> Просто протирайте влажной салфеткой. Если гель пожелтел — не пытайтесь отбелить его перекисью. Обратитесь в MANDO MEMORI.</li>
    <li><strong>Берегите от масла.</strong> Масла и растворители могут повредить полимер гелевых капсул. Не используйте масляные пропитки в зоне Gel.</li>
  </ul>
</section>

<section id="running">
  <h2>Чистка Asics после бега</h2>
  <p>Если вы используете Asics для бега, уход особенно важен — грязь, пот и влага быстро разрушают материалы.</p>
  <h3>Сразу после забега</h3>
  <ol>
    <li>Удалите грязь с подошвы — используйте палочку или щётку, чтобы очистить протектор.</li>
    <li>Промокните верх от пота сухой тканью.</li>
    <li>Выньте стельки — они должны просохнуть отдельно от кроссовка.</li>
    <li>Поставьте сушиться при комнатной температуре. Используйте распорки.</li>
  </ol>
  <h3>Раз в неделю — глубокая чистка</h3>
  <p>Пенная чистка верха, замачивание шнурков, очистка стелек с антибактериальным спреем. Это предотвращает появление неприятного запаха и разрушение материалов.</p>
  <div data-type="box" data-color="red">
    <h4>Не стирайте в машинке!</h4>
    <p>Стиральная машина разрушает гелевые капсулы и термоклеевые соединения Asics. После машинной стирки амортизация теряет 30–50% свойств.</p>
  </div>
</section>

<section id="retro">
  <h2>Уход за ретро-моделями Asics</h2>
  <p>Ретро-модели Asics вроде Gel-Lyte III, Gel-NYC и Kayano 14 требуют особого подхода из-за комбинации материалов — замша, кожа, сетка в одном кроссовке.</p>
  <ul>
    <li><strong>Замша на Gel-Lyte III</strong> — только сухая чистка резиновой щёткой. Для пятен — ластик для замши.</li>
    <li><strong>Сетка на Gel-NYC</strong> — пенная чистка с мягкой щёткой. Не трите сильно — сетка на ретро-моделях тоньше.</li>
    <li><strong>Вставки из кожи</strong> — влажная микрофибра, раз в месяц — крем-кондиционер.</li>
    <li><strong>Пожелтевшая подошва</strong> — для ретро-моделей особенно актуально. Используйте отбеливание или обратитесь в MANDO MEMORI.</li>
  </ul>
</section>

<section id="protection">
  <h2>Защита и пропитка Asics</h2>
  <p>Водоотталкивающая пропитка — обязательна для кроссовок Asics, особенно если вы носите их в городе:</p>
  <ul>
    <li><strong>Для сетки и текстиля</strong> — спрей-пропитка для текстиля (Collonil Carbon, Nikwax). Обновляйте после каждой 3-й чистки.</li>
    <li><strong>Для замши</strong> — специальная пропитка для замши (Saphir, Collonil). Не используйте силиконовые составы — они забивают поры замши.</li>
    <li><strong>Для кожи</strong> — восковая пропитка или крем с водоотталкивающим эффектом.</li>
  </ul>
  <p>Наносите пропитку на чистую и сухую обувь с расстояния 20–25 см. Дайте высохнуть 10–15 минут. Для лучшего эффекта — нанесите второй слой.</p>
</section>

