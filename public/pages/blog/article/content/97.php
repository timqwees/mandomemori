<section id="care">
  <h2>Уход за веганской кожей</h2>
  <p>Хотя веганская кожа неприхотлива, базовый уход продлевает её жизнь:</p>
  <ul>
    <li><strong>Регулярно протирайте.</strong> Влажной мягкой тканью удаляйте пыль и грязь. Не используйте абразивные губки.</li>
    <li><strong>Не пересушивайте.</strong> Не оставляйте веган-кожу на солнце или рядом с батареей — полиуретан теряет эластичность и трескается.</li>
    <li><strong>Используйте защиту.</strong> Водоотталкивающий спрей для синтетики защищает от грязи и продлевает срок службы.</li>
    <li><strong>Храните вдали от источников тепла.</strong> В жаркую погоду (выше 40 °C) полиуретан может деформироваться.</li>
    <li><strong>Избегайте острых предметов.</strong> Порезы на веган-коже замаскировать нельзя — они остаются навсегда.</li>
