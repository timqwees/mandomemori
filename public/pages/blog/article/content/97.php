<section id="what">
  <h2>Что такое веганская кожа</h2>
  <p>Веганская кожа — это общее название для искусственных материалов, которые имитируют натуральную кожу, но не содержат компонентов животного происхождения.</p>
  <ul>
    <li><strong>Полиуретан (PU).</strong> Самый распространённый тип веганской кожи. Мягкий, эластичный, дышащий. Именно его обычно называют «экокожей».</li>
    <li><strong>ПВХ (поливинилхлорид).</strong> Более дешёвая альтернатива. Жёстче полиуретана, не дышит, трескается на морозе. Лучше избегать.</li>
    <li><strong>Растительные заменители.</strong> Инновационные материалы из листьев кактуса (Desserto), яблочных шкурок (AppleSkin), виноградных выжимок (Vegea) и грибницы (Mylo). Дороже, но экологичнее.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Термин «веганская кожа» не регулируется законом. Недобросовестные производители могут называть так обычный ПВХ-дерматин. Всегда проверяйте состав на ярлычке: качественная веганская кожа — это полиуретан (PU) на хлопковой или полиэфирной основе.</p>
  </div>
</section>

<section id="types">
  <h2>Виды растительной кожи</h2>
  <p>Самые инновационные и экологичные варианты веганской кожи — растительного происхождения. Вот основные:</p>
  <ul>
    <li><strong>Кактусовая кожа (Desserto).</strong> Производится из листьев опунции в Мексике. Выглядит и ощущается как натуральная кожа. Биоразлагаема и не содержит токсичных химикатов. Используется брендами вродem Maria La Rosa.</li>
    <li><strong>Яблочная кожа (AppleSkin).</strong> Делается из отходов яблочного производства — шкурок и сердцевин. Отходы сушат, перемалывают в порошок и смешивают с полиуретаном. Использовалась компанией Apple (не путать!) для чехлов.</li>
    <li><strong>Грибная кожа (Mylo).</strong> Производится из мицелия грибов. Растёт в контролируемых условиях за 2 недели. Пока самый дорогой вид растительной кожи.</li>
    <li><strong>Ананасовая кожа (Piñatex).</strong> Из листьев ананаса. Уже используется обувными брендами. Мягкая, но менее прочная, чем синтетическая веган-кожа.</li>
  </ul>
</section>

<section id="pros">
  <h2>Плюсы веганской кожи</h2>
  <p>Почему веганская кожа набирает популярность — объективные преимущества:</p>
  <ul>
    <li><strong>Этичность.</strong> Не страдает ни одно животное. Для веганов и людей, заботящихся о правах животных, это ключевой аргумент.</li>
    <li><strong>Цена.</strong> Качественная веган-кожа дешевле хорошей натуральной кожи в 2–3 раза при сопоставимом внешнем виде.</li>
    <li><strong>Влагостойкость.</strong> Веганская кожа не впитывает воду так активно, как натуральная, и не боится соли и реагентов.</li>
    <li><strong>Простота ухода.</strong> Не требует регулярного питания кремами и защитными составами. Достаточно протирать влажной салфеткой.</li>
    <li><strong>Лёгкость.</strong> Веганская кожа легче натуральной — обувь из неё комфортнее при длительном ношении.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Экологический аспект</h4>
    <p>Производство веганской кожи имеет меньший углеродный след и не требует земель для выпаса скота. Однако полиуретан — продукт нефтехимии, и разлагается он сотнями лет. Растительные альтернативы решают эту проблему, но пока дороги.</p>
  </div>
</section>

