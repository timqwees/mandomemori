<section id="storage">
  <h2>Хранение портфеля</h2>
  <p>Правильное хранение — залог долгой жизни портфеля, особенно в период отпусков:</p>
  <ul>
    <li><strong>Форма.</strong> Набейте портфель бумагой или тканью, чтобы он не потерял форму. Особенно важно для моделей с мягкими стенками.</li>
    <li><strong>Мешок.</strong> Храните в тканевом пылевике или наволочке. В полиэтилене кожа не дышит и может покрыться плесенью.</li>
    <li><strong>Положение.</strong> Не вешайте портфель за ручку — она вытянется. Храните на полке лёжа.</li>
    <li><strong>Условия.</strong> Сухое тёмное место с температурой 15–22 °C. Вдали от батарей и солнца.</li>
    <li><strong>Влажность.</strong> Оптимальная влажность 40–60%. В сухом помещении кожа трескается, во влажном — плесневеет.</li>
  </ul>
</section>

