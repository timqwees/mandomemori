<section id="daily">
  <h2>Ежедневный уход за портфелем</h2>
  <p>Деловой портфель используется ежедневно, поэтому поддерживать его в чистоте нужно регулярно:</p>
  <ul>
    <li><strong>Протирайте после каждого дня.</strong> Мягкой сухой тканью удалите пыль. Если портфель стоял на полу — протрите дно влажной салфеткой.</li>
    <li><strong>Не перегружайте.</strong> Носите только необходимое. Перегруженный портфель деформируется, ручки растягиваются, швы расходятся.</li>
    <li><strong>Носите правильно.</strong> Если есть плечевой ремень — используйте его. Ношение портфеля только за ручку создаёт одностороннюю нагрузку на кожу и фурнитуру.</li>
    <li><strong>Избегайте контакта с водой.</strong> Если попали под дождь — сразу промокните портфель бумажным полотенцем и высушите при комнатной температуре.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно для делового стиля</h4>
    <p>Портфель должен выглядеть ухоженно, но не «новым». Лёгкие потёртости на углах и ручке — признак качественной натуральной кожи и придают портфелю шарм. Не пытайтесь оттереть их до идеала.</p>
  </div>
</section>

<section id="clean">
  <h2>Глубокая чистка</h2>
  <p>Раз в 1–2 месяца проводите глубокую чистку. Вот пошаговая инструкция:</p>
  <ol>
    <li>Освободите портфель от содержимого, выверните внутренние карманы.</li>
    <li>Пропылесосьте внутреннюю часть мягкой насадкой.</li>
    <li>Приготовьте раствор из тёплой воды и мягкого мыла (или специальной пены для кожи).</li>
    <li>Смочите салфетку из микрофибры, тщательно отожмите.</li>
    <li>Протрите всю поверхность портфеля, включая ручку, углы и дно.</li>
    <li>Чистой влажной салфеткой удалите остатки мыла.</li>
    <li>Просушите при комнатной температуре — не используйте фен и не ставьте на батарею.</li>
    <li>После высыхания нанесите питательный крем.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Никогда не используйте для чистки портфеля спирт, ацетон, жидкость для снятия лака, хлорку или бытовую химию. Они разрушают красители и высушивают кожу.</p>
  </div>
</section>

<section id="polish">
  <h2>Полировка и питание кожи</h2>
  <p>Кожа портфеля, как и кожа обуви, нуждается в питании. Без крема она пересыхает, трескается и теряет блеск:</p>
  <h3>Выбор крема</h3>
  <ul>
    <li><strong>Бесцветный питательный крем.</strong> Универсальный вариант. Питает, увлажняет, придаёт лёгкий блеск. Подходит для любого цвета кожи.</li>
    <li><strong>Крем-краситель.</strong> Восстанавливает цвет и маскирует потёртости. Выбирайте оттенок точно в тон портфеля.</li>
    <li><strong>Воск.</strong> Создаёт водоотталкивающий барьер. Используйте перед сезоном дождей.</li>
  </ul>
  <h3>Как наносить</h3>
  <ol>
    <li>Нанесите крем на мягкую ткань, а не прямо на портфель.</li>
    <li>Круговыми движениями вотрите крем в кожу.</li>
    <li>Оставьте на 10–15 минут для впитывания.</li>
    <li>Отполируйте чистой мягкой тканью до лёгкого блеска.</li>
  </ol>
  <p>Питание кожи достаточно проводить раз в 2–3 месяца.</p>
</section>

<section id="scratches">
  <h2>Удаление царапин</h2>
  <p>Царапины на портфеле неизбежны. Но их можно замаскировать:</p>
  <div data-type="compare">
    <article data-variant="good">
      <h4>Методы маскировки</h4>
      <ul>
        <li>Лёгкие царапины — разгладьте пальцем или тёплой тканью</li>
        <li>Средние — крем-краситель в тон (нанесите ватной палочкой)</li>
        <li>Глубокие — жидкая кожа (заполните царапину, разровняйте)</li>
        <li>Потёртости углов — крем-краситель + полировка</li>
      </ul>
    </article>
