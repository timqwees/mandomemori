<section id="features">
  <h2>Конструкция Speedcross: что важно знать</h2>
  <p>Salomon Speedcross имеют особенности, которые отличают их уход от обычных кроссовок:</p>
  <ul>
    <li><strong>Глубокий протектор (до 6 мм).</strong> Шипы протектора забиваются грязью и мелкими камнями. Если их не чистить — снижается сцепление, и кроссовки становятся скользкими.</li>
    <li><strong>Мембрана Gore-Tex (GTX-версии).</strong> Не все Speedcross имеют Gore-Tex, но большинство современных моделей — да. Мембрана боится машинной стирки и агрессивной химии.</li>
    <li><strong>Армированная сетка Anti-Debris.</strong> Мелкоячеистая сетка не пропускает песок и мелкие камни. Её нельзя тереть жёсткими щётками — повредите переплетение.</li>
    <li><strong>Quicklace — система быстрой шнуровки.</strong> Шнурки-тросы требуют бережного обращения. При чистке их лучше не замачивать — фиксаторы могут заржаветь.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Проверьте версию</h4>
    <p>Перед чисткой убедитесь, есть ли у ваших Speedcross мембрана Gore-Tex. Обычно это указано на бирке язычка. Если есть — используйте только средства для мембранных тканей. Если нет — можно чистить универсальными составами для синтетики.</p>
  </div>
</section>

<section id="mud">
  <h2>Удаление грязи и песка</h2>
  <p>После трейлового забега Speedcross покрыты слоем грязи. Первое и самое важное — удалить грязь до того, как она высохнет и зацементируется.</p>
  <h3>Сразу после забега</h3>
  <ol>
    <li>Вымойте подошву под струёй воды из шланга или бутылки. Не используйте сильный напор — вода может проникнуть внутрь через вентиляцию.</li>
    <li>Удалите камни из протектора специальным крючком или старой отвёрткой. Будьте осторожны — не повредите резину.</li>
    <li>Промойте подошву щёткой со средним ворсом. Движения вдоль протектора, не поперёк — чтобы не повредить шипы.</li>
  </ol>
  <h3>Если грязь уже высохла</h3>
  <p>Высохшая грязь удаляется сложнее. Постучите кроссовками друг о друга — крупные куски отвалятся. Остатки замочите на 10–15 минут в прохладной воде и удалите щёткой. Не используйте горячую воду — она может повредить клей и мембрану.</p>
  <div data-type="box" data-color="green">
    <h4>Совет trail-бегуна</h4>
    <p>Носите с собой на забег небольшую щётку и бутылку воды. Сразу после финиша, пока грязь свежая, очистите подошву — это займёт 2 минуты, а дома вам не придётся отбивать засохшие комья.</p>
  </div>
</section>

<section id="clean">
  <h2>Чистка верха и мембраны</h2>
  <p>Верх Speedcross — армированная сетка с синтетическими накладками. Для чистки используйте только мягкие методы:</p>
  <h3>Для GTX-версий (с мембраной)</h3>
  <ol>
    <li>Удалите стельки и шнурки Quicklace.</li>
    <li>Сметите сухую грязь мягкой щёткой.</li>
    <li>Приготовьте раствор специализированного средства для Gore-Tex (Nikwax Tech Wash или аналог).</li>
    <li>Нанесите мягкой губкой, протрите загрязнённые участки.</li>
    <li>Тщательно смойте остатки средства прохладной водой.</li>
  </ol>
  <h3>Для не-GTX версий</h3>
  <p>Можно использовать пенное средство для синтетики и сетки. Нанесите пену, вотрите щёткой, смойте влажной тканью. Не замачивайте кроссовки целиком.</p>
  <div data-type="box" data-color="red">
    <h4>Никогда так не делайте</h4>
    <p>Speedcross (особенно GTX-версии) нельзя стирать в машинке. Высокая температура, отжим и барабан разрушают мембрану и деформируют усиленный задник. После машинной стирки кроссовки могут потерять форму и начать пропускать воду.</p>
  </div>
</section>

<section id="dry">
  <h2>Правильная сушка после забега</h2>
  <p>Speedcross намокают не снаружи, а изнутри — от пота. После забега они влажные, и это нормально. Но неправильная сушка сокращает срок службы кроссовок в 2 раза.</p>
  <ul>
    <li><strong>Выньте стельки.</strong> Они сохнут отдельно и быстрее.</li>
    <li><strong>Набейте газетой.</strong> Бумага впитывает влагу. Меняйте каждые 4–6 часов.</li>
    <li><strong>Сушите вдали от тепла.</strong> Никаких батарей, обогревателей и фенов. Только комнатная температура.</li>
    <li><strong>Время сушки.</strong> Speedcross сохнут 18–24 часа. Не надевайте влажные кроссовки на следующий забег — мембрана работает хуже, а кожа ног размокает.</li>
  </ul>
</section>

<section id="dwr">
  <h2>Восстановление Gore-Tex и DWR</h2>
  <p>После 3–4 забегов водоотталкивающее покрытие изнашивается. Вода перестаёт скатываться и начинает впитываться в верх. Это первый признак того, что пора восстанавливать DWR.</p>
  <h3>Как восстановить</h3>
  <ol>
    <li>Почистите кроссовки по инструкции выше.</li>
    <li>Дайте полностью высохнуть.</li>
    <li>Нанесите DWR-пропитку (Nikwax TX.Direct Spray или Grangers Repel Plus).</li>
    <li>Равномерно распылите на всю поверхность верха.</li>
    <li>Оставьте на 5 минут, затем удалите излишки тканью.</li>
    <li>Активируйте пропитку теплом: фен на средней температуре с расстояния 20 см в течение 3–5 минут.</li>
  </ol>
  <div data-type="box" data-color="dark">
    <h4>Совет профессионала</h4>
    <p>Для максимальной защиты наносите DWR-пропитку на слегка влажную поверхность после чистки. Влажные волокна лучше впитывают состав, и покрытие держится на 30–40% дольше.</p>
  </div>
</section>

<section id="prevent">
  <h2>Профилактика и уход между забегами</h2>
  <p>Speedcross — рабочая обувь для трейла. Она не должна быть идеально чистой, но уход между забегами продлевает жизнь кроссовкам:</p>
  <ul>
    <li><strong>Проветривание.</strong> После каждого забега оставляйте кроссовки на проветривание на 24 часа. Не убирайте в тёмный шкаф влажными — заведётся плесень.</li>
    <li><strong>Стельки — расходник.</strong> Меняйте стельки каждые 3–4 месяца или после 300–400 км. Задёшево, а эффект колоссальный: и свежесть, и амортизация.</li>
    <li><strong>Шнурки Quicklace.</strong> Раз в месяц прочищайте механизм фиксатора от песка и грязи зубочисткой. Смазка не требуется.</li>
    <li><strong>Сезонное хранение.</strong> Если не бегаете зимой — вычистите Speedcross, набейте бумагой и храните в тканевом мешке при комнатной температуре.</li>
  </ul>
</section>

