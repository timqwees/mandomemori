<section id="diff">
  <h2>Чем отличаются замша и нубук</h2>
  <p>Основное различие — в способе обработки кожи и происхождении сырья:</p>
  <ul>
    <li><strong>Замша.</strong> Производится из внутреннего (бахтармяного) слоя шкуры животных. Используются шкуры мелкого рогатого скота (козы, овцы, телята) или свиная кожа. Ворс мягкий, густой, матовый, средней высоты (0.5–1.5 мм). Замша хорошо пропускает воздух, эластичная и очень мягкая на ощупь.</li>
    <li><strong>Нубук.</strong> Это лицевой слой кожи, который отшлифован снаружи абразивными материалами (оксид алюминия). Используется только крупный рогатый скот. Ворс мелкий (0.1–0.3 мм), почти незаметный, с лёгким блеском. Нубук более плотный и износостойкий, чем замша, но менее эластичный.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно понимать</h4>
    <p>Замша — более толстый, рыхлый и мягкий материал, чем нубук. Замша прочнее на разрыв, но меньше защищает от влаги. Нубук плотнее, дольше служит, но при контакте с водой оставляет тёмные пятна, которые сложнее удалить.</p>
  </div>
</section>

<section id="identify">
  <h2>Как отличить замшу от нубука</h2>
  <p>Перед чисткой убедитесь, что за материал перед вами. Используйте несколько признаков:</p>
  <div data-type="compare">
    <article data-variant="good">
      <h4>Замша</h4>
      <ul>
        <li>Ворс заметный, высотой 0.5–1.5 мм</li>
        <li>Матовая, без блеска</li>
        <li>Мягкая, эластичная на ощупь</li>
        <li>Толщина — обычно 2–4 мм</li>
        <li>Характерный «дышащий» вид</li>
        <li>Обувь из замши дороже нубуковой</li>
      </ul>
    </article>
    <article data-variant="bad">
      <h4>Нубук</h4>
      <ul>
        <li>Ворс очень низкий, почти незаметный</li>
        <li>С лёгким блеском, как бархат</li>
        <li>Плотнее, жёстче на ощупь</li>
        <li>Толщина — 1–2 мм</li>
        <li>Гладкий, «прилизанный» внешне</li>
        <li>Выглядит более формально</li>
      </ul>
    </article>
  </div>
  <p>Можно провести тест: проведите пальцем по поверхности. Если ворс меняет направление и остаётся след — это замша. Если ворс возвращается в исходное положение — нубук.</p>
</section>

<section id="care-suede">
  <h2>Уход за замшей</h2>
  <p>Замша требует более деликатного ухода:</p>
  <ul>
    <li><strong>Чистка.</strong> Только сухая! Резиновая щётка, ластик для замши. Влажная чистка — только специальными пенками для замши, и то в крайних случаях.</li>
    <li><strong>Вода.</strong> Замша боится воды сильнее нубука. После намокания теряет форму и может «сесть». Обязательно обрабатывайте водоотталкивающим спреем.</li>
    <li><strong>Ворс.</strong> Поднимайте ворс резиновой щёткой регулярно. Засаленные участки обрабатывайте паром (подержите над паром 10–15 секунд).</li>
    <li><strong>Пятна.</strong> Жирные пятна — тальком на 2–3 часа. Сильные загрязнения — специальной пеной для замши.</li>
    <li><strong>Пропитка.</strong> Водоотталкивающий спрей для замши наносится раз в 1–2 недели. Без него замша быстро впитывает грязь и влагу.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Совет для замши</h4>
    <p>Замшу не носят в дождь. Даже с пропиткой длительный контакт с водой вреден. Если вы попали под дождь — набейте замшевую обувь бумагой и сушите сутки при комнатной температуре. После сушки пройдитесь резиновой щёткой.</p>
  </div>
</section>

<section id="care-nubuck">
  <h2>Уход за нубуком</h2>
  <p>Нубук более устойчив к износу, но его чистка имеет свои особенности:</p>
  <ul>
    <li><strong>Чистка.</strong> Резиновая щётка для нубука (с более жёсткой щетиной, чем для замши). Для застарелых пятен — специальная щётка с латунной щетиной.</li>
    <li><strong>Вода.</strong> Нубук боится воды меньше замши, но после высыхания на нём остаются тёмные разводы. Сушить нужно сразу, промокая бумажным полотенцем.</li>
    <li><strong>Разводы.</strong> Если после высыхания остались водяные разводы — пройдитесь по всей поверхности нубука специальной щёткой, чтобы выровнять ворс.</li>
    <li><strong>Пропитка.</strong> Для нубука используйте спреи, специально предназначенные для него. Спреи для замши могут сделать нубук жирным.</li>
    <li><strong>Восстановление цвета.</strong> Для нубука есть специальные аэрозольные краски-восстановители. Подбирайте точно в тон.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Чего нельзя делать с нубуком</h4>
    <p>Не используйте для нубука масляные пропитки и кремы — они забивают поры и меняют цвет. Только спреи на водной основе, предназначенные для нубука.</p>
  </div>
</section>

<section id="choice">
  <h2>Что выбрать</h2>
  <p>Выбор между замшей и нубуком зависит от ваших задач:</p>
  <ul>
    <li><strong>Для повседневной городской обуви</strong> — нубук. Он прочнее, износостойче и проще в уходе. Классические ботинки из нубука смотрятся достойно и дольше сохраняют вид.</li>
    <li><strong>Для кроссовок и casual-обуви</strong> — замша. Она мягче, «уютнее» и даёт больше простора для дизайна. Но требует более частой чистки.</li>
    <li><strong>Для зимней обуви</strong> — нубук с водоотталкивающей пропиткой. Замша зимой страдает от соли и реагентов сильнее.</li>
    <li><strong>Для дождливой погоды</strong> — ни тот, ни другой вариант не подходит. Выбирайте гладкую кожу или резину.</li>
  </ul>
