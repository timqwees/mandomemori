<section id="types">
  <h2>Типы красок и их особенности</h2>
  <p>Прежде чем приступать к чистке, важно определить тип краски. От этого зависит выбор растворителя и метода удаления:</p>
  <ul>
    <li><strong>Масляная краска.</strong> На основе олифы или алкидных смол. Глубоко проникает в пористые материалы, сохнет долго, но после высыхания образует прочную плёнку. Удаляется уайт-спиритом или бензином.</li>
    <li><strong>Акриловая краска.</strong> На водной основе с акриловыми полимерами. Пока не высохла — смывается водой. После высыхания становится водостойкой, требуется спирт или специальный смывка.</li>
    <li><strong>Водоэмульсионная краска.</strong> Легко смывается водой даже после высыхания. Самый безопасный для обуви тип краски.</li>
    <li><strong>Нитрокраска и аэрозольные краски.</strong> Содержат агрессивные растворители. Могут повредить материал обуви, удаляются сложнее всего.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Как определить тип краски</h4>
    <p>Нанесите каплю воды на незаметном участке: если краска размягчается — она на водной основе. Если вода скатывается — краска масляная или акриловая. Запах тоже подскажет: масляная пахнет олифой, акрил — слабым химическим запахом, нитрокраска — резко ацетоном.</p>
  </div>
</section>

<section id="oil">
  <h2>Как удалить масляную краску</h2>
  <p>Масляная краска — самый сложный тип загрязнения. Она въедается в структуру материала и застывает плёнкой. Действовать нужно аккуратно, чтобы не повредить обувь.</p>
  <h3>Что понадобится</h3>
  <ul>
    <li>Уайт-спирит или очищенный бензин</li>
    <li>Ватные диски или мягкая ткань</li>
    <li>Медицинский спирт</li>
    <li>Влажные салфетки</li>
    <li>Перчатки</li>
  </ul>
  <h3>Пошаговая инструкция</h3>
  <ol>
    <li>Дайте краске полностью высохнуть. Свежую краску промокните салфеткой, не размазывая.</li>
    <li>Смочите ватный диск уайт-спиритом и аккуратно потрите загрязнённый участок.</li>
    <li>По мере растворения краски меняйте диск на чистый — не размазывайте пигмент.</li>
    <li>После удаления краски протрите участок спиртом, чтобы убрать остатки растворителя.</li>
    <li>Вымойте обувь мыльным раствором и высушите при комнатной температуре.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Важно</h4>
    <p>Не используйте ацетон и растворители на основе нитроцеллюлозы на кожаной обуви — они могут растворить краситель и повредить поверхность. На замше и нубуке применяйте только специальные очистители для деликатных материалов.</p>
  </div>
</section>

<section id="acrylic">
  <h2>Удаление акриловой и латексной краски</h2>
  <p>Акриловая краска коварна: в свежем виде она легко смывается водой, но после высыхания превращается в пластиковое покрытие. Если краска уже высохла, потребуется спирт или специальный состав.</p>
  <h3>Свежая краска</h3>
  <ol>
    <li>Промокните краску бумажной салфеткой, не втирая.</li>
    <li>Промойте участок холодной водой с мылом.</li>
    <li>Если остался след — используйте медицинский спирт на ватном диске.</li>
  </ol>
  <h3>Высохшая краска</h3>
  <ol>
    <li>Смочите участок изопропиловым спиртом или специальной смывкой для акрила.</li>
    <li>Оставьте на 5–10 минут для размягчения.</li>
    <li>Соскоблите краску тупой стороной ножа или пластиковым скребком.</li>
    <li>Повторите при необходимости.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Совет по текстилю</h4>
    <p>На текстильных кроссовках акриловую краску можно вывести смесью нашатырного спирта и уксуса в пропорции 1:1. Нанесите на пятно, оставьте на 15 минут и постирайте вручную с мылом.</p>
  </div>
</section>

<section id="water">
  <h2>Водоэмульсионная и водорастворимая краска</h2>
  <p>С водоэмульсионной краской справиться проще всего. Она на водной основе и не содержит агрессивных растворителей. Достаточно нескольких простых действий:</p>
  <ol>
    <li><strong>Свежая:</strong> смойте тёплой водой с мылом.</li>
    <li><strong>Высохшая:</strong> замочите участок тёплой водой на 10–15 минут, затем потрите мягкой щёткой.</li>
    <li><strong>Остатки:</strong> протрите спиртом для полного удаления разводов.</li>
  </ol>
  <p>Водоэмульсионная краска не содержит вредных растворителей, поэтому она безопасна для всех типов обуви. Единственное, что может потребоваться при застарелых пятнах — деликатное замачивание.</p>
