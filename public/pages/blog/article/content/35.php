<section id="tips">
  <h2>Чего делать нельзя</h2>
  <p>Чтобы не испортить обувь окончательно, запомните запрещённые приёмы:</p>
  <ul>
    <li><strong>Не трите свежую краску.</strong> Размазывание только расширяет площадь загрязнения. Промокните салфеткой, а затем аккуратно удалите остатки.</li>
    <li><strong>Не используйте ацетон на цветной обуви.</strong> Он растворяет не только краску, но и заводской краситель. В результате могут остаться светлые пятна.</li>
    <li><strong>Не применяйте металлические скребки.</strong> Царапины на коже и замше не восстановить. Только пластик или дерево.</li>
    <li><strong>Не сушите феном.</strong> Нагревание фиксирует краску в структуре материала, делая её практически неудаляемой.</li>
    <li><strong>Не замачивайте всю обувь в растворителе.</strong> Обрабатывайте только загрязнённый участок, чтобы не повредить клей и форму.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Главное правило</h4>
    <p>Перед использованием любого растворителя протестируйте его на незаметном участке обуви — внутри под язычком или на заднике под стелькой. Подождите 10 минут и оцените результат. Если цвет и структура не изменились — можно применять.</p>
