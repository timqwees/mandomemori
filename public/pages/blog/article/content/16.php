<section id="features">
  <h2>Особенности текстильных материалов</h2>
  <p>Текстиль и сетка — пористые материалы с переплетением волокон. Грязь проникает глубоко между нитями, а вода может вызвать усадку и деформацию. Основные риски при чистке:</p>
  <ul>
    <li><strong>Усадка.</strong> От горячей воды текстиль садится, и кроссовки становятся малы. Максимальная температура воды — 30 °C.</li>
    <li><strong>Выцветание.</strong> Агрессивные моющие средства вымывают красители. Используйте только специализированные составы.</li>
    <li><strong>Деформация.</strong> Сильное трение и перекручивание нарушают переплетение нитей. Сетка может порваться или растянуться.</li>
    <li><strong>Разводы.</strong> Неправильная сушка оставляет жёлтые и белые разводы от моющего средства.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Запомните главное правило</h4>
    <p>Текстиль нельзя мочить целиком — только обрабатывать пеной. Вода, затекшая внутрь между слоями материала, сохнет 2–3 дня и может вызвать неприятный запах.</p>
  </div>
</section>

<section id="daily">
  <h2>Ежедневный уход</h2>
  <p>Текстильные кроссовки требуют регулярного, но деликатного ухода. Несколько минут в день избавят вас от необходимости глубокой чистки:</p>
  <ul>
    <li><strong>Сухая щётка.</strong> После каждой носки проходитесь по поверхности мягкой щёткой — это удаляет пыль и песок, которые действуют как абразив.</li>
    <li><strong>Влажная салфетка.</strong> Свежие пятна сразу удаляйте влажной микрофиброй. Не трите — промокайте.</li>
    <li><strong>Пенка для текстиля.</strong> Раз в неделю используйте экспресс-пену: распылите, подождите 2 минуты и снимите остатки тканью.</li>
  </ul>
</section>

<section id="deep">
  <h2>Глубокая чистка текстиля</h2>
  <p>Когда ежедневного ухода недостаточно — переходите к глубокой чистке. Главное — не замачивать кроссовки в воде целиком.</p>
  <h3>Что понадобится</h3>
  <ul>
    <li>Пенное чистящее средство для текстиля</li>
    <li>Щётка с мягкой щетиной</li>
    <li>Микрофибра — 2–3 штуки</li>
    <li>Ёмкость с тёплой водой (30 °C)</li>
  </ul>
  <h3>Пошаговая инструкция</h3>
  <ol>
    <li>Выньте шнурки и стельки. Почистите их отдельно.</li>
    <li>Удалите сухую грязь щёткой. Особенно тщательно — в складках и около подошвы.</li>
    <li>Нанесите пенное средство на влажную щётку. Вспеньте.</li>
    <li>Круговыми движениями обработайте весь текстиль. Не давите слишком сильно.</li>
    <li>Снимайте пену влажной микрофиброй — меняйте сторону ткани по мере загрязнения.</li>
    <li>Повторяйте пункты 3–5, пока микрофибра не будет оставаться чистой.</li>
    <li>Промокните остатки влаги сухой тканью.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Почему пена, а не вода?</h4>
    <p>Пена содержит меньше воды, но достаточно моющих компонентов. Она проникает в волокна, не промачивая материал насквозь. Текстиль сохнет за 6–8 часов, а не за 2–3 дня. Это предотвращает размножение бактерий и появление запаха.</p>
  </div>
</section>

<section id="stains">
  <h2>Удаление стойких пятен</h2>
  <p>Сетка и текстиль быстро впитывают пятна от травы, кофе, масла и грязи. Для каждого типа пятна — свой подход:</p>
  <h3>Трава и зелень</h3>
  <p>Смешайте перекись водорода 3% с водой в пропорции 1:1. Нанесите на пятно ватным диском, оставьте на 10 минут, промокните влажной тканью. Не трите — пятно размажется.</p>
  <h3>Масло и жир</h3>
  <p>Посыпьте пятно тальком или кукурузным крахмалом, оставьте на 30 минут — порошок впитает жир. Затем удалите остатки щёткой и обработайте пятно пеной.</p>
  <h3>Грязь</h3>
  <p>Дайте грязи полностью высохнуть, затем удалите сухой щёткой. Остатки обработайте пеной. Никогда не трите мокрую грязь — она втирается глубже в волокна.</p>
  <div data-type="box" data-color="red">
    <h4>Чего избегать</h4>
    <p>Не используйте хлорный отбеливатель на текстиле — он разрушает волокна и оставляет жёлтые пятна. Также избегайте кондиционеров для белья — они забивают поры материала и ухудшают воздухопроницаемость.</p>
  </div>
</section>

<section id="whitening">
  <h2>Отбеливание белого текстиля</h2>
  <p>Белые текстильные кроссовки со временем становятся серыми. Вернуть белизну можно несколькими способами:</p>
  <ul>
    <li><strong>Сода и лимон.</strong> Смешайте соду с соком лимона до пасты, нанесите на ткань, оставьте на 20 минут, смойте пеной. Безопасно для всех типов текстиля.</li>
    <li><strong>Кислородный отбеливатель.</strong> Разведите по инструкции, нанесите на ткань на 15–20 минут. Не используйте на цветном текстиле — он обесцветится.</li>
    <li><strong>Профессиональный отбеливающий спрей.</strong> Распылите, вотрите щёткой и смойте. Самый удобный вариант для регулярного использования.</li>
  </ul>
</section>

<section id="prevent">
  <h2>Профилактика загрязнений</h2>
  <p>Текстиль — как одежда: его нужно защищать. Водоотталкивающая пропитка обязательна для текстильных кроссовок:</p>
  <ul>
    <li>Наносите пропитку перед первой ноской и обновляйте раз в 2 недели.</li>
    <li>Используйте спрей, подходящий для текстиля и синтетики.</li>
    <li>После нанесения дайте кроссовкам высохнуть 24 часа.</li>
    <li>Не носите текстильные кроссовки в сильный дождь — они промокают быстрее других.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Совет эксперта</h4>
    <p>Для текстильных кроссовок приобретите силиконовую щётку — она не царапает волокна и эффективно удаляет грязь из переплетений сетки. Такая щётка стоит недорого, но продлевает жизнь ткани в 2 раза.</p>
