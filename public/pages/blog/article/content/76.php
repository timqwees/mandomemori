<section id="materials">
  <h2>Материалы Gucci — что нужно знать</h2>
  <p>Gucci использует несколько типов материалов, и у каждого — свой подход:</p>
  <ul>
    <li><strong>Гладкая телячья кожа.</strong> Основной материал для кроссовок Ace, лоферов и туфель. Мягкая, но прочная. Требует питания и защиты.</li>
    <li><strong>Кожа Marmont.</strong> Характерная матовая, слегка зернистая кожа с тиснением GG. Менее требовательна, чем гладкая кожа.</li>
    <li><strong>Замша.</strong> Используется в некоторых моделях кроссовок и лоферов. Бархатистая, капризная.</li>
    <li><strong>GG Supreme.</strong> Знаменитая парусина Gucci с монограммами. Используется на сумках, реже — на обуви. Чистится сухой щёткой.</li>
    <li><strong>Зелёно-красная лента (Web).</strong> Полиэстеровая лента — отличительная черта Gucci. Не выцветает, но пачкается.</li>
    <li><strong>Металлическая фурнитура.</strong> Пряжки Horsebit, люверсы, заклёпки — из латуни с покрытием.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Первое правило</h4>
    <p>Gucci — обувь из премиальных натуральных материалов. Кожа Gucci «дышит» и живёт. Используйте только профессиональные средства — дешёвые кремы и пропитки могут испортить дорогую кожу навсегда.</p>
  </div>
