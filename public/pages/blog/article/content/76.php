<section id="leather">
  <h2>Чистка гладкой кожи Gucci</h2>
  <h3>Ежедневный уход</h3>
  <p>После каждой носки протирайте кожу мягкой тканью — удалите пыль и грязь. Раз в 2 недели — наносите кондиционер для кожи (Saphir Beauté du Cuir, Collonil 1909). Он питает кожу, предотвращает пересыхание и появление трещин.</p>
  <h3>Удаление пятен</h3>
  <p>Пятна на белой коже Gucci Ace — частая проблема. Используйте специальный лосьон для гладкой кожи. Не трите — наносите лосьон на ватный диск и аккуратно промокайте пятно. Для застарелых пятен — обратитесь в MANDO MEMORI.</p>
  <h3>Восстановление цвета</h3>
  <p>Белая кожа Gucci Ace со временем желтеет. Используйте белый крем-восстановитель Saphir (или аналогичный премиальный). Нанесите тонким слоем, дайте впитаться 10–15 минут, отполируйте мягкой тканью до блеска.</p>
  <div data-type="box" data-color="red">
    <h4>Не используйте</h4>
    <p>Не используйте силиконовые кремы и спреи для мгновенного блеска — они создают плёнку, которая со временем тускнеет и забивает поры кожи Gucci. Только натуральные восковые составы.</p>
  </div>
</section>

<section id="suede">
  <h2>Уход за замшевыми Gucci</h2>
  <p>Замшевая обувь Gucci (например, лоферы Princetown с замшевым верхом) выглядит роскошно, но требует тщательного ухода:</p>
  <ul>
    <li><strong>Чистка после каждой носки.</strong> Резиновая щётка поднимает ворс и удаляет пыль. Движения — только в одну сторону.</li>
    <li><strong>Удаление пятен.</strong> «Мокрые» пятна от воды или напитков — промокните бумажным полотенцем (не трите!). После высыхания — пройдитесь щёткой. Масляные пятна — присыпьте тальком или кукурузным крахмалом, оставьте на ночь, стряхните.</li>
    <li><strong>Восстановление цвета.</strong> Замша Gucci выцветает на солнце. Раз в 3–4 месяца используйте аэрозольную краску для замши. Подбирайте цвет по каталогу — у Gucci сложные оттенки.</li>
    <li><strong>Защита.</strong> Водоотталкивающая пропитка для замши (без силикона!) — обязательна. Наносите после чистки, перед каждой ноской в сырую погоду.</li>
  </ul>
</section>

<section id="webs">
  <h2>Как чистить зелёно-красную полосу (Web)</h2>
  <p>Зелёно-красная лента — отличительный знак Gucci. Она есть на кроссовках Ace, лоферах и многих других моделях. Лента сделана из полиэстера и не линяет, но пачкается.</p>
  <p><strong>Чистка:</strong> Используйте ватную палочку, смоченную в мыльной воде. Аккуратно протрите ленту, не задевая кожу вокруг. Удалите остатки мыла чистой влажной палочкой. Не трите сильно — края ленты могут распуститься.</p>
  <p><strong>Важно:</strong> Не используйте растворители и спирт — они могут растворить краску ленты. Если лента пожелтела или выцвела — замена возможна только в ателье или мастерской.</p>
</section>

<section id="horsebit">
  <h2>Уход за металлической фурнитурой Gucci</h2>
  <p>Металлические элементы (пряжки Horsebit, люверсы, заклёпки) — визитная карточка Gucci. Они изготавливаются из латуни с покрытием palladium-finish или золотом.</p>
  <ul>
    <li><strong>Чистка.</strong> Протирайте сухой мягкой тканью. Для полировки — используйте специальную салфетку для ювелирных изделий (без абразивов).</li>
    <li><strong>Потускнение.</strong> Со временем покрытие может тускнеть. Это естественный процесс старения. Не используйте полироли для металла — они стирают тонкое покрытие.</li>
    <li><strong>Окисление.</strong> Если фурнитура потемнела от влаги — протрите уксусом (1:1 с водой) и сразу высушите.</li>
    <li><strong>Царапины.</strong> Мелкие царапины на пряжке Horsebit — неизбежны. Если они вас беспокоят — мастер MANDO MEMORI может заполировать их или заменить фурнитуру.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Лайфхак</h4>
    <p>Перед чисткой всей обуви — заклейте металлические элементы малярным скотчем. Средства для кожи не должны попадать на металл.</p>
