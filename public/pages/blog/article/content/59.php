<section id="care">
  <h2>Как правильно ухаживать за лаковой обувью</h2>
  <p>Лаковая обувь простоит годы, если соблюдать простые правила:</p>
  <ul>
    <li><strong>Чистите после каждой носки.</strong> Мягкой сухой тканью удаляйте пыль и грязь. Влажная чистка — только специальными средствами для лака.</li>
    <li><strong>Не носите в дождь и снег.</strong> Вода разрушает лаковое покрытие. Если намокли — высушите при комнатной температуре без батареи.</li>
    <li><strong>Храните в чехлах.</strong> Каждая пара отдельно в тканевом мешочке, чтобы лаковые поверхности не терлись друг о друга.</li>
    <li><strong>Избегайте перепадов температур.</strong> Не носите лаковую обувь при температуре ниже -5 °C — лак трескается на морозе.</li>
    <li><strong>Используйте колодки.</strong> Они сохраняют форму и предотвращают заломы, на которых образуются трещины лака.</li>
    <li><strong>Полируйте раз в месяц.</strong> Поддерживающая полировка мягкой тканью продлевает жизнь лаковому покрытию в 2–3 раза.</li>
  </ul>
