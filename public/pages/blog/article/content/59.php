<section id="why">
  <h2>Почему лаковое покрытие портится</h2>
  <p>Лаковое покрытие — это многослойная система из полиуретановых или акриловых лаков, нанесённых на кожу. Со временем оно теряет свои свойства:</p>
  <ul>
    <li><strong>Микроцарапины.</strong> Даже мягкая пыль действует как абразив. При ходьбе частицы грязи царапают лаковую поверхность, и со временем она становится матовой.</li>
    <li><strong>Перепады температур.</strong> Лак не переносит резких перепадов: на морозе становится хрупким и трескается, на жаре размягчается и теряет форму.</li>
    <li><strong>Влага.</strong> Вода проникает через микротрещины в лаковом слое, вызывает отслоение и помутнение покрытия.</li>
    <li><strong>Химические вещества.</strong> Спирт, растворители, агрессивные чистящие средства разрушают лаковую плёнку.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Особенность лака</h4>
    <p>В отличие от гладкой кожи, лаковое покрытие нельзя «напитать» кремом — оно не впитывает. Восстановление возможно только снаружи: полировка, повторное лакирование или замена покрытия.</p>
  </div>
</section>

<section id="scratch">
  <h2>Как убрать царапины с лаковой обуви</h2>
  <p>Мелкие царапины на лаковой обуви — самая частая проблема. Вот работающие методы:</p>
  <h3>Метод 1: Вазелин или растительное масло</h3>
  <p>Для свежих поверхностных царапин. Нанесите немного вазелина на ватный диск и аккуратно вотрите в царапину круговыми движениями. Оставьте на 10–15 минут, затем отполируйте мягкой тканью. Метод работает за счёт заполнения микротрещин маслянистым составом — царапина становится менее заметной.</p>
  <h3>Метод 2: Специальный корректор для лака</h3>
  <p>В обувных магазинах продаются корректирующие карандаши и жидкости для лаковой кожи. Они заполняют царапины составом, который после высыхания образует глянцевую плёнку. Подбирайте средство под цвет обуви.</p>
  <h3>Метод 3: Лак для ногтей (экстренно)</h3>
  <p>Прозрачный лак для ногтей может временно замаскировать царапину. Нанесите тонкий слой на повреждённый участок, дайте высохнуть и отполируйте. Минус — со временем лак может пожелтеть или отслоиться.</p>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Перед применением любого средства протестируйте его на незаметном участке. Лаковое покрытие разных брендов по-разному реагирует на составы.</p>
  </div>
</section>

<section id="shine">
  <h2>Возвращение блеска лаковой обуви</h2>
  <p>Если лак потускнел, но царапин нет, вернуть зеркальный блеск помогут:</p>
  <h3>Полировка мягкой тканью</h3>
  <p>Самый безопасный способ: возьмите кусок микрофибры или фланели и полируйте обувь круговыми движениями в течение 5–10 минут. Тепло от трения разглаживает микронеровности лака и восстанавливает блеск.</p>
  <h3>Специальные полироли для лака</h3>
  <p>Профессиональные полироли для лаковой кожи (например, Saphir Vernis Rife или Collonil Lederwax) содержат микрочастицы воска, которые заполняют поры и создают зеркальный блеск. Наносите тонким слоем на чистую поверхность.</p>
  <h3>Домашний метод с уксусом</h3>
  <p>Смесь воды и уксуса (1:1) с несколькими каплями оливкового масла. Нанесите мягкой тканью на обувь, отполируйте. Метод подходит только для свежего помутнения — застарелый тусклый лак так не восстановить.</p>
  <div data-type="box" data-color="red">
    <h4>Чего избегать</h4>
    <p>Не используйте спирт, ацетон и жидкости для снятия лака — они растворяют лаковое покрытие. Также избегайте силиконовых спреев — они дают временный блеск, но со временем делают лак тусклым.</p>
  </div>
</section>

<section id="crack">
  <h2>Что делать с трещинами на лаковом покрытии</h2>
  <p>Если лаковое покрытие треснуло — это серьёзная проблема. Трещины на лаке не маскируются кремом, так как затрагивают не кожу, а именно лаковый слой.</p>
  <p><strong>Мелкие трещины:</strong> Если сетка трещин поверхностная и не затрагивает кожу, можно нанести тонкий слой прозрачного акрилового лака для кожи. Он заполнит трещины и создаст новую глянцевую поверхность. После высыхания — полировка.</p>
  <p><strong>Глубокие трещины с отслоением:</strong> Если лак отходит от кожи кусками — домашними методами не обойтись. Требуется полное удаление старого лака и нанесение нового покрытия. Это сложная процедура, которую выполняют только профессионалы.</p>
  <p><strong>Трещины на сгибах:</strong> Самый сложный случай, так как это зона постоянного движения. Даже новое покрытие может быстро потрескаться. Рекомендуется профессиональное восстановление с использованием эластичных лаковых составов.</p>
  <div data-type="box" data-color="dark">
    <h4>Важно</h4>
    <p>Не пытайтесь содрать отслоившийся лак — вместе с ним может отойти верхний слой кожи. Доверьте удаление старого покрытия мастеру.</p>
  </div>
</section>

