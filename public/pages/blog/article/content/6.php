<section id="right">
  <h2>Правильная ручная чистка — пошаговая инструкция</h2>
  <p>Единственный безопасный способ очистить кроссовки — ручная чистка. Она занимает 15–20 минут и не требует специального оборудования:</p>
  <ol>
    <li><strong>Подготовка.</strong> Выньте шнурки и стельки. Шнурки замочите в мыльном растворе отдельно. Стельки почистите мягкой щёткой с мылом — их нельзя мочить полностью.</li>
    <li><strong>Удаление сухой грязи.</strong> Щёткой с жёсткой щетиной или специальным ластиком удалите подсохшую грязь с подошвы и верха. Уделите особое внимание протектору.</li>
    <li><strong>Приготовление раствора.</strong> В тёплой воде (не выше 30°C) разведите несколько капель мягкого моющего средства — жидкое мыло, шампунь или специальный очиститель для обуви. Никаких порошков и отбеливателей.</li>
    <li><strong>Чистка верха.</strong> Смочите мягкую ткань или микрофибру в растворе, тщательно отожмите и протрите загрязнённые участки. Движения — от центра к краям, чтобы не размазывать грязь.</li>
    <li><strong>Чистка подошвы.</strong> Щёткой со средним ворсом обработайте подошву и боковые стороны. Для труднодоступных мест используйте старую зубную щётку.</li>
    <li><strong>Удаление мыла.</strong> Чистой влажной тканью (без мыла) протрите всю поверхность, чтобы убрать остатки моющего средства.</li>
    <li><strong>Просушка.</strong> Промокните поверхность сухой микрофиброй. Набейте кроссовки белой бумагой или специальными колодками для сохранения формы.</li>
  </ol>
  <div data-type="box" data-color="gray">
    <h4>Лайфхак</h4>
    <p>Для очистки белых подошв добавьте в мыльный раствор немного пищевой соды — она усиливает эффект, но не повреждает резину. Для тёмной подошвы соду использовать не стоит — могут остаться разводы.</p>
  </div>
</section>

