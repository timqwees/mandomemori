<section id="damage">
  <h2>5 видов повреждений, которые вызывает стиральная машина</h2>
  <p>Многие считают, что современные стиральные машины с деликатными режимами безопасны для обуви. Это опасное заблуждение. Вот что реально происходит внутри барабана:</p>
  <ol>
    <li><strong>Деформация верха.</strong> При отжиме на 600–800 оборотов кроссовки ударяются о стенки барабана с силой до 5 кг. Ткань растягивается, подкладка рвётся, форма необратимо меняется. Особенно страдают кроссовки с сеткой и мягким верхом.</li>
    <li><strong>Отслоение подошвы.</strong> Клей, которым крепится подошва, не рассчитан на длительное воздействие воды и моющих средств. Уже после 2–3 машинных стирок подошва начинает отходить. В зоне риска — Nike, Adidas, New Balance с контрастными подошвами.</li>
    <li><strong>Повреждение мембраны.</strong> Gore-Tex, eVent и другие мембраны разрушаются при контакте с порошками и высоких температурах. Микроскопические поры забиваются, и обувь перестаёт «дышать» и отводить влагу.</li>
    <li><strong>Потеря цвета.</strong> Трение о барабан и другие кроссовки приводит к выцветанию. Контрастные элементы, принты и светоотражатели стираются за одну-две стирки.</li>
    <li><strong>Скрип и люфт.</strong> Вода проникает внутрь межподошвы, разрушает пенную основу. После сушки внутри образуются пустоты — кроссовки начинают скрипеть и теряют амортизацию.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Реальный случай</h4>
    <p>В нашу мастерскую принесли Salomon XT-6 после одной машинной стирки. Результат: частичное отслоение подошвы, повреждение мембраны, потеря формы задника. Восстановление обошлось в 70% от стоимости новых кроссовок.</p>
  </div>
</section>

<section id="myths">
  <h2>Мифы о стирке кроссовок в стиральной машине</h2>
  <p>В интернете гуляет множество советов, которые кажутся разумными, но на деле вредят обуви. Разберём самые популярные:</p>
  <div data-type="compare">
    <article data-variant="good">
      <h4>Миф 1: «Деликатный режим безопасен»</h4>
      <p>Даже на режиме «деликатная стирка» барабан вращается и создаёт удары. Кроме того, цикл длится дольше, а значит, обувь дольше контактирует с водой и химией.</p>
    </article>
    <article data-variant="bad">
      <h4>Правда</h4>
      <p>Деликатный режим не исключает механического воздействия. Кроссовки всё равно бьются о барабан и друг о друга. Единственный способ — полное исключение машинной стирки.</p>
    </article>
  </div>
  <div data-type="compare">
    <article data-variant="good">
      <h4>Миф 2: «Специальные мешки защищают обувь»</h4>
      <p>Мешки снижают трение, но не устраняют его полностью. Влага и моющее средство всё равно проникают внутрь, а сама обувь деформируется в ограниченном пространстве мешка.</p>
    </article>
    <article data-variant="bad">
      <h4>Правда</h4>
