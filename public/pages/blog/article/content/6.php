<section id="drying">
  <h2>Как правильно сушить кроссовки после чистки</h2>
  <p>Даже после идеальной ручной чистки неправильная сушка может испортить обувь. Вот как сушить кроссовки правильно:</p>
  <ul>
    <li><strong>Комнатная температура.</strong> Сушите при 18–24°C в хорошо проветриваемом помещении. Никаких батарей, обогревателей и фенов — высокая температура разрушает клей и деформирует материалы.</li>
    <li><strong>Набивка бумагой.</strong> Белая бумага (не газеты — типографская краска может отпечататься) впитывает влагу и сохраняет форму. Меняйте бумагу каждые 3–4 часа.</li>
    <li><strong>Специальные колодки.</strong> Деревянные или пластиковые распорки для обуви — лучший вариант. Они не только сохраняют форму, но и впитывают лишнюю влагу.</li>
    <li><strong>Вентиляция.</strong> Не ставьте кроссовки в закрытый шкаф или плотный пакет — без циркуляции воздуха они будут сохнуть 2–3 дня и может появиться плесень.</li>
  </ul>
  <p>Время полного высыхания — от 12 до 24 часов в зависимости от материала. Не надевайте влажные кроссовки — это растягивает их и создаёт комфортную среду для бактерий.</p>
</section>

