<section id="materials">
  <h2>Когда машинная стирка всё-таки допустима</h2>
  <p>Строго говоря, мы не рекомендуем стирать в машинке никакую обувь. Но если вы готовы рискнуть, вот единственные случаи, когда риск минимален:</p>
  <ul>
    <li><strong>Кеды из плотного хлопка.</strong> Старые Converse Chuck Taylor или Vans из парусины без мембраны и сложных элементов — они наименее уязвимы. Но только в холодной воде и без отжима.</li>
    <li><strong>Дёшевые кроссовки из синтетической сетки.</strong> Если пара стоила до 2000 рублей и не имеет клеевых элементов, мембраны или сложной подошвы — риск оправдан.</li>
    <li><strong>Обувь для грязи.</strong> Резиновые сапоги, галоши, обувь из ПВХ — ей машинная стирка действительно не страшна. Но и смысла в ней мало — их проще помыть под краном.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Категорически нельзя</h4>
    <p>Запрещено стирать в машинке: любые кроссовки с мембраной (Gore-Tex, Climacool, Dri-Fit), кожаную обувь, кроссовки с гелевыми или воздушными подушками (Nike Air, Asics Gel), замшевые и нубуковые модели, а также любую премиальную обувь дороже 10 000 рублей.</p>
