<section id="future">
  <h2>Будущее эко-обуви</h2>
  <p>Куда движется индустрия:</p>
  <ul>
    <li><strong>Замкнутый цикл.</strong> Бренды (Nike, Adidas) начинают программы возврата старой обуви для переработки. Из старого верха делают новый.</li>
    <li><strong>Биофабрикация.</strong> Выращивание материалов в лаборатории: кожа из грибов, шёлк из дрожжей, красители из бактерий.</li>
    <li><strong>Цифровые паспорта.</strong> Каждая пара получит QR-код с информацией о материалах и углеродном следе. Прозрачность — лучшее лекарство от гринвошинга.</li>
    <li><strong>Регулирование.</strong> Евросоюз вводит обязательные стандарты экологической маркировки для обуви к 2028 году. Производителей будут штрафовать за гринвошинг.</li>
  </ul>
