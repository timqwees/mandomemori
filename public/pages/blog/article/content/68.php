<section id="what">
  <h2>Что считается экологичной обувью?</h2>
  <p>Понятие «экологичная обувь» объединяет сразу несколько характеристик:</p>
  <ul>
    <li><strong>Материалы.</strong> Использование переработанных тканей, натуральных возобновляемых материалов (органический хлопок, каучук), растительной кожи (грибы, кактус, яблоки), биоразлагаемых полимеров.</li>
    <li><strong>Производство.</strong> Снижение выбросов CO2, использование возобновляемой энергии, минимальное количество отходов, замкнутый цикл воды.</li>
    <li><strong>Упаковка.</strong> Отсутствие пластика, картон из переработанных материалов, минималистичная упаковка.</li>
    <li><strong>Логистика.</strong> Локальное производство (снижение транспортного следа), оптимизация маршрутов доставки.</li>
    <li><strong>Утилизация.</strong> Возможность переработки обуви по окончании срока службы.</li>
  </ul>
  <p>Редко какая пара обуви соответствует всем пунктам сразу. Обычно производитель выбирает 2–3 направления и делает на них акцент.</p>
</section>

<section id="brands">
  <h2>Что делают бренды: обзор 2026</h2>
  <h3>Adidas — лидер по масштабу</h3>
  <p>Adidas — один из немногих гигантов, который реально меняет производство. К 2026 году 70% полиэстера в продукции Adidas — переработанный. Линейка Parley использует пластик, собранный в океане. Ultraboost теперь производится с Primeknit из переработанных материалов.</p>
  <h3>Nike — инновации и амбиции</h3>
  <p>Nike Forward — технология создания нетканого верха из промышленных отходов. Move to Zero — программа углеродной нейтральности. Crater Foam — подошва из переработанной резины. Проблема Nike: компании далеко до углеродной нейтральности (по собственным отчётам, выбросы сокращены на 20% от базы).</p>
  <h3>Veja — эко-пионеры</h3>
  <p>Французский бренд использует органический хлопок, натуральный каучук из Амазонии, растительное дубление кожи. Упаковка — только переработанный картон. Минус: модели Veja менее долговечны, чем кроссовки из синтетики.</p>
  <h3>Allbirds — углеродно-нейтральный</h3>
  <p>Allbirds первыми объявили об углеродной нейтральности. Материалы: шерсть мериноса, эвкалиптовое волокно, полиол из сахарного тростника для подошвы. Проблема: обувь Allbirds быстро теряет форму.</p>
  <div data-type="box" data-color="gray">
    <h4>Парадокс</h4>
    <p>Экологичные материалы часто менее износостойки. Кроссовки из переработанных тканей могут прослужить 6–12 месяцев вместо 2–3 лет. Это увеличивает количество выбрасываемой обуви — обратный эффект.</p>
  </div>
</section>

<section id="truth">
  <h2>Реальность: что скрывается за эко-тегами</h2>
  <p>Гринвошинг (greenwashing) — маркетинговая практика, когда бренд создаёт ложное впечатление экологичности. Вот типичные приёмы:</p>
  <ul>
    <li><strong>Акцент на одном элементе.</strong> «Подошва из переработанной резины» — звучит зелёно, но верх из первичного полиэстера и упаковка в пластик сводят эко-эффект к минимуму.</li>
    <li><strong>Размытые термины.</strong> «Эко-дружественный материал» — что это значит? Без сертификации — пустой звук. Сертификаты: GOTS (органический текстиль), OEKO-TEX, B-Corp.</li>
    <li><strong>Углеродные компенсации.</strong> Бренд покупает углеродные кредиты и объявляет себя «углеродно-нейтральным». Но это не отменяет выбросов — это их перенос.</li>
    <li><strong>Ограниченные коллекции.</strong> Выпустить 1000 пар «эко-кроссовок» и продолжать производить миллионы обычных — не делает бренд зелёным.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Как распознать гринвошинг</h4>
    <p>Ищите конкретные цифры и сертификаты. «На 30% меньше углерода» — это конкретно. «Эко-коллекция» — это маркетинг. Проверяйте информацию в независимых источниках.</p>
  </div>
</section>

