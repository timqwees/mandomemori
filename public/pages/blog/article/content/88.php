<section id="wrong">
  <h2>Чего нельзя делать</h2>
  <p>Начнём с запретов — их нарушение приводит к необратимым повреждениям:</p>
  <ul>
    <li><strong>Не сушите на батарее.</strong> Из-за высокой температуры кожа трескается, подошва отслаивается, клей теряет свойства, а замша становится жёсткой.</li>
    <li><strong>Не используйте фен.</strong> Горячий воздух пересушивает материал неравномерно, вызывая деформацию. Холодный обдув допустим, но малоэффективен.</li>
    <li><strong>Не ставьте на обогреватель.</strong> То же, что и батарея, только хуже — перегрев может оплавить синтетические элементы.</li>
    <li><strong>Не сушите на солнце.</strong> УФ-лучи обесцвечивают материалы и ускоряют окисление подошвы.</li>
    <li><strong>Не оставляйте мокрую обувь в закрытом шкафу.</strong> Без вентиляции заведётся плесень, которая уничтожит обувь.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Почему батарея — враг</h4>
    <p>При сушке на батарее верх обуви высыхает за 1–2 часа, а внутренняя часть остаётся влажной на 8–10 часов. Этот дисбаланс создаёт напряжение в материалах, что приводит к трещинам и деформации. Температура выше 40 °C разрушает клеевые соединения.</p>
  </div>
</section>

<section id="paper">
  <h2>Метод с бумагой</h2>
  <p>Самый доступный и безопасный способ сушки — с помощью бумаги. Метод известен десятилетиями и работает безотказно:</p>
  <ol>
    <li>Снимите стельки и выньте шнурки — они сохнут отдельно.</li>
    <li>Набейте обувь скомканной бумагой максимально плотно, особенно в носок.</li>
    <li>Через 2–3 часа, когда бумага впитает влагу, замените её на сухую.</li>
    <li>Повторяйте процедуру 3–4 раза до полного высыхания.</li>
    <li>Влажные стельки положите отдельно на бумажное полотенце.</li>
  </ol>
  <p><strong>Важно:</strong> используйте белую бумагу или бумажные полотенца. Газеты с типографской краской могут окрасить внутреннюю часть обуви.</p>
  <div data-type="box" data-color="green">
    <h4>Преимущества метода</h4>
    <p>Бумага впитывает влагу, не повреждая материалы, и помогает сохранить форму обуви. Это единственный метод, рекомендованный производителями люксовой обуви.</p>
  </div>
</section>

<section id="dryer">
  <h2>Электрические сушилки</h2>
  <p>Современные сушилки для обуви — безопасная альтернатива народным методам. Но выбирать нужно правильно:</p>
  <ul>
    <li><strong>Керамические сушилки.</strong> Греются до 50–60 °C, подходят для всех типов обуви. Сушат за 3–5 часов. Недостаток: сушат неравномерно.</li>
    <li><strong>Сушилки с вентилятором.</strong> Обдувают холодным или тёплым воздухом. Безопасны, сушат за 3–4 часа. Лучший выбор для деликатных материалов.</li>
    <li><strong>УФ-сушилки.</strong> Комбинируют нагрев и ультрафиолет для дезинфекции. Убивают бактерии и грибок. Рекомендуются, если обувь часто промокает.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Совет по выбору</h4>
    <p>Для дорогой обуви выбирайте сушилки с регулировкой температуры и таймером. Качественная сушилка стоит 1500–4000 ₽ и окупается сохранённой парой обуви.</p>
  </div>
</section>

<section id="natural">
  <h2>Естественная сушка</h2>
  <p>Самый безопасный, хотя и медленный метод — сушка при комнатной температуре. Вот как сделать её максимально эффективной:</p>
  <ul>
    <li><strong>Проветривание.</strong> Поставьте обувь в хорошо проветриваемое место, вдали от сквозняков.</li>
    <li><strong>Приподнимите пятку.</strong> Поставьте обувь на решётку или перевёрнутую тарелку — так воздух циркулирует и снизу.</li>
    <li><strong>Используйте впитывающие материалы.</strong> Влаговпитывающие пакетики (силикагель) ускорят сушку. Чем больше пакетиков, тем быстрее.</li>
    <li><strong>Меняйте положение.</strong> Поворачивайте обувь раз в 2–3 часа для равномерного высыхания.</li>
  </ul>
  <p>Естественная сушка занимает 12–24 часа. Планируйте время — если обувь промокла утром, к завтрашнему утру она будет сухой.</p>
</section>

<section id="smell">
  <h2>Как избежать запаха</h2>
  <p>Промокшая обувь без правильной сушки быстро обретает стойкий неприятный запах. Вот как его предотвратить:</p>
  <div data-type="compare">
    <article data-variant="good">
      <h4>Профилактика запаха</h4>
      <ul>
        <li>Сушите с силикагелем внутри</li>
        <li>Используйте антибактериальные стельки</li>
        <li>Обрабатывайте внутренность спреем-дезодорантом</li>
        <li>Сушите стельки отдельно от обуви</li>
        <li>Проветривайте обувь после сушки 2–3 часа</li>
      </ul>
    </article>
    <article data-variant="bad">
      <h4>Если запах уже появился</h4>
      <ul>
        <li>Замораживание — положите в пакет и в морозилку на ночь</li>
        <li>Сода — насыпьте внутрь на 3–4 часа, затем пропылесосьте</li>
        <li>Уксусный раствор (1:3 с водой) — протрите внутри</li>
        <li>Пробиотический спрей — устраняет бактерии</li>
        <li>Профессиональная дезинфекция</li>
      </ul>
    </article>
  </div>
