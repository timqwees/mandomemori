<section id="inspect">
  <h2>Осмотр и оценка состояния</h2>
  <p>Перед началом сезона достаньте зимнюю обувь из мест хранения и тщательно осмотрите. Выявлять проблемы лучше сейчас, чем в морозный день по дороге на работу.</p>
  <ul>
    <li><strong>Подошва.</strong> Проверьте целостность — трещины, отслоения, износ протектора. Если подошва стёрлась до гладкого состояния, её нужно менять. Скользкая подошва зимой опасна.</li>
    <li><strong>Набойки.</strong> На каблуках оцените степень износа набоек. Стертые набойки портят походку и могут повредить каблук.</li>
    <li><strong>Швы и молнии.</strong> Проверьте все швы на наличие расхождения. Молнии должны застёгиваться плавно, без заеданий.</li>
    <li><strong>Верх.</strong> Осмотрите материал на предмет трещин, потёртостей и повреждений. Особенно внимательно — места сгиба.</li>
  </ul>
  <div data-type="box" data-color="yellow">
    <h4>Когда к мастеру</h4>
    <p>Если вы нашли трещины на подошве, серьёзные повреждения швов или сломанную молнию — несите обувь в ремонт. В сезон мастера загружены, поэтому лучше сделать это заранее.</p>
  </div>
</section>

<section id="clean">
  <h2>Глубокая чистка</h2>
  <p>После хранения на обуви могла остаться пыль, старая грязь и остатки прошлогодних пропиток. Начните с тщательной чистки:</p>
  <ol>
    <li>Сухой щёткой удалите пыль из швов и складок.</li>
    <li>Для гладкой кожи используйте пену для чистки — нанесите, протрите влажной салфеткой.</li>
    <li>Для замши и нубука пройдитесь специальной щёткой-резенкой, чтобы поднять ворс.</li>
    <li>Подошву вымойте с мылом и старой зубной щёткой — в протекторе скапливается много грязи.</li>
    <li>Тщательно высушите обувь при комнатной температуре. На это уйдёт 12–24 часа.</li>
  </ol>
  <p>Пропитка наносится только на чистую обувь, иначе грязь «запечатается» под защитным слоем.</p>
</section>

<section id="repair">
  <h2>Мелкий ремонт</h2>
  <p>Даже мелкие дефекты зимой усугубляются. Лучше устранить их заранее:</p>
  <ul>
    <li><strong>Подклейте подошву.</strong> Если края подошвы отходят, используйте обувной клей (например, Desmokol или SAR 306).</li>
    <li><strong>Обновите цвет.</strong> Для кожаной обуви нанесите крем-краситель — он замаскирует потёртости и питает кожу.</li>
    <li><strong>Замените шнурки.</strong> Зимние шнурки изнашиваются быстро — замените их на новые, лучше с водоотталкивающей пропиткой.</li>
    <li><strong>Смажьте молнию.</strong> Восковой карандаш или силиконовая смазка для молний предотвратят заедание на морозе.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Лайфхак</h4>
    <p>Перед зимой обработайте кожаные швы водоотталкивающим воском — это защитит нитки от гниения и предотвратит промокание обуви по швам.</p>
  </div>
</section>

<section id="protection">
  <h2>Водоотталкивающая защита</h2>
  <p>Самый важный этап подготовки к зиме. Без правильной пропитки обувь будет впитывать влагу, реагенты и соль.</p>
  <h3>Типы пропиток</h3>
  <ul>
    <li><strong>Спреи на водной основе.</strong> Экологичные, подходят для всех материалов. Требуют частого обновления (раз в 1–2 недели).</li>
    <li><strong>Пропитки на фторполимерах.</strong> Эффективнее, держатся до месяца. Создают невидимый барьер на поверхности.</li>
    <li><strong>Воски и кремы.</strong> Идеальны для гладкой кожи. Питают, защищают и придают блеск. Требуют нанесения щёткой.</li>
  </ul>
  <h3>Правила нанесения</h3>
  <ol>
    <li>Наносите пропитку в хорошо проветриваемом помещении.</li>
    <li>Распыляйте с расстояния 15–20 см равномерно.</li>
    <li>После нанесения оставьте обувь на 15–20 минут для впитывания.</li>
    <li>Для лучшего эффекта нанесите 2–3 слоя с промежуточной сушкой.</li>
  </ol>
