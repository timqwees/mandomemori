<section id="storage">
  <h2>Хранение летней обуви</h2>
  <p>Перед зимой нужно правильно убрать на хранение летнюю обувь, чтобы весной она была в порядке:</p>
  <ul>
    <li>Вымойте и высушите кроссовки и туфли.</li>
    <li>Обработайте их защитным спреем перед хранением.</li>
    <li>Набейте бумагой для сохранения формы.</li>
    <li>Упакуйте в тканевые мешки или коробки с вентиляцией.</li>
    <li>Храните в сухом тёмном месте при температуре 15–20 °C.</li>
  </ul>
</section>

