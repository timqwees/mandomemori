<section id="why">
  <h2>Чем опасна соль для обуви</h2>
  <p>Техническая соль — это не просто NaCl. Современные антигололёдные смеси содержат хлориды кальция и магния, которые агрессивнее обычной соли.</p>
  <ul>
    <li><strong>Химический ожог кожи.</strong> Соль вытягивает влагу из натуральной кожи, делая её сухой, жёсткой и склонной к трещинам.</li>
    <li><strong>Разрушение красителей.</strong> Кристаллы соли обесцвечивают пигменты, оставляя белые разводы.</li>
    <li><strong>Повреждение швов.</strong> Соль разъедает нитки, особенно если они синтетические. Швы теряют прочность и могут разойтись.</li>
    <li><strong>Коррозия фурнитуры.</strong> Металлические люверсы, кнопки и молнии под воздействием соли ржавеют и перестают работать.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Факт</h4>
    <p>Соль начинает разрушать обувь уже через 2–3 часа контакта. Поэтому так важно мыть обувь сразу после возвращения домой, не оставляя соль на ночь.</p>
  </div>
</section>

<section id="barrier">
  <h2>Создание барьерного слоя</h2>
  <p>Лучшая защита — это барьер, который не даёт соли контактировать с материалом обуви. Создаётся он с помощью специальных средств:</p>
  <ol>
    <li><strong>Очистите обувь.</strong> Барьерный слой наносится только на чистую и сухую поверхность.</li>
    <li><strong>Нанесите базовый слой.</strong> Для кожи используйте питательный крем или воск. Он заполнит микропоры и не даст соли проникнуть внутрь материала.</li>
    <li><strong>Закрепите водонепроницаемым спреем.</strong> Фторполимерные спреи создают невидимую плёнку, которая отталкивает воду и растворённую в ней соль.</li>
    <li><strong>Особое внимание — швам.</strong> Обработайте стыки подошвы и верха воском или силиконовой смазкой.</li>
  </ol>
