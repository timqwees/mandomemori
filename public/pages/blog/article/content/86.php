<section id="daily">
  <h2>Ежедневная защита</h2>
  <p>Даже лучшая пропитка не панацея — соль агрессивна и постепенно разрушает любой барьер. Ежедневные ритуалы помогут снизить ущерб:</p>
  <ul>
    <li><strong>Мойте обувь сразу после прихода.</strong> Тёплая вода и мягкая щётка — лучшее средство от свежих следов соли.</li>
    <li><strong>Не трите сухую соль.</strong> Если разводы уже высохли, смочите салфетку водой с уксусом (1:10) и аккуратно промокните.</li>
    <li><strong>Обновляйте пропитку раз в 2–3 недели.</strong> Активная зима с частым контактом с реагентами требует частого обновления.</li>
    <li><strong>Используйте резиновые сапоги в сильный гололёд.</strong> Иногда практичность важнее стиля.</li>
  </ul>
</section>

<section id="remove">
  <h2>Как убрать соль, если она уже появилась</h2>
  <p>Если защита не сработала и на обуви появились белые разводы — действуйте быстро:</p>
  <h3>Для гладкой кожи</h3>
  <ol>
    <li>Разведите 1 ст. ложку уксуса в 1 стакане воды.</li>
    <li>Смочите мягкую ткань, протрите разводы.</li>
    <li>Через 5 минут протрите чистой влажной тканью.</li>
    <li>Высушите и нанесите питательный крем.</li>
  </ol>
  <h3>Для замши и нубука</h3>
  <ol>
    <li>Дождитесь полного высыхания обуви.</li>
    <li>Пройдитесь по разводам резиновой щёткой для замши.</li>
    <li>Если не помогло — обработайте паром и снова пройдитесь щёткой.</li>
    <li>Восстановите водоотталкивающую пропитку.</li>
  </ol>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Не используйте для удаления соли спирт, ацетон и другие растворители. Они растворят не только соль, но и краситель. Вода с уксусом — самый безопасный состав.</p>
  </div>
</section>

