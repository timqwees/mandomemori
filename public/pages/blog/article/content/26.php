<section id="general">
  <h2>Общие правила удаления пятен</h2>
  <p>Несколько универсальных принципов, которые работают для любых пятен и материалов:</p>
  <ul>
    <li><strong>Чем быстрее — тем лучше.</strong> Свежее пятно удаляется в 5–10 раз легче застарелого. Если заметили пятно на улице — промокните влажной салфеткой, не дожидаясь возвращения домой.</li>
    <li><strong>От краёв к центру.</strong> При удалении пятна двигайтесь от краёв к центру, чтобы не увеличить площадь загрязнения.</li>
    <li><strong>Не трите — промокайте.</strong> Трение втирает пятно глубже в волокна материала. Промокайте пятно чистой тканью, впитывая загрязнение.</li>
    <li><strong>Тест на незаметном участке.</strong> Прежде чем использовать любое средство, проверьте его на внутренней стороне язычка или задника.</li>
    <li><strong>От слабого к сильному.</strong> Начинайте с самых щадящих средств (вода, мыло) и переходите к более сильным, если первое не сработало.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Что должно быть в арсенале</h4>
    <p>Влажные салфетки (без спирта), микрофибра, ватные диски, перекись водорода 3%, столовый уксус 9%, тальк или крахмал, сода, мягкая щётка. Из специализированных средств — пятновыводитель для обуви и пенный очиститель.</p>
  </div>
</section>

<section id="grease">
  <h2>Жирные и масляные пятна</h2>
  <p>Жир — одно из самых сложных загрязнений. Он глубоко проникает в материал и оставляет тёмный след.</p>
  <h3>Свежие жирные пятна</h3>
  <ol>
    <li>Сразу посыпьте пятно тальком, кукурузным крахмалом или детской присыпкой.</li>
    <li>Оставьте на 30–60 минут — порошок впитает жир.</li>
    <li>Удалите порошок мягкой щёткой.</li>
    <li>Если пятно осталось — повторите или переходите к мыльному раствору.</li>
  </ol>
  <h3>Застарелые жирные пятна</h3>
  <p>Для старых жирных пятен используйте средство для мытья посуды: нанесите каплю на влажную губку, вспеньте и аккуратно обработайте пятно. Затем смойте водой. Для замши и нубука этот метод не подходит — используйте только сухие абсорбенты.</p>
  <div data-type="box" data-color="green">
    <h4>Лайфхак для замши</h4>
    <p>Если на замше появилось жирное пятно, прогрейте его феном на средней температуре (10–15 см, 2–3 минуты). Тёплый жир станет жидким, и тальк впитает его эффективнее. Затем посыпьте тальком на ночь.</p>
  </div>
</section>

<section id="organic">
  <h2>Органические пятна (трава, кофе, вино)</h2>
  <p>Органические пятна — от травы, кофе, вина, ягод — содержат пигменты, которые проникают глубоко в структуру материала.</p>
  <h3>Трава</h3>
  <p>Смешайте перекись водорода 3% с водой в пропорции 1:1. Нанесите на пятно ватным диском, оставьте на 5–10 минут. Промокните влажной тканью. Для белой обуви можно использовать слабый раствор хлорного отбеливателя (только для текстиля!).</p>
  <h3>Кофе и чай</h3>
  <p>Смешайте уксус 9% с водой 1:2. Нанесите на пятно, через 5 минут промокните. Для светлой обуви используйте лимонный сок — он отбеливает и нейтрализует запах.</p>
  <h3>Вино (красное)</h3>
  <p>Срочно засыпьте пятно солью — она впитает влагу и пигмент. Через 10 минут стряхните соль, обработайте пятно перекисью водорода 3% (для светлой обуви) или спиртом (для тёмной).</p>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Не используйте на замше и нубуке уксус и лимонный сок — они разъедают ворс и обесцвечивают материал. Для воровых поверхностей органические пятна удаляйте только специальными пенами для замши.</p>
  </div>
</section>

<section id="salt">
  <h2>Соль и реагенты</h2>
  <p>Белые разводы от соли — зимняя проблема №1. Соль проникает в поры материала и кристаллизуется, разрушая структуру.</p>
  <h3>Как удалить</h3>
  <ol>
    <li>Смешайте столовый уксус 9% с водой в пропорции 1:3.</li>
    <li>Смочите ватный диск или ткань в растворе.</li>
    <li>Протрите разводы. Не лейте раствор на обувь — только на диск.</li>
    <li>Удалите остатки влажной тканью.</li>
    <li>Высушите и нанесите защитную пропитку.</li>
  </ol>
  <p>Для замши и нубука используйте более слабый раствор (1:5) и только на ватный диск, не допуская намокания материала.</p>
</section>

<section id="chemical">
  <h2>Чернила, краска и клей</h2>
  <p>Чернила от ручки, краска и клей — «химические» пятна, которые требуют специальных растворителей.</p>
  <h3>Чернила</h3>
  <p>Используйте спирт или медицинский антисептик (без красителей). Нанесите на ватный диск, промокните пятно. Не трите — чернила размажутся на большую площадь. Меняйте диск по мере загрязнения. Для светлой кожи после удаления чернил обработайте участок перекисью водорода.</p>
  <h3>Краска</h3>
  <p>Водоэмульсионная краска смывается водой, если она свежая. Засохшую краску удаляйте уайт-спиритом (только для гладкой кожи!). Для текстиля используйте специальный пятновыводитель.</p>
  <h3>Клей</h3>
  <p>Суперклей удаляется ацетоном или жидкостью для снятия лака (только для гладкой кожи!). Для текстиля попробуйте заморозить обувь — после заморозки клей становится хрупким и откалывается.</p>
