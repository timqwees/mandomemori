<section id="materials">
  <h2>Особенности материалов Samba и Gazelle</h2>
  <p>Adidas Samba и Gazelle имеют схожую конструкцию, но с разными нюансами. Понимание материалов — первый шаг к правильной чистке:</p>
  <ul>
    <li><strong>Samba.</strong> Кожаный верх с замшевой нашлёпкой на носке (suede toe). Подошва — цельная резина слегка жёлтого оттенка (это фишка модели, не пытайтесь её отбелить добела).</li>
    <li><strong>Gazelle.</strong> Замшевый верх (в классических версиях) или кожа с замшевыми вставками. Подошва более плоская, чем у Samba, с характерной резиновой полосой.</li>
    <li><strong>Общее.</strong> У обеих моделей — замшевые элементы, которые требуют сухой чистки и не терпят воды. Кожаные части можно мыть, но с оговорками.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важный нюанс</h4>
    <p>Подошва Samba имеет характерный молочно-жёлтый оттенок с самого начала — это дизайн, а не дефект. Не пытайтесь отбелить её добела, иначе испортите аутентичный вид. Чистите подошву Samba только мыльной водой без отбеливателей.</p>
  </div>
</section>

<section id="suede">
  <h2>Чистка замшевого носка</h2>
  <p>Замшевый носок — самая заметная часть обеих моделей и самая уязвимая. Замша впитывает грязь и влагу, теряет цвет и засаливается. Вот как правильно за ней ухаживать:</p>
  <h3>Сухая чистка (регулярная)</h3>
  <ol>
    <li>Пройдитесь по замше специальной щёткой с латунной щетиной. Движения — только в одном направлении, чтобы поднять ворс.</li>
    <li>Удалите пыль и поверхностную грязь. Если пятен нет — достаточно.</li>
  </ol>
  <h3>Влажная чистка (при загрязнениях)</h3>
  <ol>
    <li>Используйте специальную пену для замши. Нанесите на губку, вспеньте и аккуратно протрите замшу.</li>
    <li>Не лейте воду на замшу — только пену. Вода затекает под кожу и размягчает клей.</li>
    <li>Промокните остатки пены сухой микрофиброй. Не трите — просто прижмите.</li>
    <li>Высушите при комнатной температуре, потом поднимите ворс щёткой.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Секрет профессионалов</h4>
    <p>Для защиты замши Samba и Gazelle используйте водоотталкивающий спрей специально для замши. Наносите его в два слоя с промежуточной сушкой 30 минут. Обновляйте защиту раз в 2–3 недели — это предотвратит появление трудновыводимых пятен.</p>
  </div>
</section>

<section id="leather">
  <h2>Уход за кожаными элементами</h2>
  <p>Кожа на Samba и Gazelle — гладкая, но тонкая. Она требует питания и защиты, иначе появляются трещины на сгибах. Чистите кожу после того, как закончили с замшем, чтобы случайно не перенести грязь и влагу на замшу.</p>
  <h3>Что использовать</h3>
  <ul>
    <li>Пенное мыло или специализированный очиститель для гладкой кожи</li>
    <li>Мягкая микрофибра — для нанесения и полировки</li>
    <li>Питающий крем нейтрального цвета — для восстановления эластичности</li>
  </ul>
  <h3>Процесс чистки</h3>
  <ol>
    <li>Нанесите пену на микрофибру и протрите кожу круговыми движениями.</li>
    <li>Удалите остатки пены чистой влажной тканью.</li>
    <li>Дайте коже высохнуть естественным путём.</li>
    <li>Нанесите питающий крем тонким слоем, через 15 минут отполируйте.</li>
  </ol>
  <p>Для Gazelle из замши (полностью замшевый верх) не используйте крем — он испортит текстуру. Такие модели чистятся только сухим методом.</p>
</section>

<section id="sole">
  <h2>Чистка резиновой подошвы</h2>
  <p>Подошвы Samba и Gazelle легко чистить: резина плотная, не впитывает грязь. Но есть нюанс — белая полоса по периметру подошвы быстро темнеет.</p>
  <ul>
    <li><strong>Ежедневно.</strong> Протирайте подошву влажной тряпкой после каждой носки. Особенно белую полосу.</li>
    <li><strong>Еженедельно.</strong> Чистите подошву щёткой со средним ворсом в мыльной воде.</li>
    <li><strong>При загрязнениях.</strong> Используйте ластик для замши — он отлично удаляет тёмные полосы с белой резины.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Не используйте абразивные чистящие средства и металлические щётки на подошве Samba и Gazelle. Они стирают верхний слой резины, и подошва начинает быстрее пачкаться и желтеть.</p>
  </div>
</section>

<section id="laces">
  <h2>Уход за шнурками и стельками</h2>
  <p>Шнурки и стельки — недооценённые детали, которые сильно влияют на общее впечатление. На Samba и Gazelle шнурки обычно белые, и они пачкаются быстрее всего.</p>
  <ul>
    <li><strong>Стирка шнурков.</strong> Замочите в тёплой воде с отбеливателем для белья на 20–30 минут. Потрите руками. Не используйте машинку — шнурки Samba короткие и легко деформируются.</li>
    <li><strong>Замена шнурков.</strong> Лучший способ освежить вид — купить новые шнурки. Они дёшевы, а эффект — как от новой пары.</li>
    <li><strong>Стельки.</strong> Промойте в мыльном растворе, высушите при комнатной температуре. Если стельки потеряли форму или появился запах — замените на новые.</li>
  </ul>
</section>

<section id="mistakes">
  <h2>Типичные ошибки при чистке Samba и Gazelle</h2>
  <p>Владельцы этих моделей часто допускают одни и те же ошибки. Мы собрали топ-5, чтобы вы их избежали:</p>
  <ul>
    <li><strong>Мочат замшу водой.</strong> Вода на замше — гарантия разводов. Только пена или сухая чистка.</li>
    <li><strong>Сушат на батарее.</strong> От высокой температуры клей размягчается, замша становится жёсткой, кожа трескается.</li>
    <li><strong>Трут замшу влажной щёткой.</strong> Мокрая щётка размазывает грязь, а не удаляет её. Используйте сухую или слегка влажную.</li>
    <li><strong>Используют крем для кожи на замше.</strong> Крем забивает поры замши, и она становится липкой и блестящей. Восстановить такую замшу почти невозможно.</li>
    <li><strong>Пытаются отбелить подошву Samba.</strong> Подошва Samba жёлтая от природы. Отбеливание лишит кроссовки аутентичного вида.</li>
  </ul>
