    <h4>Запомните</h4>
    <p>Samba и Gazelle — не спортивная обувь, а стильная классика. Относитесь к ним соответственно: не носите в слякоть, чистите деликатно и обновляйте защиту регулярно. Если сомневаетесь — доверьте чистку мастерам MANDO MEMORI.</p>
