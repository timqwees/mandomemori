<section id="types">
  <h2>Типы кожи для сумок</h2>
  <p>Уход зависит от типа кожи. Разные виды требуют разных средств:</p>
  <ul>
    <li><strong>Гладкая кожа.</strong> Наиболее популярный тип. Устойчива к загрязнениям, легко чистится. Требует регулярного питания кремом для предотвращения трещин.</li>
    <li><strong>Зернистая кожа (Saffiano, Pebble).</strong> Имеет текстуру, которая маскирует царапины. Чистится пеной для кожи, не требует частой полировки.</li>
    <li><strong>Лаковая кожа.</strong> Требует максимально деликатного ухода — только мягкая салфетка, никаких кремов и спирта.</li>
    <li><strong>Замша и нубук.</strong> Капризные материалы — чистятся только сухими щётками и специальными ластиками. Водоотталкивающая пропитка обязательна.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Перед использованием любого средства протестируйте его на незаметном участке сумки (внутренний карман или нижняя часть). Некоторые составы могут обесцветить или повредить кожу.</p>
  </div>
</section>

<section id="daily">
  <h2>Ежедневный уход</h2>
  <p>Чтобы кожаная сумка выглядела дорого и долго служила, достаточно трёх ежедневных правил:</p>
  <ul>
    <li><strong>Не перегружайте.</strong> Кожа растягивается под весом содержимого. Носите только необходимое — тяжёлая сумка деформируется быстрее.</li>
    <li><strong>Протирайте после каждого использования.</strong> Мягкой сухой салфеткой удалите пыль. Если сумка намокла — промокните бумажным полотенцем и высушите при комнатной температуре.</li>
    <li><strong>Храните правильно.</strong> В тканевом мешке с наполнителем внутри для сохранения формы. Не вешайте пустую сумку на крючок за ручку — это растягивает кожу.</li>
  </ul>
</section>

<section id="deep">
  <h2>Глубокая чистка</h2>
  <p>Раз в 3–6 месяцев проводите глубокую чистку сумки. Это удаляет загрязнения, которые не смываются ежедневным протиранием:</p>
  <ol>
    <li>Освободите сумку от содержимого, вытряхните пыль из уголков.</li>
    <li>Приготовьте тёплый мыльный раствор (специальное средство для кожи или мягкое мыло).</li>
    <li>Смочите мягкую салфетку в растворе, тщательно отожмите (салфетка должна быть влажной, не мокрой).</li>
    <li>Протрите всю поверхность сумки, включая ручки и ремень.</li>
    <li>Чистой влажной салфеткой удалите остатки мыла.</li>
    <li>Высушите при комнатной температуре, набив бумагой для сохранения формы.</li>
    <li>После высыхания нанесите питательный крем для кожи.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Не мочите кожаную сумку полностью — вода размягчает кожу и может испортить форму. Чистите только поверхность влажной салфеткой, не заливая швы и внутреннюю часть.</p>
  </div>
</section>

<section id="stains">
  <h2>Удаление пятен</h2>
  <p>Тип пятна определяет метод удаления. Универсальных решений нет:</p>
  <div data-type="compare">
    <article data-variant="good">
      <h4>Как удалять пятна</h4>
      <ul>
        <li>Жир — промокните салфеткой, посыпьте тальком на 2 часа</li>
        <li>Чернила — ватная палочка со спиртом (только на гладкой коже)</li>
        <li>Вода (разводы) — протрите всей поверхностью влажной салфеткой</li>
        <li>Краска — профессиональный пятновыводитель для кожи</li>
        <li>Плесень — уксусный раствор (1:10) + проветривание</li>
      </ul>
    </article>
    <article data-variant="bad">
      <h4>Чего нельзя делать</h4>
      <ul>
        <li>Тереть пятно — оно размазывается глубже</li>
        <li>Использовать ацетон — он растворяет краситель</li>
        <li>Мыть замшу водой — останутся разводы</li>
        <li>Стирать сумку в машинке</li>
        <li>Использовать отбеливатели</li>
      </ul>
    </article>
  </div>
</section>

<section id="restore">
  <h2>Реставрация цвета</h2>
  <p>Со временем кожа теряет насыщенность цвета, особенно на ручках и в местах частого касания. Вернуть цвет можно несколькими способами:</p>
  <ul>
    <li><strong>Крем-краситель.</strong> Универсальное средство. Наносится на всю поверхность, питает и окрашивает одновременно. Подходит для обновления цвета раз в 3–6 месяцев.</li>
    <li><strong>Аэрозольная краска.</strong> Для кардинальной смены цвета или сильного выцветания. Требует подготовки поверхности и равномерного нанесения.</li>
    <li><strong>Профессиональная покраска.</strong> Для дорогих сумок. В MANDO MEMORI мы используем итальянские красители, которые проникают в структуру кожи и восстанавливают цвет на 100%.</li>
  </ul>
