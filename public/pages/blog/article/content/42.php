<section id="glue">
  <h2>Какой клей выбрать для подошвы</h2>
  <p>От выбора клея зависит 80% успеха ремонта. Универсальные суперклеи и ПВА не подходят — они либо хрупкие, либо не выдерживают нагрузку при ходьбе.</p>
  <ul>
    <li><strong>Полиуретановый клей (Desmokol, SAR 306, TOPEX).</strong> Лучший выбор для ремонта подошвы. Эластичный, водостойкий, выдерживает высокие нагрузки. Требует активации теплом (феном).</li>
    <li><strong>Контактный клей (Nairit, 88-СА).</strong> Подходит для резиновых подошв. Наносится на обе поверхности, после подсыхания детали соединяются. Работает без нагрева.</li>
    <li><strong>Эпоксидный клей.</strong> Используется для заполнения трещин и восстановления сколов. Твёрдый, неэластичный — не подходит для гибких участков подошвы.</li>
    <li><strong>Цианакрилатные клеи (суперклей).</strong> Только для мелкого ремонта — ими можно зафиксировать небольшой отслоившийся край. Не выдерживают нагрузку.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Какой клей точно не подходит</h4>
    <p>ПВА, Момент-гель, обычный канцелярский клей, клей для дерева. Все эти составы не выдерживают влаги, перепадов температур и нагрузок при ходьбе. Подошва отвалится в первый же дождливый день.</p>
  </div>
</section>

<section id="prep">
  <h2>Подготовка поверхности — ключевой этап</h2>
  <p>Неправильная подготовка — причина 90% неудачных ремонтов. Клей не держится на грязной, жирной или гладкой поверхности.</p>
  <h3>Что нужно сделать</h3>
  <ol>
    <li><strong>Очистите поверхности.</strong> Удалите остатки старого клея механически (наждачкой или ножом). Промойте ацетоном или уайт-спиритом для обезжиривания.</li>
    <li><strong>Зашершавьте.</strong> Пройдитесь наждачной бумагой средней зернистости (P80–P120) по обеим склеиваемым поверхностям. Клей должен проникать в микропоры.</li>
    <li><strong>Обеспыльте.</strong> Сдуйте пыль и протрите чистой тканью, смоченной в ацетоне. Не касайтесь подготовленной поверхности пальцами.</li>
    <li><strong>Просушите.</strong> Поверхности должны быть абсолютно сухими. Используйте фен (холодный обдув) для ускорения.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Для лучшей адгезии сделайте насечки на гладких поверхностях (например, на полиуретановой подошве) острым ножом в виде сетки. Глубина насечек — 1–2 мм.</p>
  </div>
</section>

<section id="stepbystep">
  <h2>Пошаговая инструкция склеивания подошвы</h2>
  <p>После подготовки приступайте к склеиванию. Используем полиуретановый клей — самый надёжный вариант.</p>
  <ol>
    <li>Нанесите клей тонким слоем на обе склеиваемые поверхности. Не жалейте клей, но и не наносите слишком толстый слой.</li>
    <li>Оставьте на 10–15 минут для подсыхания (до отлипа — клей перестаёт прилипать к пальцу, но остаётся липким).</li>
    <li>Прогрейте склеиваемые поверхности строительным феном (температура 80–100 °C) в течение 30–60 секунд. Клей должен стать прозрачным.</li>
    <li>Соедините детали и плотно прижмите. Особое внимание — краям. Используйте зажимы, струбцины или просто плотно привяжите обувь бинтом.</li>
    <li>Оставьте под нагрузкой на 12–24 часа для полной полимеризации клея.</li>
    <li>Через сутки проверьте прочность шва — попробуйте отогнуть край подошвы. Если держится крепко — обувь готова к носке.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Техника безопасности</h4>
    <p>Работайте в проветриваемом помещении. Используйте резиновые перчатки — полиуретановый клей сложно отмыть с кожи. При нагревании феном не перегревайте — горячий клей может выделять едкие пары.</p>
  </div>
</section>

<section id="crack">
  <h2>Ремонт трещин на подошве</h2>
  <p>Если подошва не отклеилась, а треснула — подход другой. Трещины обычно появляются на сгибе стопы и могут пропускать воду.</p>
  <h3>Методы ремонта трещин</h3>
  <ul>
    <li><strong>Метод 1 — заливка.</strong> Трещина расшивается V-образно, зачищается и обезжиривается. Затем заполняется полиуретановым клеем или эпоксидной смолой. После застывания излишки срезаются.</li>
    <li><strong>Метод 2 — заплатка.</strong> С внутренней стороны подошвы накладывается заплатка из резины или полиуретана на клей. Снаружи трещина маскируется герметиком для обуви.</li>
    <li><strong>Метод 3 — горячая вулканизация.</strong> Для сырой резины. Трещина заполняется сырой резиновой смесью и вулканизируется при высокой температуре. Требует специального оборудования — только в мастерской.</li>
  </ul>
  <p>Самый надёжный способ ремонта трещины — через мастерскую. Домашние методы дают временный результат.</p>
