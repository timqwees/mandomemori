<section id="partial">
  <h2>Частичное восстановление изношенной подошвы</h2>
  <p>Если подошва стёрлась с одной стороны (например, на пятке или носке), её можно восстановить без полной замены. Технология называется наращивание или локальная профилактика.</p>
  <h3>Процесс наращивания</h3>
  <ol>
    <li>Изношенный участок зачищается до ровной поверхности.</li>
    <li>Подбирается материал-донор (резина, полиуретан) нужной толщины.</li>
    <li>Заплатка вырезается по форме изношенного участка.</li>
    <li>Фиксируется на полиуретановый клей под прессом.</li>
    <li>После полимеризации края шлифуются заподлицо с родной подошвой.</li>
  </ol>
  <p>Метод отлично работает, если износ локальный и не превышает 3–4 мм по глубине. Если подошва стёрта равномерно по всей площади — проще установить профилактику на всю поверхность.</p>
  <div data-type="box" data-color="dark">
    <h4>Сравните затраты</h4>
    <p>Локальное наращивание стоит 500–1000 ₽. Профилактика (накладка на всю подошву) — 1000–2500 ₽. Полная замена подошвы — 2500–5000 ₽. Своевременная профилактика в 3–5 раз дешевле капитального ремонта.</p>
  </div>
</section>

<section id="mistakes">
  <h2>Типичные ошибки при ремонте подошвы</h2>
  <p>Даже опытные домашние мастера допускают ошибки. Вот самые распространённые:</p>
  <ul>
    <li><strong>Плохое обезжиривание.</strong> Если пропустить этот этап, клей не схватится. Используйте ацетон, уайт-спирит или специальный очиститель.</li>
    <li><strong>Склеивание без нагрева.</strong> Полиуретановый клей без активации теплом даёт только 30–40% прочности. Прогрев феном обязателен.</li>
    <li><strong>Слишком тонкий слой клея.</strong> Клей должен покрывать всю поверхность равномерно. Пропуски приводят к отслаиванию.</li>
    <li><strong>Недостаточное время выдержки.</strong> Полимеризация полиуретана занимает 12–24 часа. Если начать носить обувь через час — подошва отвалится.</li>
    <li><strong>Использование неподходящего клея.</strong> ПВА, «Момент», суперклей — не работают на обуви. Только полиуретановый или контактный клей.</li>
  </ul>
