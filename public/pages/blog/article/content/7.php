<section id="summer">
  <h2>Летний уход: почему экокожа трескается на жаре</h2>
  <p>Лето — самый опасный сезон для обуви из экокожи. В 2026 году аномальная жара становится нормой, и мастерские по ремонту обуви отмечают резкий рост обращений с треснувшей экокожей. Почему это происходит?</p>
  <p>Полиуретан — термопластичный полимер. При температуре выше 50°C (а на раскалённом асфальте или в закрытой машине температура достигает 60–70°C) полиуретан размягчается, а затем при остывании становится хрупким. Повторные циклы нагрева и охлаждения приводят к микротрещинам, которые со временем превращаются в глубокие разрывы.</p>
  <ul>
    <li><strong>Не носите экокожу в жару выше 35°C.</strong> Особенно это касается светлых оттенков — они сильнее нагреваются на солнце.</li>
    <li><strong>Не храните обувь в машине летом.</strong> В закрытом авто температура поднимается до 70°C — экокожа не выдерживает и одного сезона.</li>
    <li><strong>Используйте кондиционеры для экокожи.</strong> Специальные кондиционеры на водной основе с УФ-защитой продлевают срок службы полиуретана.</li>
    <li><strong>Чередуйте обувь.</strong> Не носите одну пару из экокожи два дня подряд — давайте материалу «отдыхать» и восстанавливать форму.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Совет для лета</h4>
    <p>В жаркую погоду отдавайте предпочтение обуви из натуральной кожи или текстиля. Экокожа хороша для демисезона, но для лета 35°C+ выбирайте дышащие материалы.</p>
  </div>
</section>

<section id="storage">
  <h2>Правила хранения кожаной обуви</h2>
  <p>Правильное хранение продлевает срок службы обуви в 2–3 раза. Вот основные правила:</p>
  <ul>
    <li><strong>Колодки и распорки.</strong> Используйте деревянные колодки для сохранения формы. Они впитывают влагу и предотвращают деформацию. Для экокожи — пластиковые колодки (дерево может окрасить светлую экокожу).</li>
    <li><strong>Тканевые мешки.</strong> Храните обувь в дышащих мешках из хлопка или флиса. Полиэтиленовые пакеты и пластиковые коробки без вентиляции создают парниковый эффект — появляется плесень.</li>
    <li><strong>Тёмное место.</strong> Храните обувь в тёмном шкафу или коробке. Свет, особенно солнечный, выцветает кожу и разрушает полиуретан.</li>
    <li><strong>Стабильная температура.</strong> Идеальные условия — 18–25°C, влажность 40–60%. Избегайте перепадов температур — они вредят и натуральной коже, и экокоже.</li>
    <li><strong>Вертикальное хранение.</strong> Не храните сапоги и высокие ботинки согнутыми в голенище — остаются заломы, которые не расправляются. Используйте специальные держатели для голенища.</li>
  </ul>
  <p>Для сезонного хранения очистите обувь, обработайте защитными средствами и набейте бумагой или колодками. Раз в месяц проветривайте — открывайте коробку на несколько часов.</p>
</section>

