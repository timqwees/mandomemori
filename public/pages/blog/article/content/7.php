<section id="types">
  <h2>Виды кожи: натуральная, спилок, экокожа</h2>
  <p>Прежде чем говорить об уходе, важно понимать, с каким материалом вы имеете дело. Три основных типа кожи используются в обуви сегодня:</p>
  <ul>
    <li><strong>Натуральная кожа (full-grain, top-grain).</strong> Изготавливается из шкуры животного с сохранением естественной структуры. Дышит, принимает форму стопы, со временем становится только удобнее. Требует регулярного питания маслами и кремами. Срок службы — 5–15 лет. Используется в премиум-сегменте: Loake, Church's, Tricker's, некоторые модели Dr. Martens.</li>
    <li><strong>Спилок (genuine leather, bonded leather).</strong> Верхний слой кожи срезается, остаётся более рыхлый нижний слой. Его прессуют с полимерными связующими. Меньше дышит, быстрее изнашивается, но стоит значительно дешевле. Часто встречается в обуви среднего сегмента (Zara, H&M, Mango).</li>
    <li><strong>Экокожа (полиуретановая, микрофибра).</strong> Искусственный материал на тканевой основе с полиуретановым покрытием. Не пропускает воду, эластичная, лёгкая, но не дышит. Главный недостаток — полиуретан со временем разрушается под воздействием УФ-лучей и высокой температуры. Срок службы — 2–4 года.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Как отличить натуральную кожу от экокожи</h4>
    <p>Капните водой на поверхность: натуральная кожа впитает воду и потемнеет, на экокоже вода останется каплей. Также можно проверить по срезу — у натуральной кожи край ворсистый и неоднородный, у экокожи ровный и гладкий.</p>
  </div>
</section>

<section id="leather">
  <h2>Уход за обувью из натуральной кожи</h2>
  <p>Натуральная кожа — живой материал, который требует регулярного питания и защиты. Без ухода она высыхает, трескается и теряет эластичность:</p>
  <ol>
    <li><strong>Очистка после каждой носки.</strong> Удаляйте пыль и грязь мягкой сухой тканью или щёткой. Влажная чистка — только при сильных загрязнениях, иначе кожа теряет естественные масла.</li>
    <li><strong>Питание кремом.</strong> Раз в 2–3 недели наносите питательный крем или бальзам для кожи. Он восстанавливает жировой баланс, предотвращает пересыхание и трещины. Наносите тонким слоем, втирайте круговыми движениями, через 15 минут полируйте мягкой тканью.</li>
    <li><strong>Защита от воды.</strong> Перед сезоном дождей обработайте обувь водоотталкивающим спреем или воском. Для гладкой кожи — воски и кремы с добавлением пчелиного воска, для замши и нубука — аэрозольные пропитки.</li>
    <li><strong>Полировка.</strong> Регулярная полировка не только придаёт блеск, но и создаёт защитный слой. Используйте специальные щётки и бархотки. Техника: нанесите крем, дайте впитаться, затем полируйте энергичными движениями до блеска.</li>
    <li><strong>Глубокая чистка.</strong> Раз в 3–4 месяца — тщательная чистка с использованием специальных пенок или шампуней для кожи. После чистки обязательно нанесите питательный крем.</li>
  </ol>
  <div data-type="box" data-color="blue">
    <h4>Важно</h4>
    <p>Не используйте для натуральной кожи средства с силиконом — они создают плёнку, которая блокирует поры и препятствует дыханию материала. Кожа быстро пересыхает и трескается под силиконовой плёнкой.</p>
  </div>
  <p>Натуральная кожа со временем темнеет и приобретает патину — это естественный процесс, который придаёт обуви уникальный характер. Не пытайтесь отбелить потемневшие участки агрессивной химией.</p>
</section>

<section id="eco">
  <h2>Уход за обувью из экокожи</h2>
  <p>Экокожа менее прихотлива, но и менее устойчива к износу. Главное правило — не агрессивная химия и не высокие температуры:</p>
  <ul>
    <li><strong>Мягкая влажная чистка.</strong> После каждой носки протирайте экокожу влажной микрофиброй. Сильные загрязнения удаляйте слабым мыльным раствором. Не трите жёсткой щёткой — повредите полиуретановое покрытие.</li>
    <li><strong>Без масел и жирных кремов.</strong> Экокожа не впитывает масла — они остаются на поверхности, притягивают пыль и создают липкую плёнку. Используйте только специальные средства для экокожи на водной основе.</li>
    <li><strong>Защита от УФ-лучей.</strong> Полиуретан разрушается под солнцем. Не оставляйте обувь из экокожи на подоконнике или под прямыми солнечными лучами. Для защиты используйте спреи с УФ-фильтрами.</li>
    <li><strong>Без переувлажнения.</strong> Не мочите экокожу полностью. Вода проникает через швы и тканевую подкладку, а сохнет экокожа очень долго — внутри может появиться плесень.</li>
    <li><strong>Освежение цвета.</strong> Для восстановления цвета используйте спреи-красители для экокожи. Они наносятся аэрозолем и восстанавливают верхний слой полиуретана.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Чего нельзя делать с экокожей</h4>
    <p>× Использовать кремы и масла для натуральной кожи — они разрушают полиуретан.</p>
    <p>× Тереть жёсткими щётками и абразивными губками — остаются царапины.</p>
    <p>× Сушить на батарее — экокожа трескается при температуре выше 40°C.</p>
    <p>× Использовать спиртосодержащие средства — растворяют полиуретановое покрытие.</p>
  </div>
</section>

