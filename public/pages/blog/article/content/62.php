<section id="styling">
  <h2>Как носить gorpcore: правила сочетания</h2>
  <p>Главное правило gorpcore — баланс. Походная обувь не должна превращать вас в туриста. Вот базовые принципы:</p>
  <h3>С чем носить трейловые кроссовки</h3>
  <ul>
    <li><strong>Широкие брюки карго.</strong> Классика gorpcore. Штаны закрывают верх кроссовка, создавая плавный силуэт. Ткань — хлопок или технический материал.</li>
    <li><strong>Прямые джинсы.</strong> Простой и понятный вариант. Джинсы должны быть свободными, не облегающими. Лучше светлые или серые.</li>
    <li><strong>Флисовая кофта + ветровка.</strong> Верх в стиле 90-х: объёмный флис, сверху лёгкая куртка-ветровка. Цвета — яркие или пастельные.</li>
    <li><strong>Шорты + гетры.</strong> Для тёплой погоды — шорты до колена и высокие гетры (как у трейлраннеров). Выглядит необычно и очень актуально.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Чего избегать</h4>
    <p>Не надевайте трейловые кроссовки с узкими брюками-скинни или классическими костюмными брюками — это создаёт диссонанс. Также избегайте «тотального лук»: полный походный комплект с курткой-пуховиком, штанами-карго и трекинговыми ботинками выглядит как подготовка к восхождению, а не как стильный образ.</p>
  </div>
</section>

<section id="mistakes">
  <h2>Типичные ошибки в gorpcore</h2>
  <p>Даже стильный тренд можно испортить неправильным подходом. Вот что делают многие:</p>
  <ul>
    <li><strong>Полный аутфит только из походной экипировки.</strong> Выглядит как поход, а не как мода. Разбавляйте gorpcore базовыми вещами: простая футболка, обычные джинсы.</li>
    <li><strong>Слишком агрессивные цвета в одном образе.</strong> Один яркий акцент — хорошо. Кроссовки кислотного цвета + флисовая куртка такого же цвета = перебор.</li>
    <li><strong>Грязная обувь.</strong> Городской gorpcore подразумевает, что кроссовки выглядят ухоженно. Налипшая грязь на протекторе — не стильно, а небрежно.</li>
  </ul>
  <div data-type="box" data-color="blue">
    <h4>Золотое правило</h4>
    <p>Одна gorpcore-вещь на образ. Кроссовки в стиле gorpcore + обычные джинсы + простая футболка = идеальный баланс.</p>
  </div>
</section>

<section id="care">
  <h2>Уход за трейловыми кроссовками в городе</h2>
  <p>Трекинговые кроссовки в городской среде сталкиваются с другими вызовами, чем в горах. Асфальт, пыль, реагенты, грязь — всё это влияет на материалы быстрее, чем в природных условиях.</p>
  <h3>Особенности чистки</h3>
  <ul>
    <li><strong>Мембрана Gore-Tex.</strong> Чистите только специальными средствами для мембраны. Обычное мыло забивает поры и нарушает водоотталкивающие свойства.</li>
    <li><strong>Протектор подошвы.</strong> Камни и песок застревают между грунтозацепами. После каждой носки проверяйте и очищайте подошву от застрявших частиц.</li>
    <li><strong>Сетка верха.</strong> Трекинговая сетка прочная, но грязь въедается в неё быстро. Используйте пенную чистку без сильного трения.</li>
    <li><strong>Восстановление DWR.</strong> После каждой 3–4 чистки обновляйте водоотталкивающую пропитку спреем или нанесением в стиральной машине.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Совет по сушке</h4>
    <p>Никогда не сушите трейловые кроссовки на батарее — мембрана разрушается от высокой температуры. Используйте распорки и сушите при комнатной температуре 24–48 часов.</p>
  </div>
</section>

