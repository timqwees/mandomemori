<section id="what">
  <h2>Что такое gorpcore</h2>
  <p>Gorpcore — стиль, в котором функциональная походная экипировка становится частью повседневного городского гардероба. Название происходит от «GORP» — Good Old Raisins and Peanuts, традиционного перекуса туристов. Тренд зародился в конце 2010-х, а пик популярности переживает именно сейчас.</p>
  <p>В отличие от «техно-стиля» или «утилитарной моды», gorpcore не просто заимствует отдельные элементы — он целиком переносит эстетику outdoor в город. Это не стилизация, а реальное использование функциональной одежды и обуви в повседневной жизни.</p>
  <p>Ключевые элементы: трекинговые кроссовки, флисовые кофты, куртки-ветровки, брюки карго, технические рюкзаки. Цвета — яркие, кислотные, с акцентом на функциональность.</p>
  <div data-type="box" data-color="gray">
    <h4>Корни тренда</h4>
    <p>Gorpcore во многом обязан revival интереса к брендам вроде Patagonia, Arc‘teryx и Salomon, которые раньше ассоциировались исключительно с альпинизмом, а теперь носятся на показах и в офисах.</p>
  </div>
</section>

<section id="why">
  <h2>Почему трейловые кроссовки стали трендом</h2>
  <p>Несколько факторов совпали, чтобы вывести трейловую обувь в мейнстрим:</p>
  <ul>
    <li><strong>Функциональность как стиль.</strong> Современная городская мода ценит удобство. Gore-Tex мембрана, агрессивный протектор, шнуровка быстрой фиксации — всё это реально полезно в городе, особенно в межсезонье.</li>
    <li><strong>Эстетика 2000-х.</strong> Ретро-бум вернул формы и цвета 2000-х, когда трейловые кроссовки были яркими и футуристичными. Salomon XT-6 — идеальный пример этой эстетики.</li>
    <li><strong>Инфлюенсеры и коллаборации.</strong> Сотрудничество Salomon с женскими брендами и выход моделей в коллаборациях с Comme des Garçons и MM6 Maison Margiela превратили трейловую обувь в объект вожделения модников.</li>
    <li><strong>Универсальность.</strong> Трекинговые кроссовки подходят для разных погодных условий и типов поверхности — от асфальта до лёгкого бездорожья. Это «одна пара на всё».</li>
  </ul>
</section>

