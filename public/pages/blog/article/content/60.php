<section id="case1">
  <h2>Кейс 1: Белые кроссовки Nike Air Force 1</h2>
  <p><strong>Проблема:</strong> Владелец носил кроссовки 2 года без должного ухода. Подошва пожелтела до оранжевого оттенка, на коже появились чёрные полосы от контакта с резиной, шнурки и язычок были серыми от грязи.</p>
  <p><strong>Работы:</strong></p>
  <ul>
    <li>Глубокая чистка верха с использованием профессионального обезжиривателя</li>
    <li>Отбеливание подошвы методом ретробрайт (перекись 12% + УФ-лампа, 6 часов)</li>
    <li>Удаление чёрных полос с кожи специальным растворителем</li>
    <li>Восстановление цвета белым кремом-красителем</li>
    <li>Замена шнурков на новые оригинальные</li>
    <li>Финишная водоотталкивающая пропитка</li>
  </ul>
  <p><strong>Результат:</strong> Кроссовки выглядят как новые. Подошва вернула заводскую белизну, кожа чистая, без потёртостей. Клиент не поверил, что это та же пара.</p>
  <div data-type="box" data-color="green">
    <h4>Затраченное время</h4>
    <p>2 дня. Стоимость реставрации — 3500 ₽ (включая отбеливание подошвы, чистку и покраску). Новая пара стоила бы 12 000 ₽.</p>
  </div>
</section>

<section id="case2">
  <h2>Кейс 2: Замшевые кроссовки Adidas Samba</h2>
  <p><strong>Проблема:</strong> Популярная модель Samba в цвете «Core Black». После года носки замша на мыске засалилась, появились белые разводы от реагентов, ворс прилёг и местами стёрся.</p>
  <p><strong>Работы:</strong></p>
  <ul>
    <li>Деликатная чистка замши специализированной пеной</li>
    <li>Восстановление ворса паровой обработкой и резиновой щёткой</li>
    <li>Удаление солевых разводов раствором уксуса (1:3)</li>
    <li>Тонировка замши аэрозольной краской в цвет</li>
    <li>Нанесение водоотталкивающей пропитки для замши</li>
  </ul>
  <p><strong>Результат:</strong> Замша восстановлена полностью — цвет равномерный, ворс поднят, разводов нет. Кроссовки выглядят как после покупки.</p>
  <div data-type="box" data-color="blue">
    <h4>Важный нюанс</h4>
    <p>При восстановлении замши важно не просто покрасить, а именно поднять ворс и очистить поры. Иначе краска ляжет пятнами и будет быстро стираться.</p>
  </div>
</section>

<section id="case3">
  <h2>Кейс 3: Кожаные туфли Loake с потёртостями</h2>
  <p><strong>Проблема:</strong> Классические оксфорды из телячьей кожи. После нескольких сезонов носки на мыске и в области сгиба появились глубокие потёртости до белого слоя. Кожа местами стёрта до основы.</p>
  <p><strong>Работы:</strong></p>
  <ul>
    <li>Глубокая очистка и обезжиривание кожи</li>
    <li>Шлифовка повреждённых участков для удаления отслоений</li>
    <li>Нанесение жидкой кожи в 3 слоя с промежуточной сушкой</li>
    <li>Шлифовка и выравнивание поверхности</li>
    <li>Тонировка анилиновым кремом в цвет (коричневый)</li>
    <li>Финишная полировка воском до блеска</li>
  </ul>
  <p><strong>Результат:</strong> Потёртости полностью скрыты, цвет восстановлен, поверхность ровная и блестящая. Туфли выглядят дорого и ухоженно.</p>
</section>

<section id="case4">
  <h2>Кейс 4: Трекинговые кроссовки Salomon Speedcross</h2>
  <p><strong>Проблема:</strong> Активное использование в городе и лёгких трейлах. Кроссовки были покрыты плотным слоем грязи, мембрана Gore-Tex потеряла водоотталкивающие свойства, подошва износилась неравномерно.</p>
  <p><strong>Работы:</strong></p>
  <ul>
    <li>Предварительное замачивание и удаление грязи щёткой средней жёсткости</li>
    <li>Чистка мембраны специальным средством для Gore-Tex</li>
    <li>Сушка с распорками при комнатной температуре (24 часа)</li>
    <li>Восстановление DWR-пропитки спреем-активатором</li>
    <li>Нанесение водоотталкивающей пропитки на текстиль и замшу</li>
  </ul>
  <p><strong>Результат:</strong> Кроссовки чистые, мембрана снова отталкивает воду. Цвет верха освежился, пропитка защищает от грязи. Клиент отметил, что кроссовки стали легче (без налипшей грязи).</p>
  <div data-type="box" data-color="green">
    <h4>Экономия</h4>
    <p>Стоимость профессиональной чистки и восстановления Gore-Tex — 2500 ₽. Новые Salomon Speedcross — около 18 000 ₽. Разница очевидна.</p>
  </div>
</section>

