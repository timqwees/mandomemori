<section id="protect">
  <h2>Водоотталкивающая защита замши</h2>
  <p>После чистки обязательно нанесите водоотталкивающую пропитку. Современные составы на основе нанотехнологий создают невидимый барьер, который отталкивает воду и грязь.</p>
  <ul>
    <li>Распыляйте пропитку с расстояния 20–25 см</li>
    <li>Наносите на чистую сухую поверхность</li>
    <li>Дайте высохнуть естественным путём (около 2 часов)</li>
    <li>Повторяйте обработку раз в 2–4 недели в зависимости от интенсивности носки</li>
  </ul>
