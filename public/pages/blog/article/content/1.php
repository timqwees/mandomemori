<section id="daily">
  <h2>Ежедневный уход за замшей</h2>
  <p>Чтобы замшевые кроссовки дольше оставались чистыми, достаточно трёх простых действий после каждой носки:</p>
  <ul>
    <li><strong>Сухая чистка щёткой.</strong> Специальная щётка для замши с резиновыми или латунными щетинками поднимает ворс и удаляет поверхностную пыль и грязь.</li>
    <li><strong>Удаление влаги.</strong> Если обувь намокла — просушите её при комнатной температуре, набив бумагой. Ни в коем случае не на батарее.</li>
    <li><strong>Обработка ластиком.</strong> Специальный ластик для замши отлично справляется с мелкими загрязнениями и засаленными участками.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Совет</h4>
    <p>Носите с собой мини-щётку для замши — достаточно пары движений после улицы, чтобы кроссовки выглядели опрятно весь день.</p>
  </div>
</section>

<section id="deep">
  <h2>Глубокая чистка замши</h2>
  <p>Раз в 2–3 недели замшевой обуви требуется более глубокая чистка:</p>
  <ol>
    <li>Удалите шнурки и стельки — их можно постирать отдельно.</li>
    <li>Сухой щёткой пройдитесь по всей поверхности, чтобы поднять ворс.</li>
    <li>Приготовьте мыльный раствор: тёплая вода + несколько капель жидкого мыла или специального шампуня для замши.</li>
    <li>Смочите мягкую ткань в растворе, тщательно отожмите и аккуратно протрите загрязнённые участки.</li>
    <li>Удалите остатки мыла влажной чистой тканью.</li>
    <li>Оставьте сушиться при комнатной температуре, набив белой бумагой для сохранения формы.</li>
  </ol>
  <div data-type="box" data-color="blue">
    <h4>Важно</h4>
    <p>Замшу нельзя полностью замачивать в воде — это разрушает структуру материала и может привести к деформации. Только локальная влажная обработка.</p>
  </div>
</section>

<section id="stains">
  <h2>Как удалить пятна с замши</h2>
  <p>Разные типы пятен требуют разного подхода:</p>
  <div data-type="compare">
    <article data-variant="good">
      <h4>Масляные пятна</h4>
      <ul>
        <li>Посыпьте тальком или крахмалом</li>
        <li>Оставьте на 2–4 часа для впитывания</li>
        <li>Счистите мягкой щёткой</li>
      </ul>
    </article>
    <article data-variant="bad">
      <h4>Водные разводы</h4>
      <ul>
        <li>Не трите — размажете ещё больше</li>
        <li>Промокните сухой салфеткой</li>
        <li>После высыхания пройдитесь щёткой</li>
      </ul>
    </article>
  </div>
  <p>Для стойких загрязнений используйте специальный пенящийся очиститель для замши. Нанесите пену, оставьте на 1–2 минуты, затем удалите чистой тканью.</p>
</section>

<section id="restore">
  <h2>Восстановление ворса замши</h2>
  <p>Со временем ворс замши приминается, и обувь выглядит «засаленной». Восстановить его можно двумя способами:</p>
  <ul>
    <li><strong>Щётка для замши.</strong> Пройдитесь по всей поверхности специальной щёткой, двигаясь в одном направлении.</li>
    <li><strong>Обработка паром.</strong> Подержите кроссовки над паром (не горячим!) 10–15 секунд, затем сразу пройдитесь щёткой. Пар поднимает примятые ворсинки.</li>
  </ul>
