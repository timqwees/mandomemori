<section id="why">
  <h2>Почему появляются разводы</h2>
  <p>Разводы после чистки возникают по трём основным причинам:</p>
  <ul>
    <li><strong>Избыток влаги.</strong> Когда вы используете слишком много воды, она проникает в материал неравномерно. При высыхании на границе влажного и сухого остаются солевые разводы.</li>
    <li><strong>Моющее средство.</strong> Если плохо смыть мыло или чистящее средство, его остатки кристаллизуются при высыхании и оставляют белые следы.</li>
    <li><strong>Сушка.</strong> Неравномерная сушка (одна часть сохнет быстрее другой) приводит к миграции растворённых веществ к краю влажного участка — там и остаются разводы.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Золотое правило</h4>
    <p>Чтобы на обуви не оставалось разводов, она должна сохнуть равномерно. Не ставьте обувь у батареи одним боком и не сушите на солнце — перепад температуры создаёт градиент влажности, который и вызывает разводы.</p>
  </div>
</section>

<section id="leather">
  <h2>Разводы на гладкой коже</h2>
  <p>На гладкой коже разводы выглядят как матовые или белые полосы на фоне гладкой блестящей поверхности.</p>
  <h3>Белые разводы от воды</h3>
  <p>Это солевые отложения, которые остаются после высыхания воды. Удалить их можно слабым раствором уксуса (1 часть уксуса на 5 частей воды). Смочите ватный диск, протрите разводы. Затем пройдитесь чистой влажной тканью и сразу вытрите насухо.</p>
  <h3>Разводы от крема</h3>
  <p>Если вы нанесли слишком много крема или не отполировали его — останутся жирные разводы. Удалите их сухой микрофиброй, интенсивно полируя круговыми движениями. Если не помогло — используйте очиститель для кожи, чтобы снять излишки крема, и нанесите заново тонким слоем.</p>
  <h3>Тёмные разводы</h3>
  <p>Появляются, когда грязь с одного участка переносится на другой губкой или тканью. Удаляются повторной чисткой всего кроссовка, а не только проблемного участка.</p>
  <div data-type="box" data-color="green">
    <h4>Лайфхак</h4>
    <p>Чтобы убрать разводы с гладкой кожи, протрите её вазелином или касторовым маслом. Масло заполняет микропоры и выравнивает отражение света — разводы становятся незаметными. Через 15 минут отполируйте мягкой тканью.</p>
  </div>
</section>

<section id="suede">
  <h2>Разводы на замше и нубуке</h2>
  <p>Разводы на замше — самые заметные и сложные в удалении. Ворс меняет направление и цвет на границе влажного и сухого участка.</p>
  <h3>Водные разводы</h3>
  <p>Если замша намокла неравномерно — останутся тёмные пятна. Решение: намочите всю поверхность замши из пульверизатора (равномерно!) и высушите при комнатной температуре. После высыхания поднимите ворс резиновой щёткой. Равномерное увлажнение всей поверхности предотвращает границу влажного и сухого.</p>
  <h3>Разводы от моющих средств</h3>
  <p>Если вы использовали слишком много пены и плохо её сняли — останутся мыльные разводы. Удалите их отпаривателем: пар размягчает остатки средства, и их можно удалить чистой тканью. Держите отпариватель на расстоянии 15–20 см.</p>
  <h3>Жирные разводы</h3>
  <p>Жирные разводы на замше — от попадания крема для кожи или жирных пальцев. Посыпьте тальком на 30 минут, затем счистите щёткой. Если не помогло — используйте специальный очиститель для замши.</p>
  <div data-type="box" data-color="red">
    <h4>Чего нельзя делать</h4>
    <p>Не трите замшу влажной щёткой — вы только размажете загрязнение. Не используйте спирт — он обесцвечивает ворс. И никогда не пытайтесь убрать разводы на замше растворителями — это разрушит материал.</p>
  </div>
</section>

<section id="textile">
  <h2>Разводы на текстиле</h2>
  <p>На текстиле разводы — это обычно остатки моющего средства или солевые отложения.</p>
  <h3>Мыльные разводы</h3>
  <p>Если после чистки остались белые полосы — средство плохо смыто. Протрите текстиль влажной микрофиброй, смоченной в чистой воде. Меняйте сторону ткани, чтобы не размазывать. Повторяйте, пока разводы не исчезнут.</p>
  <h3>Солевые разводы</h3>
  <p>Белые кристаллические разводы после высыхания — соль. Удалите их слабым раствором уксуса (1:5). Нанесите на ткань, через 5 минут смойте водой.</p>
  <h3>Жёлтые разводы</h3>
  <p>Самые сложные — появляются при неправильной сушке на солнце или от окисления материалов. Удаляйте смесью перекиси водорода 3% и воды (1:1). Нанесите, оставьте на 15 минут, смойте.</p>
</section>

<section id="sole">
  <h2>Разводы на подошве</h2>
  <p>На подошве разводы — это обычно остатки грязи, которая не полностью смыта, или солевые отложения.</p>
  <ul>
    <li><strong>Белые разводы.</strong> Удаляйте уксусом 1:3 с водой. Потрите щёткой, смойте.</li>
    <li><strong>Тёмные разводы.</strong> Появляются, когда чистите подошву после верха и переносите грязь. Всегда чистите подошву первой.</li>
    <li><strong>Мыльные разводы.</strong> Промойте подошву чистой водой и вытрите насухо.</li>
  </ul>
</section>

<section id="prevent">
  <h2>Как чистить без разводов</h2>
  <p>Профилактика разводов — правильная техника чистки. Пять правил для безупречного результата:</p>
  <ul>
    <li><strong>Меньше воды.</strong> Используйте пену, а не воду. Пена содержит мало влаги, но хорошо очищает.</li>
    <li><strong>Равномерное увлажнение.</strong> Если материал намок — добивайтесь равномерного увлажнения всей поверхности.</li>
    <li><strong>Тщательное смывание.</strong> Смывайте средство до тех пор, пока вода или ткань не станут чистыми. Остатки средства = разводы.</li>
    <li><strong>Правильная сушка.</strong> Сушите при комнатной температуре, набив бумагой. Бумага впитывает влагу равномерно.</li>
    <li><strong>Полировка.</strong> После высыхания отполируйте кожу или поднимите ворс. Это маскирует микроскопические дефекты.</li>
  </ul>
