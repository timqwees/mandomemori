<section id="why">
  <h2>Почему трескается кожа на обуви</h2>
  <p>Кожа — натуральный материал, который со временем теряет влагу и эластичность. Основные причины появления трещин:</p>
  <ul>
    <li><strong>Пересыхание.</strong> Недостаток увлажнения — главная причина. Кожа теряет натуральные масла, становится жёсткой и ломкой. Особенно быстро это происходит после контакта с водой и неправильной сушки.</li>
    <li><strong>Механические нагрузки.</strong> Места сгиба при ходьбе испытывают постоянное напряжение. Со временем микротрещины в этих зонах превращаются в глубокие разрывы.</li>
    <li><strong>Реагенты и соль.</strong> Зимние реагенты вытягивают влагу из кожи и разрушают её структуру. Даже качественная кожа после зимы может пойти трещинами.</li>
    <li><strong>Неправильное хранение.</strong> Слишком сухой воздух, близость батарей, прямые солнечные лучи — всё это сушит кожу и провоцирует растрескивание.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно знать</h4>
    <p>Разные типы кожи трескаются по-разному. Гладкая телячья кожа более эластична, а лаковое покрытие и искусственная кожа трескаются быстрее из-за меньшей пластичности верхнего слоя.</p>
  </div>
</section>

<section id="types">
  <h2>Типы трещин: какие можно восстановить</h2>
  <p>Не все трещины одинаковы. В зависимости от глубины и локализации дефекта выбирается метод реставрации:</p>
  <ul>
    <li><strong>Поверхностные микротрещины.</strong> Выглядят как сетка мелких морщин на коже. Легко восстанавливаются кремом с воском или жидкой кожей. Прогноз — отличный.</li>
    <li><strong>Трещины на сгибах.</strong> Глубокие, но локальные повреждения в местах сгиба стопы. Требуют заполнения жидкой кожей или полимерными составами. Прогноз — хороший.</li>
    <li><strong>Сквозные трещины.</strong> Полный разрыв кожи насквозь. Сложный случай, требующий профессиональной реставрации с армированием. Прогноз — осторожный.</li>
    <li><strong>Трещины на искусственной коже.</strong> Часто не подлежат ремонту, так как полимерное покрытие отслаивается, а восстановить его невозможно. Прогноз — чаще всего замена.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Хорошая новость</h4>
    <p>Около 70% трещин на натуральной коже поддаются реставрации. Даже глубокие повреждения можно сделать практически незаметными с помощью современных материалов.</p>
  </div>
</section>

<section id="home">
  <h2>Домашние методы реставрации кожи</h2>
  <p>Для небольших трещин и микротрещин можно использовать домашние методы. Вот что реально работает:</p>
  <h3>Жидкая кожа (Liquid Leather)</h3>
  <p>Специальный состав, который заполняет трещину и образует эластичную плёнку после высыхания. Продаётся в обувных магазинах в тюбиках. Наносится зубочисткой или тонкой кисточкой. После высыхания шлифуется мелкозернистой наждачкой и тонируется кремом.</p>
  <h3>Восковой крем + наполнитель</h3>
  <p>Для поверхностных трещин достаточно густого воскового крема в цвет обуви. Он заполняет микротрещины, питает кожу и маскирует дефекты. Наносится круговыми движениями, после полировки мягкой тканью.</p>
  <h3>Клей для кожи + краска</h3>
  <p>Экстренный метод: трещина заполняется специальным клеем для кожи (например, Kenda Farben или Renapur), после высыхания шлифуется и закрашивается акриловой краской для кожи. Подходит только для маленьких трещин.</p>
  <div data-type="box" data-color="red">
    <h4>Чего нельзя делать</h4>
    <p>Не используйте суперклей (цианакрилат) — он делает кожу хрупкой. Не замазывайте трещины лаком для ногтей. Не пытайтесь разогревать кожу феном — перегрев разрушает структуру материала.</p>
  </div>
</section>

