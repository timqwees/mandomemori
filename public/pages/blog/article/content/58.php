<section id="prevention">
  <h2>Профилактика трещин на коже</h2>
  <p>Предотвратить появление трещин проще, чем их реставрировать. Соблюдайте несколько правил:</p>
  <ul>
    <li><strong>Регулярно увлажняйте кожу.</strong> Используйте питательный крем для обуви раз в 1–2 недели. В сухой сезон — чаще. Крем с норковым маслом или ланолином идеален для профилактики.</li>
    <li><strong>Правильно сушите.</strong> Никогда не сушите кожаную обувь на батарее или обогревателе. Используйте распорки и сушите при комнатной температуре с естественной вентиляцией.</li>
    <li><strong>Используйте колодки.</strong> Храните обувь на колодках — они поддерживают форму и предотвращают заломы, на которых образуются трещины.</li>
    <li><strong>Защищайте от воды.</strong> Водоотталкивающая пропитка на основе воска создаёт барьер, который предотвращает вымывание натуральных масел из кожи.</li>
    <li><strong>Давайте отдых.</strong> Не носите одну и ту же пару два дня подряд. Коже нужно время, чтобы восстановить влажность и форму.</li>
  </ul>
