<section id="daily">
  <h2>Ежедневная гигиена</h2>
  <p>Кошелёк контактирует с руками, в которых есть бактерии и кожное сало. Ежедневная гигиена продлевает его свежесть:</p>
  <ul>
    <li><strong>Перебирайте содержимое.</strong> Раз в неделю вынимайте всё из кошелька и выбрасывайте чеки, старые визитки и мусор. Меньше содержимого — меньше износ.</li>
    <li><strong>Протирайте внешнюю часть.</strong> Если кошелёк из гладкой кожи — раз в неделю протирайте его мягкой тканью для удаления кожного жира.</li>
    <li><strong>Держите руки чистыми.</strong> Кажется очевидным, но после еды или контакта с краской/жиром вытирайте руки перед тем, как достать кошелёк.</li>
    <li><strong>Не храните в заднем кармане.</strong> Это самая грязная и потная зона. Если сидеть на кошельке — он деформируется, а ткань кармана протирается.</li>
  </ul>
</section>

<section id="leather">
  <h2>Чистка кожаного кошелька</h2>
  <p>Кожаный кошелёк чистится так же, как и кожаная обувь, но более деликатно, потому что кожа тоньше:</p>
  <ol>
    <li>Освободите кошелёк от содержимого.</li>
    <li>Приготовьте мыльный раствор (капля мягкого мыла на стакан воды).</li>
    <li>Смочите мягкую салфетку, отожмите почти досуха — салфетка должна быть чуть влажной.</li>
    <li>Протрите внешнюю поверхность кошелька, не заливая швы и внутреннюю часть.</li>
    <li>Чистой влажной салфеткой удалите остатки мыла.</li>
    <li>Высушите при комнатной температуре. Кошелёк сохнет быстрее обуви — обычно 3–4 часа.</li>
    <li>Нанесите тонкий слой бесцветного крема для кожи и отполируйте мягкой тканью.</li>
  </ol>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Не замачивайте кошелёк в воде. Вода может просочиться внутрь и повредить кармашки с клеевыми соединениями. Только поверхностная влажная чистка.</p>
  </div>
</section>

<section id="suede">
  <h2>Чистка замшевого кошелька</h2>
  <p>Замшевый кошелёк — самый капризный. Вода и жир для него особенно опасны:</p>
  <ol>
    <li>Используйте только сухую чистку. Резиновая щётка для замши или ластик.</li>
    <li>При сильном загрязнении — специальная пена для замши. Нанесите пену, дайте высохнуть, удалите остатки щёткой.</li>
    <li>Для жирных пятен — тальк или детская присыпка. Нанесите на пятно на 2–3 часа, затем удалите резиновой щёткой.</li>
    <li>После чистки обработайте водоотталкивающим спреем для замши.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Не пытайтесь оттереть пятно на замшевом кошельке влажной салфеткой — вы размажете грязь и испортите ворс. Только сухая чистка!</p>
  </div>
</section>

<section id="inside">
  <h2>Чистка внутренней части</h2>
  <p>Внутри кошелька скапливается пыль, крошки, частицы кожи и бактерии. Это частая причина неприятного запаха:</p>
  <ul>
    <li><strong>Пылесос.</strong> Используйте пылесос с узкой насадкой, чтобы убрать крошки и пыль из кармашков.</li>
    <li><strong>Спиртовой раствор.</strong> Для тканевой подкладки: смочите ватный диск спиртом (или водкой) и аккуратно протрите внутренние поверхности. Спирт быстро испаряется и не оставляет влаги.</li>
    <li><strong>Сода.</strong> Если есть запах — насыпьте внутрь соду на ночь, а утром вытряхните. Сода впитывает запахи.</li>
    <li><strong>Влажные салфетки.</strong> Для кожаной внутренней отделки используйте только специальные салфетки для кожи без спирта.</li>
  </ul>
  <p>Раз в месяц обязательно проветривайте кошелёк — оставляйте открытым на ночь в проветриваемом месте.</p>
  <div data-type="box" data-color="green">
    <h4>Лайфхак</h4>
    <p>Внутрь кошелька положите небольшой пакетик силикагеля. Он будет впитывать лишнюю влагу и предотвращать появление запаха.</p>
  </div>
</section>

