<section id="choosepaint">
  <h2>Как выбрать краску для кожаной обуви</h2>
  <p>От выбора краски зависит 50% успеха. Не всякая краска подходит для кожи — обычные акриловые краски для творчества трескаются и облезают.</p>
  <ul>
    <li><strong>Аэрозольные краски для кожи.</strong> Лучший выбор для домашней покраски. Равномерное нанесение, широкий выбор цветов, высокая адгезия к коже. Рекомендуемые бренды: Tarrago, Saphir, Angelus, Salamander.</li>
    <li><strong>Жидкие красители (краска в баночках).</strong> Наносятся кистью или губкой. Требуют навыка — могут остаться разводы. Подходят для мелких участков и профессиональной работы.</li>
    <li><strong>Кремы-красители.</strong> Освежают цвет, но не меняют его кардинально. Подходят для тонирования, а не для полной перекраски.</li>
    <li><strong>Спреи-аэрографы.</strong> Используются с аэрографом. Профессиональный инструмент — для дома не обязателен.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Что НЕ подходит для кожи</h4>
    <p>Обычная акриловая краска (трескается), обувной крем без красителя (не меняет цвет), акварель и гуашь, краска для тканей, автомобильная эмаль (слишком агрессивная для кожи).</p>
  </div>
</section>

<section id="prep">
  <h2>Подготовка поверхности к покраске</h2>
  <p>Подготовка — ключевой этап. Краска держится на коже только при условии идеальной очистки и правильной обработки.</p>
  <h3>Этапы подготовки</h3>
  <ol>
    <li><strong>Очистка.</strong> Вымойте обувь мыльным раствором, удалите грязь из швов и складок. Просушите при комнатной температуре.</li>
    <li><strong>Обезжиривание.</strong> Протрите поверхность спиртом, ацетоном или специальным обезжиривателем. Удалите остатки старого крема и воска.</li>
    <li><strong>Шлифовка (для глянцевой кожи).</strong> Лаковую и сильно глянцевую кожу нужно слегка заматировать мелкозернистой наждачкой (P1000–P1500), чтобы краска сцепилась с поверхностью.</li>
    <li><strong>Маскировка.</strong> Заклейте малярным скотчем подошву, молнию, шнурки и другие элементы, которые не должны быть окрашены.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Не пропускайте обезжиривание</h4>
    <p>Остатки крема, силикона и жира — главная причина, почему краска отслаивается через неделю. Если на коже есть следы силиконовой пропитки, обезжирьте дважды.</p>
  </div>
</section>

<section id="removing">
  <h2>Удаление старого покрытия (если нужно)</h2>
  <p>Если вы хотите кардинально изменить цвет (например, чёрный на белый), старую краску нужно удалить. Это сложный процесс, который требует специальных средств.</p>
  <h3>Средства для удаления краски с кожи</h3>
  <ul>
    <li><strong>Специальные смывки.</strong> Saphir Renomat, Tarrago Color Remover — бережно удаляют старую краску, не повреждая кожу.</li>
    <li><strong>Ацетон (с осторожностью).</strong> Снимает краску, но может пересушить кожу и изменить её цвет. Используйте только на небольших участках.</li>
    <li><strong>Спирт.</strong> Удаляет поверхностный слой краски. Безопаснее ацетона, но и менее эффективен.</li>
  </ul>
  <h3>Процесс удаления</h3>
  <ol>
    <li>Нанесите смывку на ватный диск.</li>
    <li>Круговыми движениями удаляйте старую краску. Меняйте диск по мере загрязнения.</li>
    <li>После удаления промойте кожу водой и дайте высохнуть.</li>
    <li>Оцените результат. Если цвет сохранился — повторите.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Предупреждение</h4>
    <p>Удаление краски с кожи может частично обесцветить материал. После смывки кожа может стать светлее или иметь неравномерный оттенок. Это нормально — финишная покраска всё выровняет.</p>
  </div>
</section>

<section id="painting">
  <h2>Пошаговая покраска кожаной обуви</h2>
  <p>После подготовки и маскировки приступайте к покраске. Работайте в проветриваемом помещении при температуре 18–25 °C.</p>
  <ol>
    <li><strong>Встряхните баллон.</strong> Не менее 30 секунд — краска должна перемешаться равномерно.</li>
    <li><strong>Тест на незаметном участке.</strong> Нанесите краску на внутреннюю поверхность или под язычок — проверьте совпадение цвета.</li>
    <li><strong>Нанесите первый слой.</strong> С расстояния 20–25 см. Движения — быстрые, равномерные. Не задерживайтесь на одном месте, чтобы не было подтёков.</li>
    <li><strong>Просушка первого слоя.</strong> 20–30 минут при комнатной температуре.</li>
    <li><strong>Нанесите второй слой.</strong> Перекройте участки, где первый слой лёг неравномерно.</li>
    <li><strong>Просушка второго слоя.</strong> 30–40 минут.</li>
    <li><strong>При необходимости — третий слой.</strong> Для кардинальной смены цвета может потребоваться 3–4 слоя.</li>
  </ol>
  <div data-type="box" data-color="dark">
    <h4>Секрет профессионалов</h4>
    <p>Перед нанесением каждого слоя прогрейте баллон в тёплой воде (не выше 40 °C) в течение 2–3 минут. Тёплая краска ложится ровнее, образует меньше разводов и лучше сцепляется с кожей.</p>
  </div>
</section>

<section id="fixation">
  <h2>Фиксация и защита цвета</h2>
  <p>После покраски цвет нужно закрепить, иначе он быстро сотрётся или смоется водой. Фиксация обязательно включает следующие шаги:</p>
  <ol>
    <li><strong>Фиксатор (закрепитель).</strong> Специальный спрей-фиксатор для кожи. Наносится через 30 минут после последнего слоя краски. Защищает цвет от влаги и истирания.</li>
    <li><strong>Просушка с фиксатором.</strong> 24 часа. В этот период обувь нельзя носить, мочить или подвергать нагрузкам.</li>
    <li><strong>Питание кожи.</strong> Через сутки после фиксации нанесите питательный крем или кондиционер для кожи. Краска может немного пересушить материал, и крем восстановит баланс.</li>
    <li><strong>Полировка.</strong> Через 2 дня можно отполировать обувь мягкой тканью для придания блеска.</li>
  </ol>
  <div data-type="box" data-color="blue">
    <h4>Совет</h4>
    <p>Новую покраску не рекомендуется мочить в течение 7 дней. В первый месяц избегайте длительного контакта с водой. Используйте водоотталкивающий спрей для дополнительной защиты цвета.</p>
  </div>
  <p>При соблюдении технологии покраска продержится 3–6 месяцев активной носки. После этого можно обновить цвет одним слоем краски без повторения всей процедуры.</p>
</section>

<section id="mistakes">
  <h2>Типичные ошибки при покраске обуви</h2>
  <p>Вот что чаще всего идёт не так и как этого избежать:</p>
  <ul>
    <li><strong>Пропуск обезжиривания.</strong> Краска отслаивается через 1–2 недели. Решение: всегда обезжиривайте перед покраской.</li>
    <li><strong>Слишком толстый слой.</strong> Краска трескается при сгибе. Решение: наносите тонкими слоями, лучше 3 тонких слоя, чем 1 толстый.</li>
    <li><strong>Слишком близкое распыление.</strong> Подтёки и неровности. Решение: держите баллон на расстоянии 20–25 см.</li>
    <li><strong>Носка обуви до полного высыхания.</strong> Краска сминается и отходит. Решение: не носите обувь 24 часа после покраски и фиксации.</li>
    <li><strong>Использование неподходящей краски.</strong> Облупление и трещины. Решение: используйте только профессиональные краски для кожи.</li>
  </ul>
