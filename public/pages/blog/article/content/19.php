<section id="weather">
  <h2>Особенности носки в разную погоду</h2>
  <p>Лаковая обувь — капризная к погодным условиям. Вот что нужно учитывать:</p>
  <ul>
    <li><strong>Дождь.</strong> Лаковое покрытие не пропускает воду, но капли оставляют разводы после высыхания. Протирайте обувь сразу после дождя сухой тканью.</li>
    <li><strong>Снег и соль.</strong> Реагенты разъедают лаковое покрытие. После прогулки по снегу тщательно вымойте подошву, а верх протрите влажной тканью.</li>
    <li><strong>Мороз.</strong> При –10 °C и ниже лак трескается. В сильные морозы носите другую обувь.</li>
    <li><strong>Жара.</strong> На солнце лак может размягчиться — на нём останутся отпечатки от пальцев и пыли. Храните обувь в тени.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Совет</h4>
    <p>Перед выходом в дождливую погоду нанесите на лаковую обувь водоотталкивающий спрей для гладкой кожи. Он создаёт невидимый барьер, с которого вода скатывается без разводов. Без пропитки капли высыхают и оставляют белёсые пятна.</p>
  </div>
</section>

<section id="storage">
  <h2>Хранение лаковой обуви</h2>
  <p>Правильное хранение — половина успеха в уходе за лаковой обувью. Основные правила:</p>
  <ul>
    <li><strong>Индивидуальные мешки.</strong> Храните каждую пару в отдельном тканевом мешке. Лаковые туфли, соприкасаясь, царапают друг друга.</li>
    <li><strong>Колодки.</strong> Используйте мягкие колодки или набивайте обувь бумагой — это предотвращает заломы на лаковом покрытии.</li>
    <li><strong>Температура.</strong> Храните при 15–25 °C. Балкон, гараж и подвал — неподходящие места для лаковой обуви.</li>
    <li><strong>Влажность.</strong> Не храните лаковую обувь в полиэтиленовых пакетах — конденсат разрушает лаковое покрытие.</li>
  </ul>
</section>

