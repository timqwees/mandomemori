<section id="scratches">
  <h2>Удаление царапин</h2>
  <p>Царапины на лаковой обуви — вопрос времени. Мелкие царапины можно замаскировать, глубокие — только реставрировать.</p>
  <h3>Мелкие царапины</h3>
  <ul>
    <li><strong>Вазелин.</strong> Вотрите вазелин в царапину, через 10 минут отполируйте. Царапина станет менее заметной.</li>
    <li><strong>Масло для лака.</strong> Специализированное масло заполняет микротрещины и выравнивает отражение света.</li>
    <li><strong>Корректор для лака.</strong> Жидкость в виде маркера, которая заполняет царапины и восстанавливает покрытие.</li>
  </ul>
  <h3>Глубокие царапины</h3>
  <p>Если царапина доходит до кожи под лаком — домашними методами не обойтись. Требуется профессиональная реставрация: шлифовка повреждённого участка, нанесение нового лакового слоя и полировка. В MANDO MEMORI такая услуга стоит от 800 ₽ за одну царапину.</p>
