<section id="what">
  <h2>Что такое лаковое покрытие</h2>
  <p>Лаковая кожа — это натуральная или искусственная кожа, покрытая слоем полиуретанового лака. Именно этот прозрачный слой создаёт знаменитое зеркальное отражение. Особенности лакового покрытия:</p>
  <ul>
    <li><strong>Не впитывает кремы.</strong> Крем для обычной кожи бесполезен на лаке — он не проникает в покрытие и остаётся на поверхности.</li>
    <li><strong>Боится растворителей.</strong> Ацетон, спирт и бензин растворяют лаковое покрытие. После контакта с ними лак становится матовым и мутным.</li>
    <li><strong>Термочувствительность.</strong> При температуре ниже –10 °C лак трескается. При нагреве выше 60 °C (на солнце или у батареи) — плавится.</li>
    <li><strong>Царапается.</strong> Лаковое покрытие твёрдое, но хрупкое. Царапины на нём видны сразу из-за разницы отражения света.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно знать</h4>
    <p>Лаковое покрытие — не вечное. Со временем (через 2–3 года активной носки) оно истончается и теряет блеск. Продлить жизнь лаку можно только правильным уходом. Агрессивное воздействие ускоряет износ в 3–4 раза.</p>
  </div>
</section>

<section id="clean">
  <h2>Чистка лаковой обуви</h2>
  <p>Главное правило при чистке лака — минимум воды и никаких абразивов. Лаковое покрытие легко царапается песком и грязью, поэтому перед чисткой обязательно удалите все твёрдые частицы.</p>
  <h3>Ежедневная чистка</h3>
  <ol>
    <li>Мягкой сухой тканью удалите пыль и песок с поверхности. Используйте микрофибру — она не царапает.</li>
    <li>При необходимости — протрите слегка влажной тканью. Не давите, просто проведите по поверхности.</li>
    <li>Сразу вытрите насухо мягкой тканью. Вода на лаковом покрытии оставляет разводы.</li>
  </ol>
  <h3>Глубокая чистка</h3>
  <p>Раз в 2–3 месяца делайте глубокую чистку специальным средством для лакированной кожи. Нанесите средство на ватный диск, протрите обувь, затем отполируйте чистой микрофиброй.</p>
  <div data-type="box" data-color="red">
    <h4>Запрещённые средства</h4>
    <p>Не используйте для лаковой обуви: спирт, ацетон, жидкость для снятия лака, уксус, лимонный сок, зубную пасту, соду и любые абразивные чистящие порошки. Всё это разрушает лаковое покрытие.</p>
  </div>
</section>

<section id="polish">
  <h2>Полировка и восстановление блеска</h2>
  <p>Главное преимущество лаковой обуви — блеск. Со временем он тускнеет. Восстановить зеркальное сияние можно несколькими способами:</p>
  <h3>Вазелин или касторовое масло</h3>
  <p>Нанесите каплю вазелина на мягкую ткань и отполируйте обувь круговыми движениями. Через 5 минут снимите остатки. Метод безопасен и даёт быстрый результат — блеск восстанавливается на 2–3 дня.</p>
  <h3>Специализированная полироль для лака</h3>
  <p>В магазинах обувной косметики продаются спреи и салфетки для лакированной кожи. Они восстанавливают блеск и создают защитный слой. Используйте раз в 1–2 недели.</p>
