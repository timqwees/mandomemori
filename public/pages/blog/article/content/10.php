<section id="apply">
  <h2>Как правильно наносить наноспрей для максимальной эффективности</h2>
  <p>Даже лучший наноспрей не сработает, если нанести его неправильно. Технология нанесения не менее важна, чем состав.</p>
  <ol>
    <li><strong>Очистка.</strong> Обувь должна быть идеально чистой и сухой. Используйте профессиональный очиститель для соответствующего материала. Любая пыль или жир снижают адгезию наночастиц.</li>
    <li><strong>Подготовка.</strong> Встряхните баллон в течение 30–60 секунд до появления характерного звука шарика. Некоторые составы требуют предварительного нагрева до комнатной температуры.</li>
    <li><strong>Дистанция.</strong> Распыляйте с расстояния 20–25 см. Слишком близко — образуются потёки, слишком далеко — состав ложится неравномерно.</li>
    <li><strong>Слой.</strong> Нанесите 2–3 тонких слоя с интервалом 10–15 минут между ними. Один толстый слой работает хуже, чем несколько тонких.</li>
    <li><strong>Сушка.</strong> Оставьте обувь при комнатной температуре на 2–4 часа для первичного высыхания и на 8–12 часов для полной полимеризации. Не используйте фен или батарею.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Лайфхак от MANDO MEMORI</h4>
    <p>Перед нанесением пропитки слегка нагрейте обувь феном (тёплым воздухом, не горячим!) — это раскрывает поры материала, и наночастицы проникают глубже. После нанесения дайте обуви «отдохнуть» 24 часа перед первой ноской.</p>
  </div>
</section>

<section id="duration">
  <h2>Сколько держится защита и когда её обновлять</h2>
  <p>Длительность защиты зависит от трёх факторов: типа состава, материала обуви и интенсивности носки. Приведём средние показатели.</p>
  <ul>
    <li><strong>Нанокерамика:</strong> 6–12 недель при ежедневной носке. Рекомендуется обновление перед каждым сезоном или после химической чистки.</li>
    <li><strong>Фторполимер:</strong> 1–3 недели. Оптимально обновлять раз в 2 недели, в дождливый сезон — каждую неделю.</li>
    <li><strong>Силикон:</strong> 3–7 дней. Практически каждый выход на улицу требует повторного нанесения.</li>
  </ul>
  <p>Признаки того, что защита перестала работать: вода перестаёт скатываться каплями и начинает впитываться, на поверхности остаются мокрые следы, обувь быстрее пачкается.</p>
  <div data-type="box" data-color="gray">
    <h4>Совет</h4>
    <p>Имейте в виду, что механическое воздействие — трение, сгибы, контакт с асфальтом — разрушает нанослой быстрее всего. Особенно уязвимы носки и пятки. При появлении потёртостей защиту нужно обновлять локально или полностью.</p>
  </div>
  <p>Если вы носите обувь не каждый день, защита держится дольше. Хранение в обувном шкафу при комнатной температуре не влияет на свойства нанослоя.</p>
</section>

<section id="myths">
  <h2>Мифы о нано-защите обуви: разбор популярных заблуждений</h2>
  <p>Вокруг наноспреев сформировалось множество мифов. Разберём самые распространённые.</p>
  <div data-type="box" data-color="red">
    <h4>Миф 1: «Наноспрей защищает от любых загрязнений»</h4>
    <p>Это не совсем так. Наноспрей отталкивает воду и жидкую грязь, но не защищает от механических загрязнений — пыли, песка, краски, чернил. Если вы наступили в краску — наноспрей не спасёт.</p>
  </div>
  <div data-type="box" data-color="red">
    <h4>Миф 2: «Одного нанесения хватит на весь сезон»</h4>
    <p>Даже самый дорогой нанокерамический состав требует обновления минимум раз в 2–3 месяца. Постоянное трение и контакт с реагентами постепенно разрушают нанослой.</p>
  </div>
  <div data-type="box" data-color="red">
    <h4>Миф 3: «Наноспрей можно наносить на грязную обувь»</h4>
    <p>Это грубейшая ошибка. Наночастицы «запечатывают» грязь в структуру материала, и после высыхания удалить её становится в разы сложнее. Всегда чистите обувь перед нанесением.</p>
  </div>
  <div data-type="box" data-color="red">
    <h4>Миф 4: «Все наноспреи одинаковые»</h4>
    <p>Составы кардинально различаются по типу наночастиц, растворителю, концентрации активных веществ и времени полимеризации. Дешёвые спреи часто содержат минимум наночастиц и максимум растворителя — эффективность минимальна.</p>
  </div>
  <div data-type="box" data-color="red">
    <h4>Миф 5: «Наноспрей можно использовать на любой обуви»</h4>
    <p>Некоторые типы нанокерамики несовместимы с лаковой кожей, деликатной замшей тонкой выделки и материалами с открытыми порами. Всегда проверяйте состав на незаметном участке перед нанесением.</p>
  </div>
</section>

