<section id="types">
  <h2>Три типа водоотталкивающей защиты обуви</h2>
  <p>Все водоотталкивающие составы на рынке делятся на три категории по химической основе. Понимание разницы — первый шаг к правильному выбору.</p>
  <h3>Фторполимерные составы</h3>
  <p>Фторполимеры — это тефлоноподобные соединения, которые создают на поверхности обуви тончайшую плёнку. Молекулы фторполимера «пришиваются» к волокнам материала на молекулярном уровне, обеспечивая водо- и грязеотталкивание. Такие составы популярны у Collonil и Salamander. Основное преимущество — универсальность: подходят для замши, нубука, гладкой кожи и текстиля.</p>
  <h3>Силиконовые пропитки</h3>
  <p>Классический вариант, известный ещё с 90-х. Силиконовые эмульсии заполняют поры материала и создают механический барьер для воды. Они дёшевы и эффективны, но имеют недостатки: силикон со временем забивает поры, и материал перестаёт «дышать». Для замши и нубука силиконовые составы не рекомендуются — они делают ворс жирным и меняют тактильные ощущения.</p>
  <div data-type="box" data-color="gray">
    <h4>На заметку</h4>
    <p>Силиконовые пропитки хороши для резиновых сапог, рабочей обуви и дешёвой кожгалантереи. Для премиальной обуви выбирайте фторполимер или нанокерамику.</p>
  </div>
  <h3>Нанокерамические покрытия</h3>
  <p>Технология 2023–2026 годов — керамические наночастицы, которые формируют на поверхности обуви сверхпрочный гидрофобный слой по принципу «эффекта лотоса». Вода собирается в капли и скатывается, не оставляя следов. Керамика даёт самую долгую защиту — до 8–12 недель при интенсивной носке. Примеры: Crep Protect, Tarrago Nano, профессиональные составы от MANDO MEMORI.</p>
</section>

<section id="nano">
  <h2>Как работают наноспреи: молекулярное соединение и гидрофобный эффект</h2>
  <p>Принцип действия наноспреев кардинально отличается от старых силиконовых пропиток. Вместо того чтобы просто покрыть поверхность плёнкой, наночастицы внедряются в структуру материала на молекулярном уровне.</p>
  <p>Разберём процесс по шагам:</p>
  <ol>
    <li><strong>Нанесение.</strong> Наноспрей распыляется на поверхность обуви с расстояния 20–25 см. Мелкодисперсный аэрозоль равномерно покрывает каждый участок.</li>
    <li><strong>Проникновение.</strong> Наночастицы проникают в микропоры материала и связываются с волокнами за счёт вандерваальсовых сил и химической адсорбции.</li>
    <li><strong>Полимеризация.</strong> В течение 2–12 часов (зависит от состава) наночастицы полимеризуются, образуя трёхмерную гидрофобную сетку.</li>
    <li><strong>Эффект лотоса.</strong> Структура поверхности становится микрошероховатой на наноуровне — вода не может смачивать материал и скатывается каплями, унося частицы грязи.</li>
  </ol>
  <div data-type="box" data-color="blue">
    <h4>Важно</h4>
    <p>Гидрофобный эффект работает только на чистой поверхности. Если нанести наноспрей на грязную обувь, наночастицы «запечатают» грязь внутрь, и удалить её станет значительно сложнее.</p>
  </div>
  <p>Современные наноспреи также обладают олеофобными свойствами — они отталкивают не только воду, но и маслянистые жидкости, что особенно важно для защиты от уличных реагентов и пищевых загрязнений.</p>
</section>

