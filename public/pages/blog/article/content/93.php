<section id="prep">
  <h2>Подготовка рюкзака к чистке</h2>
  <p>Независимо от материала, подготовка всегда одинакова. Пропустив этот этап, вы рискуете испортить вещь:</p>
  <ol>
    <li><strong>Освободите рюкзак.</strong> Выньте всё содержимое, включая мелочи из внутренних карманов.</li>
    <li><strong>Вытряхните пыль и крошки.</strong> Переверните рюкзак и хорошо вытряхните. Используйте пылесос с узкой насадкой для уголков.</li>
    <li><strong>Отстегните съёмные элементы.</strong> Лямки, поясной ремень, накладные карманы — отстегните их для отдельной чистки.</li>
    <li><strong>Проверьте ярлычок.</strong> На внутреннем шве есть бирка с рекомендациями по уходу. Следуйте им неукоснительно.</li>
    <li><strong>Протестируйте чистящее средство.</strong> На незаметном участке проверьте, как материал реагирует на выбранное средство.</li>
  </ol>
</section>

<section id="textile">
  <h2>Чистка тканевых рюкзаков</h2>
  <p>Тканевые рюкзаки (хлопок, лён, канвас) можно стирать, но с осторожностью:</p>
  <h3>Ручная стирка (предпочтительна)</h3>
  <ol>
    <li>Наполните таз тёплой водой (не выше 30 °C).</li>
    <li>Добавьте мягкое моющее средство (жидкое мыло, шампунь или средство для деликатных тканей).</li>
    <li>Замочите рюкзак на 15–20 минут.</li>
    <li>Мягкой щёткой потрите загрязнённые участки.</li>
    <li>Тщательно прополощите в чистой воде.</li>
    <li>Не выкручивайте — аккуратно отожмите руками.</li>
    <li>Сушите в расправленном виде при комнатной температуре.</li>
  </ol>
  <h3>Машинная стирка (с осторожностью)</h3>
  <ul>
    <li>Поместите рюкзак в специальный мешок для стирки.</li>
    <li>Используйте деликатный режим, температура не выше 30–40 °C.</li>
    <li>Отключите отжим — он может деформировать спинку и лямки.</li>
    <li>Не сушите в машинке — только естественная сушка.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Рюкзаки с каркасом, жёсткой спинкой и сложной конструкцией (туристические, городские с органайзерами) нельзя стирать в машинке — каркас деформируется, поролон сбивается. Только ручная стирка!</p>
  </div>
</section>

<section id="leather">
  <h2>Чистка кожаных рюкзаков</h2>
  <p>Кожаные рюкзаки стирать нельзя. Только поверхностная чистка:</p>
  <ol>
    <li>Протрите пыль сухой мягкой тканью.</li>
    <li>Приготовьте мыльный раствор (специальное средство для кожи или мягкое мыло).</li>
    <li>Смочите салфетку, отожмите до влажного состояния и протрите поверхность.</li>
    <li>Удалите остатки мыла чистой влажной салфеткой.</li>
    <li>Нанесите питательный крем для кожи тонким слоем.</li>
    <li>Через 10 минут отполируйте мягкой тканью до блеска.</li>
    <li>Внутреннюю часть обработайте мыльным раствором и просушите.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Для кожаных рюкзаков используйте водоотталкивающий спрей — он защитит от дождя и предотвратит появление солевых разводов.</p>
  </div>
</section>

<section id="synthetic">
  <h2>Чистка синтетики</h2>
  <p>Синтетические рюкзаки (нейлон, полиэстер, Oxford) — самые неприхотливые. Они быстро сохнут и легко чистятся:</p>
  <ul>
    <li><strong>Локальные загрязнения.</strong> Потрите мыльной губкой и смойте водой. Большинство пятен отходит легко.</li>
    <li><strong>Глубокая чистка.</strong> Стирайте вручную или в машинке на деликатном режиме с порошком для синтетики.</li>
    <li><strong>Жирные пятна.</strong> Обработайте средством для мытья посуды — оно отлично расщепляет жир на синтетике.</li>
    <li><strong>Сушка.</strong> Синтетика сохнет за 3–4 часа при комнатной температуре. Можно слегка отжать в полотенце.</li>
  </ul>
  <p>Синтетические рюкзаки — единственный тип, который можно стирать в машинке без особого риска.</p>
</section>

<section id="stains">
  <h2>Удаление пятен и запаха</h2>
  <p>Если после общей чистки остались стойкие пятна или запах, используйте эти методы:</p>
  <div data-type="compare">
    <article data-variant="good">
      <h4>Удаление сложных пятен</h4>
      <ul>
        <li>Жир — нанесите средство для мытья посуды на 30 минут</li>
        <li>Трава — замочите в молоке на 1 час</li>
        <li>Красное вино — промокните солью, через час смойте</li>
        <li>Чернила — ватный диск со спиртом (только для плотных тканей)</li>
      </ul>
    </article>
    <article data-variant="bad">
      <h4>Удаление запаха</h4>
      <ul>
        <li>Сода — насыпьте внутрь на ночь, пропылесосьте утром</li>
        <li>Замораживание — пакет с рюкзаком в морозилку на 8 часов</li>
        <li>Уксус — протрите внутри раствором 1:10 с водой</li>
        <li>Активированный уголь — таблетки внутрь на 2–3 дня</li>
      </ul>
    </article>
