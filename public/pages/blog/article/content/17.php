<section id="dwr">
  <h2>Восстановление DWR-покрытия</h2>
  <p>DWR (Durable Water Repellent) — внешнее водоотталкивающее покрытие. Оно изнашивается через 5–10 циклов носки. Признаки износа: вода перестаёт скатываться с ткани и впитывается.</p>
  <h3>Как восстановить DWR</h3>
  <ol>
    <li>После чистки и сушки нанесите DWR-пропитку (Nikwax TX.Direct или Grangers Repel Plus) на влажный внешний материал.</li>
    <li>Равномерно распылите спрей или нанесите жидкость губкой.</li>
    <li>Оставьте на 2–3 минуты, затем удалите излишки сухой тканью.</li>
    <li>Активируйте пропитку теплом: используйте фен на средней температуре (15–20 см от поверхности) или положите обувь в тёплое место на 20 минут.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Восстанавливайте DWR-покрытие после каждой 3–4 чистки. В идеале — сразу после покупки, даже если новые кроссовки уже обработаны. Заводская пропитка часто слабая, а дополнительный слой обеспечит лучшую защиту.</p>
  </div>
</section>

<section id="drying">
  <h2>Правильная сушка</h2>
  <p>Сушка мембранной обуви — критически важный этап. Неправильная сушка разрушает мембрану быстрее, чем неправильная чистка.</p>
  <ul>
    <li><strong>Только естественная сушка.</strong> Сушите при комнатной температуре вдали от батарей, обогревателей и прямых солнечных лучей.</li>
    <li><strong>Набейте бумагой.</strong> Бумага впитывает влагу и помогает сохранить форму. Меняйте бумагу каждые 4–6 часов.</li>
    <li><strong>Время сушки.</strong> Gore-Tex обувь сохнет 12–24 часа при нормальных условиях. Не надевайте влажные кроссовки — мембрана работает эффективнее в сухом состоянии.</li>
    <li><strong>Сушилки для обуви.</strong> Используйте только сушилки с температурой не выше 30–35 °C. Электрические сушилки с вентилятором — лучший выбор.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Миф о промокании</h4>
    <p>Многие считают, что Gore-Tex промокает «изнутри» — когда ноги потеют. На самом деле мембрана пропускает пар, но если обувь сильно загрязнена, поры забиваются и вентиляция ухудшается. Регулярная чистка восстанавливает дышащие свойства.</p>
  </div>
</section>

