<section id="what">
  <h2>Что такое Gore-Tex и как он работает</h2>
  <p>Gore-Tex — это мембрана из политетрафторэтилена (PTFE), которая содержит миллиарды микропор на квадратный сантиметр. Размер пор в 20 000 раз меньше капли воды, но в 700 раз больше молекулы пара. Поэтому вода не проникает внутрь, а пот испаряется.</p>
  <p>Уход за Gore-Tex отличается от обычной обуви, потому что:</p>
  <ul>
    <li><strong>Мембрана — внутренний слой.</strong> Она находится между внешним материалом и подкладкой. Повредить её можно косвенно — агрессивной химией или механическим воздействием.</li>
    <li><strong>DWR-покрытие.</strong> Внешний слой обработан водоотталкивающей пропиткой (DWR). Она изнашивается и требует восстановления.</li>
    <li><strong>Сложная конструкция.</strong> Gore-Tex обувь часто имеет проклеенные швы, язычок-гамаш и другие элементы, которые боятся высоких температур и растворителей.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно понимать</h4>
    <p>Если кроссовки Gore-Tex начали промокать — это не всегда значит, что мембрана порвалась. Чаще всего износилось DWR-покрытие, и вода просто не скатывается с поверхности. Восстановление пропитки решает проблему в 80% случаев.</p>
  </div>
</section>

<section id="clean">
  <h2>Как правильно чистить Gore-Tex</h2>
  <p>Чистка мембранной обуви — это всегда мягкая гигиена, без агрессивного воздействия.</p>
  <h3>Что понадобится</h3>
  <ul>
    <li>Специализированное чистящее средство для Gore-Tex (например, Nikwax Tech Wash или Grangers)</li>
    <li>Мягкая щётка или губка</li>
    <li>Тёплая вода — не выше 30 °C</li>
    <li>Микрофибра</li>
  </ul>
  <h3>Пошаговая инструкция</h3>
  <ol>
    <li>Удалите стельки и шнурки. Почистите их отдельно.</li>
    <li>Смойте крупные загрязнения мягкой щёткой под прохладной проточной водой. Не трите сильно — только сдвиньте грязь.</li>
    <li>Разведите средство для Gore-Tex в тёплой воде согласно инструкции.</li>
    <li>Нанесите раствор на кроссовки мягкой губкой. Особое внимание — грязным участкам.</li>
    <li>Тщательно смойте остатки средства прохладной водой. Остатки моющего средства снижают эффективность мембраны.</li>
    <li>Промокните излишки воды микрофиброй. Не выкручивайте и не сжимайте кроссовки.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Категорически нельзя</h4>
    <p>Использовать обычный стиральный порошок, кондиционер для белья, отбеливатель и пятновыводители. Они забивают поры мембраны и разрушают DWR-покрытие. После неправильной стирки Gore-Tex может потерять свои свойства навсегда.</p>
  </div>
</section>

