<section id="restoration">
  <h2>Восстановление покрытия каблука</h2>
  <p>Даже если каблук не сломан, со временем его покрытие истирается: появляются царапины, сколы лака, потёртости. Восстановление покрытия включает несколько этапов:</p>
  <ol>
    <li><strong>Шлифовка.</strong> Каблук зачищается от старого лака и неровностей. Используется наждачная бумага разной зернистости.</li>
    <li><strong>Грунтовка.</strong> Наносится специальный грунт для улучшения сцепления краски с поверхностью.</li>
    <li><strong>Покраска.</strong> В несколько слоёв наносится краска под цвет каблука. Каждый слой просушивается.</li>
    <li><strong>Лаковое покрытие.</strong> Финишный слой лака защищает краску от истирания и придаёт блеск.</li>
  </ol>
  <p>Восстановление покрытия возможно для всех типов каблуков: пластиковых, деревянных и металлических. Главное — точный подбор цвета краски.</p>
  <div data-type="box" data-color="blue">
    <h4>Сложный цвет</h4>
    <p>Если каблук имеет сложный оттенок (металлик, перламутр, хамелеон), восстановление покрытия лучше доверить профессионалам. В MANDO MEMORI мы колеруем краску под любой цвет, добиваясь полного совпадения с оригиналом.</p>
  </div>
