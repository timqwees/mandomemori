<section id="care">
  <h2>Уход за каблуками в повседневной носке</h2>
  <p>Чтобы каблуки служили дольше, соблюдайте несколько простых правил:</p>
  <ul>
    <li><strong>Осматривайте набойки раз в месяц.</strong> Износ набойки на шпильке заметен сразу — она становится короче на 2–3 мм.</li>
    <li><strong>Не ходите в обуви на каблуке по бездорожью.</strong> Гравий, брусчатка, стыки плитки — главные враги шпилек.</li>
    <li><strong>Снимайте обувь перед подъёмом по лестнице.</strong> Удар каблука о край ступени — частая причина сколов.</li>
    <li><strong>Храните туфли с колодками.</strong> Это сохраняет форму каблука и предотвращает деформацию.</li>
    <li><strong>Чистите каблук мягкой тканью.</strong> Абразивные губки царапают покрытие.</li>
  </ul>
