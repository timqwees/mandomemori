<section id="types">
  <h2>Типы каблуков и их поломки</h2>
  <p>Женские каблуки бывают разных типов, и у каждого — свои типичные проблемы:</p>
  <ul>
    <li><strong>Шпильки (тонкие, до 1 см).</strong> Самая частая поломка — сломанный наконечник, погнутый металлический стержень, износ полиуретановой набойки.</li>
    <li><strong>Широкие каблуки (столбики, прямоугольные).</strong> Типичные проблемы: сколы покрытия, износ набойки, деформация каблука при асимметричной нагрузке.</li>
    <li><strong>Клиновидные каблуки (танкетки).</strong> Проблемы: трещины на боковой поверхности, истирание нижнего края.</li>
    <li><strong>Французские каблуки (изогнутые).</strong> Сложность в том, что нагрузка распределяется неравномерно — чаще всего страдает внутренняя сторона.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Когда каблук не спасти</h4>
    <p>Если трещина проходит через весь корпус каблука или металлический стержень сломан внутри — ремонт невозможен. В таких случаях мастер может предложить полную замену каблука, но это возможно не для всех моделей обуви.</p>
  </div>
</section>

<section id="heeltip">
  <h2>Замена набоек на шпильках</h2>
  <p>Самая частая услуга в мастерской — замена набоек на тонких шпильках. Полиуретановый наконечник стирается быстро, особенно на асфальте и плитке.</p>
  <h3>Как часто менять набойки на шпильках</h3>
  <ul>
    <li>При ежедневной носке — каждые 2–3 месяца.</li>
    <li>При сезонной носке — раз в 4–5 месяцев.</li>
    <li>При редкой (1–2 раза в неделю) — раз в 6–8 месяцев.</li>
  </ul>
  <h3>Процесс замены</h3>
  <ol>
    <li>Мастер срезает остатки старой набойки специальным ножом.</li>
    <li>Зачищает металлический стержень каблука от ржавчины и клея.</li>
    <li>Подбирает набойку по диаметру стержня.</li>
    <li>Фиксирует новую набойку на клей и механическим способом.</li>
    <li>Обтачивает край набойки заподлицо с каблуком.</li>
  </ol>
  <div data-type="box" data-color="gray">
    <h4>На заметку</h4>
    <p>На шпильки ставят полиуретановые набойки — они мягкие, хорошо амортизируют и не стучат при ходьбе. Металлические набойки для шпилек не используются, так как они скользят и царапают пол.</p>
  </div>
</section>

