<section id="daily">
  <h2>Ежедневная летняя чистка</h2>
  <p>Летом пыль и грязь въедаются в материалы быстрее из-за жары и пота. Ежедневный уход занимает 5 минут, но предотвращает глубокие загрязнения:</p>
  <ul>
    <li><strong>После каждой носки</strong> протирайте обувь влажной салфеткой или пеной для чистки. Не оставляйте грязь на ночь.</li>
    <li><strong>Подошву</strong> мойте чаще — летом на ней скапливается пыль, мелкие камни и жвачка.</li>
    <li><strong>Шнурки</strong> стирайте раз в неделю — они впитывают пот и пыль быстрее других частей обуви.</li>
    <li><strong>Внутреннюю поверхность</strong> обрабатывайте антибактериальным спреем раз в 3–4 дня.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Почему это важно</h4>
    <p>Летом бактерии размножаются в 2–3 раза быстрее из-за тепла и влажности. Ежедневная чистка — не про эстетику, а про гигиену.</p>
  </div>
</section>

<section id="sweat">
  <h2>Защита от пота и запаха</h2>
  <p>Летняя жара провоцирует потоотделение, и обувь страдает первой. Запах и бактерии — главные проблемы тёплого сезона:</p>
  <h3>Профилактика запаха</h3>
  <ul>
    <li><strong>Стельки.</strong> Используйте сменные стельки с антибактериальной пропиткой. Меняйте их раз в 1–2 месяца.</li>
    <li><strong>Дезодорант для обуви.</strong> Наносите после каждой носки. Выбирайте спреи с пробиотиками — они не маскируют запах, а устраняют его причину.</li>
    <li><strong>Сушка.</strong> Давайте обуви «отдохнуть» 24 часа между носками. За это время она полностью высохнет изнутри.</li>
    <li><strong>Носки.</strong> Носите дышащие носки из хлопка, бамбука или мериноса. Синтетика усиливает потоотделение.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Экспресс-метод</h4>
    <p>Положите на ночь в кроссовки пакетики с силикагелем (те, что кладут в коробки с обувью). Они впитают лишнюю влагу и предотвратят размножение бактерий.</p>
  </div>
</section>

<section id="sun">
  <h2>Ультрафиолет и перегрев</h2>
  <p>Солнце — скрытая угроза для летней обуви. Ультрафиолет разрушает молекулы красителей и полимеров, а перегрев деформирует материалы.</p>
  <ul>
    <li><strong>Выцветание.</strong> Под воздействием УФ-лучей цвета выгорают за 1–2 месяца активной носки. Особенно уязвимы яркие и тёмные оттенки.</li>
    <li><strong>Деформация.</strong> Оставленные на солнце кроссовки могут «сесть» или покоробиться. Кожа теряет влагу и трескается.</li>
    <li><strong>Перегрев подошвы.</strong> Чёрная резина на асфальте нагревается до 50–60 °C, что ускоряет износ клеевых соединений.</li>
  </ul>
  <p><strong>Что делать:</strong> не оставляйте обувь на солнце, сушите в тени, для защиты используйте водоотталкивающие спреи с УФ-фильтрами.</p>
</section>

<section id="wash">
  <h2>Чистка разных материалов</h2>
  <p>Каждый летний материал требует своего подхода. Универсального метода не существует:</p>
  <div data-type="compare">
    <article data-variant="good">
      <h4>Текстиль и сетка</h4>
      <ul>
        <li>Мягкая щётка + мыльная вода</li>
        <li>Удаление пятен пятновыводителем</li>
        <li>Сушка в тени с бумагой внутри</li>
        <li>Водоотталкивающая пропитка</li>
        <li>Проветривание после каждой носки</li>
      </ul>
    </article>
    <article data-variant="bad">
      <h4>Чего избегать</h4>
      <ul>
        <li>Машинная стирка деликатных моделей</li>
        <li>Стирка в горячей воде (выше 40 °C)</li>
        <li>Отбеливатели и хлор</li>
        <li>Сушка на солнце или батарее</li>
        <li>Использование агрессивной химии</li>
      </ul>
    </article>
  </div>
  <p>Для кожаной летней обуви используйте только пену для кожи и питательный крем. Замшу чистите специальной щёткой после полного высыхания.</p>
</section>

<section id="storage">
  <h2>Летнее хранение</h2>
  <p>Даже в разгар сезона обувь нужно правильно хранить между носками:</p>
  <ul>
    <li>Используйте проветриваемые обувные полки — никаких пластиковых коробок без доступа воздуха.</li>
    <li>Вставляйте в обувь бумажные комки или колодки для сохранения формы.</li>
    <li>Не храните обувь в прихожей у входной двери — там высокая влажность и перепады температур.</li>
    <li>Раз в неделю устраивайте «день отдыха» для каждой пары — не носите одну и ту же обувь два дня подряд.</li>
  </ul>
</section>

