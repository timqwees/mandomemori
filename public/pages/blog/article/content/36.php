<section id="signs">
  <h2>Признаки износа набоек</h2>
  <p>Определить, что набойки пора менять, можно по нескольким характерным признакам. Не обязательно ждать, пока они сотрутся под ноль:</p>
  <ul>
    <li><strong>Протёрт до металла.</strong> Если вы видите металлическую основу каблука — набойка стёрта полностью. Дальнейшая носка приведёт к повреждению самого каблука.</li>
    <li><strong>Асимметричный износ.</strong> Если набойка стёрта с одной стороны сильнее — это говорит о неправильной походке или деформации каблука.</li>
    <li><strong>Стук при ходьбе.</strong> Металлический звук означает, что набойка стёрта до твёрдого основания. Срочно к мастеру.</li>
    <li><strong>Скольжение.</strong> Если подошва в области каблука стала гладкой, как стекло — набойка потеряла сцепление с поверхностью.</li>
    <li><strong>Визуальное истончение.</strong> Сравните высоту набойки с новой парой — разница в 2–3 мм уже критична.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Не откладывайте</h4>
    <p>Если набойка стёрта до пластика или металла, каждый день носки разрушает каблук. Ремонт каблука обойдётся в 2–3 раза дороже, чем своевременная замена набоек.</p>
  </div>
</section>

<section id="dangers">
  <h2>Чем опасна изношенная набойка</h2>
  <p>Многие считают, что стёртая набойка — только эстетическая проблема. На самом деле последствия серьёзнее:</p>
  <ul>
    <li><strong>Деформация каблука.</strong> При износе набойки нагрузка распределяется неправильно, каблук может наклониться или треснуть.</li>
    <li><strong>Проблемы с осанкой.</strong> Разная высота каблуков из-за асимметричного износа заставляет таз наклоняться, что приводит к болям в спине и суставах.</li>
    <li><strong>Ударная нагрузка.</strong> Набойка амортизирует удар при ходьбе. Без неё каждый шаг отдаётся в позвоночник.</li>
    <li><strong>Повреждение настила.</strong> Металлическая шпилька царапает полы, паркет и ламинат.</li>
    <li><strong>Риск падения.</strong> Гладкая набойка не имеет сцепления, особенно на мокром полу или плитке.</li>
  </ul>
  <div data-type="box" data-color="blue">
    <h4>Факт</h4>
    <p>По статистике нашей мастерской, 40% обращений по ремонту каблуков связаны с тем, что клиенты вовремя не заменили набойки. Профилактическая замена стоит от 500 ₽, а ремонт каблука — от 2000 ₽.</p>
  </div>
</section>

