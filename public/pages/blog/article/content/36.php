<section id="check">
  <h2>Как самостоятельно проверить износ</h2>
  <p>Проверить состояние набоек можно дома за 30 секунд без специальных инструментов:</p>
  <ol>
    <li>Поставьте обувь на ровную поверхность и покачайте — если качается, одна набойка стёрта сильнее.</li>
    <li>Посмотрите на каблук сзади: если видна металлическая или пластиковая основа — набойки нет.</li>
    <li>Проведите пальцем по краю набойки: если он острый и скошен — износ уже значительный.</li>
    <li>Проверьте высоту набойки — новая набойка обычно 4–6 мм, критический износ — 1–2 мм.</li>
    <li>Пройдитесь по твёрдому полу: слышите стук? Значит, набойка стёрта до основания.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Хитрость</h4>
    <p>Возьмите обе туфли и поставьте их рядом подносками друг к другу. Если каблуки на одной высоте — набойки в порядке. Разница больше 2 мм — пора в мастерскую.</p>
  </div>
</section>

<section id="frequency">
  <h2>Как часто менять набойки</h2>
  <p>Частота замены зависит от нескольких факторов. Вот средние показатели для разных типов использования:</p>
  <ul>
    <li><strong>Ежедневная носка в офисе:</strong> каждые 3–4 месяца для резиновых набоек.</li>
    <li><strong>Уличная носка в любую погоду:</strong> каждые 2–3 месяца — абразивы и реагенты ускоряют износ.</li>
    <li><strong>Сезонное использование (осень-весна):</strong> раз в полгода, перед началом сезона.</li>
    <li><strong>Редкая носка (1–2 раза в неделю):</strong> раз в 8–10 месяцев.</li>
    <li><strong>Дорогая дизайнерская обувь:</strong> каждый сезон — лучше ставить новые набойки, чем рисковать каблуком.</li>
  </ul>
  <p>Лучше всего проверять набойки каждый месяц, особенно в сезон активной носки. Заодно можно оценить состояние профилактики и подошвы в целом.</p>
