<section id="deformed">
  <h2>Критическая деформация каркаса</h2>
  <p>Каркас — основа обуви. Если он разрушен, восстановить форму и функциональность невозможно.</p>
  <p>Признаки разрушения каркаса:</p>
  <ul>
    <li><strong>Подошва не держит форму.</strong> Кроссовок или туфля «складывается» посередине при ходьбе. Это значит, что межподошва (пенный слой) потеряла жёсткость.</li>
    <li><strong>Пяточная чашка сломана.</strong> Задник не фиксирует пятку. Если задник сломан полностью — замена возможна, но если разрушен и материал вокруг — нет.</li>
    <li><strong>Перекос обуви.</strong> Обувь наклоняется набок при постановке на ровную поверхность. Это говорит о серьёзной деформации подошвы и каркаса.</li>
    <li><strong>Супинатор провалился.</strong> Внутренний супинатор (металлическая или пластиковая вставка) сломался — обувь потеряла поддержку свода стопы.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Когда ремонт невозможен</h4>
    <p>Если треснула пяточная чашка, сломался супинатор или подошва складывается пополам — ремонт экономически нецелесообразен. Даже если мастер возьмётся за работу, результат будет недолговечным.</p>
  </div>
</section>

<section id="material">
  <h2>Разрушение материалов верха</h2>
  <p>Материалы тоже имеют срок жизни. Кожа, замша, текстиль — все они со временем теряют свойства.</p>
  <ul>
    <li><strong>Кожа «поплыла».</strong> Натуральная кожа потеряла эластичность, покрылась трещинами и не реагирует на крем. Это необратимый процесс старения.</li>
    <li><strong>Экокожа рассыпалась.</strong> Искусственная кожа (полиуретан) со временем отслаивается от тканевой основы. Если пошёл процесс «осыпания» — остановить его нельзя.</li>
    <li><strong>Замша протёрта до дыр.</strong> Замша не восстанавливается в местах сквозного износа. Заплатка будет видна.</li>
    <li><strong>Текстиль «посыпается».</strong> Нитки и волокна теряют прочность — характерно для кроссовок после 2–3 сезонов активной носки.</li>
  </ul>
  <p>Скорость разрушения материалов зависит от ухода. При регулярной чистке и пропитке кожа служит 5–7 лет, при отсутствии ухода — 2–3 года. Экокожа служит 2–3 сезона независимо от ухода.</p>
  <div data-type="box" data-color="gray">
    <h4>Тест на эластичность</h4>
    <p>Надавите пальцем на материал в незаметном месте. Если появилась трещина или сетка мелких трещинок — материал потерял эластичность. Если после нажатия осталась вмятина — кожа «устала». Оба признака говорят о том, что ремонт будет неэффективным.</p>
  </div>
</section>

<section id="sole">
  <h2>Полный износ подошвы</h2>
  <p>Подошва изнашивается быстрее всего, но не каждый износ — приговор. Вот когда подошву уже не спасти:</p>
  <ul>
    <li><strong>Сквозная дыра.</strong> Если подошва протёрта до дыр — наращивание возможно, но это временное решение. Рекомендуется полная замена подошвы.</li>
    <li><strong>Массовые трещины.</strong> Если подошва покрыта сетью трещин по всей площади — материал разрушен, клей не даст надёжного соединения.</li>
    <li><strong>Отслоение по всей площади.</strong> Если подошва отходит от верха по всей длине и при этом материал подошвы стал хрупким — замена неизбежна.</li>
    <li><strong>Расслоение материала.</strong> У дешёвых подошв из вспененного полиуретана часто происходит расслоение — материал крошится. Это не лечится.</li>
  </ul>
  <div data-type="box" data-color="blue">
    <h4>Исключение</h4>
    <p>Полная замена подошвы возможна для дорогой обуви с кожаной подошвой (классические туфли). Для кроссовок и современной обуви на литой подошве замена часто невозможна из-за конструктивных особенностей. В MANDO MEMORI мы оцениваем возможность замены подошвы индивидуально.</p>
  </div>
</section>

<section id="smell">
  <h2>Неустранимый запах</h2>
  <p>Запах может стать причиной, по которой обувь придётся выбросить. Не все запахи выводятся:</p>
  <ul>
    <li><strong>Запах плесени и грибка.</strong> Если грибок проник в структуру материала и стельки, удалить его запах полностью очень сложно. Требуется профессиональная обработка, а в запущенных случаях — замена стелек и внутренней отделки.</li>
    <li><strong>Химический запах.</strong> Если обувь пропиталась бензином, маслом или растворителем — запах может остаться навсегда.</li>
    <li><strong>Запах старого пота.</strong> Если бактерии проникли глубоко в материал и стельки — даже профессиональная чистка может не помочь. Внутренняя отделка впитывает запах необратимо.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Когда проще выбросить</h4>
    <p>Если обувь имеет запах плесени после просушки, а стельки и внутренняя отделка потемнели от влаги — это признак глубокого поражения грибком. Носить такую обувь опасно для здоровья ног.</p>
  </div>
</section>

