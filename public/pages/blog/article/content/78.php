<section id="saffiano">
  <h2>Уход за кожей Saffiano и другими кожаными моделями Prada</h2>
  <p>Saffiano — визитная карточка Prada. Это кожа с характерным тиснением в виде диагональной сетки, обработанная воском. Она устойчива к царапинам и воде, что делает её практичнее обычной гладкой кожи.</p>
  <h3>Особенности Saffiano</h3>
  <ul>
    <li>Не боится царапин — тиснение маскирует мелкие повреждения</li>
    <li>Устойчива к воде благодаря восковому покрытию</li>
    <li>Не требует частого питания кремом (воск сохраняет эластичность)</li>
    <li>Сложно поддаётся покраске — воск отталкивает краситель</li>
  </ul>
  <h3>Чистка Saffiano</h3>
  <ul>
    <li><strong>Ежедневно.</strong> Протирайте влажной микрофиброй — пыль и грязь удаляются легко.</li>
    <li><strong>Пятна.</strong> Мыльный раствор с мягкой губкой. Saffiano не боится воды, но не замачивайте надолго.</li>
    <li><strong>Питание.</strong> Раз в 2–3 месяца — восковой крем для кожи (Saphir, Collonil). Наносите тонким слоем, полируйте до блеска.</li>
    <li><strong>Цвет.</strong> Saffiano редко нуждается в покраске, но при потускнении — используйте крем-краситель для гладкой кожи.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Для другой кожи Prada</h4>
    <p>Гладкая кожа Prada (без тиснения) более капризна. Используйте крем-кондиционер раз в 2 недели, избегайте замачивания. Анилиновая кожа — только сухая чистка.</p>
  </div>
</section>

<section id="technical">
  <h2>Уход за техническими тканями Prada</h2>
  <p>Prada активно использует современные синтетические материалы: нейлон (Re-Nylon), полиэстер, сетку и мембранные ткани. Особенно популярны модели из переработанного нейлона Re-Nylon — флагманская эко-линия Prada.</p>
  <ul>
    <li><strong>Чистка нейлона (Re-Nylon).</strong> Тёплая вода с мягким мылом, губка. Никаких отбеливателей и кондиционеров. Re-Nylon не впитывает грязь глубоко — поверхностные загрязнения удаляются легко.</li>
    <li><strong>Сетка.</strong> Пенный очиститель, мягкая щётка. Не трите — сетка может растянуться.</li>
    <li><strong>Мембранные ткани.</strong> Используйте средства для Gore-Tex и мембран (Nikwax, Grangers). Не нарушайте DWR-пропитку.</li>
    <li><strong>Водоотталкивающая пропитка.</strong> Для всех технических тканей Prada — обязательна. Спрей для синтетических материалов защищает от грязи и влаги.</li>
  </ul>
</section>

<section id="sneakers">
  <h2>Чистка кроссовок Prada</h2>
  <p>Кроссовки Prada — America‘s Cup, Cloudbust, Downtown — имеют сложную конструкцию из нескольких материалов.</p>
  <h3>America‘s Cup</h3>
  <p>Культовая модель. Верх — Saffiano кожа, подошва — резина с зубчатым протектором, шнурки — толстые, вощёные.</p>
  <p><strong>Чистка:</strong> Saffiano — влажная микрофибра. На подошве — щётка средней жёсткости. Шнурки — замочить в мыльном растворе на 15 минут, прополоскать. Внутренняя часть — протереть антибактериальной салфеткой.</p>
  <h3>Cloudbust</h3>
  <p>Модель из технической сетки с резиновыми вставками и массивной подошвой. Чистка: сетка — пенный очиститель, резиновые вставки — влажная ткань. Подошва — щётка, особое внимание — протектору в виде волн.</p>
  <h3>Downtown</h3>
  <p>Кроссовки из кожи Saffiano с контрастной подошвой. Чистка как America‘s Cup, но подошва требует аккуратности — белая резина желтеет. Используйте отбеливание перекисью или обратитесь в MANDO MEMORI.</p>
  <div data-type="box" data-color="gray">
    <h4>Проблемное место</h4>
    <p>У всех кроссовок Prada — задник из замши или нубука внутри пятки. Он быстро пачкается от контакта с ногой. Чистите его сухой щёткой и антибактериальным спреем.</p>
  </div>
</section>

<section id="logo">
  <h2>Уход за логотипом Prada и фурнитурой</h2>
  <p>Логотип Prada — треугольная табличка из металла, эмали или смолы. Встречается на большинстве моделей. Уход за логотипом:</p>
  <ul>
    <li><strong>Металлический треугольник.</strong> Протирайте сухой мягкой тканью. Не используйте абразивы — царапается.</li>
    <li><strong>Эмалированный.</strong> Чистите ватной палочкой с мыльной водой. Не используйте спирт — эмаль потускнеет.</li>
    <li><strong>Из смолы.</strong> Смола со временем желтеет. Это необратимый процесс, замедлить который помогает хранение вдали от УФ-лучей.</li>
  </ul>
  <p>Фурнитура (молнии, люверсы, пряжки) — из металла с покрытием. Чистите мягкой тканью, при потускнении — салфетка для ювелирки. Не используйте полироли.</p>
</section>

<section id="mistakes">
  <h2>Чего нельзя делать с обувью Prada</h2>
  <ul>
    <li><strong>Не стирайте в машинке.</strong> Даже модели из Re-Nylon не предназначены для машинной стирки — деформируются вставки из кожи.</li>
    <li><strong>Не замачивайте Saffiano.</strong> Хотя Saffiano не боится воды, длительное замачивание разрушает восковое покрытие.</li>
    <li><strong>Не используйте растворители.</strong> Ацетон, спирт, бензин — разрушают кожу Saffiano и синтетические ткани.</li>
    <li><strong>Не сушите на батарее.</strong> Термоклей отслаивается, кожа трескается.</li>
    <li><strong>Не игнорируйте пятна.</strong> Свежие пятна на Saffiano удаляются легче, чем застарелые.</li>
  </ul>
</section>

<section id="storage">
  <h2>Хранение обуви Prada</h2>
  <ul>
    <li><strong>Колодки.</strong> Для кожаных моделей — обязательно. Для кроссовок из синтетики — желательно.</li>
    <li><strong>Оригинальная коробка.</strong> Prada упаковывает обувь в прочные коробки с вентиляционными отверстиями. Используйте их.</li>
    <li><strong>Раздельное хранение.</strong> Каждую пару — в отдельном пылевике. Фурнитура может царапать соседнюю пару.</li>
    <li><strong>Температура.</strong> 15–25 °C. Избегайте чердаков и подвалов — перепады влажности вредят обуви.</li>
    <li><strong>Свет.</strong> УФ-лучи выцветают цвет Saffiano и желтят подошвы. Храните в тёмном месте.</li>
  </ul>
</section>

