<section id="textilepaint">
  <h2>Покраска текстильных кроссовок</h2>
  <p>Текстиль (сетка, нейлон, полиэстер) — сложный материал для покраски. Краска должна быть гибкой и не забивать поры, чтобы сохранить дышимость.</p>
  <h3>Специфика покраски текстиля</h3>
  <ul>
    <li><strong>Только акриловая краска для ткани.</strong> Обычные аэрозоли для кожи делают текстиль жёстким и ломким.</li>
    <li><strong>Пропитка.</strong> Краска должна пропитать волокна, а не лежать на поверхности. Наносите кистью, втирая в ткань.</li>
    <li><strong>Фиксация утюгом.</strong> После высыхания прогладьте окрашенные участки через тонкую ткань утюгом (без пара). Температура — средняя. Это фиксирует краску в волокнах.</li>
    <li><strong>2–3 слоя.</strong> Текстиль впитывает краску сильнее кожи. Первый слой может выглядеть бледным — это нормально.</li>
  </ul>
  <p>Полная покраска текстильных кроссовок в домашних условиях — задача со звёздочкой. Если не уверены — попробуйте покрасить только отдельные элементы (например, пятку или язычок).</p>
