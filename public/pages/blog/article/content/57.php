<section id="choosepaint">
  <h2>Выбор краски для разных материалов</h2>
  <p>Кроссовки делают из разных материалов, и каждый требует своей краски. Универсального варианта не существует.</p>
  <ul>
    <li><strong>Для гладкой кожи.</strong> Аэрозольная краска для кожи (Tarrago, Saphir, Angelus). Содержит пигменты и пластификаторы, которые не трескаются при сгибе.</li>
    <li><strong>Для замши и нубука.</strong> Аэрозоль для замши с пигментом. Окрашивает ворс, не склеивая его. Обычная краска для кожи испортит ворсовую поверхность.</li>
    <li><strong>Для текстиля и сетки.</strong> Акриловая краска для ткани (Angelus Textile Paint, Jacquard). Гибкая, проникает в волокна, не забивает сетку. Требует фиксации утюгом.</li>
    <li><strong>Для синтетических материалов (PU, PVC).</strong> Специальные краски для синтетики с высокой адгезией. Обычные краски отслаиваются от гладкого синтетического покрытия.</li>
    <li><strong>Для подошвы.</strong> Краска для резины (Angelus Sole Paint). Содержит эластичные полимеры, которые выдерживают нагрузку при ходьбе.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Чего нельзя делать</h4>
    <p>Не используйте акриловую краску для художественных работ — она трескается и осыпается при первом сгибе. Не используйте автомобильную эмаль — она слишком агрессивна для обувных материалов. Только профессиональные краски для обуви.</p>
  </div>
</section>

<section id="prep">
  <h2>Подготовка кроссовок к покраске</h2>
  <p>Подготовка — 60% успеха покраски. Пропуск этого этапа гарантирует, что краска отслоится через неделю.</p>
  <h3>Пошаговая подготовка</h3>
  <ol>
    <li><strong>Выньте шнурки и стельки.</strong> Они не должны мешать покраске и могут быть окрашены отдельно.</li>
    <li><strong>Очистите кроссовки.</strong> Вымойте мыльным раствором, удалите грязь из швов старой зубной щёткой. Просушите.</li>
    <li><strong>Обезжирьте.</strong> Протрите поверхность спиртом или ацетоном. Удалите следы крема, воска и силикона.</li>
    <li><strong>Зашлифуйте (для гладкой кожи).</strong> Пройдитесь мелкой наждачкой (P1000) по глянцевым участкам для улучшения сцепления краски.</li>
    <li><strong>Замаскируйте.</strong> Заклейте малярным скотчем подошву, логотипы, молнии и все элементы, которые не должны быть окрашены. Для сложных участков используйте жидкий маскировочный состав.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Важный нюанс</h4>
    <p>Обезжиривание обязательно даже для новой обуви. Заводские покрытия содержат силикон, который мешает адгезии краски. Без обезжиривания краска отслоится через 2–3 дня.</p>
  </div>
</section>

<section id="leatherpaint">
  <h2>Покраска кожаных кроссовок</h2>
  <p>Кожаные кроссовки (Nike Air Force 1, Air Jordan 1, Adidas Samba кожаные) красятся проще всего — кожа отлично принимает краску.</p>
  <h3>Процесс покраски</h3>
  <ol>
    <li>После подготовки нанесите первый слой краски аэрозолем с расстояния 20–25 см. Движения — быстрые, равномерные.</li>
    <li>Дайте высохнуть 20–30 минут.</li>
    <li>Нанесите второй слой. Проверьте, не просвечивает ли старый цвет.</li>
    <li>При необходимости — третий слой. Для чёрного цвета поверх белого может потребоваться 4–5 слоёв.</li>
    <li>Дайте полное высыхание — 2–3 часа.</li>
    <li>Нанесите фиксатор для защиты цвета от влаги и истирания.</li>
  </ol>
  <h3>Особенности для разных моделей</h3>
  <ul>
    <li><strong>Nike Air Force 1.</strong> Обратите внимание на перфорацию — краска не должна забить отверстия. Используйте тонкие слои.</li>
    <li><strong>Air Jordan 1.</strong> Сложная геометрия — уделите особое внимание маскировке союзки и "Swoosh".</li>
    <li><strong>Adidas Samba кожаные.</strong> Тонкая кожа — не переусердствуйте со слоями, чтобы не утяжелить материал.</li>
  </ul>
  <p>Если на коже есть трещины и потёртости, перед покраской заполните их жидкой кожей или восковым наполнителем.</p>
  <div data-type="box" data-color="dark">
    <h4>Совет</h4>
    <p>Для сложной кастомизации (градиент, рисунки) используйте жидкие краски Angelus и кисти. Техника требует практики — сначала потренируйтесь на бумаге. В MANDO MEMORI мы предлагаем профессиональную кастомизацию аэрографом.</p>
