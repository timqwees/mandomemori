<section id="suedepaint">
  <h2>Покраска замшевых кроссовок</h2>
  <p>Замша (Asics Gel-Lyte III, New Balance 990, Adidas Samba замшевые) требует особого подхода — любая ошибка видна на ворсе.</p>
  <h3>Особенности покраски замши</h3>
  <ul>
    <li><strong>Специальная краска.</strong> Только аэрозоль для замши и нубука. Обычная краска склеит ворс.</li>
    <li><strong>Подготовка ворса.</strong> Перед покраской поднимите ворс резиновой щёткой — краска ляжет ровнее.</li>
    <li><strong>Не переборщите.</strong> Достаточно 1–2 лёгких слоёв. Сильное намокание испортит структуру замши.</li>
    <li><strong>Восстановление ворса после покраски.</strong> После высыхания пройдитесь по поверхности резиновой щёткой или ластиком для замши.</li>
  </ul>
  <p>Покраска замши — самый сложный тип домашней покраски. Рекомендуется только для опытных. Начинающим лучше начать с кожаных кроссовок.</p>
  <div data-type="box" data-color="gray">
    <h4>Совет</h4>
    <p>Если замша на кроссовках имеет разные оттенки на разных деталях (что часто бывает у Asics и New Balance), красить нужно каждую деталь отдельно, используя маскировку межпанельных швов.</p>
  </div>
</section>

<section id="fixation">
  <h2>Фиксация и уход после покраски</h2>
  <p>После покраски цвет нужно закрепить. От этого зависит, как долго продержится новый цвет.</p>
  <h3>Фиксаторы для разных материалов</h3>
  <ul>
    <li><strong>Для кожи:</strong> фиксатор-аэрозоль (Angelus Finisher, Tarrago Fixer). Наносится через 30 минут после последнего слоя краски. Защищает от влаги и УФ-лучей.</li>
    <li><strong>Для текстиля:</strong> фиксация утюгом (как описано выше). Дополнительно можно использовать текстильный фиксатор.</li>
    <li><strong>Для замши:</strong> водоотталкивающий спрей для замши выполняет роль фиксатора.</li>
    <li><strong>Для подошвы:</strong> специальный фиксатор для резины или просто 48 часов без носки.</li>
  </ul>
  <h3>Уход за покрашенными кроссовками</h3>
  <ol>
    <li>Не носите 24 часа после фиксации — краска должна полимеризоваться.</li>
    <li>Первую неделю избегайте дождя и влаги.</li>
    <li>Чистите окрашенные кроссовки только влажной тканью, без агрессивной химии.</li>
    <li>Обновляйте водоотталкивающую пропитку раз в 2–3 недели.</li>
    <li>При появлении потёртостей — подкрасьте локально кисточкой.</li>
  </ol>
  <p>При правильном уходе домашняя покраска продержится 3–6 месяцев. Затем можно обновить цвет одним слоем краски без полного цикла.</p>
