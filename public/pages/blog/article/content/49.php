<section id="criteria">
  <h2>Основные критерии выбора сапожной мастерской</h2>
  <p>Не все мастерские одинаково хороши. Вот ключевые критерии, которые помогут отличить профессионала от дилетанта:</p>
  <ul>
    <li><strong>Специализация.</strong> Универсальные мастерские у метро часто берутся за любую работу, но качество страдает. Специализированные мастерские (по видам обуви или услуг) обычно работают лучше.</li>
    <li><strong>Срок работы.</strong> Мастерская, работающая 5–10+ лет в одном месте, дорожит репутацией. Новички на рынке — лотерея.</li>
    <li><strong>Оборудование.</strong> Хорошая мастерская оснащена профессиональным оборудованием: обувные швейные машины (не бытовые!), прессы для профилактики, термопечи, станки для шлифовки.</li>
    <li><strong>Сертификаты и материалы.</strong> Профессионалы используют сертифицированные материалы известных брендов (YKK, Riri, Vibram, Bama). Дешёвые аналоги — признак экономии на качестве.</li>
    <li><strong>Прозрачность.</strong> Мастер должен объяснить, что будет делать, какие материалы использовать и сколько это будет стоить. Если ответы уклончивые — ищите другую мастерскую.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Первый визит</h4>
    <p>Зайдите в мастерскую перед тем, как нести обувь на ремонт. Оцените чистоту рабочего места, наличие сертификатов на материалы, отношение мастера. Хороший мастер никогда не отказывается показать примеры работ.</p>
  </div>
</section>

<section id="check">
  <h2>Как проверить мастерскую перед заказом</h2>
  <p>Прежде чем доверить обувь, проведите небольшую проверку. Она займёт 10 минут, но сэкономит нервы и деньги:</p>
  <ol>
    <li><strong>Тестовый звонок.</strong> Позвоните и задайте конкретные вопросы: «Сколько стоит замена молнии на сапогах из натуральной кожи?», «Какие молнии вы используете?». Если мастер говорит невнятно или отвечает «от 500 до 3000» — насторожитесь.</li>
    <li><strong>Визит в мастерскую.</strong> Посмотрите на коробки с материалами. Должны быть бренды YKK, Opti (молнии), Bama, Vibram (подошвы), Collonil, Saphir (средства ухода).</li>
    <li><strong>Спросите про гарантию.</strong> Профессиональная мастерская даёт гарантию на работу от 14 до 60 дней. Если гарантии нет — это плохой знак.</li>
    <li><strong>Проверьте примеры работ.</strong> Попросите показать фото «до/после» или готовые работы, которые ждут выдачи. Качество строчек и швов должно быть аккуратным.</li>
    <li><strong>Оцените отношение.</strong> Мастер должен выслушать проблему, осмотреть обувь и предложить варианты. Если он говорит «сделаем» без осмотра — вероятно, качество пострадает.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Красные флаги</h4>
    <p>Отсутствие ценника, грязь на рабочем месте, рваные перчатки мастера, отказ показать примеры работ, обещания «сделать за 30 минут» для сложного ремонта, отсутствие гарантии. Один-два признака — уже повод задуматься.</p>
  </div>
</section>

