<section id="mistakes">
  <h2>Чего избегать при выборе мастерской</h2>
  <p>На основе опыта наших клиентов — типичные ошибки при выборе мастерской:</p>
  <ul>
    <li><strong>Выбор по принципу «ближе к дому».</strong> Мастерская рядом с домом может быть не лучшей. Лучше потратить 20 минут на дорогу к проверенному мастеру.</li>
    <li><strong>Самый дешёвый вариант.</strong> Экономия 300–500 ₽ может обернуться испорченной обувью. Качественный ремонт не может стоить подозрительно дёшево.</li>
    <li><strong>Доверие «мастеру с улицы».</strong> Частные мастера без мастерской и оборудования часто делают работу хуже и не дают гарантии.</li>
    <li><strong>Срочный ремонт.</strong> «Сделайте за час» — плохая идея. Качественный ремонт требует времени для полимеризации клея, сушки краски.</li>
