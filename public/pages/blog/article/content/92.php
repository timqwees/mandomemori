<section id="tools">
  <h2>Инструменты для ухода</h2>
  <p>Для ухода за замшей нужны специальные инструменты. Обычные щётки и салфетки могут испортить ворс:</p>
  <ul>
    <li><strong>Резиновая щётка (резенка).</strong> Основной инструмент. Резиновые «зубья» поднимают ворс и удаляют поверхностную грязь.</li>
    <li><strong>Ластик для замши.</strong> Похож на обычный ластик, но из специальной резины. Удаляет локальные загрязнения и жирные пятна.</li>
    <li><strong>Щётка с латунной щетиной.</strong> Для застарелых пятен и восстановления жёсткого ворса. Используйте осторожно — может повредить нежную замшу.</li>
    <li><strong>Пароочиститель (или утюг с паром).</strong> Пар поднимает ворс и помогает удалить въевшиеся загрязнения.</li>
    <li><strong>Спрей-пена для замши.</strong> Для влажной чистки. Вспенивается и удаляет грязь без промокания материала.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Не используйте</h4>
    <p>Обычные щётки для одежды, жёсткие губки, влажные салфетки со спиртом — они повреждают ворс замши и оставляют разводы. Только специализированные инструменты.</p>
  </div>
</section>

<section id="dry">
  <h2>Сухая чистка замши</h2>
  <p>Сухая чистка — основной метод для замши. Влажную чистку используют только при сильных загрязнениях. Начинайте всегда с сухой:</p>
  <ol>
    <li>Резиновой щёткой пройдитесь по всей поверхности сумки в одном направлении, чтобы поднять ворс.</li>
    <li>Пятна и потёртости обработайте ластиком для замши — просто потрите загрязнённое место.</li>
    <li>После ластика снова пройдитесь резиновой щёткой, чтобы распушить ворс.</li>
    <li>Для труднодоступных мест (швы, складки) используйте маленькую щёточку.</li>
    <li>Пылесосом с мягкой насадкой можно собрать пыль из складок.</li>
  </ol>
  <p>Регулярная сухая чистка (раз в 1–2 недели) предотвращает глубокое загрязнение замши.</p>
</section>

<section id="wet">
  <h2>Влажная чистка</h2>
  <p>Если сухая чистка не справляется, переходите к влажной. Но помните — замша не любит воду:</p>
  <ol>
    <li>Используйте специальную пену для замши. Встряхните баллон, нанесите пену на загрязнение.</li>
    <li>Мягкой щёткой вотрите пену круговыми движениями.</li>
    <li>Через 2–3 минуты удалите остатки пены чистой влажной салфеткой.</li>
    <li>Промокните бумажным полотенцем лишнюю влагу.</li>
    <li>Высушите при комнатной температуре — не используйте фен или батарею.</li>
    <li>После высыхания поднимите ворс резиновой щёткой.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Лайфхак с паром</h4>
    <p>Подержите замшевую сумку над паром от парогенератора или утюга (не касаясь обуви!). Пар откроет поры замши, и загрязнения выйдут на поверхность. Затем пройдитесь резиновой щёткой. Метод особенно эффективен для засаленных участков.</p>
  </div>
</section>

<section id="stains">
  <h2>Удаление пятен с замши</h2>
  <p>Разные пятна удаляются по-разному. Универсального метода нет, но есть проверенные решения:</p>
  <div data-type="compare">
    <article data-variant="good">
      <h4>Как удалять</h4>
      <ul>
        <li>Жир — тальк на 3–4 часа, затем щётка</li>
        <li>Вода (разводы) — дать высохнуть, потом щётка</li>
        <li>Краска — спрей-пена + ластик</li>
        <li>Грязь — сухая чистка + пар</li>
        <li>Чернила — не удалять самостоятельно!</li>
      </ul>
    </article>
    <article data-variant="bad">
      <h4>Категорически нельзя</h4>
      <ul>
        <li>Стирать замшу водой с мылом</li>
        <li>Использовать спирт, ацетон, растворители</li>
        <li>Тереть мокрое пятно — оно въедается глубже</li>
        <li>Сушить на батарее или феном</li>
        <li>Использовать отбеливатели</li>
      </ul>
    </article>
  </div>
</section>

<section id="protection">
  <h2>Защита и хранение</h2>
  <p>Замшевая сумка нуждается в защите даже больше, чем кожаная. Вот основные правила:</p>
  <ul>
    <li><strong>Водоотталкивающая пропитка.</strong> Обязательна для замши. Наносите спрей сразу после покупки и обновляйте раз в 2–4 недели. Без пропитки замша впитывает влагу и грязь как губка.</li>
    <li><strong>Хранение.</strong> Храните сумку в тканевом мешке, набитую бумагой. Не вешайте на крючок — замша растягивается быстрее кожи.</li>
    <li><strong>Влажность.</strong> Замша боится сырости. Храните в сухом месте с влажностью не более 50%. Влажный воздух приводит к появлению плесени.</li>
    <li><strong>Свет.</strong> Прямые солнечные лучи выжигают цвет замши. Храните в тёмном шкафу или коробке.</li>
  </ul>
