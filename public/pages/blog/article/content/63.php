<section id="beige">
  <h2>Бежевый и кремовый — новые белые</h2>
  <p>Белые кроссовки остаются классикой, но в 2026 году их уверенно теснят бежевые, кремовые и молочные оттенки. Почему? Практичность: бежевый менее маркий, чем белый, и желтизна на нём почти незаметна. А эстетика «тихой роскоши» сделала природные, спокойные тона главным выбором минималистов.</p>
  <p>Бежевый универсален: он сочетается с любым цветом одежды, не перетягивает внимание и визуально удлиняет ноги. Кремовый — чуть теплее и уютнее. В тренде матовые оттенки без глянца, на замше и нубуке.</p>
  <div data-type="box" data-color="green">
    <h4>Как носить</h4>
    <p>Бежевые кроссовки идеальны с total white-образами, джинсой всех оттенков и костюмными брюками. Избегайте сочетания с ярко-розовым или кислотно-зелёным — теряется элегантность.</p>
  </div>
  <h3>Уход за бежевой обувью</h3>
  <p>Светлые оттенки требуют регулярного ухода. Чистите бежевую замшу сухой щёткой после каждой носки, протирайте гладкую кожу влажной салфеткой. Для удаления пятен используйте только специализированные средства — вода оставляет разводы. Обязательно наносите водоотталкивающую пропитку раз в 2 недели.</p>
</section>

<section id="khaki">
  <h2>Хаки и оливковый — природа в городе</h2>
  <p>Под влиянием gorpcore и утилитарной моды землистые оттенки стали одними из главных в 2026 году. Хаки, оливковый, болотный — эти цвета пришли из военной и походной экипировки, но теперь прочно обосновались в повседневной моде.</p>
  <p>Обувь цвета хаки — практичный выбор. Она не пачкается так заметно, как светлая, и при этом выглядит стильно и небанально. Особенно хороши замшевые кроссовки и трекинговые модели в этом оттенке.</p>
  <p>Сочетания: хаки + песочный, хаки + бежевый, хаки + джинса. Избегайте тотального камуфляжа — это уже не модно.</p>
</section>

<section id="burgundy">
  <h2>Бургунди и бордовый — цвет премиума</h2>
  <p>Бордовый (бургунди, wine, maroon) — цвет 2026 года, который пришёл из мира дорогой обуви и одежды. Это глубокий, насыщенный оттенок, который ассоциируется с премиальностью, статусом и хорошим вкусом.</p>
  <p>На кроссовках бургунди выглядит неожиданно свежо: он заменяет привычный чёрный, но смотрится интереснее. Особенно эффектен на замше и нубуке — материал делает цвет ещё более глубоким и бархатистым.</p>
  <p>С чем носить: серые костюмы (идеальный контраст), джинсы light wash (классическое сочетание), бежевые пальто. Не сочетайте с красным или ярко-розовым — цвета конфликтуют.</p>
  <div data-type="box" data-color="red">
    <h4>Особенность</h4>
    <p>Бургунди — один из самых сложных цветов в уходе. Замша этого оттенка быстро выцветает на солнце, а на гладкой коже заметны потёртости. Требуется регулярная тонировка краской-аэрозолем.</p>
  </div>
</section>

<section id="gray">
  <h2>Серый и графитовый — тихая роскошь</h2>
  <p>«Тихая роскошь» (quiet luxury) — главный стилевой тренд 2026 года, и серый цвет — его основа. Серые кроссовки выглядят дороже чёрных, менее маркие, чем белые, и универсальнее, чем цветные.</p>
  <p>В тренде все оттенки серого: от светлого «грифельного» до глубокого «мокрого асфальта» и тёплого «шартреза». На кроссовках серый цвет подчёркивает фактуру материала — текстуру замши, плетение трикотажа, зернистость кожи.</p>
  <p>Графитовый (тёмно-серый) — отличная альтернатива чёрному. Он менее контрастный, но такой же практичный. Идеален для кроссовок люксовых брендов — Common Projects, Loro Piana, Brunello Cucinelli.</p>
</section>

<section id="metallic">
  <h2>Металлик и хром — футуризм 2026</h2>
  <p>Металлические оттенки — золото, серебро, хром, бронза — вернулись как акцентный тренд. В 2026 году их не используют полностью на кроссовках (это перебор), а добавляют деталями: элементы подошвы, логотип, вставки, шнурки.</p>
  <p>Серебряный металлик популярен в gorpcore-моделях (Salomon XT-6 в серебре — хит сезона). Золото встречается на люксовых кроссовках и обуви в стиле Old Money.</p>
  <p>Как носить: металлические акценты лучше работают в паре с минималистичным верхом — белой футболкой, серым худи, чёрными джинсами. Обилие металлика в одном образе удешевляет его.</p>
  <div data-type="box" data-color="blue">
    <h4>Уход за металликом</h4>
    <p>Металлизированное покрытие стирается быстрее обычного. Не используйте агрессивные щётки и растворители. Полируйте мягкой фланелью, при потускнении обновляйте спреем-финишем для блеска.</p>
  </div>
</section>

<section id="care">
  <h2>Уход за разными цветами обуви</h2>
  <p>Цвет обуви влияет на то, как часто и какими средствами её нужно чистить. Общие рекомендации:</p>
  <ul>
    <li><strong>Светлые оттенки (белый, бежевый, кремовый).</strong> Чистка после каждой носки. Обязательная водоотталкивающая пропитка. Используйте белый крем-восстановитель для маскировки потёртостей.</li>
    <li><strong>Тёмные и насыщенные (чёрный, бордовый, зелёный).</strong> Менее маркие в повседневной носке, но потёртости на них заметнее. Используйте тонирующие кремы или аэрозольные краски для восстановления цвета.</li>
    <li><strong>Замша и нубук всех цветов.</strong> Требуют регулярной чистки сухой щёткой и водоотталкивающей пропитки. Раз в 3–4 месяца — тонировка аэрозольной краской.</li>
    <li><strong>Металлик и лаковые покрытия.</strong> Только полировка мягкой тканью и спреи-восстановители блеска. Никаких абразивов!</li>
