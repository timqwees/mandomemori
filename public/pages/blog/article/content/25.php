<section id="fast">
  <h2>Как ускорить сушку без вреда</h2>
  <p>Иногда обувь нужна срочно. Вот безопасные способы ускорить сушку:</p>
  <h3>Метод с солью</h3>
  <p>Крупная поваренная соль отлично впитывает влагу. Насыпьте соль на сковороду, прогрейте 2 минуты (не перегревайте!), заверните в ткань и поместите внутрь обуви. Меняйте каждые 2–3 часа. Метод безопасен, если соль не горячее 40 °C.</p>
  <h3>Метод с рисом</h3>
  <p>Насыпьте сухой рис в носок, завяжите и поместите в обувь. Рис впитывает влагу не хуже силикагеля. Оставьте на 4–6 часов.</p>
  <h3>Сушилка для обуви</h3>
  <p>Современные электрические сушилки с вентилятором (без нагрева) — лучший выбор. Они продувают обувь воздухом комнатной температуры, не нагревая её. Время сушки — 3–6 часов. Не все сушилки безопасны: выбирайте модели с температурой не выше 30 °C.</p>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Используйте сушилку для обуви с УФ-лампой. Она не только сушит, но и убивает бактерии и грибок — профилактика запаха на 100%. Стоит такая сушилка 1500–3000 ₽, а служит годами.</p>
  </div>
</section>

<section id="materials">
  <h2>Особенности сушки разных материалов</h2>
  <p>Разные материалы требуют разного подхода к сушке:</p>
  <ul>
    <li><strong>Гладкая кожа.</strong> Сохнет медленно (12–24 часа). Обязательно используйте колодки или бумагу — без них кожа ссохнется и потеряет форму. После сушки нанесите питающий крем.</li>
    <li><strong>Замша и нубук.</strong> Сохнут 8–12 часов. Не используйте газеты — типографская краска может отпечататься на ворсе. Используйте белую бумагу или ткань. После сушки поднимите ворс щёткой.</li>
    <li><strong>Текстиль и сетка.</strong> Сохнут 4–8 часов. Самый быстросохнущий материал. Не требует особого ухода, но не сушите на солнце — текстиль выгорает.</li>
    <li><strong>Gore-Tex и мембрана.</strong> Сохнут 12–24 часа. Самое важное — не превышать температуру 30 °C. При нагреве выше 40 °C мембрана разрушается.</li>
    <li><strong>Резина и ПВХ.</strong> Сохнут 2–4 часа. Резина не боится влаги, но быстро сохнет при комнатной температуре.</li>
  </ul>
</section>

<section id="mistakes">
  <h2>Чего нельзя делать никогда</h2>
  <p>Некоторые методы сушки кажутся очевидными, но они навсегда портят обувь:</p>
  <ul>
    <li><strong>Батарея отопления.</strong> Температура батареи зимой — 60–80 °C. Это гарантированно разрушает клей, сушит кожу и деформирует подошву.</li>
    <li><strong>Фен.</strong> Даже на средней температуре фен даёт 50–60 °C. Мембрана и клей не выдерживают.</li>
    <li><strong>Микроволновка.</strong> Металлические элементы (люверсы, шипы) вызовут искры и пожар. Клей расплавится мгновенно.</li>
    <li><strong>Открытый огонь.</strong> Очевидно опасно. Огонь воспламеняет синтетические материалы.</li>
    <li><strong>Солнце.</strong> УФ-лучи выжигают цвет, делают резину хрупкой и ускоряют пожелтение подошвы.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Если после неправильной сушки подошва отошла или кожа потрескалась — восстановить обувь можно только профессиональным ремонтом. В некоторых случаях ремонт обходится дороже новой пары. Не рискуйте — сушите правильно.</p>
  </div>
</section>

