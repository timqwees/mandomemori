<section id="why">
  <h2>Почему неправильная сушка опасна</h2>
  <p>Многие недооценивают вред от неправильной сушки. Вот что происходит с обувью, если её сушить на батарее или обогревателе:</p>
  <ul>
    <li><strong>Кожа дубеет.</strong> При нагреве кожа теряет натуральные масла, становится жёсткой и ломкой. На сгибах появляются трещины, которые не восстанавливаются.</li>
    <li><strong>Подошва отслаивается.</strong> Клей, соединяющий подошву с верхом, размягчается от высокой температуры. Подошва может отойти частично или полностью.</li>
    <li><strong>Деформация.</strong> Неравномерное высыхание приводит к тому, что обувь «ведёт»: носок загибается вверх, задник складывается, форма теряется.</li>
    <li><strong>Запах.</strong> Внутри влажной обуви быстро размножаются бактерии. Если обувь сохнет медленно — гарантирован неприятный запах.</li>
    <li><strong>Мембрана спекается.</strong> Gore-Tex и аналоги при нагреве выше 40 °C теряют микропористую структуру. Мембрана перестаёт дышать.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Золотое правило</h4>
    <p>Максимальная температура сушки обуви — комнатная (18–25 °C). Всё, что горячее, сокращает срок службы обуви. Обувь должна сохнуть медленно и естественно.</p>
  </div>
</section>

<section id="prep">
  <h2>Подготовка к сушке</h2>
  <p>Правильная подготовка сокращает время сушки и предотвращает деформацию:</p>
  <ol>
    <li><strong>Выньте стельки и шнурки.</strong> Стельки сохнут отдельно и быстрее. Шнурки не мешают циркуляции воздуха.</li>
    <li><strong>Удалите лишнюю влагу.</strong> Промокните внешнюю поверхность сухой тканью. Внутри — промокните бумажным полотенцем или тканью.</li>
    <li><strong>Набейте газетой.</strong> Бумага отлично впитывает влагу изнутри. Комкайте газеты в большие шары — не мельчите, чтобы не пришлось выковыривать мокрую бумагу.</li>
    <li><strong>Поставьте в проветриваемое место.</strong> Не в закрытый шкаф — там нет циркуляции воздуха, и обувь будет сохнуть 2–3 дня.</li>
  </ol>
</section>

