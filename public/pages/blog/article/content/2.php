<section id="why">
  <h2>Почему желтеет подошва</h2>
  <p>Пожелтение подошвы — естественный процесс старения резины, который ускоряется под воздействием внешних факторов. Основные причины:</p>
  <ul>
    <li><strong>Окисление.</strong> Резина содержит полимеры, которые со временем вступают в реакцию с кислородом. Этот процесс необратим, но его можно замедлить и визуально устранить.</li>
    <li><strong>Ультрафиолет.</strong> Солнечный свет ускоряет окисление и разрушает молекулярные связи в резине. Белые подошвы на солнечной стороне желтеют в 2–3 раза быстрее.</li>
    <li><strong>Реагенты.</strong> Уличные реагенты, соль и химия для таяния снега вступают в реакцию с резиной, вызывая необратимые химические изменения.</li>
    <li><strong>Неправильное хранение.</strong> Хранение в полиэтиленовых пакетах или пластиковых контейнерах без доступа воздуха создаёт парниковый эффект, который ускоряет окисление.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно понимать</h4>
    <p>Пожелтение затрагивает не только поверхностный слой, но и глубинные структуры резины. Поэтому простое мытьё не помогает — нужны химические методы отбеливания.</p>
  </div>
</section>

<section id="method1">
  <h2>Метод 1: Перекись водорода + УФ (классический ретробрайт)</h2>
  <p>Самый популярный метод среди коллекционеров кроссовок — ретробрайт. Он основан на фотохимической реакции: перекись водорода под воздействием ультрафиолета выделяет активный кислород, который разрушает жёлтые пигменты.</p>
  <h3>Что понадобится</h3>
  <ul>
    <li>Перекись водорода 12% (продаётся в магазинах косметики или химреактивов)</li>
    <li>Оксид цинка или загуститель (чтобы смесь не стекала)</li>
    <li>Кисточка для нанесения</li>
    <li>Полиэтиленовая плёнка</li>
    <li>УФ-лампа или прямой солнечный свет</li>
    <li>Малярный скотч (защитить верх обуви)</li>
  </ul>
  <h3>Пошаговая инструкция</h3>
  <ol>
    <li>Тщательно вымойте и высушите подошву. Обезжирьте спиртом.</li>
    <li>Заклейте верх обуви малярным скотчем, чтобы не повредить материал.</li>
    <li>Смешайте перекись с оксидом цинка до консистенции густой сметаны.</li>
    <li>Нанесите смесь на пожелтевшие участки толстым слоем (2–3 мм).</li>
    <li>Заверните подошву в полиэтиленовую плёнку — это предотвратит высыхание.</li>
    <li>Поместите обувь под УФ-лампу или на яркое солнце на 4–8 часов.</li>
    <li>Смойте состав водой, оцените результат. При необходимости повторите.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Метод подходит только для резиновых подошв без цветного покрытия. Не используйте на замше, нубуке и коже — перекись может обесцветить материал. Работайте в перчатках и в хорошо проветриваемом помещении.</p>
  </div>
</section>

<section id="method2">
  <h2>Метод 2: Сода + уксус (домашний метод)</h2>
  <p>Если подошва пожелтела несильно, можно обойтись без агрессивной химии. Пищевая сода и уксус — классическое чистящее средство, которое бережно отбеливает резину.</p>
  <h3>Что понадобится</h3>
  <ul>
    <li>Пищевая сода — 3 столовые ложки</li>
    <li>Белый уксус 9% — 1 столовая ложка</li>
    <li>Зубная щётка с жёсткой щетиной</li>
    <li>Миска для смешивания</li>
  </ul>
  <h3>Пошаговая инструкция</h3>
  <ol>
    <li>Смешайте соду и уксус до образования густой пасты. Смесь будет шипеть — это нормально.</li>
    <li>Нанесите пасту на пожелтевшие участки подошвы.</li>
    <li>Оставьте на 15–20 минут — за это время произойдёт химическая реакция.</li>
    <li>Потрите подошву зубной щёткой круговыми движениями.</li>
    <li>Смойте тёплой водой и вытрите насухо.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Преимущества метода</h4>
    <p>Сода и уксус есть в каждом доме, метод безопасен для большинства материалов и не требует специального оборудования. Подходит для регулярного поддерживающего ухода.</p>
  </div>
  <p>Этот метод эффективен против поверхностного пожелтения, но не справляется с глубокими изменениями цвета. Если после 2–3 процедур результата нет — переходите к более сильным средствам.</p>
</section>

