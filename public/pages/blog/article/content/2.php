<section id="prevention">
  <h2>Как предотвратить пожелтение подошвы</h2>
  <p>Предотвратить пожелтение проще, чем бороться с ним. Несколько простых правил помогут сохранить белизну подошвы надолго:</p>
  <ul>
    <li><strong>Храните правильно.</strong> Кроссовки должны храниться в проветриваемом месте, в бумажных коробках или тканевых мешках. Никаких полиэтиленовых пакетов!</li>
    <li><strong>Мойте после каждой носки.</strong> Грязь и пыль содержат агрессивные частицы, которые ускоряют окисление. Достаточно протереть подошву влажной салфеткой после возвращения домой.</li>
    <li><strong>Избегайте реагентов.</strong> В гололёд старайтесь не ходить в белых кроссовках — химические реагенты практически невозможно полностью удалить из структуры резины.</li>
    <li><strong>Используйте защиту.</strong> Силиконовые спреи для подошвы создают барьер, который замедляет окисление. Обновляйте покрытие раз в 2–3 недели.</li>
    <li><strong>Не сушите на солнце.</strong> УФ-лучи ускоряют пожелтение. Сушите обувь в тени при комнатной температуре.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Совет коллекционера</h4>
    <p>Если вы не носите кроссовки месяцами — не храните их в вакуумных пакетах. Резине нужен доступ кислорода. Оптимальные условия: тёмное место с влажностью 40–60% при температуре 15–20 °C.</p>
  </div>
</section>

