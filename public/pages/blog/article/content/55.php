<section id="suede">
  <h2>Особенности замши как материала</h2>
  <p>Замша — натуральная кожа с ворсовой поверхностью, полученная путём дубления и шлифовки. Её главная особенность — пористая структура, которая определяет и преимущества, и сложности в уходе.</p>
  <ul>
    <li><strong>Плюсы замши:</strong> эластичная, пропускает воздух, выглядит благородно, комфортна при носке.</li>
    <li><strong>Минусы:</strong> впитывает грязь и влагу, ворс приминается, боится агрессивной химии, требует регулярного ухода.</li>
  </ul>
  <p>Главное отличие замши от нубука — более длинный и выраженный ворс, который хорошо заметен визуально и на ощупь. Нубук имеет короткий ворс, почти незаметный, и напоминает бархат. Соответственно, методы реставрации различаются.</p>
  <div data-type="box" data-color="gray">
    <h4>Важно знать</h4>
    <p>Замша — деликатный материал. Средства для гладкой кожи (кремы, воски) категорически не подходят для замши. Они забивают поры, склеивают ворс и оставляют жирный блеск, который невозможно удалить. Только специальные средства для замши и нубука.</p>
  </div>
</section>

<section id="cleaning">
  <h2>Чистка замши — подготовка к реставрации</h2>
  <p>Перед любой реставрацией замшу нужно тщательно очистить. Грязь и пыль будут мешать восстановлению цвета и ворса.</p>
  <h3>Сухая чистка (регулярная)</h3>
  <ul>
    <li>Резиновая щётка для замши — поднимает ворс и удаляет пыль.</li>
    <li>Ластик для замши (специальный, не канцелярский) — убирает засаленности и мелкие пятна.</li>
    <li>Каучуковая губка — для финишной очистки и полировки ворса.</li>
  </ul>
  <h3>Влажная чистка (для сильных загрязнений)</h3>
  <ol>
    <li>Используйте пену для замши (Tarrago Foam Cleaner, Collonil Cleaner). Распылите пену на поверхность.</li>
    <li>Аккуратно потрите мягкой щёткой круговыми движениями.</li>
    <li>Удалите пену влажной чистой тканью.</li>
    <li>Просушите при комнатной температуре (не на батарее!).</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Не мочите замшу полностью</h4>
    <p>Замша не любит воды. Полное замачивание может привести к усадке и потере формы. Используйте только пенные очистители, не лейте воду прямо на обувь.</p>
  </div>
</section>

<section id="nap">
  <h2>Восстановление ворса замши</h2>
  <p>После чистки ворс замши может быть примят или неравномерен. Восстановление ворса — ключевой этап, который возвращает замше характерный вид.</p>
  <h3>Методы восстановления ворса</h3>
  <ul>
    <li><strong>Резиновая щётка.</strong> Водите щёткой в одном направлении (не хаотично), слегка надавливая. Ворс поднимается и становится пушистым.</li>
    <li><strong>Ластик для замши.</strong> Используется для локального поднятия ворса на засаленных участках. Трите ластиком в одном направлении.</li>
    <li><strong>Наждачная бумага (P1000-P1500).</strong> Для очень примятого ворса — аккуратно пройдитесь мелкой наждачкой по поверхности. Используйте только на крайне запущенных случаях.</li>
    <li><strong>Пар.</strong> Подержите обувь над паром 10–15 секунд, дайте остыть и пройдитесь щёткой. Пар поднимает даже сильно спрессованный ворс.</li>
  </ul>
  <p>Лучший результат даёт комбинация: сначала пар, затем щётка, затем ластик для финишной обработки.</p>
  <div data-type="box" data-color="green">
    <h4>Бытовая хитрость</h4>
    <p>Если нет специальной щётки для замши, используйте чистую щётку для одежды с натуральной щетиной или даже зубную щётку с мягкой щетиной. Результат будет чуть хуже, но для лёгкой реставрации — подойдёт.</p>
  </div>
</section>

<section id="color">
  <h2>Восстановление цвета замши</h2>
  <p>Со временем замша выцветает, особенно на выступающих участках. Восстановить цвет можно с помощью специальных средств.</p>
  <h3>Средства для восстановления цвета</h3>
  <ul>
    <li><strong>Аэрозоль-освежитель цвета.</strong> Содержит пигменты, которые обновляют цвет. Распыляется с расстояния 20–30 см. После высыхания нужно поднять ворс щёткой.</li>
    <li><strong>Краска-пропитка для замши.</strong> Сочетает восстановление цвета и водоотталкивающую защиту. Удобно — два в одном.</li>
    <li><strong>Пенный краситель.</strong> Наносится губкой, втирается в замшу. Требует больше времени, но даёт более глубокое окрашивание.</li>
  </ul>
  <h3>Пошаговая покраска замши</h3>
  <ol>
    <li>Очистите и высушите обувь.</li>
    <li>Восстановите ворс щёткой.</li>
    <li>Заклейте малярным скотчем подошву и другие неокрашиваемые детали.</li>
    <li>Распылите краску-аэрозоль равномерно с расстояния 20 см.</li>
    <li>Дайте высохнуть 30–40 минут.</li>
    <li>Нанесите второй слой при необходимости.</li>
    <li>После полного высыхания поднимите ворс резиновой щёткой.</li>
  </ol>
  <div data-type="box" data-color="dark">
    <h4>Важный совет</h4>
    <p>Цвет замши после покраски может казаться темнее, чем ожидалось. Когда краска полностью высохнет (через 24 часа) и вы поднимите ворс, оттенок станет на 1–2 тона светлее. Учитывайте это при выборе цвета краски.</p>
  </div>
</section>

<section id="stains">
  <h2>Удаление засаленных участков на замше</h2>
  <p>Со временем на замше появляются засаленные участки — чаще всего в области пятки, на заднике и в местах частого контакта с руками. Они выглядят как тёмные, блестящие пятна.</p>
  <h3>Методы удаления засаленностей</h3>
  <ul>
    <li><strong>Ластик для замши.</strong> Самый безопасный метод. Трите засаленный участок ластиком — ворс поднимается, жир удаляется.</li>
    <li><strong>Раствор нашатырного спирта.</strong> 1 часть нашатыря, 3 части воды. Смочите ватный диск, аккуратно протрите засаленный участок. Затем промойте влажной тканью и просушите.</li>
    <li><strong>Специальный шампунь для замши.</strong> Нанесите пену, потрите щёткой, удалите остатки. Хорошо выводит жирные загрязнения.</li>
    <li><strong>Уксусный раствор.</strong> 1 часть уксуса, 3 части воды. Нанесите на участок, затем промойте и просушите. Уксус нейтрализует засаленность.</li>
  </ul>
  <p>После любого влажного метода обязательно поднимите ворс щёткой после высыхания.</p>
