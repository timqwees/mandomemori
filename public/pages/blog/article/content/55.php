<section id="protection">
  <h2>Водоотталкивающая защита после реставрации</h2>
  <p>После чистки, восстановления ворса и цвета замшу необходимо защитить от влаги и грязи. Водоотталкивающая пропитка — обязательный финальный этап.</p>
  <h3>Как выбрать пропитку</h3>
  <ul>
    <li><strong>Фторполимерные пропитки (Collonil, Tarrago).</strong> Создают невидимую плёнку, которая отталкивает воду и грязь. Не нарушают воздухопроницаемость. Лучший выбор для замши.</li>
    <li><strong>Силиконовые пропитки.</strong> Более дешёвые, но забивают поры и ухудшают «дыхание» материала. Для замши не рекомендуются.</li>
    <li><strong>Восковые пропитки.</strong> Категорически не подходят для замши — склеивают ворс и оставляют жирный блеск.</li>
  </ul>
  <h3>Как наносить пропитку</h3>
  <ol>
    <li>Убедитесь, что обувь чистая, сухая и восстановлена по ворсу.</li>
    <li>Распылите пропитку с расстояния 20–25 см равномерно по всей поверхности.</li>
    <li>Дайте высохнуть 15–20 минут.</li>
    <li>Нанесите второй слой для максимальной защиты.</li>
    <li>После полного высыхания (2–3 часа) обувь готова к носке.</li>
  </ol>
  <p>Обновляйте пропитку каждые 2–3 недели в сезон активной носки. Эффективная защита продлевает срок службы замши в 2–3 раза.</p>
