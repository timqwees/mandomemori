<section id="clean">
  <h2>Этап 1: глубокая чистка обуви после лета</h2>
  <p>За лето обувь накапливает пыль, песок, пот, остатки кремов и пропиток. Всё это нужно удалить перед нанесением осенней защиты. Поверхностная влажная уборка не подойдёт — требуется глубокая чистка.</p>
  <h3>Чистка гладкой кожи</h3>
  <ol>
    <li>Удалите шнурки и стельки.</li>
    <li>Сметите сухую грязь щёткой с мягким ворсом.</li>
    <li>Нанесите пенящийся очиститель для гладкой кожи — он удаляет жировые отложения, остатки старого крема и уличную грязь.</li>
    <li>Протрите поверхность влажной микрофиброй, чтобы убрать остатки пены.</li>
    <li>При необходимости используйте очищающий лосьон для подготовки кожи к нанесению крема.</li>
  </ol>
  <h3>Чистка замши и нубука</h3>
  <ol>
    <li>Пройдитесь специальной щёткой с латунным ворсом, чтобы поднять ворс и удалить поверхностные загрязнения.</li>
    <li>Обработайте стойкие пятна ластиком для замши.</li>
    <li>Нанесите пенку для замши — вспеньте, оставьте на 2–3 минуты, затем удалите чистой тканью.</li>
    <li>После высыхания снова поднимите ворс щёткой.</li>
  </ol>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Не пытайтесь отмыть замшу мыльным раствором — это забивает поры и портит структуру ворса. Используйте только специализированные средства.</p>
  </div>
</section>

<section id="repair">
  <h2>Этап 2: мелкий ремонт обуви перед осенью</h2>
  <p>Осень — не время для экспериментов с обувью, у которой есть скрытые повреждения. Влага и реагенты моментально проникнут внутрь через микротрещины и превратят мелкую проблему в катастрофу.</p>
  <h3>Замена набоек</h3>
  <p>Полностью изношенные каблуки и набойки — главная причина промокания обуви. Проверьте состояние полиуретановых или резиновых набоек: если стёрто более 70% — замените перед сезоном. Средняя стоимость замены набоек у мастера — 300–600 ₽ за пару.</p>
  <h3>Ремонт клеевых швов</h3>
  <p>Осмотрите место соединения подошвы с верхом обуви. Если заметили расхождения или отслоения — проклейте специальным обувным клеем или отнесите мастеру. Попадание воды в эти щели гарантирует намокание ног и разрушение подошвы за один сезон.</p>
  <h3>Профилактика подошвы</h3>
  <p>Тонкая кожаная подошва требует установки профилактики — резиновой накладки, которая защищает от истирания и скольжения. Без неё осенью на мокрой плитке вы рискуете не только повредить подошву, но и получить травму.</p>
  <div data-type="box" data-color="blue">
    <h4>Совет</h4>
    <p>Перед осенью рекомендуем пройти полную диагностику в обувной мастерской. Специалист проверит состояние набоек, подошвы, швов и внутренней отделки за 5–10 минут. В MANDO MEMORI диагностика бесплатна при заказе ремонта.</p>
  </div>
  <h3>Замена стелек</h3>
  <p>За лето стельки впитывают пот и теряют амортизационные свойства. Замените их на новые — желательно с антибактериальной пропиткой. Это не только гигиенично, но и снижает риск грибковых заболеваний в сырой осенней обуви.</p>
</section>

<section id="protect">
  <h2>Этап 3: водоотталкивающая пропитка обуви</h2>
  <p>После чистки и ремонта — самый важный этап. Без водоотталкивающей обработки любые усилия по подготовке обойдутся даром. Современные пропитки создают невидимый барьер, который защищает от воды, грязи и реагентов.</p>
  <p>Для разных материалов — разные типы пропиток:</p>
  <ul>
    <li><strong>Гладкая кожа:</strong> используйте фторполимерные спреи или натуральные воски. Воск даёт дополнительный блеск и питание, спрей — более равномерное покрытие без жирного блеска.</li>
    <li><strong>Замша и нубук:</strong> только аэрозольные наноспреи без силикона. Пропитка должна быть невидимой и не изменять тактильные свойства ворса. Идеально — нанокерамические составы.</li>
    <li><strong>Текстиль и мембрана:</strong> специальные пропитки для Gore-Tex и аналогичных мембран. Они не забивают поры и сохраняют дышащие свойства.</li>
    <li><strong>Лаковая кожа:</strong> минимальная защита — только мягкая полироль без растворителей. Лаковая кожа наименее подвержена намоканию, но боится царапин и реагентов.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Правило трёх слоёв</h4>
    <p>Профессионалы MANDO MEMORI наносят пропитку в 3 слоя с промежуточной сушкой 15–20 минут. Каждый слой усиливает защиту и увеличивает её долговечность. Один толстый слой — ошибка новичков.</p>
  </div>
</section>

