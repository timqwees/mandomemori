<section id="why">
  <h2>Почему осень — главный враг обуви</h2>
  <p>Осенний период создаёт комплекс разрушающих факторов, каждый из которых сам по себе вреден, а вместе они превращаются в «идеальный шторм» для обуви:</p>
  <ul>
    <li><strong>Химические реагенты.</strong> Противогололёдные смеси, которые коммунальные службы начинают рассыпать уже в октябре, содержат соль, хлорид кальция и другие агрессивные вещества. Они разъедают кожу, вытягивают влагу и оставляют трудновыводимые белёсые разводы.</li>
    <li><strong>Постоянная влажность.</strong> Осенние дожди и слякоть приводят к тому, что обувь не успевает полностью высохнуть между носками. Постоянная влажность — идеальная среда для грибка, плесени и разрушения клеевых швов.</li>
    <li><strong>Перепады температур.</strong> Утром — около нуля, днём — +10–12 °C, вечером — снова заморозки. Циклы замерзания и оттаивания разрушают структуру материалов, особенно пористых — замши, нубука, текстиля.</li>
    <li><strong>Абразивный износ.</strong> Песок и мелкий гравий, смешиваясь с водой, работают как наждачная бумага, стирая верхний слой кожи и повреждая покрытие.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Факт</h4>
    <p>По статистике MANDO MEMORI, 70% обращений за реставрацией обуви приходится на ноябрь–февраль. Самая частая проблема — повреждение кожевенного покрова реагентами, которую можно было предотвратить своевременной обработкой.</p>
