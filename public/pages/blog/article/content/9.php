<section id="drying">
  <h2>Сушка трекинговых кроссовок</h2>
  <p>Правильная сушка для мембранной обуви даже важнее, чем правильная чистка. Влага, оставшаяся внутри, разрушает мембрану и создаёт идеальную среду для бактерий и плесени:</p>
  <ul>
    <li><strong>Шерстяные колодки или формодержатели.</strong> Лучший вариант для сушки трекинговых кроссовок. Они впитывают влагу изнутри и сохраняют форму. Если их нет — используйте белую бумагу (не газеты) и меняйте её каждые 2–3 часа.</li>
    <li><strong>Никаких батарей и обогревателей.</strong> Расстояние от кроссовок до источника тепла должно быть не менее 2 метров. Идеальная сушка — в хорошо проветриваемом помещении при 20–24°C. Полное высыхание занимает 24–48 часов.</li>
    <li><strong>Специальные сушилки для обуви.</strong> Электрические сушилки с обдувом холодным или тёплым воздухом (не горячим!) — отличная инвестиция для частых трейлов. Они сокращают время сушки до 6–8 часов без риска для мембраны.</li>
    <li><strong>Не надевайте влажными.</strong> Влажные кроссовки растягиваются, теряют форму, а мембрана в мокром состоянии становится хрупкой. Дождитесь полного высыхания — даже небольшое количество влаги внутри приведёт к разрушению полимеров.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Совет профессионалов</h4>
    <p>После чистки и сушки обработате внутреннюю поверхность антибактериальным спреем. Трекинговые кроссовки интенсивно потеют — это создаёт среду для размножения бактерий. Спрей на основе серебра или цинка уничтожает запах и продлевает свежесть обуви.</p>
  </div>
  <p>Особое внимание — дренажным отверстиям, которые есть на многих трекинговых кроссовках (Salomon, On). Убедитесь, что они не забиты грязью после чистки. При необходимости прочистите их тонкой иглой или зубочисткой.</p>
</section>

