<section id="why">
  <h2>Почему трекинговые кроссовки требуют особого ухода</h2>
  <p>Трекинговая обувь принципиально отличается от городских кроссовок по конструкции и используемым материалам. Вот почему обычная чистка здесь не работает:</p>
  <ul>
    <li><strong>Многослойный верх.</strong> Salomon XT-6, Hoka Speedgoat, Asics Trabuco состоят из 3–5 слоёв материалов: внешняя сетка, мембрана, внутренняя подкладка, усилители, накладки. Каждый слой требует своего подхода. Агрессивное трение или неподходящая химия могут повредить один из слоёв и нарушить работу всей системы.</li>
    <li><strong>Gore-Tex / eVent мембрана.</strong> Это тонкая (менее 0.1 мм) полимерная плёнка с микропорами, которая пропускает пар, но не пропускает воду. Поры могут забиваться грязью и маслами — обувь перестаёт «дышать» и начинает промокать. До 70% случаев потери мембранных свойств связаны с неправильной чисткой.</li>
    <li><strong>Сложный протектор.</strong> Агрессивный рисунок подошвы Vibram, Continental или Michelin забивается грязью, камнями, песком и глиной. Простая влажная тряпка не справится — нужна щётка с жёсткой щетиной для глубоких канавок.</li>
    <li><strong>Чувствительные полимеры.</strong> Промежуточная подошва из EVA-пены (Energy Foam, CloudTec, FlyteFoam) разрушается при контакте с растворителями, спиртом и высокими температурами. Даже обычный стиральный порошок может повредить пенную структуру.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Ошибка, которая стоит дорого</h4>
    <p>Стирка трекинговых кроссовок с мембраной в стиральной машине — самая частая причина обращения в мастерские. Барабан отжима повреждает мембрану, а порошок забивает поры. Восстановление мембранных свойств после машинной стирки невозможно — только замена.</p>
  </div>
</section>

<section id="prep">
  <h2>Подготовка к чистке: стельки, шнурки, сухая грязь</h2>
  <p>Правильная подготовка занимает 5 минут, но предотвращает 90% проблем при чистке трекинговых кроссовок:</p>
  <ol>
    <li><strong>Извлеките стельки.</strong> Трекинговые стельки часто анатомические, с антимикробной пропиткой и гелевыми вставками. Не мочите их полностью — протрите мягкой тканью с мыльным раствором, затем влажной чистой тканью. Сушите отдельно от кроссовок при комнатной температуре.</li>
    <li><strong>Снимите шнурки.</strong> Шнурки трекинговых кроссовок обычно круглые и толстые, с защитой от истирания. Замочите их на 15 минут в тёплой воде с мылом, затем потрите руками и прополощите. Не используйте кондиционер — он снижает прочность волокон.</li>
    <li><strong>Удалите сухую грязь.</strong> После каждого выхода на трейл дайте грязи высохнуть, затем удалите её щёткой с жёсткой щетиной. Особое внимание — протектору подошвы. Используйте специальный крючок для чистки протектора (гуимплейт) или старую зубную щётку. Камешки из протектора удаляйте пинцетом или тонким инструментом.</li>
    <li><strong>Проверьте на повреждения.</strong> Осмотрите кроссовки: нет ли разрывов мембраны, отслоений подошвы, повреждений усилений. Если есть — сначала отремонтируйте, потом чистите.</li>
  </ol>
  <div data-type="box" data-color="gray">
    <h4>Инструменты для чистки</h4>
    <p>Что должно быть под рукой: щётка с жёсткой щетиной для подошвы, мягкая щётка для верха, микрофибра, гуимплейт или скребок для протектора, пинцет для камней. Специальных дорогих инструментов не нужно — подойдут старые зубные щётки разной жёсткости.</p>
  </div>
</section>

<section id="wash">
  <h2>Техника чистки мембранной обуви</h2>
  <p>После подготовки приступайте к влажной чистке. Алгоритм для трекинговых кроссовок с мембраной:</p>
  <ol>
    <li><strong>Приготовьте раствор.</strong> Тёплая вода (не выше 30°C — кипяток разрушает мембрану) и специальное средство для мембранной обуви: Nikwax Tech Wash, Penguins Sport-Wash, Grangers Performance Wash. Обычное мыло и шампуни оставляют осадок, который забивает поры мембраны.</li>
    <li><strong>Нанесите раствор.</strong> Смочите мягкую щётку или губку в растворе и нанесите пену на загрязнённые участки. Не трите слишком сильно — мембрана тонкая, и можно повредить её изнутри. Движения — круговые, без нажима.</li>
    <li><strong>Обработайте протектор.</strong> Для подошвы используйте более жёсткую щётку, но не касайтесь ею верха. Протектор можно чистить с бóльшим усилием — резина Vibram прочная и не боится щёток.</li>
    <li><strong>Удалите остатки средства.</strong> Чистой влажной микрофиброй протрите всю поверхность. Важно убрать все следы моющего средства — остатки химии разрушают мембрану при высыхании.</li>
    <li><strong>Мембранная пропитка.</strong> После чистки и высыхания обязательно нанесите водоотталкивающую пропитку для мембранной обуви. Nikwax TX.Direct Spray-On или аналоги — они не забивают поры, а восстанавливают DWR-покрытие верха.</li>
  </ol>
  <div data-type="box" data-color="blue">
    <h4>Температурный режим</h4>
    <p>Критически важно: температура воды не выше 30°C, температура сушки — комнатная. Gore-Tex теряет свойства при нагреве выше 50°C. Не используйте парогенераторы и отпариватели для размягчения грязи — пар повреждает мембрану.</p>
  </div>
</section>

<section id="membrane">
  <h2>Как не повредить Gore-Tex и другие мембраны</h2>
  <p>Мембрана — самый дорогой и уязвимый элемент трекинговых кроссовок. Вот что её разрушает и как этого избежать:</p>
  <div data-type="compare">
    <article data-variant="good">
      <h4>Что безопасно для мембраны</h4>
      <ul>
        <li>Тёплая вода до 30°C</li>
        <li>Специальные моющие средства (Nikwax, Grangers, Penguins)</li>
        <li>Мягкая щётка и микрофибра</li>
        <li>Сушка при комнатной температуре</li>
        <li>Пропитки-восстановители DWR (Nikwax TX.Direct)</li>
      </ul>
    </article>
    <article data-variant="bad">
      <h4>Что убивает мембрану</h4>
      <ul>
        <li>Горячая вода и пар (выше 40°C)</li>
        <li>Стиральный порошок, мыло, шампуни, кондиционеры</li>
        <li>Отбеливатели и пятновыводители с хлором</li>
        <li>Стиральная машина и отжим</li>
        <li>Кондиционеры для тканей — забивают поры</li>
      </ul>
    </article>
  </div>
  <p>Первый признак повреждения мембраны — обувь начинает промокать там, где раньше не промокала. Если это произошло, есть два варианта: восстановить DWR-покрытие (помогает в 50% случаев) или обратиться к профессионалам для диагностики.</p>
  <div data-type="box" data-color="red">
    <h4>Проверка мембраны</h4>
    <p>Простой тест: капните воду на верх кроссовка. Если вода собирается в каплю и скатывается — DWR-покрытие работает. Если вода впитывается и оставляет тёмное пятно — покрытие изношено. Сначала восстановите DWR, только потом проверяйте мембрану на промокание.</p>
  </div>
</section>

