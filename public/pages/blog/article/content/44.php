<section id="wear">
  <h2>Когда беговая обувь изнашивается</h2>
  <p>Срок службы беговых кроссовок измеряется не в месяцах, а в километрах. Усреднённые данные:</p>
  <ul>
    <li><strong>500–800 км</strong> — стандартный ресурс для большинства беговых моделей (Nike Pegasus, Asics Gel-Kayano, Adidas Ultraboost).</li>
    <li><strong>300–500 км</strong> — для лёгких гоночных моделей (Nike Vaporfly, Adidas Adios Pro). Амортизация в них тоньше и изнашивается быстрее.</li>
    <li><strong>800–1200 км</strong> — для моделей с увеличенной амортизацией (Hoka Clifton, Asics Nimbus).</li>
    <li><strong>200–300 км</strong> — для трейловых кроссовок по пересечённой местности. Протектор стирается быстрее, но амортизация может оставаться дольше.</li>
  </ul>
  <p>После преодоления этих дистанций амортизация кроссовок снижается на 30–50%, даже если визуально обувь выглядит нормально. Бег в изношенных кроссовках увеличивает нагрузку на колени и позвоночник.</p>
  <div data-type="box" data-color="red">
    <h4>Главное правило</h4>
    <p>Если пробежали больше 800 км — амортизация уже не восстановится никаким ремонтом. Ремонт может продлить жизнь обуви, но не вернёт амортизирующие свойства. Для бега такие кроссовки уже не годятся. Используйте их как повседневные.</p>
  </div>
</section>

<section id="repairable">
  <h2>Что можно отремонтировать в беговых кроссовках</h2>
  <p>Некоторые дефекты беговой обуви успешно устраняются в мастерской. Вот что подлежит ремонту:</p>
  <ul>
    <li><strong>Отклеилась подошва.</strong> Распространённая проблема у беговых кроссовок. Подошва отходит от середины или носка. Ремонт: склеивание полиуретановым клеем. Стоимость: 500–1500 ₽.</li>
    <li><strong>Протёртая сетка верха.</strong> В области мизинца или пятки сетка может протираться. Ремонт: заплатка из сетчатого материала с внутренней стороны. Стоимость: 500–1000 ₽.</li>
    <li><strong>Износ протектора.</strong> Если подошва стёрта, но амортизация ещё в порядке — можно нарастить изношенный участок или установить профилактику. Стоимость: 1000–2000 ₽.</li>
    <li><strong>Разошёлся шов.</strong> Восстанавливается прошивкой на обувной машине. Стоимость: 300–800 ₽.</li>
    <li><strong>Замена стельки.</strong> Родные стельки в беговых кроссовках изнашиваются быстрее всего. Замена на анатомические стельки восстанавливает комфорт. Стоимость стелек: 500–1500 ₽.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Ремонтируйте беговую обувь до того, как пробежали 800 км. После этого порога амортизация уже не работает, и даже идеальный ремонт подошвы или верха не сделает кроссовки пригодными для бега.</p>
  </div>
</section>

<section id="cantfix">
  <h2>Что отремонтировать нельзя</h2>
  <p>К сожалению, многие элементы беговых кроссовок не подлежат восстановлению. Вот основные «приговоры»:</p>
  <ul>
    <li><strong>Умершая амортизация.</strong> Пена (EVA, Boost, FlyteFoam) со временем спрессовывается и теряет упругость. Это необратимый процесс. Вернуть амортизацию невозможно.</li>
    <li><strong>Трещина в межподошве.</strong> Если треснула сама пенная середина кроссовка (midsole), склеивание не поможет — материал разрушен.</li>
    <li><strong>Деформация пяточной чашки.</strong> Пяточный стаканчик (карбоновая или пластиковая вставка) сломался или деформировался — замена невозможна без полной переборки кроссовка.</li>
    <li><strong>Карбоновая пластина сломалась.</strong> В гоночных моделях карбоновая пластина отвечает за возврат энергии. При поломке восстановление невозможно.</li>
    <li><strong>Сильный перекос подошвы.</strong> Если одна сторона подошвы стёрта значительно сильнее — стопа бегуна имеет неправильную биомеханику. Нужен не ремонт, а консультация ортопеда.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Когда проще купить новые</h4>
    <p>Если ремонт стоит больше 50% от цены новых кроссовок — экономически выгоднее купить новую пару. Сравните: средняя цена беговых кроссовок — 8000–15000 ₽. Капитальный ремонт за 4000–7000 ₽ не вернёт амортизацию.</p>
  </div>
</section>

<section id="midsole">
  <h2>Ремонт амортизации и подошвы</h2>
  <p>Единственный способ «восстановить» амортизацию — установка дополнительной стельки с хорошим демпфированием или замена подошвы с сохранением межподошвы. Но это исключение, а не правило.</p>
  <p>Для подошвы беговых кроссовок доступны следующие виды ремонта:</p>
  <ol>
    <li><strong>Склеивание отслоившейся подошвы.</strong> Актуально для кроссовок, которые пробежали 200–400 км. Подошва могла отойти из-за брака или попадания воды. После склеивания служат дальше.</li>
    <li><strong>Замена протектора.</strong> В некоторых моделях (Asics, Brooks) можно заменить только протекторную резину, не трогая пенную середину. Делается в специализированных мастерских.</li>
    <li><strong>Профилактика на подошву.</strong> Тонкая резиновая накладка защищает подошву от дальнейшего износа. Подходит, если амортизация ещё живая (менее 500 км пробега).</li>
  </ol>
  <div data-type="box" data-color="blue">
    <h4>Дилемма бегуна</h4>
    <p>Ремонт подошвы беговых кроссовок оправдан, когда кроссовки пробежали 300–500 км и имеют локальный износ. В MANDO MEMORI мастера оценят состояние амортизации перед ремонтом — если она ещё в порядке, мы отремонтируем подошву. Если нет — честно скажем, что пора покупать новые.</p>
  </div>
</section>

<section id="upper">
  <h2>Ремонт верха беговых кроссовок</h2>
  <p>Верх беговых кроссовок (сетка, текстиль, мембрана) часто изнашивается раньше подошвы. Особенно у бегунов, которые неправильно шнуруются или имеют широкую стопу.</p>
  <ul>
    <li><strong>Протёртость в районе мизинца.</strong> Наиболее частая проблема. Ремонтируется установкой заплатки из дышащей сетки. Заплатка ставится с внутренней стороны под стельку.</li>
    <li><strong>Дыры на пятке.</strong> Возникают из-за трения пятки о задник. Укрепляется заплаткой из замши или плотной ткани.</li>
    <li><strong>Повреждение мембраны (Gore-Tex).</strong> Мембрана не подлежит ремонту — при разрыве она теряет водонепроницаемые свойства. Можно только заклеить отверстие специальной лентой, но это снизит дышимость.</li>
    <li><strong>Разрыв по шву.</strong> Зашивается на обувной машине с использованием прочных капроновых ниток.</li>
  </ul>
  <p>Ремонт верха обычно стоит 500–1500 ₽ и продлевает жизнь кроссовкам на 2–4 месяца.</p>
  <div data-type="box" data-color="gray">
    <h4>Профилактика износа верха</h4>
    <p>Правильная шнуровка — ключ к сохранению верха. Не затягивайте шнурки слишком туго в области подъёма. Используйте технику с дополнительным отверстием на пятке (heel lock) для фиксации пятки без перетягивания средней части.</p>
  </div>
</section>

<section id="prolong">
  <h2>Как продлить жизнь беговой обуви</h2>
  <p>Несколько простых практик помогут беговым кроссовкам служить дольше:</p>
  <ul>
    <li><strong>Чередуйте две пары.</strong> Пена межподошвы восстанавливает форму за 24 часа. Если бегать каждый день в одной паре, амортизация умирает на 30% быстрее.</li>
    <li><strong>Не стирайте в машинке.</strong> Машинная стирка разрушает пенную середину. Чистите вручную мягкой щёткой с мылом.</li>
    <li><strong>Сушите при комнатной температуре.</strong> Не на батарее и не феном — высокая температура разрушает пену и клей.</li>
    <li><strong>Храните в сухом месте.</strong> Влажность ускоряет деградацию межподошвы. После пробежки просушите кроссовки.</li>
    <li><strong>Следите за километражем.</strong> Записывайте пробег каждой пары. После 600–800 км отправляйте кроссовки «на пенсию» и используйте для повседневной носки.</li>
