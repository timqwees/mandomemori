<section id="principle">
  <h2>Принцип удаления масляных пятен</h2>
  <p>Масло — неполярное вещество, которое не растворяется в воде. Поэтому вода бесполезна против масляных пятен. Принцип борьбы — использовать абсорбенты (вещества, впитывающие масло) или растворители (вещества, растворяющие масло).</p>
  <ul>
    <li><strong>Абсорбенты</strong> — тальк, крахмал, сода, активированный уголь. Они впитывают масло с поверхности.</li>
    <li><strong>Растворители</strong> — спирт, средство для мытья посуды, уайт-спирит (только для гладкой кожи). Они растворяют масло, после чего оно удаляется тканью.</li>
  </ul>
  <p>Первый шаг всегда — абсорбент. Если он не справляется, переходите к растворителям. Но помните: растворители агрессивны и подходят не для всех материалов.</p>
  <div data-type="box" data-color="gray">
    <h4>Главное правило</h4>
    <p>Никогда не трите масляное пятно влажной тканью — вы размажете масло на большую площадь. Всегда начинайте с сухого абсорбента.</p>
  </div>
</section>

<section id="fresh">
  <h2>Свежие масляные пятна</h2>
  <p>Свежее масляное пятно — идеальный кандидат на полное удаление. Чем быстрее вы начнёте, тем выше шанс убрать его без следа.</p>
  <h3>Экстренный алгоритм</h3>
  <ol>
    <li><strong>Промокните излишки масла</strong> бумажной салфеткой. Не трите!</li>
    <li><strong>Засыпьте абсорбентом.</strong> Тальк, кукурузный крахмал или сода — что есть под рукой. Толстый слой (2–3 мм) на всё пятно.</li>
    <li><strong>Оставьте на 30–60 минут.</strong> Абсорбент впитает масло с поверхности.</li>
    <li><strong>Счистите щёткой.</strong> Мягкой щёткой удалите абсорбент.</li>
  </ol>
  <p>Если после этого пятно осталось — повторите процедуру с новой порцией абсорбента. Свежие пятна требуют 1–2 циклов.</p>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Для максимальной эффективности слегка прогрейте пятно феном (средняя температура, 10–15 см от поверхности). Тёплое масло становится более жидким и легче впитывается в абсорбент. Не перегревайте — материал не должен стать горячим.</p>
  </div>
</section>

<section id="old">
  <h2>Застарелые масляные пятна</h2>
  <p>Старые масляные пятна — вызов даже для профессионалов. Масло успело проникнуть глубоко в структуру материала и частично окислиться, оставив тёмный пигмент.</p>
  <h3>Для гладкой кожи</h3>
  <p>Используйте средство для мытья посуды. Нанесите каплю на влажную губку, вспеньте и обработайте пятно. Оставьте на 10–15 минут. Затем смойте тёплой водой и вытрите насухо. Средство для посуды создано для расщепления жиров — оно эффективно растворяет масло.</p>
  <h3>Для текстиля</h3>
  <p>Смешайте средство для мытья посуды с перекисью водорода 3% в пропорции 1:1. Нанесите на пятно, вотрите мягкой щёткой. Оставьте на 20 минут, смойте. Этот состав расщепляет масло и одновременно отбеливает пигмент.</p>
  <h3>Если ничего не помогает</h3>
  <p>Некоторые масляные пятна (особенно на замше и нубуке) невозможно удалить домашними средствами. В этом случае нужна профессиональная обработка. В MANDO MEMORI используют профессиональные обезжириватели, которые удаляют масло, не повреждая материал.</p>
</section>

<section id="leather">
  <h2>Масло на гладкой коже</h2>
  <p>Гладкая кожа — лучший материал для удаления масляных пятен. Она менее пористая, и масло не проникает глубоко.</p>
  <h3>Метод 1: Абсорбент + утюг</h3>
  <ol>
    <li>Посыпьте пятно тальком или крахмалом.</li>
    <li>Накройте бумажным полотенцем.</li>
    <li>Прогладьте утюгом на средней температуре (без пара). Тепло расплавит масло, и оно впитается в тальк и бумагу.</li>
    <li>Повторите 2–3 раза, меняя бумагу.</li>
  </ol>
  <h3>Метод 2: Спирт (для стойких пятен)</h3>
  <p>Смочите ватный диск медицинским спиртом, протрите пятно. Масло растворится в спирте, и его можно будет удалить тканью. После спирта обязательно нанесите питающий крем — спирт сушит кожу.</p>
  <div data-type="box" data-color="red">
    <h4>Предостережение</h4>
    <p>Не используйте метод с утюгом на искусственной коже и экокоже — высокая температура расплавит полиуретановое покрытие. Для синтетических материалов используйте только холодные методы.</p>
  </div>
</section>

<section id="suede">
  <h2>Масло на замше и нубуке</h2>
  <p>Замша и нубук — самый сложный случай. Ворс впитывает масло как губка, и удалить его почти невозможно без специальных средств.</p>
  <h3>Метод 1: Тальк на ночь</h3>
  <p>Посыпьте пятно толстым слоем талька, оставьте на 8–12 часов (на ночь). Утром удалите тальк щёткой для замши. Если пятно посветлело — повторите.</p>
  <h3>Метод 2: Специальный очиститель для замши</h3>
  <p>Купите пенное средство для чистки замши. Нанесите пену, подождите 3–5 минут, снимите чистой тканью. Пена растворяет масло и вытягивает его из ворса.</p>
  <h3>Метод 3: Затирка пятна</h3>
  <p>Если пятно застарелое и не уходит — попробуйте аккуратно затереть его ластиком для замши или мелкой наждачной бумагой (нулёвкой). Вы буквально стираете верхний слой ворса вместе с маслом. После этого подкрасьте участок краской для замши.</p>
  <div data-type="box" data-color="dark">
    <h4>Совет эксперта</h4>
    <p>Масло на замше — практически необратимое повреждение в домашних условиях. Даже если пятно посветлело, оно всё равно может остаться заметным. Лучшее решение — обратиться в MANDO MEMORI, где используют профессиональные обезжириватели, безопасные для деликатного ворса.</p>
  </div>
</section>

<section id="textile">
  <h2>Масло на текстиле</h2>
  <p>Текстиль — демократичный материал: масло можно удалять довольно агрессивными средствами.</p>
  <ul>
    <li><strong>Средство для мытья посуды.</strong> Нанесите на пятно, вспеньте, оставьте на 15 минут, смойте. Лучший выбор для синтетики.</li>
    <li><strong>Сода + вода.</strong> Сделайте кашицу, нанесите на 30 минут. Сода впитывает масло и отбеливает ткань.</li>
    <li><strong>Уайт-спирит (только для плотной синтетики).</strong> Нанесите на ватный диск, протрите пятно. После удаления масла промойте мыльным раствором.</li>
    <li><strong>Кислородный отбеливатель.</strong> Замочите кроссовки (или нанесите раствор на пятно) на 30–60 минут. Эффективен для белого текстиля.</li>
  </ul>
