<section id="types">
  <h2>Типы кожи и их особенности</h2>
  <p>Не вся кожа одинакова. Подход к чистке зависит от типа кожи. Основные виды:</p>
  <ul>
    <li><strong>Гладкая кожа (Full-grain).</strong> Самая качественная и долговечная. Имеет естественную текстуру, дышит, хорошо переносит чистку и кремы. Требует регулярного питания.</li>
    <li><strong>Наппа (Nappa).</strong> Мягкая, эластичная кожа с бархатистой поверхностью. Чувствительна к влаге и механическим повреждениям. Чистить только мягкими средствами.</li>
    <li><strong>Лакированная кожа.</strong> Покрыта полиуретановым лаком. Не впитывает крем, боится растворителей и царапин. Чистится только влажной тканью.</li>
    <li><strong>Зернистая кожа (Suede, Nubuck).</strong> Фактически — шлифованная кожа с ворсом. Боится воды, чистится сухим методом или специальными пенками.</li>
    <li><strong>Экокожа.</strong> Искусственный материал на полиуретановой основе. Менее требовательна, но боится высоких температур и растворителей.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Как определить тип кожи</h4>
    <p>Капните воду на незаметный участок. На гладкой коже вода останется каплей и не впитается быстро. На замше и нубуке — впитается мгновенно. Экокожа не впитывает воду совсем — она останется на поверхности до испарения.</p>
  </div>
</section>

<section id="daily">
  <h2>Ежедневный уход</h2>
  <p>Ежедневная забота о кожаной обуви занимает 2–3 минуты, но продлевает её жизнь на годы. Вот базовый алгоритм:</p>
  <ul>
    <li><strong>Удаление пыли.</strong> После каждой носки протирайте обувь влажной микрофиброй. Пыль — абразив, который царапает кожу при ходьбе.</li>
    <li><strong>Просушка.</strong> Если обувь намокла — набейте газетой и оставьте при комнатной температуре. Не ставьте на батарею! Кожа от перегрева дубеет и трескается.</li>
    <li><strong>Полировка.</strong> Раз в неделю полируйте обувь мягкой тканью для поддержания блеска. Это не только эстетика, но и защита — полировка закрывает микропоры.</li>
  </ul>
</section>

<section id="deepclean">
  <h2>Глубокая чистка</h2>
  <p>Раз в 1–2 месяца кожаной обуви нужна глубокая чистка с удалением застарелой грязи и старого крема.</p>
  <h3>Что понадобится</h3>
  <ul>
    <li>Очиститель для гладкой кожи (Saphir Renomat, Angelus Easy Cleaner или аналоги)</li>
    <li>Щётка с мягкой щетиной</li>
    <li>Микрофибра — 3–4 штуки</li>
    <li>Ватные диски и палочки для труднодоступных мест</li>
  </ul>
  <h3>Пошаговая инструкция</h3>
  <ol>
    <li>Удалите шнурки и стельки. Если возможно — достаньте их полностью.</li>
    <li>Сметите сухую грязь мягкой щёткой.</li>
    <li>Нанесите очиститель на микрофибру (не на обувь). Протрите всю поверхность.</li>
    <li>Особо загрязнённые участки обработайте щёткой круговыми движениями.</li>
    <li>Снимите остатки средства чистой влажной микрофиброй.</li>
    <li>Дайте коже высохнуть 30–40 минут.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Чего избегать</h4>
    <p>Не используйте хозяйственное мыло, жидкость для мытья посуды и спирт — они нарушают pH кожи и делают её ломкой. Гладкая кожа не терпит агрессивных щелочей.</p>
  </div>
</section>

<section id="condition">
  <h2>Питание и увлажнение</h2>
  <p>Чистка очищает кожу, но удаляет и натуральные масла. После чистки кожа «голодна» — ей нужно питание. Крем для обуви выполняет три функции:</p>
  <ul>
    <li><strong>Питание.</strong> Натуральные масла и воски проникают в поры, восстанавливая эластичность кожи.</li>
    <li><strong>Цвет.</strong> Пигментированные кремы восстанавливают цвет и маскируют потёртости.</li>
    <li><strong>Защита.</strong> Восковое покрытие защищает от влаги и грязи.</li>
  </ul>
  <h3>Выбор крема</h3>
  <ul>
    <li><strong>Бесцветный крем.</strong> Универсальный вариант для поддержания состояния. Подходит для всех цветов.</li>
    <li><strong>Цветной крем.</strong> Выбирайте тон в тон обуви. Восстанавливает цвет и маскирует мелкие дефекты. Для светлой кожи используйте крем светлее — тёмный оттенок может дать нежелательный тон.</li>
    <li><strong>Воск (Wax).</strong> Даёт глубокий блеск и мощную защиту. Используется для парадной обуви.</li>
  </ul>
  <h3>Как наносить крем</h3>
  <ol>
    <li>Нанесите крем тонким слоем круговыми движениями.</li>
    <li>Дайте впитаться 10–15 минут</li>
    <li>Отполируйте мягкой щёткой или тканью до блеска.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Совет профессионала</h4>
    <p>Питайте кожу не чаще раза в 1–2 недели. Избыток крема забивает поры, и кожа перестаёт дышать. Между чистками используйте кондиционер-спрей для свежести.</p>
  </div>
</section>

<section id="protect">
  <h2>Защита от влаги и реагентов</h2>
  <p>Влага и соль — главные враги кожаной обуви. Водоотталкивающая пропитка обязательна для осени и зимы.</p>
  <ul>
    <li><strong>Для гладкой кожи.</strong> Используйте спрей на основе восков или силикона. Наносите на чистую сухую кожу за 12 часов до выхода.</li>
    <li><strong>Для замши и нубука.</strong> Специальные фторполимерные пропитки, которые не меняют цвет и текстуру.</li>
    <li><strong>Периодичность.</strong> Обновляйте защиту раз в 2–3 недели, в дождливый сезон — раз в неделю.</li>
  </ul>
  <p>Если обувь всё же намокла и на ней появились белые солевые разводы — протрите их раствором воды с уксусом 1:1, затем чистой водой.</p>
</section>

<section id="storage">
  <h2>Правильное хранение</h2>
  <p>Хранение влияет на состояние кожи не меньше, чем чистка. Основные правила:</p>
  <ul>
    <li><strong>Колодки или наполнитель.</strong> Используйте деревянные колодки из кедра — они впитывают влагу и сохраняют форму.</li>
    <li><strong>Проветривание.</strong> Храните обувь в тканевых мешках, а не в полиэтиленовых пакетах. Коже нужен воздух.</li>
    <li><strong>Температура.</strong> Идеальные условия: 15–20 °C и влажность 40–60%. Избегайте хранения рядом с батареями.</li>
    <li><strong>Вертикальное хранение.</strong> Не складывайте обувь друг на друга — это деформирует задник.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Сезонное хранение</h4>
    <p>Перед сезонным хранением проведите полный цикл: глубокая чистка, питание кремом и защита пропиткой. Убедитесь, что обувь полностью сухая. Храните в бумажных коробках с отверстиями для вентиляции.</p>
  </div>
</section>

