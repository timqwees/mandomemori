<section id="what">
  <h2>Что такое нубук и чем он отличается от замши</h2>
  <p>Нубук — это натуральная кожа, отшлифованная с лицевой стороны. В отличие от замши, которая делается из бахтармы (внутреннего слоя), нубук обрабатывается снаружи. Это даёт более мелкий и плотный ворс.</p>
  <p>Основные отличия:</p>
  <ul>
    <li><strong>Ворс.</strong> У нубука он короче и бархатистее, у замши — длиннее и рыхлее.</li>
    <li><strong>Водостойкость.</strong> Нубук менее боится воды, хотя мочить его тоже не рекомендуется. Замша от воды грубеет и теряет вид.</li>
    <li><strong>Износостойкость.</strong> Нубук изнашивается медленнее замши и лучше держит форму.</li>
    <li><strong>Уход.</strong> Нубук можно чистить специальными пенками, замша предпочитает сухую чистку.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно знать</h4>
    <p>В продаже есть натуральный нубук (из кожи КРС) и искусственный нубук (микрофибра). Искусственный нубук более устойчив к воде и грязи, но хуже дышит и быстрее протирается на сгибах. Методы чистки для них одинаковы.</p>
  </div>
</section>

<section id="daily">
  <h2>Ежедневный уход</h2>
  <p>Нубуку нужна регулярная забота, но она проще, чем кажется:</p>
  <ul>
    <li><strong>Сухая чистка.</strong> После каждой носки проходите по поверхности специальной щёткой для нубука. Она поднимает ворс и удаляет пыль. Движения — в одном направлении.</li>
    <li><strong>Ластик для нубука.</strong> При появлении потёртостей используйте ластик — он стирает верхний слой грязи и выравнивает ворс.</li>
    <li><strong>Не сушите на батарее.</strong> Как и все виды кожи, нубук от перегрева дубеет и теряет эластичность.</li>
  </ul>
</section>

<section id="deep">
  <h2>Глубокая чистка нубука</h2>
  <p>Глубокая чистка требуется раз в 1–2 месяца или при сильных загрязнениях.</p>
  <h3>Что понадобится</h3>
  <ul>
    <li>Пенное средство для нубука (Saphir Omnidaim, Collonil Nubuk Cleaner)</li>
    <li>Щётка для нубука (резиновая или с латунной щетиной)</li>
    <li>Губка или мягкая микрофибра</li>
    <li>Ватные диски</li>
  </ul>
  <h3>Пошаговая инструкция</h3>
  <ol>
    <li>Удалите сухую грязь резиновой щёткой. Резиновая щетина вытягивает пыль из глубины ворса эффективнее обычной.</li>
    <li>Нанесите пену на губку и вспеньте. Не лейте средство на обувь — работайте только пеной.</li>
    <li>Обработайте всю поверхность круговыми движениями. Если пена потемнела — меняйте сторону губки.</li>
    <li>Снимите остатки пены сухой микрофиброй. Не трите — прижимайте и убирайте загрязнения.</li>
    <li>Высушите при комнатной температуре. После высыхания пройдитесь щёткой для поднятия ворса.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Совет эксперта</h4>
    <p>Не пытайтесь ускорить сушку нубука феном. Горячий воздух делает ворс жёстким и ломким. Если нубук намок — просто набейте обувь бумагой и оставьте на 12–24 часа. Бумага впитает влагу, а ворс сам восстановится.</p>
  </div>
</section>

<section id="stains">
  <h2>Удаление пятен</h2>
  <p>Нубук впитывает жидкости быстрее гладкой кожи, поэтому с пятнами нужно действовать оперативно:</p>
  <h3>Жирные пятна</h3>
  <p>Посыпьте свежее пятно тальком или кукурузным крахмалом. Оставьте на 3–4 часа (лучше на ночь) — порошок впитает жир. Удалите щёткой. Если след остался — повторите.</p>
  <h3>Водные пятна</h3>
  <p>Разводы от воды на нубуке — частая проблема. Удалите их ватным диском, смоченным в слабом растворе уксуса (1 часть уксуса на 5 частей воды). Промокните, дайте высохнуть и поднимите ворс.</p>
  <h3>Краска и чернила</h3>
  <p>Не пытайтесь вывести чернила с нубука самостоятельно — вы только разотрёте их глубже. Обратитесь в мастерскую. В MANDO MEMORI мы используем профессиональные средства для удаления чернил с воровых поверхностей.</p>
  <div data-type="box" data-color="red">
    <h4>Чего нельзя делать</h4>
    <p>Не используйте на нубуке спирт, ацетон, бензин, жидкость для снятия лака и мыльные растворы. Они обезжиривают ворс, и нубук становится жёстким и блестящим. Восстановить такой нубук можно только глубокой шлифовкой.</p>
  </div>
</section>

<section id="restore">
  <h2>Восстановление цвета и ворса</h2>
  <p>Со временем нубук выцветает на солнце и затирается на сгибах. Вернуть цвет и освежить ворс можно с помощью краски-аэрозоля для нубука.</p>
  <h3>Краска для нубука</h3>
  <ol>
    <li>Тщательно вычистите и высушите обувь.</li>
    <li>Заклейте малярным скотчем подошву и элементы из других материалов.</li>
    <li>Распылите краску с расстояния 20–25 см равномерными слоями.</li>
    <li>Дайте каждому слою высохнуть 15–20 минут. Обычно нужно 2–3 слоя.</li>
    <li>После высыхания пройдитесь щёткой для поднятия ворса.</li>
  </ol>
  <p>Краска для нубука не только восстанавливает цвет, но и маскирует потёртости. Выбирайте цвет в тон оригиналу или на тон темнее.</p>
  <h3>Восстановление ворса</h3>
  <p>Если ворс примялся (на сгибах или от неправильного хранения), поднимите его щёткой. Для застарелых заломов используйте отпариватель: пар размягчает волокна, и ворс поднимается легче.</p>
  <div data-type="box" data-color="dark">
    <h4>Совет</h4>
    <p>Не красите нубук самостоятельно, если не уверены в цвете или материале. Неправильный оттенок или слишком толстый слой краски могут безнадёжно испортить обувь. В MANDO MEMORI подбирают цвет компьютерным спектрофотометром — точное попадание гарантировано.</p>
  </div>
</section>

<section id="protect">
  <h2>Защита нубука</h2>
  <p>Водоотталкивающая пропитка — обязательный этап ухода за нубуком. Без защиты он впитывает грязь и влагу, как губка.</p>
  <ul>
    <li><strong>Выбор пропитки.</strong> Используйте спрей, специально разработанный для нубука. Обычные пропитки для гладкой кожи забивают ворс и делают нубук липким.</li>
    <li><strong>Периодичность.</strong> Наносите защиту перед первой ноской и обновляйте раз в 2–3 недели. В дождливый сезон — раз в неделю.</li>
    <li><strong>Нанесение.</strong> Распыляйте на чистую сухую обувь с расстояния 20 см. Дайте высохнуть 12 часов.</li>
  </ul>
</section>

