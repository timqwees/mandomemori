<section id="daily">
  <h2>Ежедневный уход за белой обувью</h2>
  <p>Главный секрет белых кроссовок — регулярность. Если чистить обувь после каждой носки, глубокой чистки может не понадобиться месяцами. Вот минимальная программа:</p>
  <ul>
    <li><strong>Влажная салфетка.</strong> Сразу после возвращения протрите кроссовки микрофиброй. Свежая пыль и грязь удаляются без усилий, а засохшая грязь требует больше времени и может оставить разводы.</li>
    <li><strong>Щётка для подошвы.</strong> Держите у входа щётку с жёсткой щетиной и чистите подошву после каждой прогулки. Это предотвращает накопление реагентов и мелких камней в протекторе.</li>
    <li><strong>Пенка для экспресс-чистки.</strong> Силиконовые пенки для обуви позволяют освежить белый верх за 2 минуты без воды. Распылите пену, вотрите мягкой щёткой и снимите остатки сухой тканью.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Лайфхак от профессионалов</h4>
    <p>Держите в сумке или машине детские влажные салфетки без спирта. Если заметили свежее пятно — протрите сразу, не дожидаясь возвращения домой. Пятна от кофе, травы или грязи выводятся в 5 раз легче, пока они свежие.</p>
  </div>
</section>

<section id="sole">
  <h2>Отбеливание подошвы</h2>
  <p>Жёлтая подошва — главный враг белых кроссовок. Даже если верх идеально чистый, жёлтая полоса выдаёт возраст обуви. Для отбеливания подошвы используйте один из трёх методов в зависимости от степени пожелтения.</p>
  <h3>Метод 1: Сода и перекись (лёгкое пожелтение)</h3>
  <p>Смешайте 2 столовые ложки соды с перекисью водорода 3% до состояния пасты. Нанесите на подошву, вотрите зубной щёткой и оставьте на 20 минут. Смойте тёплой водой. Повторяйте раз в 1–2 недели.</p>
  <h3>Метод 2: Ретробрайт (среднее пожелтение)</h3>
  <p>Для полупрозрачных подошв (таких как Nike Air Force 1) используйте смесь перекиси 12% с оксидом цинка. Нанесите толстым слоем, оберните плёнкой и поставьте под УФ-лампу на 4–6 часов. Метод даёт до 90% восстановления белизны.</p>
  <div data-type="box" data-color="red">
    <h4>Осторожно</h4>
    <p>Не используйте ацетон или жидкость для снятия лака — они растворяют резину и делают подошву липкой. Также избегайте хлорного отбеливателя: он даёт кратковременный эффект, но разрушает структуру резины.</p>
  </div>
</section>

<section id="upper">
  <h2>Чистка верха из разных материалов</h2>
  <p>Верх белых кроссовок может быть из кожи, замши, текстиля или синтетики. Каждый материал требует своего подхода:</p>
  <h3>Гладкая кожа</h3>
  <p>Протирайте кожу влажной микрофиброй. Для стойких загрязнений используйте пенное мыло или специальный очиститель для гладкой кожи. После чистки нанесите питающий крем — он сохранит эластичность и предотвратит трещины.</p>
  <h3>Замша и нубук</h3>
  <p>Для замши используйте сухую чистку специальной щёткой. Если замша намокла и испачкалась — дайте ей полностью высохнуть, потом пройдитесь щёткой для поднятия ворса. Для жирных пятен используйте тальк или кукурузный крахмал.</p>
  <h3>Текстиль и сетка</h3>
  <p>Текстильные кроссовки чистите пенным очистителем: нанесите пену, вотрите мягкой щёткой и снимите остатки тканью. Не трите сильно — можно повредить переплетение нитей. Для белого текстиля используйте отбеливающий спрей для обуви.</p>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Никогда не используйте одну и ту же щётку для разных материалов. Щетина, испачканная кремом для кожи, оставит жирные следы на замше. Держите отдельные щётки для каждого типа материала.</p>
  </div>
</section>

<section id="laces">
  <h2>Шнурки и стельки</h2>
  <p>Шнурки и стельки — самые недооценённые элементы белой обуви. Потёртые серые шнурки портят впечатление даже от идеально чистой пары. Правило простое: шнурки должны быть белыми, а стельки — свежими.</p>
  <h3>Чистка шнурков</h3>
  <ul>
    <li>Замочите шнурки в тёплой воде с пятновыводителем для белья на 30 минут.</li>
    <li>Потрите руками или постирайте в мешочке для стирки в машинке на деликатном режиме.</li>
    <li>Если шнурки не отбелились — замените на новые. Шнурки стоят недорого, а освежают вид кардинально.</li>
  </ul>
  <h3>Чистка стелек</h3>
  <p>Стельки впитывают пот и запахи, поэтому их нужно чистить или менять раз в 3–4 месяца. Промойте стельки в мыльной воде, потрите мягкой щёткой и высушите при комнатной температуре. Используйте угольные стельки — они впитывают влагу и предотвращают запах.</p>
</section>

<section id="prevent">
  <h2>Как сохранить белизну надолго</h2>
  <p>Предотвратить загрязнение проще, чем бороться с последствиями. Несколько правил от мастеров MANDO MEMORI:</p>
  <ul>
    <li><strong>Водоотталкивающий спрей.</strong> Наносите защиту на все белые кроссовки перед первой ноской. Спрей создаёт невидимый барьер: грязь и вода скатываются, не впитываясь в материал.</li>
    <li><strong>Не носите в слякоть.</strong> Белые кроссовки — обувь для сухой погоды. В дождь и снег они пачкаются мгновенно, а реагенты проникают глубоко в структуру резины и ткани.</li>
    <li><strong>Правильное хранение.</strong> Храните кроссовки в бумажной коробке вдали от солнечного света. УФ-лучи ускоряют пожелтение и делают белый цвет серым.</li>
    <li><strong>Чередуйте обувь.</strong> Не носите одну пару два дня подряд. Кроссовкам нужно время, чтобы высохнуть и «отдохнуть» — это продлевает срок службы в 2–3 раза.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Совет эксперта</h4>
    <p>Купите вторую пару белых кроссовок и носите их по очереди. Одна пара — гуляете, вторая — сохнет и отдыхает. Так обе пары прослужат дольше, а вы всегда будете в чистой обуви.</p>
  </div>
</section>

