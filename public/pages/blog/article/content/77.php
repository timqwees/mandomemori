<section id="materials">
  <h2>Материалы Loro Piana: что нужно знать</h2>
  <p>Loro Piana использует материалы, которые требуют особо деликатного подхода:</p>
  <ul>
    <li><strong>Кашемир Baby Cashmere.</strong> Верх кроссовок из тончайшего кашемира. Невероятно мягкий, но крайне уязвимый к механическим воздействиям. Склонен к пиллингу (катышкам).</li>
    <li><strong>Кожа Loro Piana.</strong> Телячья кожа высшего качества — мягкая, тонкая, с натуральной текстурой. Часто не имеет защитного покрытия, что делает её чувствительной к влаге и грязи.</li>
    <li><strong>Замша.</strong> Бархатистая, с высоким ворсом. Используется в осенних коллекциях. Боится воды и грязи.</li>
    <li><strong>Подошва.</strong> Кожаная подошва на классических моделях, резиновая Vibram — на кроссовках и ботинках для города.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Принцип Loro Piana</h4>
    <p>Loro Piana красит материалы натуральными пигментами. Это означает, что окраска может быть менее стойкой, чем у синтетических аналогов. Будьте осторожны с влажной чисткой.</p>
  </div>
</section>

<section id="cashmere">
  <h2>Уход за кашемировыми кроссовками</h2>
  <p>Кашемировые кроссовки Loro Piana (модели Open Walk, Summer Walk, 360) — вершина комфорта и уязвимости. Кашемир — деликатный материал, который требует почти музейного обращения.</p>
  <h3>Чистка кашемира</h3>
  <ul>
    <li><strong>Только сухая чистка.</strong> Вода убивает кашемир — садится, теряет форму, появляются разводы. Используйте мягкую щётку для кашемира (или чистую сухую губку).</li>
    <li><strong>Катышки.</strong> Кашемир Loro Piana скатывается быстрее обычного из-за тонкости волокна. Удаляйте катышки специальной машинкой для кашемира или аккуратно бритвой (лезвие — под очень острым углом).</li>
    <li><strong>Пятна.</strong> Не трите! Промокните пятно белой тканью. Если не отходит — несите в профессиональную химчистку, специализирующуюся на кашемире.</li>
    <li><strong>Пар.</strong> Для восстановления формы и освежения — отпаривание парогенератором с расстояния 20–30 см. Пар убивает бактерии и разглаживает ворс.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Чего нельзя делать с кашемиром</h4>
    <p>Не используйте стиральную машину, отбеливатели, кондиционеры для белья, фен. Не сушите на батарее. Не гладьте утюгом. После любого контакта с водой — сушите только горизонтально, вдали от источников тепла.</p>
  </div>
</section>

<section id="leather">
  <h2>Уход за кожаной обувью Loro Piana</h2>
  <p>Кожа Loro Piana — анилинового крашения, без защитного покрытия. Это значит, что она максимально натуральная и дышащая, но и максимально уязвимая.</p>
  <h3>Ежедневный уход</h3>
  <ul>
    <li><strong>Протирайте мягкой фланелью</strong> после каждой носки — удаляйте пыль и лёгкие загрязнения.</li>
    <li><strong>Не используйте воду.</strong> Для кожи Loro Piana вода — враг. Используйте специальный лосьон для анилиновой кожи (Saphir, Loro Piana own care).</li>
    <li><strong>Питание.</strong> Раз в 2–3 недели — крем-кондиционер на основе норкового масла. Он питает кожу, не забивая поры.</li>
    <li><strong>Полировка.</strong> Восковой крем (паста) — для создания защитного слоя. Loro Piana рекомендует собственные средства ухода, но подходят и премиальные Saphir Médaille d‘Or.</li>
  </ul>
  <h3>Удаление царапин</h3>
  <p>Мелкие царапины на коже Loro Piana — часть «характера» обуви. Но если они вас беспокоят: разотрите кожу пальцем — тепло разгладит микроцарапины. Если не помогло — используйте крем-краситель в цвет. Нанесите тонким слоем, дайте впитаться, отполируйте.</p>
</section>

<section id="suede">
  <h2>Уход за замшей и нубуком Loro Piana</h2>
  <p>Замша Loro Piana — из шкур козлят и ягнят, нежнее и тоньше обычной. Нубук — из кожи крупного рогатого скота, плотнее, но также чувствителен.</p>
  <ul>
    <li><strong>Резиновая щётка.</strong> После каждой носки — аккуратно пройдитесь по замше, поднимая ворс.</li>
    <li><strong>Пятна.</strong> Только специальный ластик для замши. Никакой воды!</li>
    <li><strong>Пропитка.</strong> Используйте аэрозольную пропитку для замши премиум-класса (Collonil Carbon, Loro Piana care). Наносите на чистую сухую замшу с расстояния 25 см. Перед первой ноской — обязательно.</li>
    <li><strong>Сушка.</strong> Если замша намокла — промокните бумажным полотенцем, набейте бумагой, сушите при комнатной температуре. После высыхания — расчешите щёткой.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Для замши Loro Piana не используйте силиконовые пропитки. Силикон забивает поры и нарушает воздухообмен, от чего замша «задыхается» и теряет эластичность.</p>
  </div>
</section>

<section id="sole">
  <h2>Уход за подошвой Loro Piana</h2>
  <p>Кожаная подошва — классика Loro Piana для туфель и лоферов. Она элегантна, но непрактична в городе.</p>
  <p><strong>Кожаная подошва:</strong> После покупки рекомендуется установить профилактику (тонкую резиновую накладку) в MANDO MEMORI. Это продлевает жизнь подошвы в 3–4 раза. Кожаная подошва боится воды — на улице в мокрую погоду она промокает. После намокания — сушите 48 часов.</p>
  <p><strong>Резиновая подошва Vibram:</strong> На кроссовках и ботинках. Не требует особого ухода — просто чистите щёткой. Следите за износом протектора — при истирании до гладкого состояния замените подошву в мастерской.</p>
</section>

<section id="storage">
  <h2>Хранение обуви Loro Piana</h2>
  <p>Премиальная обувь требует премиального хранения:</p>
  <ul>
    <li><strong>Колодки из кедра.</strong> Дорогие, но необходимые. Кедр впитывает влагу, предотвращает появление запаха и сохраняет форму. Loro Piana продаёт собственные колодки.</li>
    <li><strong>Пылевики.</strong> Используйте тканевые мешочки из коробки. Обувь Loro Piana не должна храниться в полиэтилене.</li>
    <li><strong>Климат-контроль.</strong> Идеальные условия: температура 18–22 °C, влажность 45–55%. Не храните вблизи батарей и кондиционеров.</li>
    <li><strong>Раздельное хранение.</strong> Кашемировые и кожаные модели храните отдельно — кашемир впитывает запахи и жир от кожи.</li>
  </ul>
</section>

