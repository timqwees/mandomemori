<section id="sole">
  <h2>Желтизна на подошве</h2>
  <p>Жёлтая подошва — проблема, знакомая каждому владельцу белых кроссовок. Резина желтеет от окисления быстрее других материалов.</p>
  <h3>Профилактика</h3>
  <ul>
    <li>Чистите подошву после каждой носки влажной тканью.</li>
    <li>Не сушите на солнце — УФ-лучи ускоряют пожелтение.</li>
    <li>Используйте силиконовый спрей для подошвы — он создаёт защитный барьер.</li>
  </ul>
  <h3>Удаление желтизны с подошвы</h3>
  <ol>
    <li><strong>Сода с перекисью.</strong> Смешайте до пасты, нанесите на подошву, оставьте на 30 минут. Потрите щёткой, смойте.</li>
    <li><strong>Ретробрайт.</strong> Для сильного пожелтения используйте метод с перекисью 12% и УФ-лампой (описан в статье про отбеливание подошвы).</li>
    <li><strong>Профессиональное отбеливание.</strong> В MANDO MEMORI используем профессиональные составы, которые проникают в структуру резины и удаляют желтизну на молекулярном уровне.</li>
