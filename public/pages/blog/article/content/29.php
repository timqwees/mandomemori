<section id="reasons">
  <h2>Почему появляются жёлтые пятна</h2>
  <p>Прежде чем бороться с желтизной, нужно понять её причину. От неё зависит метод удаления:</p>
  <ul>
    <li><strong>Пот.</strong> Самая частая причина. Пот содержит мочевину и жирные кислоты, которые при окислении на воздухе желтеют. Пятна от пота появляются на стельках, язычке и вокруг лодыжки.</li>
    <li><strong>Неправильная сушка.</strong> Сушка на батарее или под прямыми солнечными лучами ускоряет окисление материалов. Белая обувь желтеет от перегрева.</li>
    <li><strong>Реагенты.</strong> Уличные химические реагенты вступают в реакцию с материалами и оставляют жёлтые разводы, особенно на подошве.</li>
    <li><strong>Старение.</strong> Со временем белые материалы естественным образом желтеют из-за окисления полимеров. Этот процесс необратим, но его можно замедлить.</li>
    <li><strong>Неправильное хранение.</strong> Хранение в полиэтиленовых пакетах создаёт парниковый эффект, который ускоряет пожелтение.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно определить причину</h4>
    <p>Если желтизна от пота — убирайте её перекисью или уксусом. Если от окисления — используйте кислородный отбеливатель. Если от реагентов — поможет лимонная кислота. Универсального метода нет.</p>
  </div>
</section>

<section id="leather">
  <h2>Удаление желтизны с гладкой кожи</h2>
  <p>Гладкая кожа — пористый материал, в который желтизна проникает глубоко. Поверхностное мытьё не помогает.</p>
  <h3>Метод 1: Лимонный сок</h3>
  <p>Разрежьте лимон, протрите срезом жёлтые участки. Оставьте на 10 минут, затем смойте влажной тканью. Лимонная кислота отбеливает и не сушит кожу так сильно, как перекись.</p>
  <h3>Метод 2: Перекись водорода 3%</h3>
  <p>Смочите ватный диск перекисью и протрите пятно. Оставьте на 5–10 минут. Повторяйте до исчезновения желтизны. После обработки обязательно нанесите питающий крем — перекись сушит кожу.</p>
  <h3>Метод 3: Специализированный отбеливающий крем</h3>
  <p>Для кожаной обуви существуют отбеливающие кремы (например, Saphir Renovateur для белой кожи). Они одновременно очищают, отбеливают и питают кожу. Дороже домашних средств, но безопаснее и эффективнее.</p>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Не используйте ацетон, бензин и хлорный отбеливатель на коже. Они разрушают структуру кожи и делают её шершавой. Кожа после таких средств не восстанавливается.</p>
  </div>
</section>

<section id="textile">
  <h2>Выведение жёлтых пятен с текстиля</h2>
  <p>Текстиль — самый удобный материал для удаления желтизны. Он выдерживает более агрессивные средства.</p>
  <h3>Метод 1: Кислородный отбеливатель</h3>
  <p>Лучший выбор для белого текстиля. Разведите порошок по инструкции, нанесите на пятно на 15–20 минут. Смойте тёплой водой. Кислородный отбеливатель удаляет желтизну, не разрушая волокна.</p>
  <h3>Метод 2: Сода + перекись</h3>
  <p>Смешайте соду и перекись водорода 3% до консистенции пасты. Нанесите на пятно, вотрите щёткой. Оставьте на 30 минут, затем смойте. Повторяйте 2–3 раза при сильной желтизне.</p>
  <h3>Метод 3: Аспирин</h3>
  <p>Растворите 5 таблеток аспирина в стакане воды. Нанесите раствор на пятна и оставьте на 2–3 часа. Аспирин содержит ацетилсалициловую кислоту, которая отбеливает ткань. Смывать не обязательно — дайте высохнуть.</p>
</section>

<section id="suede">
  <h2>Жёлтые пятна на замше и нубуке</h2>
  <p>Замша и нубук — самые сложные материалы для удаления желтизны. Вода и агрессивные средства портят ворс.</p>
  <ul>
    <li><strong>Специальный очиститель.</strong> Используйте пенное средство для замши/нубука. Нанесите пену на пятно, подождите 5 минут, снимите чистой тканью. Не трите!</li>
    <li><strong>Ластик для замши.</strong> Если желтизна слабая — попробуйте удалить её ластиком. Он стирает верхний слой ворса вместе с пожелтевшим участком.</li>
    <li><strong>Краска для замши.</strong> Если желтизна не удаляется — закрасьте её краской для замши в цвет оригинального материала. Выбирайте оттенок тон в тон.</li>
  </ul>
