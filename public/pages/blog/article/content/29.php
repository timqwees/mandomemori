<section id="prevent">
  <h2>Как предотвратить появление жёлтых пятен</h2>
  <p>Профилактика — лучший способ борьбы с желтизной. Несколько простых правил:</p>
  <ul>
    <li><strong>Сушите правильно.</strong> Только при комнатной температуре, вдали от батарей и солнца.</li>
    <li><strong>Чистите регулярно.</strong> Жёлтые пятна легче предотвратить, чем удалить. После каждой носки протирайте обувь влажной салфеткой.</li>
    <li><strong>Используйте защиту.</strong> Водоотталкивающий спрей защищает не только от воды, но и от реагентов, которые вызывают желтизну.</li>
    <li><strong>Храните в бумаге.</strong> Бумажные коробки или тканевые мешки — никаких полиэтиленовых пакетов.</li>
    <li><strong>Не носите в слякоть.</strong> Белая обувь — для сухой погоды. Реагенты и соль — главные провокаторы желтизны.</li>
  </ul>
</section>

