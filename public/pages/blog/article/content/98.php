<section id="myth1">
  <h2>Миф 1: Gore-Tex не промокает никогда</h2>
  <p><strong>Правда:</strong> Gore-Tex водонепроницаем, но при определённых условиях обувь может промокнуть изнутри или снаружи.</p>
  <ul>
    <li><strong>Пот.</strong> Если ноги сильно потеют, влага может не успевать выводиться через мембрану. Обувь будет влажной внутри, хотя снаружи сухая. Это не промокание, а конденсация.</li>
    <li><strong>Загрязнённая мембрана.</strong> Грязь и пыль забивают микропоры мембраны. Сначала страдает воздухопроницаемость, а затем — водонепроницаемость. Грязный Gore-Tex промокает быстрее.</li>
    <li><strong>Повреждение DWR-покрытия.</strong> Внешний слой обуви имеет водоотталкивающую пропитку (DWR). Когда она стирается, ткань намокает, и вода перестаёт скатываться. Обувь тяжелеет, хотя мембрана всё ещё не пропускает воду внутрь.</li>
    <li><strong>Износ.</strong> Со временем мембрана теряет свойства. При активном использовании Gore-Tex сохраняет водонепроницаемость 2–5 лет.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Вывод</h4>
    <p>Gore-Tex не промокает в нормальных условиях, но требует ухода: чистки и восстановления DWR-слоя. Без ухода водонепроницаемость снижается.</p>
  </div>
</section>

<section id="myth2">
  <h2>Миф 2: Gore-Tex не дышит</h2>
  <p><strong>Правда:</strong> Gore-Tex дышит, но его воздухопроницаемость ниже, чем у обычной ткани.</p>
  <p>Многие жалуются, что в Gore-Tex обуви ноги потеют. Но проблема чаще в неправильном подборе:</p>
  <ul>
    <li><strong>Сезонность.</strong> Gore-Tex рассчитан на холодную и умеренную погоду. В жару (+25 °C и выше) в Gore-Tex кроссовках ногам будет жарко — мембрана не справляется с интенсивным потоотделением.</li>
    <li><strong>Уровень активности.</strong> Gore-Tex работает эффективнее при движении. В состоянии покоя (стояние в очереди) пар выводится медленнее.</li>
    <li><strong>Толщина утеплителя.</strong> Зимние Gore-Tex ботинки с утеплителем менее «дышащие», чем лёгкие кроссовки с мембраной без утепления.</li>
    <li><strong>Носки.</strong> В Gore-Tex обуви нужно носить дышащие носки из мериноса или синтетики. Хлопковые носки намокают от пота и создают парниковый эффект.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Сравнение с другими мембранами</h4>
    <p>Gore-Tex — золотой стандарт по соотношению водонепроницаемости и воздухопроницаемости. Дешёвые аналоги (например, мембраны за 1000–2000 ₽) либо хуже держат воду, либо совсем не дышат. Non-мерки — это не Gore-Tex.</p>
  </div>
</section>

<section id="myth3">
  <h2>Миф 3: Gore-Tex вечен</h2>
  <p><strong>Правда:</strong> Gore-Tex имеет ограниченный срок службы.</p>
  <p>Даже при идеальном уходе мембрана теряет свои свойства со временем. Основные причины:</p>
  <ul>
    <li><strong>Механический износ.</strong> Постоянные сгибы, давление, трение разрушают мембрану. Чаще всего Gore-Tex выходит из строя в области сгиба стопы.</li>
    <li><strong>Загрязнение.</strong> Микрочастицы грязи и кожного жира забивают поры мембраны. Воздухопроницаемость падает на 30–50% при сильном загрязнении.</li>
    <li><strong>Химическое воздействие.</strong> Моющие средства, стиральные порошки и кондиционеры разрушают мембрану. Gore-Tex можно стирать только специальными средствами.</li>
    <li><strong>Износ DWR.</strong> Внешнее водоотталкивающее покрытие стирается через 6–12 месяцев активного использования. Его нужно восстанавливать.</li>
  </ul>
  <p>При бережном использовании Gore-Tex сохраняет основные свойства 3–5 лет. После этого водонепроницаемость и «дышание» постепенно снижаются.</p>
</section>

