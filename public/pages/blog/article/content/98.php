<section id="care">
  <h2>Как ухаживать за Gore-Tex</h2>
  <p>Чтобы мембрана служила долго, соблюдайте несколько правил:</p>
  <ol>
    <li><strong>Чистите после каждой носки.</strong> Удаляйте грязь мягкой щёткой. Грязь — враг мембраны номер один.</li>
    <li><strong>Стирайте только специальными средствами.</strong> Обычный стиральный порошок забивает поры. Используйте шампуни для мембранной одежды и обуви (Nikwax, Grangers).</li>
    <li><strong>Не используйте кондиционеры.</strong> Они разрушают водоотталкивающее покрытие.</li>
    <li><strong>Восстанавливайте DWR-покрытие.</strong> Раз в 3–6 месяцев обрабатывайте обувь водоотталкивающим спреем для мембран.</li>
    <li><strong>Сушите вдали от батарей.</strong> Высокая температура разрушает мембрану. Сушите при комнатной температуре, набив бумагой.</li>
