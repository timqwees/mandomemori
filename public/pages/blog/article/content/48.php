  <ul>
    <li><strong>Любимая пара.</strong> Обувь, в которой вы проходили важные события. Эмоциональная ценность выше рыночной.</li>
    <li><strong>Лимитированная коллекция.</strong> Кроссовки, которые больше не выпускаются. Найти такую же пару невозможно.</li>
