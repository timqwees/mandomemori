<section id="calculation">
  <h2>Как посчитать экономическую выгоду</h2>
  <p>Используйте простую формулу: <strong>Коэффициент выгоды = Стоимость ремонта / (Цена новой пары × Остаточный ресурс)</strong></p>
  <p>Где остаточный ресурс — это доля потенциального срока службы, которую вы получите после ремонта:</p>
  <ul>
    <li>Мелкий ремонт (набойки, швы, склеивание): +30–50% к сроку.</li>
    <li>Средний ремонт (профилактика, замена молнии): +40–60% к сроку.</li>
    <li>Капитальный ремонт (замена подошвы, реставрация): +50–80% к сроку.</li>
  </ul>
  <p><strong>Пример:</strong> Кроссовки за 10000 ₽. Замена подошвы — 3000 ₽. Остаточный ресурс — 60%. Получаем: 3000 / (10000 × 0,6) = 0,5. То есть каждый рубль ремонта даёт 2 рубля «новой обуви». Ремонт выгоден.</p>
  <p><strong>Другой пример:</strong> Кроссовки за 4000 ₽. Полная реставрация — 4000 ₽. Коэффициент: 4000 / (4000 × 0,7) = 1,42. Невыгодно — проще купить новые.</p>
  <div data-type="box" data-color="green">
    <h4>Эмпирическое правило</h4>
    <p>Если ремонт стоит менее 30% от цены новой пары — ремонтируйте. Если 30–50% — считайте по формуле. Если более 50% — почти всегда выгоднее купить новые.</p>
  </div>
</section>

