<section id="pigskin">
  <h2>Замша Pigskin — главный материал New Balance</h2>
  <p>Pigskin (свиная замша) — визитная карточка New Balance. Это плотная, бархатистая замша с характерной текстурой. Она прочнее обычной замши, но требует регулярного ухода.</p>
  <h3>Особенности Pigskin</h3>
  <ul>
    <li>Более плотная и толстая, чем обычная замша</li>
    <li>Хорошо держит форму, не разнашивается безнадёжно</li>
    <li>Менее восприимчива к воде, чем телячья замша</li>
    <li>Отлично принимает тонировку и краску</li>
  </ul>
  <h3>Чистка Pigskin</h3>
  <ul>
    <li><strong>После каждой носки</strong> — резиновая щётка для замши. Пройдитесь по всей поверхности, чтобы поднять ворс и удалить пыль.</li>
    <li><strong>Пятна</strong> — ластик для замши (NB продаёт собственный, но подойдёт любой). Для стойких пятен — специальный очиститель для замши.</li>
    <li><strong>Мытьё</strong> — только в крайнем случае. Пигмент Pigskin может вымываться. Если намочили — промокните полотенцем и сушите при комнатной температуре.</li>
    <li><strong>Восстановление цвета</strong> — раз в 3–4 месяца используйте аэрозольную краску для замши в цвет. Pigskin отлично принимает краску — цвет восстанавливается до 100%.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Pigskin — натуральный материал, который «дышит». Не используйте силиконовые пропитки — они забивают поры и нарушают воздухообмен. Только пропитки на водной основе для замши.</p>
  </div>
</section>

<section id="mesh">
  <h2>Уход за сеткой Hypoknit</h2>
  <p>Hypoknit — технология бесшовного трикотажа New Balance, которая используется на современных моделях (990v6, Fresh Foam, 1080). Это плотная эластичная сетка с усиленными зонами.</p>
  <ul>
    <li><strong>Чистка</strong> — пенный очиститель + мягкая щётка. Не трите сильно — Hypoknit эластичный, растягивается при грубом воздействии.</li>
    <li><strong>Смывание</strong> — влажной микрофиброй. Не замачивайте — вода удаляет DWR-пропитку.</li>
    <li><strong>Водоотталкивающая защита</strong> — обязательна. Используйте спрей для высокотехнологичных тканей. Восстанавливает DWR и защищает от грязи.</li>
    <li><strong>Сушка</strong> — с распорками, при комнатной температуре. Hypoknit сохнет быстро (4–6 часов).</li>
  </ul>
</section>

<section id="990">
  <h2>Чистка New Balance 990 серии</h2>
  <p>990, 990v5, 990v6 — культовые модели New Balance. В 2026 году 990v6 — абсолютный хит продаж. Состоят из комбинации Pigskin, Hypoknit и полиуретановых вставок.</p>
  <h3>Пошаговая чистка 990v6</h3>
  <ol>
    <li><strong>Шнурки и стельки</strong> — извлеките. Шнурки постирайте отдельно. Стельки протрите влажной тканью с антибактериальным средством.</li>
    <li><strong>Сухая чистка Pigskin</strong> — резиновая щётка по всей поверхности из замши.</li>
    <li><strong>Чистка Hypoknit</strong> — пенный очиститель на мягкой губке, протрите сетку. Не затрагивайте замшу.</li>
    <li><strong>Полиуретановые вставки</strong> — протрите влажной тканью. Они не впитывают грязь, но пыль на них заметна.</li>
    <li><strong>Подошва</strong> — щётка средней жёсткости с мыльной водой. У 990v6 подошва с протектором — чистите все канавки.</li>
    <li><strong>Сушка</strong> — 12–24 часа при комнатной температуре.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Совет для владельцев 990</h4>
    <p>Серый цвет 990-х — самый популярный, но потёртости на Pigskin заметны. Используйте спрей-освежитель цвета для замши раз в месяц. Это поддерживает насыщенность серого.</p>
  </div>
</section>

<section id="574">
  <h2>Чистка New Balance 574</h2>
  <p>574 — самая доступная и популярная модель NB. Состоит из Pigskin (носок и задник), замши и текстильной вставки в середине.</p>
  <p><strong>Чистка:</strong> Как и 990-е, 574 требует раздельного подхода: замша — сухая чистка, текстиль — пена. Главная проблема 574 — белая подошва, которая желтеет быстро. Для отбеливания используйте соду с уксусом или ретробрайт.</p>
  <p><strong>Замена шнурков:</strong> У 574 толстые шнурки — их сложно качественно почистить. Проще купить новые (оригинал или аналог).</p>
</section>

<section id="whites">
  <h2>Уход за белыми New Balance</h2>
  <p>Белые New Balance (особенно 574 и 2002R в белых расцветках) требуют более тщательного ухода:</p>
  <ul>
    <li><strong>Белая замша Pigskin.</strong> Чистите белой резиновой щёткой (чтобы не переносить цвет от тёмной щётки). Используйте белую краску-аэрозоль для замши при потускнении.</li>
    <li><strong>Белая подошва.</strong> Чистите после каждой носки. Раз в месяц — отбеливание перекисью или содой. Сильное пожелтение — к профессионалам MANDO MEMORI.</li>
    <li><strong>Белая сетка.</strong> Только пенная чистка. Не используйте хлор — разрушает волокна.</li>
  </ul>
</section>

<section id="miu">
  <h2>Уход за моделями Made in USA / Made in UK</h2>
  <p>Премиальные модели New Balance (990-е, 991, 992) производятся в США и Великобритании из материалов высшего качества. Они стоят дорого и требуют соответствующего ухода.</p>
  <ul>
    <li><strong>Только ручная чистка.</strong> Премиальные NB не переживают машинную стирку — термоклеевая конструкция разрушается.</li>
    <li><strong>Оригинальные средства.</strong> New Balance выпускает собственную линию ухода. Используйте NB Care Kit — он разработан специально для материалов NB.</li>
