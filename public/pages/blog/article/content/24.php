<section id="first">
  <h2>Первые шаги сразу после похода</h2>
  <p>Лучшее время для чистки треккинговых ботинок — сразу после возвращения, пока грязь не высохла и не въелась. Разработайте привычку уделять обуви 10–15 минут после каждого похода:</p>
  <ul>
    <li><strong>Удалите стельки и шнурки.</strong> Они будут чиститься отдельно. Заодно проверьте, нет ли внутри песка и камней.</li>
    <li><strong>Смойте крупную грязь.</strong> Используйте прохладную воду и мягкую щётку. Не применяйте сильный напор воды — он может протолкнуть грязь внутрь ботинка.</li>
    <li><strong>Очистите протектор.</strong> Крючком или тупой палочкой удалите камни из подошвы. Уделите внимание центральным канавкам — именно они отвечают за сцепление.</li>
    <li><strong>Промокните влагу.</strong> Сухой тряпкой уберите лишнюю воду с поверхности и изнутри.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Почему это важно</h4>
    <p>Грязь содержит кислотные соединения из почвы, которые разъедают пропитку и мембрану. Песок действует как абразив, стирая верхний слой материала. Камни в протекторе давят на резину и могут пробить подошву насквозь.</p>
  </div>
</section>

<section id="clean">
  <h2>Глубокая чистка ботинок</h2>
  <p>После удаления крупной грязи переходите к глубокой чистке. Даже если ботинки выглядят чистыми — в порах кожи и ткани остаётся пыль и микрочастицы.</p>
  <h3>Для кожаных ботинок</h3>
  <ol>
    <li>Используйте мягкое мыло или специальный очиститель для кожи.</li>
    <li>Нанесите на влажную губку, вспеньте и протрите всю поверхность.</li>
    <li>Не используйте жёсткие щётки — они царапают кожу.</li>
    <li>Тщательно смойте остатки мыла.</li>
  </ol>
  <h3>Для текстильных ботинок с мембраной</h3>
  <ol>
    <li>Используйте специализированное средство для мембранных тканей.</li>
    <li>Нанесите пену, оставьте на 5 минут для воздействия.</li>
    <li>Потрите мягкой щёткой, смойте прохладной водой.</li>
  </ol>
  <h3>Для комбинированных материалов</h3>
  <p>Сначала обработайте текстильные участки, затем кожаные. Используйте разные щётки и губки для разных материалов.</p>
  <div data-type="box" data-color="red">
    <h4>Чего нельзя делать</h4>
    <p>Не используйте агрессивные моющие средства, отбеливатели и растворители. Они разрушают мембрану и сушат кожу. Также не мойте треккинговые ботинки в посудомоечной машине — температура и моющие средства убивают обувь.</p>
  </div>
</section>

<section id="membrane">
  <h2>Уход за мембраной (Gore-Tex, Event)</h2>
  <p>Большинство треккинговых ботинок оснащены мембраной. Мембрана — это «сердце» походной обуви, и от её состояния зависит, будут ли ваши ноги сухими.</p>
  <h3>Признаки износа мембраны</h3>
  <ul>
    <li>Ботинки начали промокать во время дождя</li>
    <li>Ноги быстро становятся влажными от пота</li>
    <li>Внутренняя подкладка отслаивается</li>
  </ul>
  <h3>Что делать</h3>
  <p>Восстановите DWR-покрытие с помощью специализированной пропитки. Если ботинки всё равно промокают — возможно, мембрана повреждена физически. В этом случае требуется профессиональный ремонт.</p>
  <p>Для очистки мембраны используйте только средства, маркированные как безопасные для Gore-Tex. Они не содержат масел, мыла и кондиционеров, которые забивают микропоры мембраны.</p>
</section>

<section id="dry">
  <h2>Сушка: как и сколько</h2>
  <p>Сушка треккинговых ботинок — самый ответственный этап. Неправильная сушка убивает обувь быстрее, чем самый грязный поход.</p>
  <ul>
    <li><strong>Выньте стельки</strong> — они сохнут 2–3 часа, ботинки без них — 12–24 часа.</li>
    <li><strong>Набейте газетой</strong> — меняйте каждые 4–6 часов. Бумага отлично впитывает влагу изнутри.</li>
    <li><strong>Поставьте в проветриваемое место</strong> — не на солнце, не у батареи, не в закрытый шкаф.</li>
    <li><strong>Не используйте фен</strong> — горячий воздух сушит кожу и клей. Мембрана от перегрева спекается.</li>
    <li><strong>Электрические сушилки</strong> — можно, но с температурой не выше 30 °C. Идеально — сушилки с вентилятором без подогрева.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Совет бывалых туристов</h4>
    <p>Если нужно высушить ботинки быстро — используйте силикагель (кошачий наполнитель). Насыпьте гранулы в носок, завяжите и поместите внутрь ботинка на ночь. Силикагель впитывает влагу в 3–4 раза эффективнее газеты.</p>
  </div>
</section>

<section id="impregnation">
  <h2>Пропитка и восстановление защиты</h2>
  <p>После чистки и сушки треккинговые ботинки нужно обработать защитным составом. Это восстанавливает водоотталкивающие свойства и продлевает срок службы.</p>
  <h3>Выбор пропитки</h3>
  <ul>
    <li><strong>Для кожи.</strong> Воски и масла (Saphir, Grangers). Глубоко питают и защищают от воды.</li>
    <li><strong>Для текстиля и мембраны.</strong> Фторполимерные спреи (Nikwaz TX.Direct, Grangers Repel Plus). Не забивают поры.</li>
    <li><strong>Для замши/нубука.</strong> Специальные спреи, сохраняющие ворс.</li>
  </ul>
  <h3>Как наносить</h3>
  <ol>
    <li>Пропитка наносится на чистую влажную поверхность — так она глубже проникает.</li>
    <li>Распылите равномерно с расстояния 20 см.</li>
    <li>Оставьте на 5–10 минут, удалите излишки.</li>
    <li>Активируйте теплом (фен, 20 см, средняя температура, 3–5 минут).</li>
    <li>Оставьте сушиться на 12–24 часа.</li>
  </ol>
  <div data-type="box" data-color="dark">
    <h4>Как часто пропитывать</h4>
    <p>Перед каждым походом проверяйте водоотталкивающие свойства: капните воду на ботинок. Если вода собирается в капли и скатывается — защита есть. Если впитывается — пора обновлять пропитку. В среднем — каждые 3–4 выхода.</p>
  </div>
</section>

<section id="storage">
  <h2>Хранение до следующего похода</h2>
  <p>Правильное хранение между походами — залог того, что ботинки не потеряют форму и свойства:</p>
  <ul>
    <li><strong>Чистота.</strong> Храните только чистую и полностью сухую обувь.</li>
    <li><strong>Форма.</strong> Набейте ботинки бумагой или используйте колодки. Они не дадут коже ссохнуться.</li>
    <li><strong>Место.</strong> Тёмное сухое место с температурой 15–20 °C. Не храните на балконе, в гараже или подвале.</li>
    <li><strong>Вентиляция.</strong> Храните в тканевых мешках или бумажных коробках. Не используйте полиэтиленовые пакеты.</li>
  </ul>
</section>

