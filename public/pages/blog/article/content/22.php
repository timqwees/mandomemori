<section id="mistakes">
  <h2>Типичные ошибки при чистке велюра</h2>
  <p>Мы собрали самые частые ошибки, которые совершают владельцы велюровой обуви:</p>
  <ul>
    <li><strong>Чистка щёткой для замши.</strong> Щётки для замши слишком грубы для нежного велюра. Используйте только резиновые или латунные щётки для велюра.</li>
    <li><strong>Замачивание в воде.</strong> Вода — главный враг велюра. От воды он теряет ворс и становится гладким.</li>
    <li><strong>Сушка на батарее.</strong> Высокая температура делает велюр жёстким и ломким.</li>
    <li><strong>Использование крема для кожи.</strong> Крем забивает поры велюра, и он становится липким и блестящим.</li>
    <li><strong>Отбеливание хлоркой.</strong> Хлорный отбеливатель разрушает велюр — после него материал расползается.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Запомните</h4>
    <p>Велюр — материал, который требует максимально деликатного подхода. Если сомневаетесь в чистке — не экспериментируйте. Доверьте велюровую обувь профессионалам MANDO MEMORI.</p>
