<section id="what">
  <h2>Велюр vs замша vs нубук</h2>
  <p>Многие путают велюр с замшей и нубуком, но это разные материалы. Понимание разницы — ключ к правильному уходу:</p>
  <ul>
    <li><strong>Велюр.</strong> Кожа с коротким, густым и низким ворсом («под бархат»). Производится из более мягких видов кожи. Ворс направлен в одну сторону и создаёт эффект переливания.</li>
    <li><strong>Замша.</strong> Кожа с более длинным и рыхлым ворсом. Делается из бахтармы (внутреннего слоя). Более пористая и водопроницаемая.</li>
    <li><strong>Нубук.</strong> Шлифованная лицевая кожа с очень коротким ворсом. Более плотная и износостойкая, чем велюр.</li>
  </ul>
  <p>Велюр — самый деликатный из трёх материалов. Его ворс легко приминается, и на нём остаются следы от воды и касаний. Требует максимально бережного ухода.</p>
  <div data-type="box" data-color="gray">
    <h4>Как отличить велюр</h4>
    <p>Проведите пальцем по поверхности. На велюре след от пальца будет заметен как более тёмная полоса, которая исчезает через несколько секунд. На замше след остаётся дольше. На нубуке следа почти не видно.</p>
  </div>
</section>

<section id="daily">
  <h2>Ежедневный уход за велюром</h2>
  <p>Секрет долгой жизни велюра — в ежедневной профилактике. Всего 2–3 минуты после каждой носки сохранят обувь в идеальном состоянии:</p>
  <ul>
    <li><strong>Щётка для велюра.</strong> После каждой носки проходите по поверхности резиновой щёткой для велюра. Она поднимает ворс и удаляет пыль. Движения — только в одном направлении.</li>
    <li><strong>Ластик.</strong> Специальный ластик для велюра (или обычный канцелярский) быстро удаляет свежие загрязнения и потёртости.</li>
    <li><strong>Расчёсывание ворса.</strong> Если ворс примялся — пройдитесь щёткой с металлической щетиной (латунной). Будьте аккуратны: металл может поцарапать нежную поверхность.</li>
    <li><strong>Проветривание.</strong> Не убирайте велюровую обувь сразу после носки. Дайте ей проветриться 30–60 минут, а затем почистите.</li>
  </ul>
</section>

<section id="clean">
  <h2>Чистка велюра: сухой и влажный метод</h2>
  <h3>Сухая чистка (основной метод)</h3>
  <p>Для велюра сухая чистка — приоритет. Используйте резиновую щётку и ластик. Если на поверхности есть засаленные участки, пройдитесь по ним ластиком — он стирает верхний слой загрязнений.</p>
  <h3>Влажная чистка (только при сильных загрязнениях)</h3>
  <p>Влажную чистку велюра проводите только в крайнем случае. Велюр от воды теряет бархатистость.</p>
  <ol>
    <li>Используйте специальную пену для велюра или очень слабый мыльный раствор.</li>
    <li>Нанесите только пену — без воды. Работайте мягкой губкой.</li>
    <li>Промокните пену сухой микрофиброй после каждой секции.</li>
    <li>Высушите при комнатной температуре (12–24 часа).</li>
    <li>После высыхания поднимите ворс щёткой.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>После влажной чистки велюр может стать жёстким и потерять бархатистость. Если это произошло — обработайте его отпаривателем: пар размягчит волокна. Держите отпариватель на расстоянии 15–20 см от поверхности, не ближе.</p>
  </div>
</section>

<section id="stains">
  <h2>Удаление пятен с велюра</h2>
  <p>Пятна на велюре — самая частая причина обращения в мастерские. Вот как действовать в зависимости от типа пятна:</p>
  <h3>Свежая грязь</h3>
  <p>Дайте грязи высохнуть, затем удалите щёткой. Не трите влажную грязь — вы втираете её глубже в ворс.</p>
  <h3>Жир и масло</h3>
  <p>Посыпьте тальком или крахмалом, оставьте на ночь. Утром удалите щёткой. При необходимости повторите.</p>
  <h3>Соль и реагенты</h3>
  <p>Белые разводы от соли удаляйте ватным диском, смоченным в слабом растворе уксуса (1:10 с водой). После высыхания поднимите ворс.</p>
  <h3>Вода</h3>
  <p>Разводы от воды на велюре — самые распространённые. Удалите их сухой чистой щёткой, интенсивно расчёсывая в разных направлениях. Если не помогло — используйте отпариватель.</p>
  <div data-type="box" data-color="green">
    <h4>Лайфхак</h4>
    <p>Если на велюре появились участки с разным оттенком (примятости), пройдитесь по ним сухой металлической щёткой для велюра. Она поднимет ворс, и цвет выровняется. Не давите — работайте лёгкими движениями.</p>
  </div>
</section>

<section id="restore">
  <h2>Восстановление ворса и цвета</h2>
  <p>Со временем велюр выцветает, затирается и теряет бархатистость. Восстановить его можно несколькими способами:</p>
  <h3>Поднятие ворса</h3>
  <p>Используйте щётку с металлической щетиной. Движения — короткие и лёгкие. Если ворс не поднимается — обработайте паром. Пар размягчает затвердевшие волокна и возвращает велюру первоначальную мягкость.</p>
  <h3>Краска для велюра</h3>
  <p>Для восстановления цвета используйте аэрозольную краску для велюра. Технология нанесения:</p>
  <ol>
    <li>Тщательно вычистите и высушите обувь.</li>
    <li>Заклейте подошву и элементы из других материалов.</li>
    <li>Нанесите краску с расстояния 25 см в 2–3 тонких слоя.</li>
    <li>Дайте каждому слою высохнуть 20 минут.</li>
    <li>После полного высыхания (2–3 часа) поднимите ворс щёткой.</li>
  </ol>
  <div data-type="box" data-color="dark">
    <h4>Совет эксперта</h4>
    <p>Краска для велюра сильно меняет оттенок в зависимости от количества слоёв. Если красите впервые — сделайте пробу на незаметном участке (внутренняя сторона язычка). В MANDO MEMORI для точного подбора цвета используем компьютерный спектрофотометр.</p>
  </div>
