<section id="daily">
  <h2>Ежедневные ритуалы</h2>
  <p>То, что вы делаете с обувью каждый день, важнее генеральной чистки раз в месяц. Пять минут в день продлевают жизнь обуви на годы:</p>
  <ul>
    <li><strong>Снимайте обувь аккуратно.</strong> Не снимайте кроссовки, наступая на пятку другой ногой — вы разбиваете задник. Используйте ложку для обуви.</li>
    <li><strong>Удаляйте грязь сразу.</strong> После возвращения домой протрите обувь влажной салфеткой или щёткой. Высохшая грязь — это абразив, который царапает материал.</li>
    <li><strong>Просушивайте.</strong> Если обувь была влажной, набейте её бумагой и оставьте на ночь. Сухая обувь дольше носится.</li>
    <li><strong>Проветривайте.</strong> Не убирайте обувь в шкаф сразу — дайте ей постоять на проветриваемой полке 1–2 часа.</li>
    <li><strong>Используйте ложку для обуви.</strong> Это сохраняет форму задника и предотвращает заломы.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Почему это работает</h4>
    <p>Основная причина износа обуви — не погода, а микрочастицы грязи и пыли, которые действуют как наждачная бумага. Ежедневное удаление этих частиц — лучшая профилактика.</p>
  </div>
</section>

<section id="weekly">
  <h2>Еженедельный уход</h2>
  <p>Раз в неделю уделите обуви 15–20 минут для более глубокого ухода:</p>
  <ol>
    <li><strong>Инспекция.</strong> Осмотрите обувь на предмет повреждений: трещины, отходящая подошва, износ швов.</li>
    <li><strong>Чистка.</strong> Для кожаной обуви — пена для чистки + питательный крем. Для замши — резиновая щётка + спрей-пропитка.</li>
    <li><strong>Стельки.</strong> Достаньте стельки и проветрите их. Если они износились — замените новыми.</li>
    <li><strong>Шнурки.</strong> Постирайте или замените шнурки. Чистые шнурки преображают обувь визуально на 50%.</li>
    <li><strong>Пропитка.</strong> Обновите водоотталкивающий слой. Для активной носки — раз в 1–2 недели.</li>
  </ol>
</section>

<section id="protect">
  <h2>Защита на все сезоны</h2>
  <p>Универсальная система защиты работает круглый год. Единственное, что меняется — тип пропитки:</p>
  <div data-type="compare">
    <article data-variant="good">
      <h4>Базовая защита</h4>
