        <li>Водоотталкивающий спрей — для всех материалов</li>
        <li>Питательный крем — для гладкой кожи (зимой жирнее, летом легче)</li>
        <li>Спрей для замши — с УФ-фильтром летом, с защитой от соли зимой</li>
        <li>Дезодорант для обуви — после каждой носки</li>
        <li>Сменные стельки — каждые 2–3 месяца</li>
