<section id="store">
  <h2>Хранение между носками</h2>
  <p>Даже если вы носите обувь каждый день, между носками она должна храниться правильно:</p>
  <ul>
    <li><strong>Колодки или бумага.</strong> Вставляйте в обувь колодки или плотные бумажные комки сразу после снятия. Это предотвращает заломы и сохраняет форму.</li>
    <li><strong>Проветриваемое место.</strong> Храните обувь на открытых стеллажах или полках с решёткой. Никаких закрытых пластиковых коробок для ежедневной обуви.</li>
    <li><strong>Впитыватели влаги.</strong> Силикагель внутри обуви между носками предотвращает появление запаха.</li>
    <li><strong>Ротация.</strong> Не носите одну пару два дня подряд — давайте ей 24 часа на восстановление формы и просушку.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Держите на обувной полке набор для быстрого ухода: щётка, салфетка из микрофибры, мини-спрей пропитки и ложка для обуви. Тогда уход займёт 1 минуту.</p>
  </div>
</section>

<section id="rotation">
  <h2>Правило ротации</h2>
  <p>Самый эффективный способ продлить жизнь обуви — носить разные пары по очереди:</p>
  <ul>
    <li><strong>Минимум 2–3 пары в активном гардеробе.</strong> Чередование даёт обуви время «отдохнуть» между носками. За 24 часа материал восстанавливает форму, а внутренняя часть полностью высыхает.</li>
    <li><strong>Сезонная ротация.</strong> Меняйте обувь по сезонам: демисезонная, зимняя, летняя. Ношение не по сезону ускоряет износ.</li>
    <li><strong>Обувь для разных задач.</strong> Не ходите в одной паре и на работу, и на прогулку, и в спортзал. Для каждой активности — своя обувь.</li>
  </ul>
  <p>Соблюдая ротацию, вы продлеваете срок службы каждой пары в 2–3 раза.</p>
</section>

