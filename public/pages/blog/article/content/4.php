<section id="alternative">
  <h2>Что делать, если пятна не выводятся</h2>
  <p>Иногда, несмотря на все старания, пятно остаётся. Бежевый цвет сложен в колеровке, и самостоятельное подкрашивание часто приводит к появлению ещё более заметных дефектов.</p>
  <h3>Варианты решения</h3>
  <ul>
    <li><strong>Краска-спрей.</strong> Для замши и нубука существуют аэрозольные краски в баллончиках. Выбирайте оттенок на 1–2 тона светлее оригинала — тёмный цвет будет заметен. Наносите с расстояния 30 см в 2–3 слоя.</li>
    <li><strong>Крем-краска для кожи.</strong> Для гладкой кожи используйте крем с пигментом. Нанесите тонким слоем, дайте впитаться 15 минут, отполируйте мягкой тканью.</li>
    <li><strong>Профессиональное подкрашивание.</strong> Самый безопасный вариант. В MANDO MEMORI мы подбираем оттенок под конкретную пару с помощью спектрофотометра — это исключает ошибку в цвете.</li>
    <li><strong>Полная перекраска.</strong> Если пятно занимает больше 30% площади кроссовка и не выводится, имеет смысл полностью перекрасить обувь. Актуальный тренд 2026 — перекраска белых кроссовок в бежевый и оливковый.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Совет профессионала</h4>
    <p>Не пытайтесь отбелить бежевую замшу или кожу — это разрушит пигмент и приведёт к необратимым повреждениям. Бежевый — сложный составной цвет (обычно смесь белого, жёлтого и коричневого пигментов), изменить его в домашних условиях практически невозможно.</p>
