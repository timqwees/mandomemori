<section id="trend">
  <h2>Почему бежевый пришёл на смену белому</h2>
  <p>Переход от белого к бежевому — не каприз дизайнеров, а осознанный тренд, который сформировался под влиянием нескольких факторов:</p>
  <ul>
    <li><strong>Эстетика quiet luxury.</strong> Эпоха логотипов и кричащих цветов сменилась тихой роскошью. Приглушённые оттенки бежевого, кремового, карамельного и какао выглядят дорого без демонстративности. Именно этот тренд продвигают Loro Piana, Brunello Cucinelli и The Row.</li>
    <li><strong>Практичность в городе.</strong> Бежевый менее маркий, чем белый, и не требует ежедневного ухода. На бежевой замше и коже пыль и лёгкие загрязнения почти незаметны — достаточно сухой щётки раз в 2–3 дня.</li>
    <li><strong>Сезонность.</strong> Белый цвет ассоциируется с весной и летом, тогда как бежевый — всесезонная база. Кроссовки бежевого оттенка носят круглый год, что повышает их «cost per wear».</li>
    <li><strong>Сникер-культура</strong>. Коллекционеры кроссовок всё чаще выбирают «off-white» и натуральные оттенки. Лимитированные расцветки бежевого (Shattered Backboard, Sail, Bone) держат цену на вторичном рынке лучше классического белого.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Цифры 2026</h4>
    <p>По данным Lyst Index, поисковые запросы «beige sneakers» выросли на 240% за последний год. Спрос на бежевые кроссовки превышает предложение в 2,5 раза — это самый быстрорастущий сегмент рынка обуви.</p>
