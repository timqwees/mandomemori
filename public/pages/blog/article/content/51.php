<section id="why">
  <h2>Почему обувь теряет цвет</h2>
  <p>Прежде чем выбирать метод восстановления, важно понять причину потери цвета. От этого зависит выбор средства и технологии:</p>
  <ul>
    <li><strong>Ультрафиолет.</strong> Солнечный свет разрушает пигменты в красителях. Особенно быстро выцветает красная, синяя и зелёная обувь. Чёрная, белая и бежевая — медленнее.</li>
    <li><strong>Реагенты и соль.</strong> Зимние реагенты агрессивно воздействуют на красители, буквально «выъедая» цвет из верхнего слоя материала.</li>
    <li><strong>Механическое истирание.</strong> Потёртости на сгибах, пятке и носке — результат трения. Краска стирается физически, и на этих участках появляются светлые пятна.</li>
    <li><strong>Неправильная чистка.</strong> Агрессивные моющие средства, растворители и щётки с жёсткой щетиной смывают краситель с поверхности.</li>
    <li><strong>Естественное старение.</strong> Со временем (2–5 лет) любой краситель теряет яркость. Это естественный процесс, замедлить его можно правильным уходом.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Тест на стойкость</h4>
    <p>Проведите влажной белой тканью по поверхности обуви. Если ткань окрасилась — краситель активный и его можно восстановить пропиткой. Если ткань не окрасилась — краситель глубоко въелся, и требуется покраска.</p>
  </div>
</section>

<section id="creams">
  <h2>Кремы-красители для обуви</h2>
  <p>Крем-краситель — самый щадящий способ восстановления цвета. Он не меняет структуру материала, а только освежает оттенок и заполняет микротрещины.</p>
  <h3>Когда использовать крем-краситель</h3>
  <ul>
    <li>Общее потускнение цвета без глубоких потёртостей.</li>
    <li>Мелкие царапины и потёртости на гладкой коже.</li>
    <li>Для регулярного поддерживающего ухода (раз в 2–3 недели).</li>
    <li>Когда нужно не изменить цвет, а освежить его.</li>
  </ul>
  <h3>Как наносить крем-краситель</h3>
  <ol>
    <li>Очистите обувь от грязи и пыли. При необходимости — обезжирьте.</li>
    <li>Нанесите небольшое количество крема на мягкую ткань.</li>
    <li>Втирайте круговыми движениями, равномерно распределяя по поверхности.</li>
    <li>Дайте высохнуть 10–15 минут.</li>
    <li>Отполируйте мягкой щёткой или тканью до блеска.</li>
  </ol>
  <p>Кремы лучше всего подходят для гладкой кожи. На замше и нубуке они работают хуже — могут оставить жирный блеск.</p>
  <div data-type="box" data-color="green">
    <h4>Какой крем выбрать</h4>
    <p>Рекомендуем профессиональные кремы Saphir (Франция) и Tarrago (Испания). Они содержат натуральные воски и пигменты, которые питают кожу и восстанавливают цвет. Не используйте дешёвые кремы сомнительного производства — они могут испортить цвет и забить поры кожи.</p>
  </div>
</section>

<section id="sprays">
  <h2>Аэрозольные краски для обуви</h2>
  <p>Аэрозольные краски — более серьёзное средство для восстановления цвета. Они обеспечивают равномерное покрытие и подходят для всех типов материалов.</p>
  <h3>Когда использовать аэрозоль</h3>
  <ul>
    <li>Сильные потёртости по всей поверхности.</li>
    <li>Неравномерный цвет — участки разного оттенка.</li>
    <li>Полное изменение цвета обуви (кастомизация).</li>
    <li>Обувь из текстиля, сетки, замши — для кремов эти материалы сложны.</li>
  </ul>
  <h3>Технология покраски аэрозолем</h3>
  <ol>
    <li>Тщательно вымойте и высушите обувь. Заклейте малярным скотчем подошву и участки, которые не должны быть окрашены.</li>
    <li>Нанесите первый слой с расстояния 20–25 см. Движения — быстрые, равномерные, не задерживайтесь на одном месте.</li>
    <li>Дайте высохнуть 15–20 минут.</li>
    <li>Нанесите второй слой для равномерного покрытия.</li>
    <li>После высыхания закрепите цвет фиксатором (если предусмотрено производителем).</li>
  </ol>
  <p>Для замши и нубука после покраски необходимо восстановить ворс специальной щёткой.</p>
  <div data-type="box" data-color="red">
    <h4>Важно</h4>
    <p>Аэрозольные краски токсичны. Работайте в хорошо проветриваемом помещении или на улице. Используйте респиратор и перчатки. Не распыляйте рядом с открытым огнём.</p>
  </div>
</section>

<section id="leather">
  <h2>Реставрация цвета кожаной обуви</h2>
  <p>Кожа — самый благодарный материал для восстановления цвета. Она хорошо впитывает красители и долго держит цвет.</p>
  <h3>Пошаговая инструкция для кожи</h3>
  <ol>
    <li><strong>Подготовка.</strong> Очистите кожу от загрязнений. Для глубокой очистки используйте мыльный раствор, затем протрите спиртом для обезжиривания.</li>
    <li><strong>Восстановление.</strong> Если на коже есть царапины, заполните их жидкой кожей или восковым наполнителем. После высыхания отшлифуйте.</li>
    <li><strong>Покраска.</strong> Нанесите аэрозольную краску или крем-краситель в 2 слоя. Каждый слой должен полностью высохнуть.</li>
    <li><strong>Фиксация.</strong> Закрепите цвет специальным фиксатором. Это предотвратит выцветание и смывание.</li>
    <li><strong>Питание.</strong> После покраски нанесите питательный крем для восстановления жирового баланса кожи.</li>
  </ol>
  <div data-type="box" data-color="dark">
    <h4>Сложные цвета</h4>
    <p>Если кожа имеет сложный оттенок (металлик, анилиновый, двухцветное окрашивание) — подбор краски лучше доверить профессионалам. В MANDO MEMORI мы колеруем краску вручную, добиваясь 100% совпадения с оригинальным цветом.</p>
  </div>
</section>

<section id="suede">
  <h2>Восстановление цвета замши и нубука</h2>
  <p>Замша и нубук — пористые материалы, и восстановление цвета на них сложнее, чем на гладкой коже. Зато есть специальные средства именно для этих материалов.</p>
  <h3>Средства для замши и нубука</h3>
  <ul>
    <li><strong>Аэрозоли-освежители цвета.</strong> Восстанавливают оттенок, не меняя структуры материала. Распыляются с расстояния 20–30 см.</li>
    <li><strong>Пропитки с пигментом.</strong> Сочетают восстановление цвета и водоотталкивающую защиту. Лучший выбор для зимней обуви.</li>
    <li><strong>Пенные очистители с красителем.</strong> Мягко очищают и одновременно обновляют цвет. Подходят для регулярного ухода.</li>
  </ul>
  <h3>Особенности покраски замши</h3>
  <ol>
    <li>Используйте только специальные средства для замши — обычная краска для кожи испортит ворс.</li>
    <li>После нанесения краски поднимите ворс резиновой щёткой или ластиком для замши.</li>
    <li>Не используйте крем-красители — они забивают поры и создают жирный блеск.</li>
    <li>После покраски обработайте обувь водоотталкивающим спреем.</li>
  </ol>
  <p>Восстановление цвета замши в домашних условиях возможно, но требует аккуратности. Если не уверены — доверьте реставрацию мастеру.</p>
