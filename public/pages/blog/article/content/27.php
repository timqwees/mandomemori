<section id="urgency">
  <h2>Экстренные меры: первые 5 минут</h2>
  <p>Время — главный враг винного пятна. В первые минуты пигмент находится на поверхности, и его можно удалить почти полностью. Дальше вино проникает в структуру материала, и задача усложняется.</p>
  <h3>Алгоритм первой помощи</h3>
  <ol>
    <li><strong>Промокните, не трите.</strong> Чистой тканью или бумажной салфеткой промокните вино. Движения — только промокающие, без трения. Трение втирает вино глубже.</li>
    <li><strong>Засыпьте солью.</strong> Соль — экстренный абсорбент. Засыпьте пятно толстым слоем соли, она впитает влагу и пигмент. Оставьте на 5–10 минут.</li>
    <li><strong>Стряхните соль.</strong> Удалите соль сухой тканью или щёткой. Если пятно осталось — переходите к целенаправленным методам.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Важно</h4>
    <p>Не используйте для промокания цветные салфетки — краситель может перейти на обувь. Белая ткань или обычная бумажная салфетка — лучший выбор. Также не используйте влажные салфетки со спиртом — он закрепляет пятно.</p>
  </div>
</section>

<section id="leather">
  <h2>Удаление вина с гладкой кожи</h2>
  <p>Гладкая кожа — лучший материал для удаления винных пятен. Она не впитывает жидкость мгновенно, как замша или текстиль, поэтому у вас есть 10–15 минут.</p>
  <h3>Свежее пятно</h3>
  <ol>
    <li>Промокните излишки вина.</li>
    <li>Смешайте воду с небольшим количеством мягкого мыла.</li>
    <li>Ватным диском, смоченным в растворе, протрите пятно от краёв к центру.</li>
    <li>Промокните чистой влажной тканью, затем сухой.</li>
  </ol>
  <h3>Застарелое пятно</h3>
  <p>Если пятно высохло — используйте перекись водорода 3%. Нанесите на ватный диск, приложите к пятну на 2–3 минуты. Затем промокните влажной тканью. Для тёмной кожи будьте осторожны — перекись может осветлить участок. Используйте её только на светлой коже.</p>
  <p>После удаления вина обязательно нанесите питающий крем на кожу — перекись и мыло сушат материал.</p>
  <div data-type="box" data-color="red">
    <h4>Чего нельзя делать</h4>
    <p>Не используйте уксус или лимонный сок на гладкой коже — они разъедают защитный слой. Также не трите пятно щёткой — только ватные диски и мягкая ткань.</p>
  </div>
</section>

<section id="suede">
  <h2>Выведение вина с замши и нубука</h2>
  <p>Замша и нубук — самые сложные материалы для удаления вина. Они впитывают жидкость мгновенно, и пятно проникает глубоко в ворс.</p>
  <h3>Экстренный метод</h3>
  <ol>
    <li>Промокните вино чистой тканью.</li>
    <li>Засыпьте тальком или кукурузным крахмалом толстым слоем.</li>
    <li>Оставьте на 3–4 часа (лучше на ночь). Тальк впитает вино вместе с пигментом.</li>
    <li>Удалите тальк мягкой щёткой для замши.</li>
  </ol>
  <h3>Если пятно осталось</h3>
  <p>Используйте специальный очиститель для замши или слабый раствор уксуса (1 часть на 5 частей воды). Нанесите на ватный диск, аккуратно промокните пятно. Не лейте раствор на замшу — только на диск. После высыхания поднимите ворс щёткой.</p>
  <div data-type="box" data-color="dark">
    <h4>Совет эксперта</h4>
    <p>Если домашние методы не помогли — не пытайтесь закрасить пятно. Краска для обуви может дать другой оттенок на участке с остатками вина. Лучше обратитесь в MANDO MEMORI: мастера используют профессиональные составы, которые выводят вино из ворса, не повреждая материал.</p>
  </div>
</section>

<section id="textile">
  <h2>Чистка текстиля от вина</h2>
  <p>Текстиль — второй по сложности материал после замши. Волокна ткани впитывают вино глубоко, но у текстиля есть преимущество: его можно обрабатывать более агрессивными средствами.</p>
  <h3>Свежее пятно</h3>
  <ol>
    <li>Промокните излишки вина.</li>
    <li>Нанесите перекись водорода 3% на пятно. Она вступит в реакцию с пигментом и обесцветит его.</li>
    <li>Через 5 минут промокните влажной тканью.</li>
    <li>Повторите при необходимости.</li>
  </ol>
  <h3>Застарелое пятно</h3>
  <p>Смешайте по 1 столовой ложке перекиси водорода 3% и средства для мытья посуды. Нанесите на пятно, вотрите мягкой щёткой. Оставьте на 10 минут, затем смойте водой. Этот метод безопасен для белого и светлого текстиля. Для тёмного текстиля используйте только мыльный раствор.</p>
</section>

<section id="white">
  <h2>Особенности для белой обуви</h2>
  <p>Красное вино на белой обуви — самый драматичный вариант. Но белый цвет даёт преимущество: вы можете использовать отбеливающие средства.</p>
  <ul>
    <li><strong>Кислородный отбеливатель.</strong> Нанесите на пятно на 10–15 минут. Подходит для текстиля и гладкой кожи (с осторожностью).</li>
    <li><strong>Перекись водорода 3%.</strong> Наносите на пятно каждые 5 минут до полного обесцвечивания.</li>
    <li><strong>Сода + лимон.</strong> Смешайте до пасты, нанесите на пятно на 20 минут.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Помните</h4>
    <p>После удаления вина с белой обуви может остаться легкий желтоватый фон. Это остатки пигмента. Удалите его смесью соды и перекиси — нанесите кашицу на 15 минут, затем смойте.</p>
  </div>
</section>

