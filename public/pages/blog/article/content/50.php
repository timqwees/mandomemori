<section id="risks">
  <h2>Риски самостоятельного ремонта брендовой обуви</h2>
  <p>Многие владельцы дорогой обуви пытаются сэкономить на ремонте, делая его самостоятельно. Чем это грозит:</p>
  <ul>
    <li><strong>Потеря цвета и фактуры.</strong> Неправильно подобранный краситель может изменить оттенок. На брендовой обуви часто используются сложные цвета (металлик, перламутр, составные оттенки), которые невозможно повторить бытовыми красками.</li>
    <li><strong>Повреждение материала.</strong> Агрессивные растворители и неправильные кремы разрушают структуру дорогой кожи. Gucci и Prada используют особые виды кожи (Saffiano, Matelassé), которые требуют специальных средств.</li>
    <li><strong>Нарушение конструкции.</strong> Брендовая обувь часто имеет сложную конструкцию: карбоновые вставки, гелевые подушки, бесшовные технологии. Непрофессиональное вмешательство нарушает работу этих элементов.</li>
    <li><strong>Потеря гарантии.</strong> Если обувь на гарантии производителя — самостоятельный ремонт аннулирует её. Даже официальные бутики отказываются от обслуживания после «домашнего» ремонта.</li>
    <li><strong>Снижение стоимости.</strong> Неудачный ремонт может снизить стоимость обуви на 50–70%. Для коллекционных моделей это критично.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Главный риск</h4>
    <p>Экономия 2000–5000 ₽ на профессиональном ремонте может привести к тому, что обувь за 100 000 ₽ превратится в бесполезный предмет гардероба. Профессиональный ремонт — это страховка вашей инвестиции.</p>
