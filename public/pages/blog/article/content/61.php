<section id="minimalism">
  <h2>Минимализм и лаконичность</h2>
  <p>Тихий минимализм (quiet luxury) из мира высокой моды добрался до кроссовок. В тренде — чистые линии, отсутствие лишнего декора, натуральные оттенки и высокое качество материалов. Бренды Common Projects, Axel Arigato, Veja и новинки Loro Piana задают этот тренд.</p>
  <p>Ключевые признаки тренда: замша и гладкая кожа вместо синтетики, минимум логотипов, плоская подошва средней толщины, нейтральная цветовая гамма (бежевый, серый, пудровый, молочный). Такие кроссовки универсальны — они подходят к костюму, джинсам и даже платьям. Это инвестиция на годы, а не на один сезон.</p>
  <div data-type="box" data-color="gray">
    <h4>Почему это тренд</h4>
    <p>После нескольких лет «агрессивных» кроссовок с массивными подошвами (Triple S, Yeezy 700) потребители устали от визуального шума. Минимализм — это осознанный выбор в пользу универсальности и долговечности.</p>
  </div>
</section>

<section id="retro">
  <h2>Ретро-формы 90-х и 00-х</h2>
  <p>Ностальгия по 90-м и 00-м не отпускает моду. В 2026 году мы видим возвращение силуэтов, которые были популярны 20–30 лет назад: массивные «папины» кроссовки, баскетбольные модели, лёгкие беговые формы эпохи джоггинга.</p>
  <p>Nike Air Max Plus — снова на пике, Adidas выпускает ретро-версии Samba и Gazelle в новых цветах. New Balance 990v6 — абсолютный хит, который носят и с костюмами, и с шортами. Salomon XT-6 — ретро-футуризм из 2000-х.</p>
  <p>Главное правило: ретро не означает «старомодно». Современные ретро-кроссовки используют новые технологии амортизации и материалы, сохраняя винтажную эстетику.</p>
  <div data-type="box" data-color="green">
    <h4>Модель года</h4>
    <p>New Balance 990v6 — по опросам 2026 года, самая универсальная модель. Она сочетает ретро-дизайн с современными технологиями (подошва FuelCell, сетка из переработанного полиэстера).</p>
  </div>
</section>

<section id="gorpcore">
  <h2>Gorpcore и трейловая эстетика</h2>
  <p>Gorpcore — термин, родившийся из аббревиатуры «Good Old Raisins and Peanuts» (традиционный трейловый перекус). Это тренд на использование походной и трейловой экипировки в повседневной городской одежде.</p>
  <p>В 2026 году gorpcore не просто жив — он эволюционирует. Трекинговые кроссовки Salomon, Asics, Hoka, On Running стали мейнстримом. Их носят не только в горы, но и в офис, на прогулки и встречи. Особенность тренда — функциональность как стиль: Gore-Tex мембрана, шнуровка быстрой фиксации, агрессивный протектор — всё это стало частью городской моды.</p>
  <p>Как носить: с широкими брюками карго, флисовой кофтой, технической курткой. Главное — не переборщить, чтобы образ не стал буквально походным.</p>
</section>

<section id="seamless">
  <h2>Бесшовный верх</h2>
  <p>Технологии Flyknit (Nike) и Primeknit (Adidas) захватили рынок. В 2026 году практически у каждого крупного бренда есть линейка бесшовных кроссовок, где верх — это единая вязаная конструкция.</p>
  <p>Преимущества: лёгкость (вес кроссовка — от 180 г), дышащий верх без швов, которые натирают, идеальная посадка по ноге благодаря эластичности трикотажа. Минусы: сложность в уходе (трикотаж пачкается быстрее кожи) и меньшая износостойкость.</p>
  <p>Тренд 2026 — гибридные конструкции, где бесшовный верх комбинируется с кожаными или замшевыми вставками для прочности. Примеры: Nike Air Force 1 Knit, Adidas Ultraboost Light.</p>
  <div data-type="box" data-color="red">
    <h4>Важно об уходе</h4>
    <p>Бесшовные трикотажные кроссовки требуют деликатной стирки — только руками, без агрессивных щёток. После чистки обязательно используйте водоотталкивающую пропитку, чтобы продлить жизнь материалу.</p>
  </div>
</section>

<section id="colors">
  <h2>Цветовая палитра сезона 2026</h2>
  <p>Цвета этого года — про спокойствие и природу. Главные оттенки:</p>
  <ul>
    <li><strong>Бежевый и кремовый.</strong> Универсальная база, которая заменяет белый. Не маркий, но светлый, подходит под любую одежду.</li>
    <li><strong>Хаки и оливковый.</strong> От gorpcore наследие — землистые оттенки, которые сочетаются с джинсой и карго.</li>
    <li><strong>Бургунди и бордовый.</strong> Глубокий насыщенный цвет, который добавляет образу благородства. Хорош на замше.</li>
    <li><strong>Серый и графитовый.</strong> Тренд на «тихую роскошь» привёл серый цвет в топ. Он универсален, но смотрится дороже чёрного.</li>
    <li><strong>Металлик.</strong> Золото, серебро, хром — но не по всей поверхности, а акцентами: вставки, логотип, элементы подошвы.</li>
  </ul>
  <div data-type="box" data-color="blue">
    <h4>Совет стилиста</h4>
    <p>Если хотите попасть в тренд 2026 — выбирайте кроссовки в бежево-серой гамме. Они не выйдут из моды минимум 2–3 сезона.</p>
  </div>
</section>

<section id="eco">
  <h2>Экологичные материалы</h2>
  <p>Экологичность — не просто тренд, а требование времени. В 2026 году потребитель всё чаще смотрит на состав и происхождение материалов. Бренды отвечают:</p>
  <ul>
    <li><strong>Переработанный полиэстер.</strong> Nike использует Flyknit из переработанных пластиковых бутылок, Adidas — Primeknit из океанского пластика.</li>
    <li><strong>Растительная кожа.</strong> Mylo (из мицелия грибов) и Desserto (из кактуса) появляются в коллекциях Tommy Hilfiger и Stella McCartney.</li>
    <li><strong>Натуральный каучук.</strong> Veja, Allbirds и другие эко-бренды используют каучук из Амазонии для подошв.</li>
    <li><strong>Биоразлагаемые подошвы.</strong> Разработаны составы, которые разлагаются в компосте за 5 лет, а не за 200.</li>
  </ul>
  <p>Важный нюанс: экологичность не всегда означает долговечность. Материалы из переработки могут быть менее износостойкими, чем синтетика. За ними нужен особый уход.</p>
  <div data-type="box" data-color="dark">
    <h4>Факт 2026</h4>
    <p>По данным отчёта Fashion Revolution, 67% опрошенных в 2026 году готовы переплатить до 20% за кроссовки из экологичных материалов.</p>
  </div>
</section>

