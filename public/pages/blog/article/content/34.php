<section id="types">
  <h2>Виды царапин и способы их оценки</h2>
  <p>Прежде чем выбирать метод, оцените глубину повреждения:</p>
  <ul>
    <li><strong>Поверхностные царапины.</strong> Повреждён только верхний слой кожи (пигментация). Царапина выглядит как светлая полоса на фоне основного цвета. Такие царапины легко маскируются кремом или полировкой.</li>
    <li><strong>Царапины средней глубины.</strong> Повреждён не только пигментный слой, но и верхняя часть структуры кожи. Царапина заметна на ощупь — чувствуется неровность. Требует заполнения реставрационным составом.</li>
    <li><strong>Глубокие царапины и порезы.</strong> Кожа повреждена на всю толщину. Видны волокна кожи. Такие царапины требуют профессиональной реставрации с заполнением и перекраской.</li>
  </ul>
  <p>Простой тест: проведите ногтем по царапине. Если ноготь не цепляется — царапина поверхностная. Если цепляется и чувствуется углубление — средняя или глубокая.</p>
  <div data-type="box" data-color="gray">
    <h4>Важно понимать</h4>
    <p>Царапина на коже — не разрыв. Кожа — достаточно эластичный материал, и при правильной обработке царапина может «затянуться» почти до незаметного состояния. Но полное исчезновение возможно только для поверхностных повреждений.</p>
  </div>
</section>

<section id="light">
  <h2>Мелкие поверхностные царапины</h2>
  <p>Поверхностные царапины — самые лёгкие в устранении. Они затрагивают только пигментный слой, и достаточно «закрасить» повреждённый участок.</p>
  <h3>Метод 1: Крем для обуви в цвет</h3>
  <p>Самый простой и доступный способ. Возьмите крем для обуви подходящего цвета (тон в тон). Нанесите тонким слоем на царапину ватной палочкой. Через 10 минут отполируйте мягкой тканью. Царапина станет практически незаметной.</p>
  <h3>Метод 2: Восковой карандаш</h3>
  <p>Специальные восковые карандаши для обуви — идеальный инструмент для маскировки царапин. Достаточно провести карандашом по царапине и затереть пальцем. Воск заполняет микротрещины и выравнивает цвет.</p>
  <h3>Метод 3: Полировка</h3>
  <p>Если царапина едва заметна — попробуйте отполировать участок мягкой тканью с каплей вазелина или касторового масла. Масло заполняет микроповреждения и выравнивает отражение света.</p>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Для маскировки царапин на тёмной коже используйте крем на тон темнее основного цвета. Он лучше заполняет царапину и делает её менее заметной. Для светлой — на тон светлее или точно в цвет.</p>
  </div>
</section>

<section id="medium">
  <h2>Царапины средней глубины</h2>
  <p>Если царапина чувствуется на ощупь, одного крема недостаточно — нужно заполнить углубление.</p>
  <h3>Метод 1: Жидкая кожа (Liquid Leather)</h3>
  <p>Специальный реставрационный состав, который заполняет царапины и трещины. Продаётся в тюбиках разных цветов.</p>
  <ol>
    <li>Очистите поверхность вокруг царапины спиртом (обезжиривание).</li>
    <li>Нанесите «жидкую кожу» зубочисткой или тонкой кисточкой — строго в царапину.</li>
    <li>Дайте высохнуть 2–3 часа.</li>
    <li>Излишки, выступившие за края царапины, аккуратно удалите лезвием или мелкой наждачной бумагой (нулёвкой).</li>
    <li>Отполируйте участок мягкой тканью.</li>
  </ol>
  <h3>Метод 2: Восковой наполнитель</h3>
  <p>Твёрдый восковой наполнитель для обуви. Разогрейте восковой мелок зажигалкой или феном, нанесите на царапину, разровняйте шпателем. Излишки снимите лезвием после застывания.</p>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>«Жидкая кожа» и восковые наполнители — колоризированные средства. Подобрать цвет в домашних условиях сложно. Если промахнётесь с оттенком — царапина станет заметнее. Рекомендуем профессиональный подбор цвета в MANDO MEMORI.</p>
  </div>
</section>

<section id="deep">
  <h2>Глубокие царапины и порезы</h2>
  <p>Глубокие повреждения — случай для профессионалов. Домашние методы не обеспечат качественного результата.</p>
