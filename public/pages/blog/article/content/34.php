<section id="varnish">
  <h2>Царапины на лаковой коже</h2>
  <p>Лаковое покрытие царапается легче других, но и восстановление здесь имеет свои особенности — лаковый слой можно отполировать.</p>
  <h3>Мелкие царапины</h3>
  <ul>
    <li><strong>Вазелин.</strong> Вотрите вазелин в царапину круговыми движениями. Через 10 минут отполируйте. Метод работает благодаря тому, что масло заполняет микротрещины лака.</li>
    <li><strong>Полироль для лака.</strong> Специализированные средства для полировки лакированной кожи. Восстанавливают блеск и делают царапины незаметными.</li>
  </ul>
  <h3>Глубокие царапины</h3>
  <p>Если лак повреждён до основы — простой полировкой не обойтись. Нужно удалить старый лак на повреждённом участке и нанести новый. Это сложная процедура, которую лучше доверить мастерской. В MANDO MEMORI восстанавливаем лаковое покрытие до заводского состояния.</p>
</section>

<section id="prevent">
  <h2>Профилактика царапин</h2>
  <p>Предотвратить царапины полностью невозможно, но можно минимизировать их появление:</p>
  <ul>
    <li><strong>Регулярный уход.</strong> Крем для обуви создаёт защитный слой, который уменьшает вероятность царапин. Используйте крем раз в 1–2 недели.</li>
    <li><strong>Колодки при хранении.</strong> Деревянные колодки сохраняют форму и предотвращают заломы, которые выглядят как царапины.</li>
    <li><strong>Аккуратность.</strong> Избегайте контакта с острыми предметами: бордюрами, камнями, ножками стульев.</li>
    <li><strong>Защитные спреи.</strong> Водоотталкивающие спреи создают дополнительный барьер на поверхности кожи.</li>
    <li><strong>Чередование обуви.</strong> Не носите одну пару ежедневно — давайте коже отдых и время на восстановление.</li>
  </ul>
</section>

