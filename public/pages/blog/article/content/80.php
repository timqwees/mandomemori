<section id="cleaning">
  <h2>Как чистить Golden Goose</h2>
  <p>Для кроссовок Golden Goose используйте максимально деликатный подход, чтобы не повредить намеренные дефекты и вставки из разных материалов.</p>
  <h3>Материалы и инструменты</h3>
  <ul>
    <li>Мягкая щётка с натуральной щетиной</li>
    <li>Салфетка из микрофибры</li>
    <li>Деликатное мыло или пена для чистки обуви</li>
    <li>Ватные палочки для труднодоступных мест</li>
  </ul>
  <h3>Пошаговая инструкция</h3>
  <ol>
    <li>Сухой щёткой удалите пыль и грязь со всей поверхности, уделяя внимание швам.</li>
    <li>Приготовьте раствор тёплой воды с небольшим количеством мыла. Смочите салфетку и отожмите — она должна быть влажной, не мокрой.</li>
    <li>Протрите поверхность кроссовка лёгкими движениями, не нажимая на замшевые и кожаные вставки.</li>
    <li>Труднодоступные места (швы, вокруг звезды) прочистите ватной палочкой.</li>
    <li>Влажной чистой салфеткой удалите остатки мыла.</li>
    <li>Высушите при комнатной температуре вдали от батарей и солнца.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Не используйте агрессивные чистящие средства, спирт и ацетон — они могут снять заводское состаривание и повредить кожу. Никогда не стирайте Golden Goose в стиральной машине: стразики отлетят, подошва отслоится, кожа деформируется.</p>
  </div>
</section>

<section id="care">
  <h2>Ежедневный уход</h2>
  <p>Чтобы кроссовки Golden Goose радовали вас долго, достаточно соблюдать несколько простых правил:</p>
  <ul>
    <li><strong>Чистите после каждой носки.</strong> Вечером уделите 2 минуты — пройдитесь щёткой по всей поверхности, чтобы убрать пыль.</li>
    <li><strong>Используйте водоотталкивающий спрей.</strong> Для защиты от дождя и грязи обработайте кроссовки пропиткой для кожи и замши. Делайте это раз в 2 недели.</li>
    <li><strong>Меняйте шнурки.</strong> Шнурки пачкаются быстрее всего. Имейте запасную пару и стирайте их отдельно вручную.</li>
    <li><strong>Сушите правильно.</strong> Если кроссовки промокли, набейте их бумагой и сушите при комнатной температуре. Бумага впитает влагу и сохранит форму.</li>
  </ul>
</section>

<section id="storage">
  <h2>Хранение и защита</h2>
  <p>Golden Goose — инвестиция, поэтому хранить их нужно с умом:</p>
  <ul>
    <li><strong>Родная коробка.</strong> Храните кроссовки в фирменной коробке или тканевом мешке. Пластиковые контейнеры без вентиляции вредны для кожаных вставок.</li>
    <li><strong>Колодки.</strong> Используйте деревянные колодки, чтобы кроссовки держали форму. Кедровые колодки ещё и отталкивают моль.</li>
    <li><strong>Влажность.</strong> Оптимальные условия — тёмное сухое место с влажностью 40–60%. Не храните в ванной или на балконе.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Для сохранения формы вставляйте в кроссовки бумажные комки, когда не носите их. Меняйте бумагу раз в месяц, чтобы избежать застоя влаги.</p>
  </div>
</section>

