<section id="leather">
  <h2>Чистка кожаного верха</h2>
  <p>Классические AF1 делают из натуральной кожи с микропористой структурой. Грязь проникает в поры, поэтому простое протирание не всегда эффективно.</p>
  <h3>Что понадобится</h3>
  <ul>
    <li>Мягкая щётка с натуральной щетиной</li>
    <li>Пенное очищающее средство для гладкой кожи</li>
    <li>Микрофибра — 2 штуки (одна влажная, одна сухая)</li>
    <li>Питающий крем для кожи</li>
  </ul>
  <h3>Пошаговая инструкция</h3>
  <ol>
    <li>Удалите пыль и сухую грязь мягкой щёткой. Не давите сильно — порезы и царапины на коже AF1 практически не маскируются.</li>
    <li>Нанесите пенное средство на влажную микрофибру (не на обувь!). Вспеньте и протрите кожу круговыми движениями.</li>
    <li>Снимите остатки пены чистой влажной тряпкой. Важно не дать пене засохнуть на коже — останутся разводы.</li>
    <li>Высушите кроссовки при комнатной температуре, набейте бумагой для сохранения формы.</li>
    <li>Нанесите тонкий слой питающего крема, через 15 минут отполируйте мягкой тканью.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Чего нельзя делать</h4>
    <p>Не используйте щётки с жёсткой щетиной (они оставляют микроцарапины), спиртосодержащие средства (сушат кожу) и хозяйственное мыло (нарушает pH-баланс кожи). После таких экспериментов кожа AF1 становится тусклой и начинает трескаться.</p>
  </div>
