<section id="perforation">
  <h2>Уход за перфорацией</h2>
  <p>Перфорированный носок — отличительная черта AF1. Через отверстия дышит стопа, но туда же забивается мельчайшая грязь. Чистка перфорации требует терпения:</p>
  <ul>
    <li><strong>Сухая чистка.</strong> Используйте мягкую зубную щётку. Сухими круговыми движениями прочистите каждое отверстие. Не мочите перфорацию — вода затекает внутрь и сохнет долго.</li>
    <li><strong>Ватные палочки.</strong> Для глубокой чистки используйте ватные палочки, смоченные в пене для обуви. Аккуратно прочистите каждое отверстие.</li>
    <li><strong>Пылесос.</strong> Если в отверстия забился песок и мелкие камни — используйте пылесос с узкой насадкой. Не трите — абразивные частицы царапают кожу.</li>
  </ul>
</section>

<section id="sole">
  <h2>Отбеливание массивной подошвы</h2>
  <p>Подошва AF1 — визитная карточка модели, но именно она желтеет в первую очередь. Чем больше площадь резины, тем заметнее пожелтение. Для AF1 подходят два метода отбеливания:</p>
  <h3>Для белой резины</h3>
  <p>Используйте смесь пищевой соды и перекиси водорода 3% в пропорции 2:1. Нанесите на подошву, потрите щёткой и оставьте на 30 минут. Смойте водой. Повторяйте раз в 2 недели.</p>
  <h3>Для полупрозрачной подошвы (Ice Sole)</h3>
  <p>Мутная полупрозрачная подошва — частая проблема AF1 с «ледяной» подошвой. Используйте ретробрайт: перекись 12% + оксид цинка, нанесите, оберните плёнкой и поставьте под УФ-лампу на 6–8 часов.</p>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Многие владельцы AF1 красят жёлтую подошву белым акрилом. Это временное решение: краска трескается при ходьбе и выглядит неестественно. Лучше доверить отбеливание профессионалам — в MANDO MEMORI используют составы, проникающие в структуру резины, а не просто окрашивающие поверхность.</p>
