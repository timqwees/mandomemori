<section id="structure">
  <h2>Конструкция AF1: что важно знать</h2>
  <p>Nike Air Force 1 — модель со сложной конструкцией, где сочетаются несколько материалов. Понимание конструкции — ключ к правильному уходу:</p>
  <ul>
    <li><strong>Кожаный верх.</strong> В классических версиях используется натуральная кожа. Она пористая и впитывает грязь, но хорошо переносит чистку правильными средствами.</li>
    <li><strong>Перфорация на носке.</strong> Отверстия для вентиляции — самое уязвимое место. Через них грязь и влага проникают внутрь, а агрессивные средства могут повредить подкладку.</li>
    <li><strong>Массивная подошва.</strong> Подошва AF1 склонна к пожелтению из-за большой площади резины и особенностей состава. Полупрозрачные версии (Ice Sole) требуют особого ухода.</li>
    <li><strong>Вставка Air.</strong> Воздушная капсула в пятке — хрупкий элемент. Избегайте кислотных составов и давления на пятку при чистке.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>На заметку</h4>
    <p>С 2024 года Nike использует более экологичные составы резины для подошвы, которые желтеют быстрее классических. Если ваши новые AF1 начали желтеть через 2–3 месяца — это нормально, процесс можно замедлить правильным уходом.</p>
