<section id="mesh">
  <h2>Чистка сетчатых вставок</h2>
  <p>Современные версии AF1 (например, Flyknit и Utility) имеют сетчатые вставки. Сетка требует деликатного подхода:</p>
  <ul>
    <li>Используйте только пенное средство — не лейте воду на сетку.</li>
    <li>Наносите пену мягкой щёткой и сразу промокайте микрофиброй.</li>
    <li>Сушите вдали от батарей и солнца — сетка деформируется от перегрева.</li>
    <li>Для белой сетки используйте отбеливающий спрей для текстиля.</li>
  </ul>
