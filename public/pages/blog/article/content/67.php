<section id="recycled">
  <h2>Переработанные ткани — главный материал 2026</h2>
  <p>Если вы купите кроссовки в 2026 году, с вероятностью 70% их верх будет сделан из переработанного полиэстера. Nike Flyknit, Adidas Primeknit, New Balance Hypoknit — все эти технологии в 2026 году используют нити из переработанных пластиковых бутылок или океанского пластика.</p>
  <p>Adidas пообещала к 2026 году полностью перевести линейку Ultraboost на переработанный полиэстер. Nike использует технологизацию Nike Forward — нетканый материал, который создаётся из промышленных отходов и требует на 75% меньше энергии в производстве.</p>
  <p>Что это значит для потребителя: кроссовки становятся легче, дышат лучше, но износостойкость переработанных тканей пока уступает синтетике первого цикла.</p>
  <div data-type="box" data-color="green">
    <h4>Цифра</h4>
    <p>По данным Ellen MacArthur Foundation, на одну пару кроссовок из Flyknit из переработанного полиэстера уходит 5 пластиковых бутылок.</p>
  </div>
</section>

<section id="vegan">
  <h2>Растительная кожа: кактус, грибы, яблоки</h2>
  <p>Поиск альтернатив натуральной коже и ПВХ-заменителям привёл к появлению целого семейства растительных кож. В 2026 году эти материалы — уже не лабораторные эксперименты, а коммерческие продукты.</p>
  <ul>
    <li><strong>Desserto (кактус).</strong> Кожа из кактуса, производимая в Мексике. На ощупь как натуральная кожа, но полностью растительная. Используется в лимитированных коллекциях Tommy Hilfiger.</li>
    <li><strong>Mylo (грибной мицелий).</strong> Кожа из корневой системы грибов. Более эластична, чем кактусовая, подходит для кроссовок. Stella McCartney и Adidas выпустили совместную модель с Mylo.</li>
    <li><strong>AppleSkin (яблочные выжимки).</strong> Материал из отходов яблочного производства (Австрия, Италия). Самый доступный из растительных аналогов.</li>
    <li><strong>Piñatex (ананас).</strong> Кожа из волокон ананасовых листьев. Слегка текстурированная, используется для вставок и декора.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Скептическая заметка</h4>
    <p>Растительная кожа пока не так износостойка, как натуральная. Исследования показывают, что Mylo выдерживает 200 000 циклов сгибания, а натуральная кожа — до 500 000. Технологии совершенствуются, но в 2026 году рано говорить о полной замене.</p>
  </div>
</section>

<section id="bio">
  <h2>Био-материалы и инновации 2026</h2>
  <p>Помимо растительной кожи, индустрия экспериментирует с принципиально новыми материалами:</p>
  <ul>
    <li><strong>Биоразлагаемый полиуретан.</strong> Подошвы, которые разлагаются в компосте за 3–5 лет, а не за 200. Бренды Veja и Allbirds уже используют такие подошвы в массовом производстве.</li>
    <li><strong>Графеновое покрытие.</strong> Nike и Inov-8 используют графен (одноатомный слой углерода) в подошвах для увеличения износостойкости. Подошва с графеном служит в 2 раза дольше.</li>
    <li><strong>Аэрогели.</strong> В кроссовках для бега появились промежуточные подошвы из аэрогеля — он поглощает удар лучше EVA-пены при весе в 3 раза меньше.</li>
    <li><strong>Самовосстанавливающиеся полимеры.</strong> Экспериментальный материал, который «заживляет» мелкие царапины при нагреве. Пока — только в концептах.</li>
  </ul>
</section>

<section id="classic">
  <h2>Классические материалы — без них никуда</h2>
  <p>Несмотря на инновации, классические материалы никуда не уходят. В 2026 году они по-прежнему в топе:</p>
  <ul>
    <li><strong>Натуральная кожа.</strong> Телячья кожа и козья кожа остаются золотым стандартом для премиального сегмента. Тренд — более мягкая, неокрашенная кожа с натуральной текстурой.</li>
    <li><strong>Замша и нубук.</strong> В 2026 году замша снова в моде — её бархатистая текстура идеально вписывается в тренд на «тихую роскошь». Особенно популярна замша Pigskin (свиная) в моделях New Balance.</li>
    <li><strong>Резина и EVA.</strong> Беспроигрышные материалы для подошв. Тренд — полупрозрачные резиновые смеси, которые дают эффект «айс» (ледяная подошва).</li>
  </ul>
</section>

<section id="care">
  <h2>Уход за новыми материалами</h2>
  <p>Каждый новый материал требует своего подхода к чистке и защите:</p>
  <ul>
    <li><strong>Переработанный полиэстер.</strong> Чистите пенными очистителями без отбеливателя. Не гладить, не отжимать. После стирки — водоотталкивающая пропитка для текстиля.</li>
    <li><strong>Растительная кожа (Mylo, Desserto).</strong> Не терпит переувлажнения. Чистите влажной салфеткой, не используйте агрессивные чистящие средства. Для защиты — силиконовый спрей.</li>
    <li><strong>Биоразлагаемые подошвы.</strong> Более мягкие, чем обычная резина. Избегайте хождения по крупному гравию и абразивным поверхностям. Чистите мягкой щёткой.</li>
    <li><strong>Графеновые покрытия.</strong> Устойчивы к износу, но не любят ультрафиолет. Храните вдали от прямых солнечных лучей.</li>
  </ul>
  <div data-type="box" data-color="blue">
    <h4>Универсальный совет</h4>
    <p>Независимо от материала, обязательно наносите водоотталкивающую пропитку на новые кроссовки перед первой ноской. Это продлевает жизнь на 30–50%.</p>
  </div>
</section>

