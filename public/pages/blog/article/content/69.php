    <h4>Если кастом испачкался</h4>
    <p>Не трите пятно! Аккуратно промокните влажной салфеткой. Если не помогает — несите в MANDO MEMORI: мы знаем, как чистить окрашенные поверхности без повреждения рисунка.</p>
