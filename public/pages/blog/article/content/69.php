<section id="care">
  <h2>Уход за кастомизированной обувью</h2>
  <p>Кастомная роспись — не заводское покрытие. Она требует бережного обращения:</p>
  <ul>
    <li><strong>Чистка только вручную.</strong> Никаких машинных стирок и агрессивных щёток. Используйте мягкую микрофибру и специализированные средства для окрашенных поверхностей.</li>
    <li><strong>Без растворителей.</strong> Спирт, ацетон, жидкости для снятия лака мгновенно стирают акриловую краску. Используйте только средства для окрашенных материалов.</li>
    <li><strong>Меньше воды.</strong> Избегайте замачивания — вода проникает под слой краски и вызывает отслоение. Чистите локально, без погружения в воду.</li>
    <li><strong>Фиксатор — раз в 3 месяца.</strong> Слой фиксатора стирается со временем. Обновляйте его каждые 3–4 месяца, чтобы краска не осыпалась.</li>
    <li><strong>Защита от УФ.</strong> Краска выгорает на солнце. Не оставляйте кастомные кроссовки на подоконнике или в машине на солнце.</li>
  </ul>
