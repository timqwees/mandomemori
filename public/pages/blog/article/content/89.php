<section id="materials">
  <h2>Виды резиновой обуви</h2>
  <p>Современные резиновые сапоги делают из разных материалов, и от этого зависит уход:</p>
  <ul>
    <li><strong>Натуральная резина.</strong> Эластичная, экологичная, но чувствительная к перепадам температур. Со временем может растрескиваться. Требует бережного хранения.</li>
    <li><strong>ПВХ (поливинилхлорид).</strong> Бюджетный вариант. Менее эластичный, на морозе дубеет и трескается. Легко чистится, но боится высоких температур.</li>
    <li><strong>Полиуретан.</strong> Современный материал — лёгкий, прочный, эластичный. Долговечнее резины и ПВХ. Лучший выбор для частого использования.</li>
    <li><strong>Неопрен.</strong> Используется во вкладках для утепления. Сам по себе не водонепроницаем, но в сочетании с резиной даёт тепло и комфорт.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Независимо от материала, резиновые сапоги не «дышат». Носите их с хлопковыми носками и используйте впитывающие стельки, чтобы избежать парникового эффекта.</p>
  </div>
</section>

<section id="clean">
  <h2>Как чистить резиновые сапоги</h2>
  <p>Резиновая обувь неприхотлива в чистке, но есть нюансы, которые стоит знать:</p>
  <ol>
    <li><strong>Смойте грязь тёплой водой.</strong> Не используйте горячую — резина и ПВХ боятся высоких температур.</li>
    <li><strong>Мойте мягкой губкой или тканью.</strong> Жёсткие щётки могут оставить царапины на поверхности.</li>
    <li><strong>Для стойких загрязнений</strong> используйте мыльный раствор или специальное средство для резины. Не применяйте растворители и ацетон.</li>
    <li><strong>Просушите.</strong> Протрите сухой тканью и оставьте при комнатной температуре. Не ставьте на солнце или батарею.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Лайфхак</h4>
    <p>Если сапоги потеряли блеск, протрите их глицерином или силиконовой смазкой. Это вернёт глянец и создаёт защитный слой от грязи.</p>
  </div>
</section>

<section id="cracks">
  <h2>Как предотвратить трещины</h2>
  <p>Трещины на резиновых сапогах — самая частая проблема. В отличие от кожи, резину сложно отремонтировать. Проще предотвратить повреждения:</p>
  <ul>
    <li><strong>Избегайте перепадов температур.</strong> Не носите резиновые сапоги в сильный мороз — резина дубеет и трескается при −10 °C и ниже.</li>
    <li><strong>Не сушите на солнце.</strong> УФ-лучи разрушают молекулярную структуру резины, делая её хрупкой.</li>
    <li><strong>Храните в прохладном тёмном месте.</strong> Идеальная температура хранения — 10–20 °C.</li>
    <li><strong>Используйте силиконовую смазку.</strong> Раз в сезон обрабатывайте поверхность силиконовым спреем — он сохраняет эластичность.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Чего нельзя делать</h4>
    <p>Не пытайтесь заклеить трещину на резиновых сапогах обычным суперклеем — он делает резину ещё более хрупкой. Используйте специальный клей для резины (например, SAR 306) или обратитесь в мастерскую.</p>
  </div>
</section>

<section id="storage">
  <h2>Хранение резиновых сапог</h2>
  <p>Резиновые сапоги капризны в хранении. Несколько правил, чтобы они не испортились между сезонами:</p>
  <ul>
    <li><strong>Храните в вертикальном положении.</strong> Если сапоги лежат согнутыми, на сгибах образуются трещины.</li>
    <li><strong>Не складывайте голенище.</strong> Складки на резине — зона риска. Подвешивайте сапоги за петли или ставьте на специальные подставки.</li>
    <li><strong>Вставьте распорки.</strong> В голенище можно вставить пластиковые бутылки или плотный картон, чтобы оно не слипалось и не трескалось.</li>
    <li><strong>Тёмное прохладное место.</strong> Чем меньше света и тепла, тем дольше проживут резиновые сапоги.</li>
  </ul>
</section>

