<section id="science">
  <h2>Наука окисления: почему резина желтеет</h2>
  <p>Жёлтый оттенок на белой подошве — это результат химической реакции окисления полимеров. В составе резиновой смеси кроссовок используются полимеры на основе бутадиена, стирола и изопрена. При контакте с кислородом воздуха эти молекулы вступают в реакцию, образуя хромофорные группы — они поглощают синий свет и отражают жёлтый.</p>
  <p>Производители добавляют в резину антиоксиданты и стабилизаторы, которые замедляют окисление. Но у этих добавок есть побочный эффект — они сами желтеют при контакте с озоном и УФ-излучением. Некоторые антиоксиданты на основе фенолов со временем мигрируют на поверхность и кристаллизуются, создавая характерный жёлтый налёт.</p>
  <div data-type="box" data-color="gray">
    <h4>Интересный факт</h4>
    <p>Пожелтение — обратимый процесс в течение первых 6–12 месяцев. Чем дольше резина подвергается окислению, тем глубже проникает жёлтый пигмент. Застарелое пожелтение (более 2 лет) часто необратимо — окисление затрагивает всю толщу материала.</p>
  </div>
  <p>Разные бренды используют разные резиновые смеси, поэтому скорость пожелтения варьируется. Nike Air Force 1 желтеют медленно из-за большого содержания диоксида титана в резине. Adidas Yeezy 350 с полупрозрачной резиновой вставкой желтеют быстрее — в их составе меньше стабилизаторов для сохранения прозрачности.</p>
</section>

<section id="factors">
  <h2>Что ускоряет пожелтение подошвы</h2>
  <p>Окисление — естественный процесс, но некоторые факторы ускоряют его в разы:</p>
  <ul>
    <li><strong>Ультрафиолетовое излучение.</strong> Солнечный свет запускает фотохимические реакции, ускоряя окисление. Кроссовки, хранящиеся на витрине или подоконнике, желтеют в 3–4 раза быстрее, чем те, что хранятся в коробке.</li>
    <li><strong>Озон.</strong> Озоновый слой разрушается, и уровень приземного озона растёт. Озон — мощный окислитель, он атакует двойные связи в полимерных цепочках. Кроссовки в городах с плохой экологией желтеют быстрее.</li>
    <li><strong>Нагревание.</strong> Высокая температура (выше 35°C) ускоряет химические реакции. Кроссовки в машине летом, на батарее или возле обогревателя желтеют за считанные недели.</li>
    <li><strong>Влажность.</strong> Влага катализирует окисление. Хранение во влажном помещении (ванная, подвал) ускоряет пожелтение и может вызвать появление плесени на подошве.</li>
    <li><strong>Неправильное хранение.</strong> Полиэтиленовые пакеты, вакуумная упаковка и герметичные контейнеры без доступа воздуха — парадоксально, но они ускоряют пожелтение. Внутри накапливаются газы от резины, включая оксиды азота, которые агрессивно окисляют полимеры.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Главный враг белой подошвы</h4>
    <p>Солнце + тепло + герметичная упаковка = идеальные условия для пожелтения. Именно поэтому кроссовки на витрине магазина под софитами желтеют быстрее, чем те же модели в коробке на складе.</p>
  </div>
</section>

<section id="method1">
  <h2>Химический метод: перекись водорода + УФ (Retrobright)</h2>
  <p>Метод Retrobright пришёл из сообщества реставраторов ретро-консолей и компьютеров. Он использует фотокаталитическую реакцию для разрыва хромофорных связей:</p>
  <ol>
    <li><strong>Подготовка.</strong> Заклейте малярным скотчем все текстильные и кожаные элементы кроссовка — перекись может обесцветить цветные ткани и кожу.</li>
    <li><strong>Нанесение перекиси.</strong> Смешайте перекись водорода 12% (пергидроль) с загустителем — оксидом магния или карбоксиметилцеллюлозой, чтобы получить пасту консистенции сметаны. Нанесите пасту на пожелтевшую подошву.</li>
    <li><strong>УФ-обработка.</strong> Поместите кроссовки под прямые солнечные лучи или специальную УФ-лампу на 4–8 часов. Ультрафиолет активирует перекись, которая расщепляет окрашенные молекулы.</li>
    <li><strong>Очистка.</strong> Смойте пасту тёплой водой, протрите подошву мягкой щёткой и высушите.</li>
  </ol>
  <div data-type="box" data-color="blue">
    <h4>Важно</h4>
    <p>Не оставляйте перекись дольше 8 часов — длительное воздействие может сделать резину хрупкой. Повторяйте процедуру раз в 2–3 недели при необходимости. Метод безопасен только для резиновых подошв, не используйте его на полиуретановых — они разрушатся.</p>
  </div>
  <p>Метод Retrobright даёт наилучший результат на свежем пожелтении (до 1 года). Для застарелой желтизны эффективность снижается до 40–50%.</p>
</section>

<section id="method2">
  <h2>Механический метод: шлифовка и полировка</h2>
  <p>Если пожелтение поверхностное, его можно удалить механически — снять верхний слой окисленной резины. Метод подходит только для гладкой подошвы без выраженного протектора:</p>
  <ol>
    <li><strong>Мойка.</strong> Тщательно вымойте подошву с мыльным раствором и высушите. На грязной поверхности абразив будет размазывать грязь.</li>
    <li><strong>Шлифовка.</strong> Используйте мелкозернистую наждачную бумагу (зернистость 800–1200) или абразивную губку. Шлифуйте круговыми движениями, без сильного нажима. Регулярно смахивайте резиновую пыль.</li>
    <li><strong>Полировка.</strong> После удаления жёлтого слоя отполируйте поверхность войлочным кругом или мягкой тканью с полиролью для пластика. Это вернёт блеск и гладкость.</li>
    <li><strong>Защита.</strong> Нанесите тонкий слой силиконовой смазки или специальной защитной пропитки для резины.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Минусы метода</h4>
    <p>Шлифовка снимает 0.1–0.3 мм резины, что истончает подошву. Для кроссовок со сложным протектором (баскетбольные, трекинговые) метод неприменим. Также не используйте шлифовку на прозрачных резиновых вставках — структура будет заметно отличаться.</p>
  </div>
</section>

