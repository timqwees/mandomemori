  <ul>
    <li><strong>Salon Care 40 Volume Creme.</strong> Профессиональный крем-окислитель для волос (12% перекиси), который стал популярен в сообществе реставраторов. Наносится на 15–30 минут под плёнку. Эффективен для поверхностного пожелтения, но требует 3–5 процедур для полного результата.</li>
    <li><strong>Sole Bright (поставщики: SneakersER, Crep Protect).</strong> Специализированный состав для отбеливания подошвы кроссовок. Содержит смесь перекиси и оптических отбеливателей. Наносится на 2–4 часа под УФ-лампу. Результат — после первой же процедуры.</li>
    <li><strong>Angelus Sole Bright.</strong> Гель-отбеливатель для подошв. Наносится кисточкой, не требует загущения. Работает за 3–6 часов на солнце. Один флакон рассчитан на 3–4 пары кроссовок.</li>
