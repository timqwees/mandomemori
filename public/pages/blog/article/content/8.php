<section id="prevent">
  <h2>Профилактика: как предотвратить пожелтение подошвы</h2>
  <p>Предотвратить окисление дешевле и проще, чем бороться с его последствиями. Вот семь правил, которые сохранят подошву белой:</p>
  <ol>
    <li><strong>Храните в дышащей коробке.</strong> Лучшая упаковка — оригинальная коробка с микроотверстиями или тканевый мешок. Никаких герметичных пакетов и вакуумных контейнеров.</li>
    <li><strong>В прохладном тёмном месте.</strong> Идеальная температура хранения — 18–20°C. Никакого света, особенно солнечного. Шкаф в прихожей или специальный стеллаж в кладовке — отличное решение.</li>
    <li><strong>Силикагель внутрь.</strong> Положите в коробку пакетики с силикагелем — они впитывают лишнюю влагу и снижают скорость реакции окисления.</li>
    <li><strong>Регулярная очистка.</strong> После каждой носки протирайте подошву влажной салфеткой. Пыль и частицы грязи содержат металлы, которые катализируют окисление.</li>
    <li><strong>Защитная пропитка.</strong> Раз в месяц обрабатывайте подошву спреем с УФ-фильтром или силиконовой пропиткой. Создаётся барьер между резиной и кислородом.</li>
    <li><strong>Не надевайте два дня подряд.</strong> Резине нужно время, чтобы «отдохнуть» — восстановить форму и выпустить накопившиеся газы. Чередуйте хотя бы три пары.</li>
    <li><strong>Используйте Insolia или аналоги.</strong> Специальные антиоксидантные вставки в обувь замедляют окисление. Они есть в премиальных моделях, но можно купить отдельно и вложить в кроссовки.</li>
  </ol>
</section>

