<section id="transition">
  <h2>Переход к зимней обуви</h2>
  <p>Ближе к концу осени пора готовить зимнюю обувь. Как плавно перейти между сезонами:</p>
  <ul>
    <li><strong>Достаньте зимнюю обувь заранее.</strong> За 2–3 недели до наступления холодов достаньте её из хранения и проветрите.</li>
    <li><strong>Проверьте состояние.</strong> После весенне-летнего хранения осмотрите на предмет повреждений, плесени, деформации.</li>
    <li><strong>Обновите защиту.</strong> Нанесите зимнюю пропитку с защитой от соли и реагентов.</li>
    <li><strong>Почистите осеннюю обувь перед хранением.</strong> Демисезонная обувь должна уйти на покой чистой и обработанной.</li>
  </ul>
