<section id="autumn">
  <h2>Осенние угрозы для обуви</h2>
  <p>Осенью обувь сталкивается с особыми испытаниями. Знание врага в лицо помогает лучше защищаться:</p>
  <ul>
    <li><strong>Влага.</strong> Постоянные дожди и лужи — главная причина промокания и последующей деформации.</li>
    <li><strong>Грязь.</strong> Мокрая земля и пыль образуют абразивную смесь, которая царапает кожу и замшу.</li>
    <li><strong>Листва.</strong> Мокрая листва содержит кислоты, которые могут обесцветить светлую обувь.</li>
    <li><strong>Перепады температур.</strong> Днём +10, ночью −2 — такой контраст вреден для клеевых соединений и кожи.</li>
    <li><strong>Первые реагенты.</strong> В конце осени дороги начинают посыпать песком и солью — готовьтесь заранее.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Осенью после каждой прогулки обувь должна быть очищена от грязи. Высохшая на поверхности грязь въедается в материал и удаляется с большим трудом.</p>
  </div>
</section>

<section id="clean">
  <h2>Ежедневная чистка осенью</h2>
  <p>Осень — не время для лени. Ежедневный уход занимает 5–10 минут, но продлевает жизнь обуви на годы:</p>
  <ol>
    <li><strong>Удалите грязь.</strong> Сразу после возвращения домой смойте грязь тёплой водой с мягкой щёткой. Не ждите, пока высохнет.</li>
    <li><strong>Промокните влагу.</strong> Бумажным полотенцем уберите лишнюю воду с поверхности.</li>
    <li><strong>Высушите.</strong> Набейте обувь бумагой и оставьте при комнатной температуре. Не используйте батарею!</li>
    <li><strong>Нанесите крем/спрей.</strong> После полного высыхания обновите защитный слой.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Лайфхак</h4>
    <p>Носите с собой компактную салфетку для обуви. Если вы попали под дождь, протрите обувь сразу — это предотвратит образование грязевых пятен.</p>
  </div>
</section>

<section id="repair">
  <h2>Мелкий ремонт перед зимой</h2>
  <p>Осень — последний шанс отремонтировать обувь перед зимней нагрузкой:</p>
  <ul>
    <li><strong>Проверьте набойки.</strong> Если набойки стёрлись — замените. Скользкая осенняя обувь — риск падения.</li>
    <li><strong>Подклейте подошву.</strong> Отходящие края подошвы нужно заклеить, пока под них не набилась грязь.</li>
    <li><strong>Замените шнурки.</strong> Осенью шнурки быстро изнашиваются от влаги. Замените на синтетические — они долговечнее.</li>
    <li><strong>Обработайте молнию.</strong> Восковой карандаш для молний предотвратит заедание и защитит от коррозии.</li>
    <li><strong>Обновите водоотталкивающий слой.</strong> Летняя пропитка уже не работает — нужно нанести новую.</li>
  </ul>
</section>

