<section id="salt">
  <h2>Удаление соли и реагентов</h2>
  <p>Соль и антигололёдные реагенты — главные враги зимней обуви. Белые разводы портят внешний вид и разрушают материал. Действовать нужно сразу после того, как обувь высохла после последней носки.</p>
  <h3>Солевые разводы на коже</h3>
  <ol>
    <li>Смешайте 1 столовую ложку уксуса 9% с 1 стаканом воды.</li>
    <li>Смочите мягкую ткань в растворе и протрите разводы.</li>
    <li>Через 5 минут протрите чистой влажной салфеткой.</li>
    <li>Высушите и нанесите питательный крем.</li>
  </ol>
  <h3>Солевые разводы на замше</h3>
  <ol>
    <li>Дождитесь полного высыхания обуви.</li>
    <li>Пройдитесь по разводам специальной резиновой щёткой для замши.</li>
    <li>Если разводы остались — обработайте их паром и поднимите ворс щёткой.</li>
  </ol>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Не пытайтесь оттереть соль влажной салфеткой на замше — это размажет разводы и испортит ворс. Сначала дайте высохнуть, потом чистите щёткой.</p>
  </div>
</section>

<section id="clean">
  <h2>Глубокая чистка материалов</h2>
  <p>После удаления солей — тотальная чистка всей обуви. За зиму в складках и швах накапливается грязь, которую нужно тщательно удалить перед хранением.</p>
  <ul>
    <li><strong>Гладкая кожа.</strong> Очистите пеной для кожи, затем протрите влажной салфеткой. Нанесите питательный крем для восстановления жирового слоя.</li>
    <li><strong>Замша и нубук.</strong> Прочистите специальной щёткой-резенкой. При необходимости используйте спрей-пену для замши — нанесите, вспеньте, удалите салфеткой.</li>
    <li><strong>Мех и овчина.</strong> Сапоги с мехом выверните и просушите. Мех расчешите редкой расчёской. При сильном загрязнении используйте шампунь для шерсти.</li>
    <li><strong>Подошва.</strong> Вымойте с мылом и щёткой, удалите камни из протектора. Проверьте целостность.</li>
  </ul>
</section>

<section id="restore">
  <h2>Реставрация повреждений</h2>
  <p>За зиму обувь могла получить микротравмы. Весна — лучшее время для реставрации:</p>
  <ul>
    <li><strong>Царапины на коже.</strong> Замаскируйте кремом-красителем в тон. Для глубоких царапин используйте жидкую кожу.</li>
    <li><strong>Потёртости.</strong> На замше — поднимите ворс щёткой. На коже — обработайте кремом с пигментом.</li>
    <li><strong>Замена набоек.</strong> Если набойки стёрлись — замените их. Не ждите следующей зимы.</li>
    <li><strong>Деформация.</strong> Если обувь потеряла форму — вставьте колодки и слегка смочите их водой для восстановления формы.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>После реставрации дайте обуви «отдохнуть» 2–3 дня перед нанесением финишной защиты. Кремы и краски должны полностью впитаться.</p>
  </div>
</section>

<section id="protect">
  <h2>Финишная защита</h2>
  <p>Перед хранением обувь нужно законсервировать — защитить от пыли, моли и перепадов влажности:</p>
  <ul>
    <li>Нанесите на кожу тонкий слой питательного крема или воска. Он будет питать материал во время хранения.</li>
    <li>Замшу и нубук обработайте водоотталкивающим спреем — это предотвратит впитывание пыли.</li>
    <li>Внутрь положите впитывающие влагу пакетики (силикагель) или бумажные комки.</li>
    <li>Для профилактики моли положите в коробку лавандовые саше или кедровые шарики.</li>
  </ul>
</section>

<section id="store">
  <h2>Правильное хранение</h2>
  <p>Заключительный этап — упаковка и размещение на хранение. От этого зависит, как обувь будет выглядеть следующей зимой.</p>
  <div data-type="compare">
    <article data-variant="good">
      <h4>Правильное хранение</h4>
      <ul>
        <li>Колодки или бумага для формы</li>
        <li>Тканевые мешки или коробки с отверстиями</li>
        <li>Тёмное помещение, 18–22 °C</li>
        <li>Влажность 40–60%</li>
        <li>Силикагель внутрь обуви</li>
      </ul>
    </article>
    <article data-variant="bad">
      <h4>Ошибки хранения</h4>
      <ul>
        <li>Полиэтиленовые пакеты — «парниковый эффект»</li>
        <li>Хранение на балконе — перепады температур</li>
        <li>Грязная обувь — размножение бактерий</li>
        <li>Стопка друг на друге — деформация верха</li>
        <li>Рядом с батареей — пересушивание кожи</li>
      </ul>
