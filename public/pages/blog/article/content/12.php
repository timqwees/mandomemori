<section id="materials">
  <h2>Какие материалы используются в люксовой обуви</h2>
  <p>Премиальные бренды используют материалы, которые редко встречаются в масс-маркете. Каждый тип требует индивидуального подхода к чистке, увлажнению и защите.</p>
  <h3>Телячья кожа (Calfskin)</h3>
  <p>Самый популярный материал в люксовой обуви — используется в большинстве моделей Prada, Gucci, Santoni. Тонкая, но прочная кожа с гладкой поверхностью требует мягкого очищения и регулярного увлажнения. Ключевая особенность — высокая чувствительность к пересушиванию: при неправильном хранении телячья кожа трескается буквально за пару недель.</p>
  <h3>Лаковая кожа (Patent Leather)</h3>
  <p>Особый тип кожи с глянцевым покрытием, популярный в коллекциях Prada и Gucci. Лаковое покрытие не впитывает воду и грязь, но крайне уязвимо к царапинам, высоким температурам и растворителям. При неправильном хранении лак «плывёт» и теряет блеск навсегда.</p>
  <h3>Сатин и текстиль</h3>
  <p>Вечерние туфли многих домов — Dior, Valentino, Jimmy Choo — часто шьются из сатина, шёлка и других деликатных тканей. Такие материалы нельзя мочить или тереть щёткой. Любое трение оставляет необратимые повреждения нитей.</p>
  <h3>Экзотические кожи</h3>
  <p>Loro Piana, Bottega Veneta, Hermès используют кожу питона, крокодила, страуса и ящерицы. Эти материалы имеют уникальную текстуру с натуральными чешуйками и порами. Они чрезвычайно капризны: неправильный крем может забить чешуйки и лишить кожу естественного блеска, а избыточная влага вызывает деформацию.</p>
  <div data-type="box" data-color="gray">
    <h4>На заметку</h4>
    <p>Если вы не уверены в типе материала вашей обуви — посмотрите на внутренний ярлык или обратитесь к консультанту бренда. В MANDO MEMORI мы бесплатно идентифицируем материал по фотографии.</p>
