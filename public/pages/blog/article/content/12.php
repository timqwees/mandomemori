<section id="care">
  <h2>Деликатная чистка люксовой обуви: что можно и что нельзя</h2>
  <p>Главное правило: люксовая обувь не терпит агрессивной химии. Забудьте об универсальных мыльных растворах, спиртовых салфетках и щётках с жёстким ворсом.</p>
  <h3>Что можно использовать</h3>
  <ul>
    <li><strong>Микрофибра.</strong> Идеальный инструмент для ежедневной очистки — мягкая ткань без ворса, которая не царапает поверхность. Только новая или тщательно выстиранная.</li>
    <li><strong>Специализированные очистители.</strong> Бренды Saphir, Colonil, Tarrago выпускают линейки для премиальной обуви. Они имеют нейтральный pH и не разрушают красители и покрытия.</li>
    <li><strong>Кондиционеры для кожи.</strong> После каждой чистки наносите увлажняющий кондиционер. Он восстанавливает липидный слой кожи, делая её эластичной и устойчивой к влаге.</li>
    <li><strong>Щётки из конского волоса.</strong> Только мягкие, натуральные щётки. Никакой латуни или резины — они повреждают покрытие люксовых брендов.</li>
  </ul>
  <h3>Чего нельзя делать никогда</h3>
  <div data-type="box" data-color="red">
    <h4>Запрещено для люксовой обуви</h4>
    <p>× Использовать влажные салфетки для рук — спирт и отдушки разрушают красители.</p>
    <p>× Применять обувную косметику из масс-маркета — дешёвые силиконы и парафины забивают поры.</p>
    <p>× Тереть замшу латунной щёткой — для люксовой замши только натуральный каучуковый ластик.</p>
    <p>× Замачивать обувь — вода проникает в межподкладку и вызывает деформацию.</p>
    <p>× Использовать фен для сушки — высокая температура разрушает клеевые соединения и сушит кожу.</p>
  </div>
</section>

<section id="storage">
  <h2>Правильное хранение люксовой обуви</h2>
  <p>Хранение — второй по значимости фактор после чистки. По статистике MANDO MEMORI, 35% повреждений люксовой обуви связаны с неправильным хранением между сезонами.</p>
  <h3>Пылевые мешки (Dust Bags)</h3>
  <p>Каждая пара люксовой обуви поставляется с фирменным пылевым мешком. Используйте их обязательно! Мешок защищает от пыли, света и случайных царапин при хранении на полке. Никогда не храните обувь в оригинальной коробке без мешка — картон впитывает влагу и передаёт её материалам.</p>
  <h3>Распорки (Shoe Trees)</h3>
  <p>Деревянные распорки из кедра — мастхэв для люксовой обуви. Они выполняют три функции: сохраняют форму обуви (особенно важно для туфель с узким мысом), впитывают избыточную влагу после носки и ароматизируют натуральным кедровым маслом. Инвестиция в пару качественных распорок (2000–4000 ₽) окупается многократным продлением жизни обуви.</p>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Вставляйте распорки сразу после того, как снимете обувь — пока кожа ещё тёплая. Это позволяет сохранить форму наиболее эффективно. Оставляйте минимум на 2 часа.</p>
  </div>
  <h3>Климат и освещение</h3>
  <ul>
    <li><strong>Температура:</strong> 18–22 °C. Избегайте хранения рядом с батареями и обогревателями.</li>
    <li><strong>Влажность:</strong> 45–60%. Сухой воздух сушит кожу, влажный — провоцирует грибок и плесень.</li>
    <li><strong>Свет:</strong> УФ-лучи выцветают красители. Храните в шкафу или в пылевых мешках. Особенно чувствительны: розовый, красный, синий цвета.</li>
    <li><strong>Расстояние:</strong> Держите пары на расстоянии минимум 2–3 см друг от друга, чтобы избежать трения и царапин.</li>
  </ul>
</section>

