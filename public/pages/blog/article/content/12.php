<section id="restore">
  <h2>Когда нужна реставрация премиальной обуви</h2>
  <p>Даже при идеальном уходе люксовая обувь со временем требует профессиональной реставрации. Вот признаки, что пора обратиться к специалистам:</p>
  <h3>Обновление цвета (Recoloring)</h3>
  <p>Самый востребованный вид реставрации. Со временем красители выцветают, особенно на носках и пятках. Профессиональное перекрашивание восстанавливает цвет до заводского уровня. В MANDO MEMORI мы используем анилиновые красители итальянского бренда Saphir — они проникают глубоко в структуру кожи и не отслаиваются.</p>
  <h3>Замена подошвы (Resoling)</h3>
  <p>Люксовые туфли часто имеют кожаную подошву, которая стирается за один сезон активной носки. Замена подошвы на оригинальную или премиальную резиновую — стандартная услуга для брендов вроде Santoni, Magnanni, Loro Piana. Важно: настаивайте на оригинальной подошве от бренда или высококачественном аналоге.</p>
  <h3>Восстановление замши и нубука</h3>
  <p>Если замша засалилась, потеряла цвет или покрылась трудновыводимыми пятнами — профессиональная химчистка с распариванием и окрашиванием вернёт ей первозданный вид. Для люксовой замши мы не используем агрессивные растворители — только пенную очистку.</p>
