<section id="types">
  <h2>Типы швов на обуви</h2>
  <p>В современной обуви используется несколько типов швов, каждый со своими особенностями ремонта:</p>
  <ul>
    <li><strong>Машинные швы (челночный стежок).</strong> Стандартная строчка на обувной машине. Используется для соединения деталей верха, подкладки, задника. Самый распространённый тип — его ремонт относительно прост.</li>
    <li><strong>Цепной стежок.</strong> Эластичный шов, который используется в кроссовках и спортивной обуви. При разрыве распускается «лесенкой». Требует специальной машины для восстановления.</li>
    <li><strong>Ручные швы (седельный, сквозной).</strong> Встречаются на дорогой обуви ручной работы. Самые крепкие, но и самые дорогие в ремонте. Выполняются вручную вощёной ниткой.</li>
    <li><strong>Клеевые швы с проклейкой.</strong> Комбинированный метод: детали сначала проклеиваются, затем прошиваются. При расхождении требуется повторная проклейка и прошивка.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Как определить тип шва</h4>
    <p>Посмотрите на изнаночную сторону: если стежки переплетаются — это челночный стежок. Если образуют цепочку — цепной. Ручной шов отличается идеально ровным натяжением и одинаковыми стежками с обеих сторон.</p>
  </div>
</section>

<section id="causes">
  <h2>Почему расходятся швы</h2>
  <p>Понимание причины расхождения шва поможет избежать повторной поломки. Основные причины:</p>
  <ul>
    <li><strong>Заводской брак.</strong> Слишком редкая строчка, слабое натяжение нити, использование тонких ниток. Чаще всего проявляется в бюджетной обуви.</li>
    <li><strong>Перегрузка материала.</strong> Шов расходится в местах наибольшего натяжения: на сгибе стопы, в области мизинца, на заднике. Особенно характерно для тесной обуви.</li>
    <li><strong>Износ нити.</strong> Со временем нитки теряют прочность из-за трения, влаги и реагентов. Примерно через 2–3 года активной носки.</li>
    <li><strong>Разрыв материала.</strong> Кожа или текстиль рвутся по краю шва. В этом случае нужно не просто перешивать, а укреплять материал или ставить заплатку.</li>
    <li><strong>Неправильный ремонт.</strong> Попытки зашить обувь обычными нитками или неподходящей иглой приводят к тому, что шов расходится снова через несколько дней.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Не шейте сами</h4>
    <p>Бытовая швейная машина не подходит для обуви — она не пробивает толстые материалы и не даёт правильного натяжения. А ручной шов обычной ниткой без воска быстро перетрётся. Доверяйте ремонт швов профессионалам.</p>
  </div>
</section>

<section id="machine">
  <h2>Ремонт машинных швов</h2>
  <p>Это самый распространённый случай. Мастер восстанавливает шов на специальной обувной машине, которая работает с толстыми материалами и усиленными нитками.</p>
  <h3>Процесс ремонта</h3>
  <ol>
    <li><strong>Осмотр.</strong> Мастер оценивает длину расхождения и состояние материала по краям шва.</li>
    <li><strong>Подготовка.</strong> Удаляются остатки старой нити. Края материала при необходимости выравниваются.</li>
    <li><strong>Проклейка.</strong> Для усиления края шва могут проклеиваться специальным обувным клеем.</li>
    <li><strong>Прошивка.</strong> Шов прокладывается заново на обувной машине. Используются капроновые или лавсановые армированные нитки.</li>
    <li><strong>Фиксация.</strong> Концы нити оплавляются или завязываются узлом, чтобы шов не распустился.</li>
  </ol>
  <p>Важный момент: мастер должен попасть в существующие отверстия от иглы. Если шить по новым отверстиям, материал может порваться. Опытный мастер всегда использует старые проколы.</p>
  <div data-type="box" data-color="green">
    <h4>Стоимость</h4>
    <p>Ремонт машинного шва длиной до 5 см — 300–500 ₽, до 15 см — 600–1000 ₽, полная перестрочка детали — от 1500 ₽.</p>
  </div>
</section>

<section id="hand">
  <h2>Ремонт ручных и декоративных швов</h2>
  <p>Швы ручной работы требуют особого подхода. Используется техника седельного шва — двухниточного переплетения, который применяется в дорогой обуви.</p>
  <h3>Особенности ремонта ручных швов</h3>
  <ul>
    <li>Используется только ручная работа — машинный шов будет выглядеть инородно.</li>
    <li>Применяются вощёные льняные или капроновые нити — они не гниют и не растягиваются.</li>
    <li>Две иглы — одна продевается в противоположные отверстия, создавая переплетение.</li>
    <li>Сила натяжения каждой петли выверяется вручную для равномерности.</li>
  </ul>
  <p>Ручной ремонт стоит дороже, но он полностью сохраняет аутентичность обуви. Для премиальных брендов (Edward Green, John Lobb, Berluti) это единственно правильный подход.</p>
