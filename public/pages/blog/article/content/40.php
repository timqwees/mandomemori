<section id="prevention">
  <h2>Как укрепить швы профилактически</h2>
  <p>Если швы начали расходиться на новой обуви или вы хотите предотвратить проблему, существуют профилактические меры:</p>
  <ul>
    <li><strong>Дополнительная строчка.</strong> Мастер может проложить усиливающий шов по наиболее нагруженным участкам. Особенно актуально для детской и спортивной обуви.</li>
    <li><strong>Проклейка краёв.</strong> Перед прошивкой края деталей проклеиваются специальным клеем — это снижает нагрузку на нить и предотвращает разрыв материала.</li>
    <li><strong>Установка подкладки.</strong> Если шов расходится из-за износа материала, мастер может установить внутреннюю усиливающую подкладку из прочной ткани.</li>
    <li><strong>Использование более толстой нити.</strong> Если позволяет конструкция обуви, замена тонкой заводской нити на более толстую армированную продлевает жизнь шва в 2–3 раза.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Когда профилактика не нужна</h4>
    <p>Если обуви больше 3–4 лет и материал вокруг шва начал «сыпаться» — профилактика не поможет. В этом случае износ материала уже критический, и проще рассмотреть покупку новой пары.</p>
  </div>
</section>

<section id="cant">
  <h2>Когда шов не подлежит ремонту</h2>
  <p>К сожалению, не все швы можно восстановить. Вот признаки того, что ремонт шва не имеет смысла:</p>
  <ul>
    <li><strong>Материал крошится вокруг отверстий.</strong> Если кожа или текстиль потеряли эластичность и рвутся при малейшем натяжении — новый шов не продержится.</li>
    <li><strong>Множественные расхождения.</strong> Если шов расходится в 3–4 местах по всей длине — это системный износ, а не локальная проблема.</li>
    <li><strong>Гнилые нитки.</strong> Если нитки рассыпаются от прикосновения — материал вокруг них тоже потерял прочность.</li>
    <li><strong>Деформация детали.</strong> Если из-за разошедшегося шва деталь растянулась, порвалась или изменила форму — простая прошивка не восстановит геометрию.</li>
