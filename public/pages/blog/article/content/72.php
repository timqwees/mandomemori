<section id="samba">
  <h2>Samba, Gazelle — уход за замшевыми вставками</h2>
  <p>Adidas Samba и Gazelle — главный тренд 2026. Их замшевая вставка на носке — отличительная черта, но она же и главная проблема в уходе.</p>
  <h3>Чистка замши на Adidas</h3>
  <ul>
    <li><strong>После каждой носки</strong> проходитесь по замше резиновой щёткой — это поднимает ворс и удаляет пыль.</li>
    <li><strong>Пятна</strong> удаляйте специальным ластиком или очистителем для замши. Вода оставляет разводы.</li>
    <li><strong>Не мочите замшу.</strong> Если замша намокла — промокните бумажным полотенцем и дайте высохнуть естественным путём. Не используйте фен.</li>
    <li><strong>Водоотталкивающая пропитка</strong> — обязательна. Нанесите спрей для замши перед первой ноской и обновляйте каждые 2 недели.</li>
  </ul>
  <h3>Чистка кожаной основы</h3>
  <p>Основная часть Samba/Gazelle — гладкая кожа. Протирайте влажной тканью, наносите крем-кондиционер раз в 2 недели. Белая кожа на Samba требует отбеливающего крема раз в месяц.</p>
  <div data-type="box" data-color="gray">
    <h4>Особая осторожность</h4>
    <p>Замша на Samba и Gazelle часто окрашена — примятую замшу на ярких моделях (красный, синий, зелёный) восстанавливайте вместе с тонировкой. Обратитесь в MANDO MEMORI для профессионального восстановления цвета.</p>
  </div>
</section>

<section id="ultraboost">
  <h2>Ultraboost — уход за Primeknit и подошвой Boost</h2>
  <p>Ultraboost — технологичная модель с трикотажным верхом Primeknit и подошвой Boost из термопластичного полиуретана (TPU).</p>
  <h3>Чистка Primeknit</h3>
  <ul>
    <li><strong>Только ручная стирка.</strong> Тёплая вода, мягкое мыло (лучше — специализированный пенный очиститель). Никаких машинок!</li>
    <li><strong>Мягкая щётка.</strong> Используйте щётку с мягкой щетиной, круговыми движениями. Сильное трение повреждает трикотаж.</li>
    <li><strong>Не отжимать.</strong> После стирки дайте воде стечь. Не выкручивайте — деформируется верх.</li>
    <li><strong>Сушка.</strong> На воздухе при комнатной температуре. Используйте бумажные полотенца внутри для впитывания влаги. Никаких батарей.</li>
  </ul>
  <h3>Уход за подошвой Boost</h3>
  <p>Boost — сотни мелких капсул TPU, спрессованных в подошву. Особенности: белую подошву Boost сложно отбелить из-за пористой структуры. Грязь забивается между капсулами — чистите мягкой щёткой после каждой носки. Избегайте контакта с химическими реагентами и маслом — они разрушают TPU.</p>
</section>

<section id="yeezy">
  <h2>Yeezy — уникальная конструкция и материалы</h2>
  <p>Линейка Yeezy от Adidas (сейчас без участия Kanye West, но модели продолжают выпускать) — одни из самых сложных в уходе кроссовок.</p>
  <h3>Yeezy 350 (Primeknit + Boost)</h3>
  <p>Верх — тонкий Primeknit двухслойного плетения. Чистка: пена-очиститель, мягкая щётка, никакого трения. Подошва — полупрозрачный Boost, который желтеет быстрее обычного. Ретробрайт возможен, но сложен из-за формы подошвы.</p>
  <h3>Yeezy 500 (замша + сетка)</h3>
  <p>Комбинация замши, сетки и кожи. Замшу чистите сухим методом. Сетку — пенной чисткой. Подошва adiPRENE+ не требует особого ухода, но её форма собирает много грязи — чистите протектор зубочисткой.</p>
  <h3>Yeezy 700 (кожа + сетка + замша)</h3>
  <p>Самая сложная модель для чистки — три материала в одном кроссовке. Используйте разные методы для каждой зоны. Лучше доверить профессионалам MANDO MEMORI.</p>
  <div data-type="box" data-color="red">
    <h4>Важно</h4>
    <p>Yeezy 350 с полупрозрачной подошвой нельзя долго хранить на свету — подошва желтеет даже без носки. Храните в коробке в тёмном месте.</p>
  </div>
</section>

<section id="leather">
  <h2>Уход за кожаными Adidas (Stan Smith, Superstar)</h2>
  <p>Классические кожаные модели — самые неприхотливые. Но и за ними нужно ухаживать:</p>
  <ul>
    <li><strong>Протирайте после носки.</strong> Влажная микрофибра удаляет пыль и предотвращает въедание грязи в поры кожи.</li>
    <li><strong>Питание кожи.</strong> Раз в 2 недели — крем-кондиционер. Stan Smith с белой кожей требуют отбеливающего крема раз в месяц.</li>
    <li><strong>Перфорация на Stan Smith.</strong> Отверстия на носке забиваются пылью. Чистите их мягкой зубной щёткой.</li>
    <li><strong>Подошва.</strong> У Stan Smith резиновая подошва часто желтеет — используйте ретробрайт или профессиональное отбеливание в MANDO MEMORI.</li>
  </ul>
