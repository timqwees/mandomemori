<section id="boost">
  <h2>Как отбелить пожелтевшую подошву Boost</h2>
  <p>Пожелтение подошвы Boost — бич всех владельцев Ultraboost и Yeezy. Пористая структура TPU впитывает грязь и желтеет неравномерно.</p>
  <p><strong>Домашний метод:</strong> Перекись водорода 12% + сода до пасты. Нанести на подошву, обернуть плёнкой, оставить на солнце на 2–4 часа. Результат — средний.</p>
  <p><strong>Профессиональный метод:</strong> В MANDO MEMORI используем профессиональные отбеливающие составы, которые проникают в поры TPU и удаляют желтизну на молекулярном уровне. Результат — до 90% белизны.</p>
