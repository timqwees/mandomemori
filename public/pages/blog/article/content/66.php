<section id="what">
  <h2>Что такое бесшовные кроссовки</h2>
  <p>Бесшовные кроссовки — это обувь, у которой верх выполнен без швов, единой вязаной конструкцией. В отличие от классической обуви, где верх собирается из нескольких кусков материала (носок, боковины, задник) и сшивается, бесшовный верх создаётся на специальных 3D-вязальных машинах за один цикл.</p>
  <p>Результат: верх кроссовка похож на чулок или носок — эластичный, лёгкий, без швов, которые могут натирать. Технология позволяет управлять плотностью вязки на разных участках: в зоне носка — более дышащая, в области пятки — усиленная.</p>
  <p>Первыми технологию массово применили Nike (Flyknit) и Adidas (Primeknit) в начале 2010-х. К 2026 году бесшовные технологии есть у всех крупных брендов.</p>
  <div data-type="box" data-color="gray">
    <h4>Цифры</h4>
    <p>Производство бесшовного верха снижает количество отходов на 60% по сравнению с раскроем кожи. Поэтому этот тренд ещё и экологичный.</p>
