<section id="care">
  <h2>Как ухаживать за бесшовными кроссовками</h2>
  <p>Трикотажный верх — нежный и капризный. Правильный уход продлевает жизнь бесшовных кроссовок в 2–3 раза.</p>
  <h3>Основные правила</h3>
  <ul>
    <li><strong>Чистка только руками.</strong> Стиральная машина разрушает эластичные волокна. Используйте таз с тёплой водой и мягким мылом (или специализированным пенным очистителем).</li>
    <li><strong>Никаких жёстких щёток.</strong> Только мягкая щётка или губка. Сильное трение повреждает трикотаж и создаёт катышки.</li>
    <li><strong>Не отжимайте.</strong> После стирки просто дайте воде стечь. Не скручивайте кроссовки — это деформирует трикотаж.</li>
    <li><strong>Сушка только на воздухе.</strong> Никаких батарей и фенов. Используйте распорки для сохранения формы. Сушите вдали от прямых солнечных лучей.</li>
    <li><strong>Водоотталкивающая пропитка.</strong> Обязательна для бесшовного верха. Спрей для текстиля защитит от влаги и грязи. Обновляйте после каждой 3-й чистки.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Чего нельзя делать</h4>
    <p>Не отбеливайте белые трикотажные кроссовки хлором — волокна разрушатся. Не используйте кондиционеры для белья, стиральный порошок с гранулами и пятновыводители — они оставляют разводы и забивают поры трикотажа.</p>
  </div>
  <h3>Как убрать затяжки</h3>
  <p>Затяжки — главная беда бесшовного верха. Не дёргайте торчащую нить — она стянет петли. Аккуратно втяните нить с обратной стороны с помощью иголки или специального инструмента. Если затяжка большая — отнесите в мастерскую MANDO MEMORI.</p>
