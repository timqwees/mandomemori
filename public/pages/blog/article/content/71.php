<section id="leather">
  <h2>Уход за кожаными Nike (Air Force 1, Dunks)</h2>
  <p>Кожаные модели Nike — самые популярные и самые простые в уходе, но требуют регулярности.</p>
  <h3>Ежедневный уход</h3>
  <p>После каждой носки протирайте кожу влажной микрофиброй, чтобы удалить пыль и грязь. Раз в неделю наносите питательный крем для кожи — он предотвращает пересыхание и растрескивание на сгибах. Используйте кремы на основе норкового масла или ланолина.</p>
  <h3>Глубокая чистка</h3>
  <p>Раз в 2–3 недели делайте полную чистку: снимите шнурки, выньте стельки. Очистите кожу специальным лосьоном для гладкой кожи (Collonil, Saphir, Tarrago). Для трудных пятен — используйте мыльный раствор с мягкой щёткой. Избегайте замачивания — вода портит кожу.</p>
  <h3>Восстановление цвета</h3>
  <p>На белых Air Force 1 потёртости заметны особенно. Используйте белый крем-краситель для кожи или аэрозольную краску. Нанесите на чистую поверхность, дайте впитаться, отполируйте мягкой тканью.</p>
  <div data-type="box" data-color="green">
    <h4>Лайфхак</h4>
    <p>Чтобы кожаные Nike дольше держали форму — используйте распорки. Особенно это важно для Air Force 1 — их массивный силуэт деформируется без поддержки.</p>
  </div>
</section>

<section id="flyknit">
  <h2>Уход за кроссовками из Flyknit</h2>
  <p>Трикотажный верх Flyknit — лёгкий и дышащий, но капризный. Ошибки в уходе могут убить кроссовки за пару месяцев.</p>
  <ul>
    <li><strong>Только ручная чистка.</strong> Стиральная машина разрушает эластичные волокна. Используйте пену-очиститель без воды.</li>
    <li><strong>Мягкая щётка.</strong> Жёсткая щетина создаёт катышки и затяжки. Только мягкая щётка (щетина — средняя или мягкая) или губка.</li>
    <li><strong>Не отбеливайте.</strong> Хлор и кислородные отбеливатели разрушают полиэстер. Для белого Flyknit — только специальные средства для спортивной обуви.</li>
    <li><strong>Водоотталкивающая пропитка.</strong> Обязательна после каждой 2-й чистки. Спрей для текстиля защитит от грязи и влаги.</li>
  </ul>
</section>

<section id="airmax">
  <h2>Уход за подошвой Air Max</h2>
  <p>Подошва Nike Air Max требует особой осторожности — воздушные капсулы уязвимы для механических повреждений.</p>
  <ul>
    <li><strong>Избегайте острых предметов.</strong> Не ходите в Air Max по гравию, щебню и битому стеклу. Прокол воздушной капсулы — неремонтопригоден.</li>
    <li><strong>Чистка подошвы.</strong> Используйте мягкую щётку и мыльный раствор. Не используйте жёсткие абразивы — они царапают прозрачные вставки Air.</li>
    <li><strong>Пожелтение.</strong> Полупрозрачные элементы Air Max желтеют со временем. Удалить желтизну с них сложно — требуется ретробрайт или профессиональная обработка.</li>
    <li><strong>Хранение.</strong> Не храните Air Max на солнце — УФ-лучи ускоряют пожелтение и разрушают полиуретан воздушных капсул.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Никогда не прокалывайте «пузырь» Air, если он сдулся — восстановить его невозможно. Если воздушная капсула лопнула — только замена подошвы в мастерской.</p>
  </div>
</section>

<section id="suede">
  <h2>Уход за замшевыми Nike (Dunk Low Premium)</h2>
  <p>Замша на Nike встречается реже, чем кожа, но модели из замши (особенно Dunks) очень популярны. Замша требует деликатного подхода:</p>
  <ul>
    <li><strong>Сухая чистка после каждой носки.</strong> Используйте резиновую щётку для замши, чтобы поднять ворс и удалить пыль.</li>
    <li><strong>Избегайте воды.</strong> Вода оставляет на замше некрасивые разводы. Для удаления пятен используйте ластик для замши или специальный очиститель.</li>
    <li><strong>Восстановление ворса.</strong> Если замша примялась — пройдитесь по ней паровой струёй (отпариватель) и расчешите резиновой щёткой.</li>
    <li><strong>Защита.</strong> Обязательно нанесите водоотталкивающую пропитку для замши перед первой ноской. Обновляйте каждые 2 недели.</li>
  </ul>
</section>

<section id="whites">
  <h2>Как сохранить белые Nike в идеальном состоянии</h2>
  <p>Белые кроссовки Nike (особенно Air Force 1) требуют самого тщательного ухода. Вот чек-лист:</p>
  <ol>
    <li><strong>Чистка после каждой носки.</strong> Протрите подошву влажной салфеткой, убедите пыль с верха мягкой кистью. Это занимает 30 секунд, но предотвращает въедание грязи.</li>
    <li><strong>Отбеливание подошвы раз в месяц.</strong> Используйте мягкий отбеливатель для резины или перекись водорода с содой.</li>
    <li><strong>Не сушите на солнце.</strong> УФ-лучи вызывают пожелтение белой резины. Сушите в тени.</li>
    <li><strong>Используйте защиту.</strong> Водоотталкивающий спрей для текстиля и кожи — обязателен. Он отталкивает грязь и облегчает ежедневную чистку.</li>
    <li><strong>Профессиональная чистка.</strong> Раз в 2–3 месяца отдавайте белые кроссовки в MANDO MEMORI на глубокую чистку. Вернём белизну даже застарелой паре.</li>
  </ol>
</section>

<section id="storage">
  <h2>Хранение кроссовок Nike</h2>
  <p>Правильное хранение продлевает жизнь кроссовок на 1–2 года:</p>
  <ul>
    <li><strong>Используйте распорки.</strong> Они сохраняют форму, особенно у моделей с массивным силуэтом (Air Force 1, Air Max).</li>
    <li><strong>Храните в коробках.</strong> Оригинальная коробка Nike обеспечивает правильную вентиляцию. Не используйте полиэтиленовые пакеты.</li>
    <li><strong>Не ставьте одну пару на другую.</strong> Тяжёлые кроссовки деформируют лёгкие. Храните пары рядом.</li>
    <li><strong>Температура и влажность.</strong> Идеально: 15–20 °C, влажность 40–60%. Избегайте батарей, подоконников и ванных комнат.</li>
  </ul>
</section>

