<section id="when">
  <h2>Когда реставрация имеет смысл</h2>
  <p>Не каждая пара достойна реставрации. Прежде чем нести кроссовки в мастерскую, оцените их по трём критериям:</p>
  <ul>
    <li><strong>Эмоциональная ценность.</strong> Лимитированные коллаборации, подписные модели, кроссовки с историей — их реставрация оправдана независимо от стоимости.</li>
    <li><strong>Рыночная стоимость.</strong> Если пара стоит на вторичном рынке от 30 000 ₽ — реставрация экономически выгодна. Винтажные модели Nike Air Jordan, Air Max, Yeezy, Balenciaga — классические кандидаты на восстановление.</li>
    <li><strong>Степень износа.</strong> Если разрушена колодка, порвана мембрана или деформирована пятка — восстановить обувь до приемлемого состояния практически невозможно.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Когда проще купить новые</h4>
    <p>Если цена реставрации превышает 60% стоимости новой пары — чаще всего выгоднее купить новые кроссовки. Исключение — лимитированные и винтажные модели, чья ценность не измеряется розничной ценой.</p>
  </div>
  <h3>Что можно восстановить</h3>
  <p>Современные технологии реставрации позволяют вернуть к жизни практически любой элемент кроссовка:</p>
  <ul>
    <li>Царапины и потёртости на коже и замше</li>
    <li>Пожелтевшая подошва (см. нашу статью об отбеливании)</li>
    <li>Отслоение подошвы (переклейка)</li>
    <li>Порванные швы (перестрочка)</li>
    <li>Повреждённая фурнитура (люверсы, крючки, молнии)</li>
    <li>Выцветший цвет (восстановление или полная перекраска)</li>
  </ul>
</section>

<section id="scratches">
  <h2>Удаление царапин и потёртостей</h2>
  <p>Царапины и потёртости — самая частая причина, по которой обувь отправляют на реставрацию. Кроссовки трутся о бордюры, педали автомобиля, стулья в офисе — и на коже или замше остаются следы.</p>
  <h3>Для гладкой кожи</h3>
  <ol>
    <li><strong>Очистка.</strong> Удалите загрязнения с поверхности кожи. Для глубоких царапин используйте ватную палочку с изопропиловым спиртом — он обезжиривает повреждённый участок.</li>
    <li><strong>Шлифовка.</strong> Мелкой наждачной бумагой (P1200–P2000) аккуратно зашлифуйте края царапины. Важно: только саму царапину, не затрагивая окружающую кожу.</li>
    <li><strong>Грунтовка.</strong> Нанесите специальный филлер для кожи — он заполняет углубления и выравнивает поверхность. Дайте высохнуть 30–40 минут.</li>
    <li><strong>Краска.</strong> Нанесите акриловую краску для кожи в 2–3 слоя с промежуточной сушкой 15–20 минут.</li>
    <li><strong>Фиксация.</strong> Закрепите результат финишным покрытием (матовым или глянцевым в зависимости от типа кожи).</li>
  </ol>
  <div data-type="box" data-color="blue">
    <h4>Важно</h4>
    <p>Для замши шлифовка не применяется — это разрушит ворс. Царапины на замше удаляются паром и последующим поднятием ворса специальной щёткой. Если царапина глубокая — используется подкрашивание аэрозольной краской.</p>
  </div>
  <h3>Для замши и нубука</h3>
  <ul>
    <li>Лёгкие потёртости: обработайте ластиком для замши, затем поднимите ворс щёткой.</li>
    <li>Средние повреждения: обработайте паром и пройдитесь щёткой с латунной щетиной.</li>
    <li>Глубокие царапины: используйте краску-спрей для замши, нанося точечно на повреждённый участок.</li>
  </ul>
</section>

<section id="paint">
  <h2>Восстановление цвета и перекраска</h2>
  <p>Выцветание — естественный процесс для обуви, которая регулярно носится на солнце. Особенно страдают кроссовки ярких и насыщенных цветов: красные, синие, зелёные.</p>
  <h3>Восстановление родного цвета</h3>
  <p>Если обувь выцвела равномерно, можно восстановить цвет без полной перекраски. Для этого используются пигментированные кремы и кондиционеры, которые возвращают насыщенность цвета и питают материал.</p>
  <ul>
    <li><strong>Для гладкой кожи:</strong> крем-краска Sapir, Tarrago, Saphir Médaille d'Or. Нанесите тонким слоем, дайте впитаться 20 минут, отполируйте мягкой щёткой.</li>
    <li><strong>Для замши:</strong> аэрозольная краска в цвет оригинала. Наносите в 2–3 слоя с расстояния 30 см.</li>
    <li><strong>Для текстиля:</strong> текстильные красители (Rit, Dylon) — только для полностью текстильных кроссовков без кожаных вставок.</li>
  </ul>
  <h3>Полная перекраска</h3>
  <p>Полная перекраска — радикальная мера, которая применяется, когда восстановить родной цвет невозможно или когда хочется полностью обновить образ обуви.</p>
  <p>В 2026 году особенно популярна перекраска белых кроссовок в бежевые, оливковые, серые и насыщенно-бордовые оттенки. Технически процесс включает:</p>
  <ol>
    <li>Полное обезжиривание и грунтовку поверхности</li>
    <li>Маскировку элементов, которые не должны быть покрашены (подошва, логотипы, стельки)</li>
    <li>Нанесение базового слоя краски аэрографом</li>
    <li>Нанесение финишного покрытия для защиты цвета</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Перекраска — необратимый процесс. После нанесения акрилового покрытия вернуть оригинальный цвет невозможно. Перекрашивайте только обувь, которую готовы носить в новом цвете минимум 1–2 года.</p>
  </div>
</section>

<section id="sole">
  <h2>Ремонт и замена подошвы</h2>
  <p>Подошва изнашивается быстрее всего, особенно если вы много ходите по асфальту. Стереть протектор до гладкого состояния можно за 6–12 месяцев активной носки.</p>
  <h3>Виды ремонта подошвы</h3>
  <ul>
    <li><strong>Переклейка отслоившейся подошвы.</strong> Если подошва отошла в носке или пятке — достаточно очистить поверхности, нанести контактный клей (Barge, Safir Renovating) и зафиксировать обувь в колодке на 24 часа.</li>
    <li><strong>Наварка протектора.</strong> Для кроссовок с толстой подошвой возможна наварка нового слоя резины. Используется жидкая резина или полиуретановые составы, которые наносятся на изношенную поверхность.</li>
    <li><strong>Полная замена подошвы.</strong> Самый сложный вариант. Требует полного удаления старой подошвы, подбора материала по твёрдости и форме, точной посадки на колодку. Выполняется только в профессиональных мастерских.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Совет</h4>
    <p>Если протектор стёрся до гладкого состояния — не откладывайте ремонт. Дальнейшая носка разрушает основную камеру подошвы, и восстановить её будет в 3–4 раза дороже.</p>
  </div>
</section>

<section id="stitching">
  <h2>Восстановление строчек и фурнитуры</h2>
  <p>Швы на кроссовках не только декоративный элемент, но и функциональный — они держат конструкцию. Порванные строчки могут привести к деформации всего кроссовка.</p>
  <h3>Когда нужна перестрочка</h3>
  <ul>
    <li>Разошёлся шов в области мыска — кроссовок потеряет форму и будет сдавливать пальцы.</li>
    <li>Порван шов на заднике — пятка будет болтаться, появятся мозоли.</li>
    <li>Разошлась строчка на язычке — язычок будет съезжать в сторону.</li>
  </ul>
  <p>Для перестрочки используется промышленная швейная машина, способная работать с толстыми нитками (полиэстер 40–60 текс). Домашние швейные машины редко справляются с плотными материалами кроссовок.</p>
  <h3>Фурнитура</h3>
  <p>Люверсы (дырочки для шнурков), крючки для шнурков, молнии и заклёпки — всё это подлежит замене. Стандартный набор для реставрации фурнитуры включает:</p>
  <ul>
    <li>Люверсы разных диаметров (5–12 мм)</li>
    <li>Крючки для шнурков (для ботинок и высоких кроссовок)</li>
    <li>Хольнитены и заклёпки для фиксации ремешков</li>
    <li>Магнитные и обычные пряжки</li>
  </ul>
  <div data-type="box" data-color="blue">
    <h4>Важное о фурнитуре</h4>
    <p>При замене люверсов используйте латунные — они не ржавеют и служат в 5–10 раз дольше стальных. Нержавеющая сталь — бюджетный аналог, но она тяжелее и может оставлять тёмные следы на шнурках.</p>
  </div>
</section>

