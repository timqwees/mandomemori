<section id="design">
  <h2>Конструкция Triple S: что нужно знать перед чисткой</h2>
  <p>Balenciaga Triple S — это не просто кроссовки, а инженерная конструкция. Понимание их устройства поможет избежать ошибок:</p>
  <ul>
    <li><strong>Три слоя подошвы.</strong> Подошва Triple S состоит из трёх независимых слоёв: средний слой — пена EVA, нижний — резина с протектором, промежуточный — резина с логотипом. Слои склеены между собой, но при грубом воздействии могут отслаиваться.</li>
    <li><strong>Сетка (mesh).</strong> Верхняя часть — крупноячеистая сетка. Она прочная, но грязь въедается в ячейки.</li>
    <li><strong>Замшевые вставки.</strong> На носке и пятке — замша. Часто цвета, отличного от основной расцветки.</li>
    <li><strong>Гладкая кожа.</strong> На язычке и заднике — гладкая кожа с тиснением Balenciaga.</li>
    <li><strong>Искусственное старение.</strong> Некоторые элементы намеренно выглядят изношенными — потертости, потрескавшаяся краска, грязь. Это часть дизайна.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Не все загрязнения на Triple S — грязь. Некоторые «дефекты» — намеренная часть дизайна. Перед чисткой убедитесь, что вы не стираете то, что задумано дизайнером.</p>
  </div>
</section>

<section id="cleaning">
  <h2>Чистка верха Balenciaga Triple S</h2>
  <h3>Сетка (Mesh)</h3>
  <p>Сетка — самая загрязняемая часть. Используйте пенный очиститель (Collonil, Tarrago). Нанесите на мягкую щётку, аккуратно протрите сетку круговыми движениями. Не трите слишком сильно — сетка может растянуться и потерять форму. Удалите остатки пены влажной микрофиброй.</p>
  <h3>Замшевые вставки</h3>
  <p>Замша на Triple S часто окрашена в яркие цвета (красный, синий, жёлтый). Используйте только сухую чистку резиновой щёткой. Никакой воды или пены на замше — краска может размазаться или выцвести. Для пятен — ластик для замши или специальный очиститель.</p>
  <h3>Гладкая кожа</h3>
  <p>Язычок и задник — из гладкой кожи. Протирайте влажной микрофиброй. Раз в месяц — крем-кондиционер для кожи. Цвет крема — в тон коже (обычно белый или чёрный).</p>
  <div data-type="box" data-color="red">
    <h4>Осторожно!</h4>
    <p>Не используйте крем для кожи на замшевых вставках — жир впитается и оставит тёмные пятна, которые невозможно удалить. Крем — только на гладкую кожу!</p>
  </div>
</section>

<section id="sole">
  <h2>Чистка многослойной подошвы Triple S</h2>
  <p>Подошва Triple S — самая сложная часть для чистки из-за её конструкции. Три слоя собирают грязь в щелях между ними.</p>
  <h3>Инструменты</h3>
  <ul>
    <li>Зубочистки или деревянные палочки — для очистки щелей между слоями подошвы</li>
    <li>Щётка средней жёсткости — для резинового протектора</li>
    <li>Мягкая щётка — для средней пеной части (EVA)</li>
    <li>Мыльный раствор или специализированный очиститель для подошв</li>
  </ul>
  <h3>Пошаговая чистка подошвы</h3>
  <ol>
    <li><strong>Сухая чистка.</strong> Удалите все камни и песок из протектора зубочисткой.</li>
    <li><strong>Чистка щелей.</strong> Деревянной палочкой, обёрнутой влажной микрофиброй, пройдитесь по всем стыкам между слоями подошвы.</li>
    <li><strong>Мойка.</strong> Щёткой средней жёсткости с мыльной водой очистите протектор и боковые поверхности подошвы.</li>
    <li><strong>Средний слой (EVA).</strong> Чистите только мягкой щёткой — EVA легко царапается.</li>
    <li><strong>Сушка.</strong> Промокните бумажным полотенцем, дайте высохнуть 12 часов.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>Не используйте отбеливатели на средней части подошвы (EVA) — она пожелтеет неравномерно. Для белых Triple S — только профессиональное отбеливание в MANDO MEMORI.</p>
  </div>
</section>

<section id="distressed">
  <h2>Состаренный декор: чистить или не чистить</h2>
  <p>Многие расцветки Triple S имеют намеренный эффект состаривания: потёртости, потрескавшаяся краска, следы «износа». Владельцы часто задаются вопросом — надо ли это чистить?</p>
  <p>Ответ: нет. Состаренные элементы — часть дизайна Balenciaga. Если вы сотрёте «патину» — кроссовки потеряют задумку дизайнера и могут выглядеть как дешёвая подделка.</p>
  <p>Как отличить грязь от дизайна: износ расположен симметрично на обоих кроссовках, имеет характерный рисунок (трещины краски, потёртости на мыске). Если загрязнение на одном кроссовке, а на втором его нет — это грязь, её можно чистить.</p>
</section>

<section id="mistakes">
  <h2>Чего нельзя делать с Balenciaga Triple S</h2>
  <ul>
    <li><strong>Не стирайте в машинке.</strong> Тройная подошва сломает барабан, а верх необратимо деформируется.</li>
    <li><strong>Не используйте отбеливатели.</strong> Хлор и перекись разрушают EVA-пену и обесцвечивают замшу.</li>
    <li><strong>Не сушите на батарее.</strong> Температура разрушает клей между слоями подошвы — слои могут отслоиться.</li>
    <li><strong>Не замачивайте полностью.</strong> Вода проникает между слоями подошвы и вызывает коррозию клея. Мыть — только локально.</li>
    <li><strong>Не используйте жёсткие щётки на EVA.</strong> Пена легко царапается, и царапины невозможно убрать.</li>
  </ul>
