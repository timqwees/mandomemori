<section id="principle">
  <h2>Как чернила взаимодействуют с кожей</h2>
  <p>Чтобы понимать, как удалять чернила, нужно знать их состав. Большинство чернил для ручек — на водной или масляной основе с добавлением пигментов и закрепителей. Водные чернила проникают в кожу быстро, но удаляются легче. Масляные — глубже и устойчивее.</p>
  <p>Кожа — пористый материал. Чернила проникают в поры, и чем дольше они находятся на поверхности, тем глубже впитываются. Свежие чернила можно удалить почти полностью. Застарелые — только осветлить или замаскировать.</p>
  <ul>
    <li><strong>Свежие чернила (до 1 часа)</strong> — удаляются в 90% случаев.</li>
    <li><strong>Чернила до 24 часов</strong> — удаляются в 60–70% случаев, может остаться светлый след.</li>
    <li><strong>Чернила старше 1 дня</strong> — сложный случай, гарантировать полное удаление нельзя.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Первое правило</h4>
    <p>Не трите свежее чернильное пятно! Трение размазывает чернила на большую площадь и втирает их глубже в поры. Всегда промокайте, а не трите.</p>
  </div>
</section>

<section id="smooth">
  <h2>Удаление чернил с гладкой кожи</h2>
  <p>Гладкая кожа — самый подходящий материал для удаления чернил. Она менее пористая, чем замша, и лучше переносит обработку спиртом.</p>
  <h3>Метод 1: Медицинский спирт (лучший для гладкой кожи)</h3>
  <ol>
    <li>Смочите ватный диск или ватную палочку медицинским спиртом. Не лейте спирт на обувь — только на диск.</li>
    <li>Промокните чернильное пятно. Не трите!</li>
    <li>Меняйте диск по мере загрязнения чернилами.</li>
    <li>Продолжайте, пока чернила не перестанут переходить на диск.</li>
    <li>Промокните остатки спирта влажной тканью.</li>
  </ol>
  <p>Важно: спирт может слегка осветлить кожу. После обработки обязательно нанесите питающий крем в цвет обуви.</p>
  <h3>Метод 2: Лак для волос (экстренный метод)</h3>
  <p>Если под рукой нет спирта — используйте лак для волос. Распылите на пятно с расстояния 15–20 см, сразу промокните тканью. Лак содержит спирт и работает по тому же принципу. Повторите 2–3 раза.</p>
  <h3>Метод 3: Специализированные средства</h3>
  <p>Для стойких чернил используйте профессиональные очистители: Saphir Renomat, Angelus Stain Remover или Tarrago Stain Remover. Нанесите средство на ватный диск, обработайте пятно по инструкции.</p>
  <div data-type="box" data-color="green">
    <h4>Секрет профессионалов</h4>
    <p>После удаления чернил с гладкой кожи может остаться «тень» — обесцвеченный участок. Замаскируйте его цветным кремом для обуви. Нанесите крем тонким слоем на всю поверхность кроссовка, а не только на проблемное место — так цвет будет равномерным.</p>
  </div>
</section>

<section id="suede">
  <h2>Чернила на замше и нубуке</h2>
  <p>Замша и нубук — самый сложный случай. Спирт и вода портят ворс, поэтому обычные методы не работают. Удаление чернил с замши лучше доверить профессионалам, но если хотите попробовать сами — действуйте максимально аккуратно.</p>
  <h3>Метод 1: Специальный очиститель для замши</h3>
  <p>Купите пенное средство для удаления пятен с замши (Collonil Nubuk Cleaner, Saphir Omnidaim). Нанесите пену на пятно, подождите 3–5 минут, снимите чистой тканью. Пена вытягивает чернила из ворса, не разрушая его структуру.</p>
  <h3>Метод 2: Тальк</h3>
  <p>Если чернила свежие — посыпьте пятно тальком толстым слоем. Тальк впитает влагу из чернил вместе с пигментом. Оставьте на 3–4 часа, затем счистите щёткой для замши.</p>
  <h3>Метод 3: Ластик для замши</h3>
  <p>Если пятно небольшое и поверхностное — попробуйте затереть его ластиком для замши. Вы буквально стираете верхний слой ворса вместе с чернилами. После этого подкрасьте участок краской для замши.</p>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Не используйте спирт, уксус, ацетон и другие растворители на замше и нубуке. Они обесцвечивают ворс и делают материал жёстким. После такой обработки замша может стать неузнаваемой.</p>
  </div>
</section>

<section id="varnish">
  <h2>Чернила на лаковой коже</h2>
  <p>Лаковая кожа — особый случай. Лаковое покрытие не пропускает чернила внутрь, поэтому они остаются на поверхности. Это облегчает удаление, но усложняет выбор средств — растворители повреждают лаковый слой.</p>
  <h3>Метод 1: Влажная ткань</h3>
  <p>Попробуйте просто протереть пятно влажной микрофиброй. Многие чернила (особенно водные) не успевают закрепиться на лаковой поверхности и смываются за 1–2 раза.</p>
  <h3>Метод 2: Мягкое мыло</h3>
  <p>Если вода не помогла — нанесите каплю мягкого мыла на влажную ткань и протрите пятно. Не трите сильно — лак царапается.</p>
  <h3>Метод 3: Спирт (только в крайнем случае)</h3>
  <p>Если чернила не удаляются — используйте медицинский спирт на ватной палочке. Обрабатывайте только пятно, не касаясь чистого лака вокруг. Сразу после удаления промойте участок водой и отполируйте вазелином.</p>
</section>

<section id="mistakes">
  <h2>Чего нельзя делать</h2>
  <p>Ошибки, которые часто совершают при попытке удалить чернила:</p>
  <ul>
    <li><strong>Использовать пятновыводитель для одежды.</strong> Он не предназначен для кожи и может содержать отбеливатели.</li>
    <li><strong>Тереть щёткой.</strong> Трение втирает чернила глубже в поры кожи.</li>
    <li><strong>Замачивать обувь.</strong> Вода разносит чернила по всей поверхности.</li>
    <li><strong>Использовать ацетон или жидкость для снятия лака.</strong> Они растворяют не только чернила, но и верхний слой кожи.</li>
    <li><strong>Пытаться закрасить чернила.</strong> Чернила могут проступить сквозь краску через время. Сначала удалите, потом красьте.</li>
  </ul>
</section>

