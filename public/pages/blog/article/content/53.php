<section id="prevention">
  <h2>Как защитить обувь перед следующей зимой</h2>
  <p>Лучший способ избежать проблем с цветом после зимы — правильная подготовка и профилактика:</p>
  <ul>
    <li><strong>Водоотталкивающая пропитка.</strong> Нанесите на обувь 2–3 слоя пропитки перед началом зимнего сезона. Обновляйте каждые 2–3 недели в течение зимы.</li>
    <li><strong>Защита от реагентов.</strong> Специальные воски и кремы с высокой водоотталкивающей способностью создают барьер, через который соль не проникает в материал.</li>
    <li><strong>Ежедневный уход.</strong> После каждой носки зимой очищайте обувь от снега и реагентов мягкой влажной тканью. Не оставляйте соль на поверхности.</li>
    <li><strong>Правильная сушка.</strong> Сушите обувь при комнатной температуре с использованием колодок или бумаги. Не на батарее — высокая температура фиксирует соль в материале.</li>
    <li><strong>Восковая защита.</strong> Для кожаной зимней обуви используйте твёрдые воски — они создают прочный барьер против реагентов.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Чек-лист подготовки к зиме</h4>
    <p>Чистка → питание/кондиционирование → восстановление цвета (если нужно) → 2 слоя водоотталкивающей пропитки → восковая защита (для кожи). За 1–2 недели до начала зимы. Процедура продлевает срок службы обуви на 2–3 сезона.</p>
