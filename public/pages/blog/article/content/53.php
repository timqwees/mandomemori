<section id="salt">
  <h2>Как соль и реагенты влияют на цвет</h2>
  <p>Понимание механизма повреждения поможет правильно выбрать метод восстановления:</p>
  <ul>
    <li><strong>Химическое воздействие.</strong> Реагенты (хлориды, нитраты) вступают в реакцию с красителями, обесцвечивая их. Особенно страдают тёмные оттенки — чёрный становится серым, коричневый — грязно-бежевым.</li>
    <li><strong>Кристаллизация соли.</strong> Соль проникает в поры материала и кристаллизуется при высыхании. Кристаллы разрывают волокна и выталкивают краситель на поверхность, создавая белые разводы.</li>
    <li><strong>Пересушивание.</strong> Соль вытягивает влагу из кожи и замши, делая материал ломким. Сухая кожа теряет цвет быстрее.</li>
    <li><strong>Микротрещины.</strong> Перепады температур и солевая агрессия создают микротрещины в покрытии, через которые вымывается пигмент.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Главное правило</h4>
    <p>Не пытайтесь удалить солевые разводы простым мытьём — вода не растворяет соль, а только разносит её глубже в материал. Используйте специальные составы или профессиональный подход.</p>
  </div>
</section>

<section id="removesalt">
  <h2>Удаление солевых разводов</h2>
  <p>Первый шаг в восстановлении цвета — удаление соли. Если оставить соль на обуви, она продолжит разрушать краситель.</p>
  <h3>Методы удаления соли</h3>
  <ul>
    <li><strong>Уксусный раствор.</strong> Смешайте 1 часть столового уксуса (9%) и 3 части воды. Смочите мягкую ткань и протрите разводы. Уксус нейтрализует солевые отложения. После обработки протрите чистой влажной тканью.</li>
    <li><strong>Специальные средства.</strong> Профессиональные очистители для солевых разводов: Tarrago De-Salter, Collonil Anti-Salt. Более эффективны, чем домашние методы.</li>
    <li><strong>Лимонный сок.</strong> Разбавьте сок лимона водой 1:2 и нанесите на разводы. Кислота растворяет соль. Через 5–10 минут смойте водой.</li>
  </ul>
  <h3>Пошаговая инструкция</h3>
  <ol>
    <li>Просушите обувь при комнатной температуре. Не используйте батарею.</li>
    <li>Щёткой удалите сухую соль с поверхности.</li>
    <li>Нанесите выбранное средство мягкой тканью.</li>
    <li>Через 10–15 минут удалите остатки влажной тканью.</li>
    <li>Просушите и оцените результат. При необходимости повторите.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Важно</h4>
    <p>После удаления соли обязательно нанесите питательный крем (для кожи) или кондиционер (для замши). Соль вытягивает жиры из материала, и их нужно восполнить, чтобы цвет лёг ровно.</p>
  </div>
</section>

<section id="leatherrestore">
  <h2>Восстановление цвета гладкой кожи после зимы</h2>
  <p>После удаления соли и просушки приступайте к восстановлению цвета. Для гладкой кожи алгоритм такой:</p>
  <ol>
    <li><strong>Питание.</strong> Нанесите питательный крем (Saphir Renovateur, Collonil Leather Cream). Оставьте на 30 минут, затем отполируйте. Крем восстанавливает жировой баланс и подготавливает кожу к покраске.</li>
    <li><strong>Тонирование.</strong> Используйте крем-краситель в тон обуви. Нанесите тонким слоем, втирая круговыми движениями. Если цвет потерян сильно — нанесите 2–3 слоя.</li>
    <li><strong>Полировка.</strong> После высыхания (15–20 минут) отполируйте мягкой щёткой или бархоткой.</li>
    <li><strong>Защита.</strong> Нанесите водоотталкивающий спрей для защиты цвета от будущих воздействий.</li>
  </ol>
  <p>Для сильно повреждённого цвета — с белыми пятнами и глубокими потёртостями — может потребоваться аэрозольная краска. В этом случае следуйте инструкции полной покраски с подготовкой и фиксацией.</p>
  <div data-type="box" data-color="dark">
    <h4>Тест на цвет</h4>
    <p>Перед нанесением крема-красителя проверьте оттенок на незаметном участке (под язычком). Нанесите, подождите высыхания и посмотрите совпадение. Разные партии обуви могут иметь разные оттенки.</p>
  </div>
</section>

<section id="suederestore">
  <h2>Восстановление цвета замши и нубука после зимы</h2>
  <p>Замша и нубук страдают от соли сильнее кожи — пористая структура впитывает реагенты глубже.</p>
  <h3>Пошаговое восстановление замши</h3>
  <ol>
    <li><strong>Чистка.</strong> После удаления соли прочистите замшу специальной щёткой для поднятия ворса. Движения — в одну сторону.</li>
    <li><strong>Освежение цвета.</strong> Используйте аэрозоль-освежитель для замши и нубука. Распылите с расстояния 20 см равномерно по всей поверхности.</li>
    <li><strong>Восстановление ворса.</strong> После высыхания пройдитесь резиновой щёткой или ластиком для замши, чтобы поднять ворс.</li>
    <li><strong>Защита.</strong> Нанесите водоотталкивающую пропитку для замши. Это предотвратит повреждение цвета зимой.</li>
  </ol>
  <p>Если замша потеряла цвет пятнами — можно использовать аэрозольную краску для замши с пигментом. Важно подобрать точный оттенок.</p>
