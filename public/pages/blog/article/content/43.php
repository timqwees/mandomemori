<section id="what">
  <h2>Что такое профилактика и зачем она нужна</h2>
  <p>Профилактика — это сменная накладка, которая крепится на подошву обуви. Она принимает на себя основной износ, сохраняя родную подошву в идеальном состоянии. Когда профилактика стирается, её заменяют новой, а подошва под ней остаётся как новая.</p>
  <h3>Основные преимущества</h3>
  <ul>
    <li><strong>Сохранение подошвы.</strong> Родная подошва не контактирует с асфальтом, реагентами и абразивами. Она остаётся в заводском состоянии.</li>
    <li><strong>Экономия денег.</strong> Замена профилактики стоит 800–2500 ₽, а полная замена подошвы — 2500–5000 ₽. Профилактика в 3 раза дешевле.</li>
    <li><strong>Лучшее сцепление.</strong> Профилактика из современных материалов обеспечивает лучшее сцепление с поверхностью, чем заводская подошва.</li>
    <li><strong>Амортизация.</strong> Качественная полиуретановая профилактика смягчает удар при ходьбе, снижая нагрузку на суставы.</li>
    <li><strong>Защита от воды.</strong> Профилактика герметизирует нижнюю часть обуви, защищая от проникновения влаги через изношенные участки.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Сколько служит профилактика</h4>
    <p>При ежедневной носке — 4–6 месяцев. При сезонной — до года. Толщина профилактики варьируется от 2 до 8 мм: чем толще, тем дольше ходит. Средняя толщина 4–5 мм.</p>
  </div>
</section>

<section id="materials">
  <h2>Виды профилактики по материалу</h2>
  <p>Профилактика изготавливается из разных материалов, и выбор зависит от типа обуви и условий носки:</p>
  <ul>
    <li><strong>Полиуретан.</strong> Самый популярный материал. Эластичный, износостойкий, не скользит. Подходит для большинства типов обуви. Минус — может скользить на мокром полу.</li>
    <li><strong>Термоэластопласт (ТЭП).</strong> Морозостойкий материал для зимней обуви. Сохраняет эластичность при низких температурах, не трескается на морозе.</li>
    <li><strong>Резина.</strong> Традиционный материал. Хорошее сцепление, высокая износостойкость. Тяжелее полиуретана, менее эластична.</li>
    <li><strong>Полиуретан с металлической вставкой.</strong> Комбинированный вариант для тех, кто много ходит. Металл предотвращает протёр профилактики на пятке, увеличивая срок службы до года.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Рекомендация</h4>
    <p>Для повседневной обуви выбирайте полиуретан толщиной 4–5 мм. Для зимней — ТЭП 5–6 мм. Для треккинговой — резину 6–8 мм с глубоким протектором.</p>
  </div>
</section>

