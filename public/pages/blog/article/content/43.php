<section id="install">
  <h2>Как устанавливают профилактику</h2>
  <p>Установка профилактики — процесс, который требует профессионального оборудования. Вот как это делается в мастерской:</p>
  <ol>
    <li><strong>Снятие старой профилактики (если есть).</strong> Остатки старой накладки удаляются с подошвы.</li>
    <li><strong>Зачистка подошвы.</strong> Подошва шлифуется, обезжиривается. Удаляются неровности.</li>
    <li><strong>Разогрев профилактики.</strong> Материал размягчается в термопечи или специальном станке для лучшего прилегания.</li>
    <li><strong>Крепление.</strong> Профилактика фиксируется на полиуретановый клей и прикатывается роликом для удаления воздуха.</li>
    <li><strong>Обрезка.</strong> Излишки профилактики обрезаются по контуру подошвы. Мастер повторяет все изгибы и углы.</li>
    <li><strong>Шлифовка края.</strong> Края профилактики шлифуются заподлицо с подошвой. Место соединения становится незаметным.</li>
