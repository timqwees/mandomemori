<section id="tips">
  <h2>Полезные советы</h2>
  <ul>
    <li><strong>Пометьте коробки.</strong> Приклейте фото обуви или подпишите коробку — летом легко забыть, какие ботинки в какой коробке.</li>
    <li><strong>Не храните всё вместе.</strong> Кожаная и замшевая обувь не должны соприкасаться — замша может окраситься от красителей кожи.</li>
    <li><strong>Колодки — не роскошь.</strong> Деревянные колодцы не только сохраняют форму, но и впитывают лишнюю влагу и отпугивают моль.</li>
    <li><strong>Проверка раз в месяц.</strong> Уделите 5 минут в месяц, чтобы осмотреть хранящуюся обувь. Заметить проблему на ранней стадии — значит решить её быстро.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Лайфхак</h4>
    <p>Положите в коробки с обувью сухие лавандовые саше или кусочки кедрового дерева. Это отпугивает моль и придаёт приятный аромат.</p>
  </div>
