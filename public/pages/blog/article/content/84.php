<section id="winter">
  <h2>Хранение зимней обуви</h2>
  <p>Зимние сапоги, ботинки и угги отправляются на заслуженный отдых. Чтобы они пережили лето без потерь, соблюдайте последовательность:</p>
  <ol>
    <li><strong>Чистка.</strong> Тщательно вымойте и просушите обувь. Любое загрязнение за лето въестся и станет пятном, которое уже не отмыть.</li>
    <li><strong>Обработка.</strong> Нанесите питательный крем на кожу и водоотталкивающую пропитку на замшу. Это предотвратит пересыхание.</li>
    <li><strong>Форма.</strong> Вставьте колодки или плотные бумажные комки. Без них обувь может «сесть» и потерять форму.</li>
    <li><strong>Упаковка.</strong> Используйте тканевые мешки или коробки с вентиляционными отверстиями. В полиэтилене обувь «задохнётся» и покроется плесенью.</li>
  </ol>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Перед длительным хранением убедитесь, что обувь полностью сухая изнутри. Влажная обувь — идеальная среда для грибка и плесени. Сушите не менее 24 часов при комнатной температуре.</p>
  </div>
</section>

<section id="summer">
  <h2>Хранение активной обуви</h2>
  <p>Летняя обувь в активной носке тоже требует правильного хранения между использованиями:</p>
  <ul>
    <li><strong>Проветривание.</strong> После возвращения домой не убирайте кроссовки сразу в шкаф. Оставьте их на 1–2 часа в проветриваемом месте, чтобы вышла влага.</li>
    <li><strong>Ротация.</strong> Имейте 2–3 пары для лета и носите их по очереди. Обувь должна отдыхать минимум 24 часа после дня носки.</li>
    <li><strong>Впитыватели влаги.</strong> Положите в каждую пару пакетик силикагеля. Это предотвратит появление запаха и продлит свежесть.</li>
    <li><strong>УФ-защита.</strong> Не храните обувь на подоконнике или открытых стеллажах под прямыми солнечными лучами. Цвет выгорит за 2–3 недели.</li>
  </ul>
</section>

<section id="mold">
  <h2>Как избежать плесени</h2>
  <p>Плесень на обуви — последствие неправильного хранения. Она появляется от избытка влаги и отсутствия циркуляции воздуха. Вот меры профилактики:</p>
  <ul>
    <li><strong>Влажность в помещении</strong> должна быть 40–60%. Если в квартире влажно — используйте осушитель воздуха.</li>
    <li><strong>Не храните в подвале или на балконе</strong> — там перепады температур и высокая влажность.</li>
    <li><strong>Раз в месяц проветривайте</strong> хранящуюся обувь: достаньте из коробок, дайте «подышать» пару часов.</li>
    <li><strong>Используйте абсорбенты:</strong> силикагель, активированный уголь, соль в тканевых мешочках.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Если плесень уже появилась</h4>
    <p>Не пытайтесь смыть плесень водой — споры расползутся по всей поверхности. Удалите сухой щёткой на улице, обработайте поверхность уксусным раствором (1:1 с водой) и просушите на солнце в течение 2–3 дней.</p>
  </div>
</section>

