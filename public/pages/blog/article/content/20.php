<section id="350">
  <h2>Yeezy 350 V2: уход за Primeknit</h2>
  <p>Yeezy 350 V2 — самая популярная модель. Её верх из трикотажа Primeknit лёгкий и дышащий, но быстро впитывает грязь. Неправильная чистка может растянуть или деформировать трикотаж.</p>
  <h3>Подготовка</h3>
  <ul>
    <li>Выньте шнурки и стельки. Они чистятся отдельно.</li>
    <li>Удалите крупную грязь мягкой сухой щёткой.</li>
    <li>Не замачивайте кроссовки — вода ослабляет нити Primeknit.</li>
  </ul>
  <h3>Чистка Primeknit</h3>
  <ol>
    <li>Используйте специальную пену для деликатного текстиля. Нанесите на влажную щётку.</li>
    <li>Чистите круговыми движениями без сильного нажима. Primeknit — это трикотаж, и сильное трение растягивает его.</li>
    <li>Снимайте пену влажной микрофиброй сразу — не давайте ей засохнуть.</li>
    <li>При необходимости повторите на самых загрязнённых участках.</li>
  </ol>
  <p>Для белых цветов Yeezy 350 используйте отбеливающий спрей для текстиля после чистки. Нанесите на влажный трикотаж, оставьте на 5 минут, затем промокните тканью.</p>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Primeknit не любит машинную стирку. Высокая температура и центрифуга нарушают структуру вязаного полотна. После машинной стирки Yeezy могут потерять форму навсегда — верх станет мешковатым.</p>
  </div>
</section>

<section id="500">
  <h2>Yeezy 500: замша, кожа и mesh</h2>
  <p>Yeezy 500 — гибрид из замши, гладкой кожи и сетки. Каждый материал требует своего подхода. Чистите их последовательно, чтобы избежать переноса грязи с одного материала на другой.</p>
  <h3>Замшевые вставки</h3>
  <p>Чистите сухой щёткой с латунной щетиной. Для жирных пятен используйте тальк или специальный очиститель для замши. Не мочите замшу — вода оставляет разводы.</p>
  <h3>Кожаные элементы</h3>
  <p>Протрите влажной микрофиброй. Для стойких загрязнений — пена для гладкой кожи. После чистки нанесите питающий крем, избегая попадания на замшу.</p>
  <h3>Сетка (mesh)</h3>
  <p>Чистите пенным средством и мягкой щёткой. Сетка на 500-й модели мелкая — используйте зубную щётку для труднодоступных мест.</p>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Не используйте одни и те же щётки для замши и кожи. Ворс, испачканный кремом для кожи, оставит жирные следы на замше. Держите отдельные инструменты для каждого материала.</p>
  </div>
</section>

<section id="700">
  <h2>Yeezy 700: многослойная конструкция</h2>
  <p>Yeezy 700 — самая сложная в чистке модель. У неё до 8 различных материалов и фактур: замша, кожа, сетка, нубук, резиновые вставки и светоотражающие элементы (3M).</p>
  <ul>
    <li><strong>Замша и нубук.</strong> Только сухая чистка латунной щёткой. Не лейте воду.</li>
    <li><strong>Кожа.</strong> Пена для гладкой кожи, затем питающий крем.</li>
    <li><strong>Сетка.</strong> Пенная чистка, без замачивания.</li>
    <li><strong>Светоотражающие элементы.</strong> Протирайте только влажной микрофиброй без химии — агрессивные средства могут повредить светоотражающее покрытие.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Совет профессионалов</h4>
    <p>При чистке Yeezy 700 двигайтесь сверху вниз: сначала замша, потом кожа, потом сетка и резина. Это предотвращает попадание грязи с более загрязнённых нижних участков на деликатный верх.</p>
  </div>
</section>

<section id="sole">
  <h2>Чистка и защита подошвы</h2>
  <p>Подошва Yeezy — отдельная история. У 350 она полупрозрачная с характерными точками-стабилизаторами, у 500 — массивная с глубокими канавками, у 700 — с резиновыми нашивками.</p>
  <h3>Yeezy 350 — полупрозрачная подошва</h3>
  <p>Полупрозрачная резина желтеет быстрее обычной. Чистите смесью соды и воды с помощью зубной щётки. Не используйте отбеливатели на хлорной основе — они делают резину мутной.</p>
  <h3>Yeezy 500 и 700 — резина с протектором</h3>
  <p>Используйте щётку с жёсткой щетиной и мыльную воду. Грязь в канавках протектора удаляйте зубочисткой или специальным крючком. Не используйте металлические инструменты — они оставляют царапины.</p>
</section>

<section id="laces">
  <h2>Шнурки, стельки и внутренняя часть</h2>
  <p>Внутренняя часть Yeezy пачкается не меньше внешней. Пот, пыль и частицы кожи оседают на стельках и подкладке.</p>
  <ul>
    <li><strong>Шнурки Yeezy.</strong> Замочите в тёплой воде с пятновыводителем. Для белых шнурков используйте кислородный отбеливатель. Не трите — трикотажные шнурки распускаются.</li>
    <li><strong>Стельки.</strong> Промойте в мыльной воде, потрите мягкой щёткой. Высушите при комнатной температуре.</li>
    <li><strong>Внутренняя подкладка.</strong> Протрите влажной микрофиброй с небольшим количеством пены. Остатками пены — не замачивайте. Для удаления запаха используйте дезодорирующий спрей.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Совет коллекционера</h4>
    <p>После чистки Yeezy всегда обрабатывайте их водоотталкивающим спреем. Для Primeknit используйте пропитку для текстиля, для замшевых элементов — отдельную защиту для замши. Это продлевает чистоту в 2–3 раза.</p>
  </div>
