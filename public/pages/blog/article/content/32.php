<section id="habits">
  <h2>Привычки, которые убирают запах навсегда</h2>
  <p>Устранение запаха — разовая акция. Чтобы он не вернулся, измените привычки:</p>
  <ul>
    <li><strong>Сушите обувь между носками.</strong> Никогда не носите одну пару два дня подряд. Обувь должна сохнуть минимум 24 часа.</li>
    <li><strong>Меняйте стельки каждые 3 месяца.</strong> Даже если они выглядят чистыми, в них накопились бактерии.</li>
    <li><strong>Носите носки из натуральных материалов.</strong> Хлопок, бамбук, шерсть мериноса. Синтетика — главный друг бактерий.</li>
    <li><strong>Используйте антибактериальные спреи.</strong> Обрабатывайте обувь изнутри после каждой носки. Это занимает 10 секунд.</li>
    <li><strong>Проветривайте обувь.</strong> Не убирайте её сразу в шкаф — дайте постоять на открытом воздухе 30–60 минут.</li>
    <li><strong>Имейте 2–3 пары в ротации.</strong> Чем больше пар, тем реже каждая носится и тем лучше сохнет.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Комплексный подход</h4>
    <p>Запах не вернётся, если вы одновременно: снизите потливость (носки + гигиена стоп), уберёте среду для бактерий (сушка + стельки) и будете регулярно обрабатывать обувь антибактериальными средствами. Одно из трёх — не работает.</p>
