<section id="root">
  <h2>Коренные причины запаха</h2>
  <p>Запах — это бактерии. Они живут на коже стоп, на стельках и внутри обуви. Бактериям нужны два условия для размножения: влага и тепло. Пот обеспечивает влагу, закрытая обувь — тепло. В результате бактерии размножаются и выделяют пахучие соединения.</p>
  <p>Почему одни люди сталкиваются с запахом постоянно, а другие — никогда?</p>
  <ul>
    <li><strong>Интенсивность потоотделения.</strong> Гипергидроз (чрезмерная потливость) — медицинская причина, которая требует лечения.</li>
    <li><strong>Материал обуви.</strong> Синтетические материалы не дышат и создают парниковый эффект. Кожа и текстиль — дышат.</li>
    <li><strong>Качество стелек.</strong> Тонкие синтетические стельки не впитывают пот — он остаётся на поверхности и питает бактерии.</li>
    <li><strong>Носки.</strong> Синтетические носки усугубляют потливость и запах. Хлопок и бамбук — решение.</li>
    <li><strong>Грибок стопы.</strong> Грибковая инфекция даёт характерный резкий запах. В этом случае лечение обуви бесполезно без лечения стопы.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важный факт</h4>
    <p>Обувь не пахнет сама по себе. Запах — результат жизнедеятельности микроорганизмов. Если убить бактерии и не дать им размножаться снова — запах не вернётся. Именно на этом основана профессиональная обработка.</p>
  </div>
</section>

<section id="diagnostics">
  <h2>Диагностика: откуда именно пахнет</h2>
  <p>Прежде чем бороться с запахом, определите его источник:</p>
  <ul>
    <li><strong>Стельки.</strong> Выньте стельки и понюхайте отдельно. Если запах идёт от них — замените стельки. В 70% случаев проблема решается заменой стелек.</li>
    <li><strong>Внутренняя подкладка.</strong> Если пахнет подкладка — нужна антибактериальная обработка внутренней поверхности.</li>
    <li><strong>Верх обуви.</strong> Если пахнет снаружи — грязь впитала запах и медленно отдаёт его. Нужна глубокая чистка.</li>
    <li><strong>Подошва.</strong> Резина сама по себе не пахнет, но протектор может накапливать грязь, которая со временем начинает издавать запах.</li>
  </ul>
</section>

<section id="clean">
  <h2>Глубокая чистка как основа</h2>
  <p>Без глубокой чистки избавиться от запаха невозможно. Дезодоранты только маскируют его. Вот пошаговый план:</p>
  <h3>Шаг 1: Чистка стелек</h3>
  <p>Промойте стельки в мыльной воде с добавлением соды. Потрите щёткой. Если запах остался — выбросьте стельки и купите новые. Стельки — расходный материал, не пытайтесь их реанимировать, если они впитали запах.</p>
  <h3>Шаг 2: Чистка внутренней поверхности</h3>
  <p>Протрите внутреннюю часть обуви раствором воды с уксусом (1:3) или перекисью водорода. Для труднодоступных мест используйте ватные палочки. После обработки высушите обувь естественным путём.</p>
  <h3>Шаг 3: Чистка внешней поверхности</h3>
  <p>Вычистите внешнюю грязь — она тоже может быть источником запаха. Используйте пену для соответствующего материала.</p>
  <h3>Шаг 4: Полная просушка</h3>
  <p>Сушите обувь 24–48 часов при комнатной температуре. Влажная среда — рай для бактерий. Обувь должна быть полностью сухой изнутри и снаружи.</p>
  <div data-type="box" data-color="green">
    <h4>Важный нюанс</h4>
    <p>Даже если после чистки запах исчез, бактерии могли остаться в микропорах материала. Поэтому чистка — только первый этап. Следующий — антибактериальная обработка.</p>
  </div>
</section>

<section id="bacteria">
  <h2>Уничтожение бактерий</h2>
  <p>После чистки нужно уничтожить бактерии, которые могли выжить в глубине материала.</p>
  <h3>Метод 1: Заморозка</h3>
  <p>Поместите обувь в герметичный пакет и оставьте в морозилке при –20 °C на 24 часа. Бактерии погибают при низких температурах. Важно: пакет должен быть герметичным, чтобы обувь не впитала запахи из морозилки.</p>
  <h3>Метод 2: УФ-обработка</h3>
  <p>Ультрафиолет убивает бактерии и грибок. Используйте специальные УФ-сушилки для обуви (например, Scholl или Timson). Достаточно 30–60 минут обработки.</p>
  <h3>Метод 3: Пробиотический спрей</h3>
  <p>Спреи на основе пробиотиков заселяют обувь полезными бактериями, которые вытесняют гнилостные микроорганизмы. Эффект сохраняется 2–4 недели. Лучший вариант для профилактики после профессиональной чистки.</p>
  <h3>Метод 4: Озонирование</h3>
  <p>Озон — мощный окислитель, который уничтожает любые микроорганизмы. Домашние озонаторы есть в продаже, но мы рекомендуем профессиональное озонирование в MANDO MEMORI — оно безопаснее и эффективнее.</p>
