<section id="why">
  <h2>Почему важно менять набойки вовремя</h2>
  <p>Мужская обувь имеет свои конструктивные особенности: каблук часто шире и массивнее, а нагрузка на него выше. Вот почему нельзя затягивать с заменой набоек:</p>
  <ul>
    <li><strong>Сохранение геометрии каблука.</strong> Изношенная набойка деформирует каблук, и следующая набойка будет держаться хуже.</li>
    <li><strong>Защита настила.</strong> Металлические крепления набойки царапают пол — это неудобно дома и в офисе.</li>
    <li><strong>Безопасность.</strong> Стёртая резина не обеспечивает сцепление с мокрым асфальтом и плиткой.</li>
    <li><strong>Комфорт.</strong> Качественная набойка амортизирует удар при ходьбе, снижая нагрузку на колени и позвоночник.</li>
    <li><strong>Эстетика.</strong> Обшарпанный каблук выдаёт небрежность владельца, даже если верх обуви в идеальном состоянии.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>На заметку</h4>
    <p>В среднем мужские туфли требуют замены набоек каждые 4–6 месяцев при ежедневной носке. Для ботинок и зимней обуви интервал меньше — 3–4 месяца из-за реагентов и абразивов.</p>
  </div>
</section>

<section id="types">
  <h2>Виды набоек для мужской обуви</h2>
  <p>Набойки различаются по материалу, форме и способу крепления. Правильный выбор зависит от типа обуви и условий носки:</p>
  <ul>
    <li><strong>Резиновые набойки.</strong> Классический выбор для мужской обуви. Хорошо амортизируют, не скользят, бесшумные. Идеальны для осени и весны.</li>
    <li><strong>Полиуретановые набойки.</strong> Лёгкие и упругие, отлично подходят для летней обуви. Минус — быстрее стираются на абразивных поверхностях.</li>
    <li><strong>Кожаные набойки.</strong> Встречаются на дорогой итальянской обуви. Требуют особого ухода и частой замены, но выглядят аристократично.</li>
    <li><strong>Комбинированные набойки.</strong> Резина с металлическим сердечником — оптимальный вариант для зимней обуви и активной носки. Ходят до года.</li>
  </ul>
  <div data-type="box" data-color="blue">
    <h4>Рекомендация мастера</h4>
    <p>Для классических мужских туфель мы рекомендуем резиновые набойки средней жёсткости. Для ботинок и полуботинок — комбинированные с металлическим сердечником. Они не стираются до нуля и не требуют столь частой замены.</p>
  </div>
</section>

<section id="stepbystep">
  <h2>Пошаговый процесс замены набоек</h2>
  <p>Профессиональная замена набойки в мастерской занимает 15–30 минут. Вот как проходит процесс:</p>
  <ol>
    <li><strong>Осмотр и диагностика.</strong> Мастер оценивает степень износа текущих набоек, состояние каблука и подбирает подходящий материал.</li>
    <li><strong>Удаление старых набоек.</strong> Специальным инструментом срезаются остатки старой набойки. Удаляются все крепёжные элементы.</li>
    <li><strong>Зачистка каблука.</strong> Место крепления зачищается от клея, грязи и пыли. Поверхность должна быть идеально ровной.</li>
    <li><strong>Примерка новой набойки.</strong> Новая набойка подгоняется по форме каблука. При необходимости обрезается лишний материал.</li>
    <li><strong>Фиксация.</strong> Набойка крепится на специальный обувной клей и/или механические крепления. Время фиксации — 2–3 минуты под прессом.</li>
    <li><strong>Финишная обработка.</strong> Края набойки шлифуются, каблук полируется. Набойка подгоняется заподлицо с каблуком.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Совет</h4>
    <p>После замены набоек не надевайте обувь сразу — дайте клею полностью схватиться в течение 2–3 часов. Влажная погода в первые сутки может ослабить сцепление.</p>
  </div>
</section>

