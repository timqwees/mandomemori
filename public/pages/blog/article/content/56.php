<section id="beyond">
  <h2>Что лучше не делать дома</h2>
  <p>Границы домашней реставрации существуют. Вот что лучше доверить профессионалам:</p>
  <ul>
    <li><strong>Замена молнии на сапогах.</strong> Требует обувной машины и специальных навыков. Самостоятельная замена обычно выглядит неаккуратно.</li>
    <li><strong>Замена набоек на шпильках.</strong> Без специального инструмента набойка будет сидеть ненадёжно.</li>
    <li><strong>Ремонт трещин в межподошве.</strong> Как мы уже говорили — не лечится дома.</li>
    <li><strong>Восстановление экзотической кожи.</strong> Кожа питона, крокодила, ската требует профессиональных знаний.</li>
    <li><strong>Полная замена подошвы на литых кроссовках.</strong> Чаще всего невозможна даже профессионально.</li>
    <li><strong>Устранение глубокого запаха плесени.</strong> Требует профессиональной обработки и озонирования.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Золотое правило</h4>
    <p>Если стоимость профессионального ремонта меньше цены новой пары обуви в 3–4 раза и вы не уверены в своих силах — доверьтесь мастеру. Экономия 1000–2000 ₽ не стоит риска испортить обувь.</p>
  </div>
</section>

