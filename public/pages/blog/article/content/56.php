<section id="tools">
  <h2>Инструменты и материалы для домашней реставрации</h2>
  <p>Чтобы приступить к реставрации, понадобится минимальный набор инструментов и материалов. Его стоимость — 2000–4000 ₽, что сопоставимо с одним визитом в мастерскую.</p>
  <h3>Базовый набор для реставрации</h3>
  <ul>
    <li><strong>Щётки:</strong> резиновая для замши, мягкая для кожи, жёсткая для подошвы.</li>
    <li><strong>Средства для чистки:</strong> пена для замши, мыльный раствор, спирт для обезжиривания.</li>
    <li><strong>Полиуретановый клей:</strong> Desmokol, SAR 306 или TOPEX — для склеивания подошвы.</li>
    <li><strong>Краски:</strong> аэрозольные краски Tarrago или Saphir для кожи/замши, крем-краситель для тонирования.</li>
    <li><strong>Фиксаторы и пропитки:</strong> водоотталкивающая пропитка, фиксатор цвета.</li>
    <li><strong>Расходники:</strong> малярный скотч, ватные диски, перчатки, наждачная бумага (P400-P1500).</li>
    <li><strong>Инструменты:</strong> фен строительный (для активации клея), ластик для замши, колодки.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Экономия</h4>
    <p>Инвестиция в набор для реставрации окупается за 2–3 применения. Одна покраска в мастерской стоит 2000–4000 ₽, а баночка профессиональной краски (500–1000 ₽) хватит на 4–6 пар обуви.</p>
  </div>
</section>

<section id="painting">
  <h2>Покраска и восстановление цвета</h2>
  <p>Самый популярный вид домашней реставрации. Подходит для освежения цвета или полной перекраски обуви.</p>
  <h3>Что можно покрасить дома</h3>
  <ul>
    <li>Гладкую кожу — аэрозолем или кремом-красителем.</li>
    <li>Замшу и нубук — аэрозолем для замши.</li>
    <li>Текстильные кроссовки — акриловой краской для ткани.</li>
    <li>Подошву — специальной краской для резины.</li>
  </ul>
  <h3>Основные правила</h3>
  <ol>
    <li>Тщательная очистка и обезжиривание перед покраской.</li>
    <li>Защита неокрашиваемых участков малярным скотчем.</li>
    <li>Нанесение краски тонкими слоями (2–3 слоя).</li>
    <li>Каждый слой — полное высыхание (15–30 минут).</li>
    <li>Фиксация цвета закрепителем.</li>
    <li>24 часа без носки после финального слоя.</li>
  </ol>
  <p>Если вы красите первый раз — потренируйтесь на старой ненужной обуви. Набив руку, приступайте к основной паре.</p>
  <div data-type="box" data-color="green">
    <h4>Совет новичку</h4>
    <p>Начните с крема-красителя для кожи. Он прощается с ошибками — если цвет лёг неравномерно, можно стереть влажной тканью и нанести заново. С аэрозолем такой возможности нет.</p>
  </div>
</section>

<section id="scratch">
  <h2>Удаление царапин и потёртостей</h2>
  <p>Царапины на кожаной обуви — неизбежность. К счастью, большинство из них можно убрать в домашних условиях.</p>
  <h3>Методы удаления царапин</h3>
  <ul>
    <li><strong>Крем-краситель в тон.</strong> Заполняет мелкие царапины и маскирует их. Нанесите крем, втирая в царапину, затем отполируйте.</li>
    <li><strong>Восковой карандаш.</strong> Заполняет глубокие царапины. Воск разогревается (можно пальцами), втирается в царапину, излишки снимаются пластиковой картой.</li>
    <li><strong>Жидкая кожа.</strong> Для глубоких повреждений. Наносится шпателем, после высыхания шлифуется мелкой наждачкой и тонируется.</li>
    <li><strong>Оливковое масло (временное решение).</strong> Нанесите каплю на царапину на кожаной обуви — потёртость станет менее заметной. Действует 1–2 дня.</li>
  </ul>
  <div data-type="box" data-color="dark">
    <h4>Для замши</h4>
    <p>Царапины на замше не замазывают — их маскируют поднятием ворса. Пройдитесь по царапине ластиком для замши или обработайте паром, затем поднимите ворс щёткой. Царапина станет практически незаметной.</p>
  </div>
</section>

<section id="gluing">
  <h2>Склеивание и ремонт подошвы</h2>
  <p>Отклеившаяся подошва — одна из самых частых проблем, с которой можно справиться дома.</p>
  <h3>Необходимые материалы</h3>
  <ul>
    <li>Полиуретановый клей (Desmokol, SAR 306).</li>
    <li>Наждачная бумага (P80-P120).</li>
    <li>Ацетон или уайт-спирит для обезжиривания.</li>
    <li>Строительный фен.</li>
  </ul>
  <h3>Пошаговая инструкция</h3>
  <ol>
    <li>Удалите остатки старого клея с подошвы и верха обуви.</li>
    <li>Зашершавьте обе поверхности наждачной бумагой.</li>
    <li>Обезжирьте ацетоном или уайт-спиритом.</li>
    <li>Нанесите клей тонким слоем на обе поверхности. Подождите 10–15 минут (до отлипа).</li>
    <li>Прогрейте феном (80–100 °C) в течение 30–60 секунд, пока клей не станет прозрачным.</li>
    <li>Соедините детали и плотно прижмите.</li>
    <li>Оставьте под нагрузкой на 12–24 часа.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Когда не стоит клеить самим</h4>
    <p>Если подошва отклеилась по всей площади вместе с межподошвой (пенным слоем) — требуется профессиональный ремонт. Домашнее склеивание не обеспечит достаточной прочности, и подошва отвалится снова через 1–2 недели.</p>
  </div>
</section>

<section id="hardware">
  <h2>Замена фурнитуры и шнурков</h2>
  <p>Самый простой вид реставрации, доступный каждому. Замена фурнитуры может кардинально обновить внешний вид обуви.</p>
  <ul>
    <li><strong>Шнурки.</strong> Самый дешёвый способ обновить кроссовки. Замените стандартные шнурки на вощёные, круглые или неоновые — и обувь заиграет по-новому.</li>
    <li><strong>Люверсы (дырочки для шнурков).</strong> Если они сломались или поржавели, можно заменить с помощью щипцов для люверсов. Набор люверсов и щипцы продаются в обувных магазинах.</li>
    <li><strong>Крючки для шнурков.</strong> На ботинках и берцах крючки часто выходят из строя. Замена требует плоскогубцев и набора новых крючков.</li>
    <li><strong>Декоративные элементы.</strong> Пряжки, цепочки, шипы — можно заменить или добавить новые для кастомизации.</li>
  </ul>
  <p>Замена фурнитуры — отличный способ персонализировать обувь и сделать её уникальной. Кастомизация шнурками или люверсами занимает 10–15 минут и стоит 200–500 ₽.</p>
