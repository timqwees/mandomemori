<section id="why">
  <h2>Почему обувь теряет форму</h2>
  <p>Понимание причин деформации поможет выбрать правильный метод восстановления:</p>
  <ul>
    <li><strong>Естественное растяжение.</strong> Кожа и замша имеют свойство растягиваться в местах наибольшего давления — в области мизинца и пятки. Это нормально для любой натуральной обуви.</li>
    <li><strong>Неправильное хранение.</strong> Хранение обуви без распорок, скомканной в шкафу — главная причина потери формы. Особенно страдают сапоги и ботинки.</li>
    <li><strong>Влажность и неправильная сушка.</strong> Мокрая обувь, высушенная на батарее, деформируется необратимо. Материалы сжимаются неравномерно.</li>
    <li><strong>Носка не по размеру.</strong> Если обувь мала или велика, она деформируется по-своему: малая — разнашивается вширь, большая — сминается в носке.</li>
    <li><strong>Износ каркаса.</strong> У кроссовок и обуви на литой подошве каркас со временем теряет жёсткость, и обувь «садится».</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Важно</h4>
    <p>Если обувь потеряла форму из-за износа каркаса (у кроссовок после 800+ км пробега, у туфель после 5+ лет носки), восстановление формы будет временным. Структура уже разрушена.</p>
  </div>
</section>

<section id="lasts">
  <h2>Использование колодок и распорок</h2>
  <p>Самый эффективный способ сохранить и восстановить форму — использовать обувные колодки. Они бывают нескольких типов:</p>
  <ul>
    <li><strong>Деревянные колодки (кедр).</strong> Лучший выбор. Кедр не только держит форму, но и впитывает влагу, устраняет запахи. Подходят для хранения и восстановления формы.</li>
    <li><strong>Пластиковые колодки с регулировкой.</strong> Имеют механизм расширения — можно увеличивать размер для растяжки. Удобны для восстановления формы после влаги.</li>
    <li><strong>Пневматические колодки.</strong> Надувные вставки, которые заполняют внутреннее пространство обуви. Подходят для сложных форм и кроссовок.</li>
    <li><strong>Распорки для сапог.</strong> Специальные вставки, которые держат голенище в расправленном состоянии и предотвращают заломы.</li>
  </ul>
  <h3>Как использовать колодки для восстановления формы</h3>
  <ol>
    <li>Плотно набейте обувь бумагой или вставьте колодку соответствующего размера.</li>
    <li>Слегка увлажните проблемные участки из пульверизатора.</li>
    <li>Оставьте на 24–48 часов до полного высыхания.</li>
    <li>Для сложных случаев повторите процедуру 2–3 раза.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Лайфхак</h4>
    <p>Используйте влажные газеты вместо колодок: скомкайте газеты, слегка смочите и плотно заполните обувь. По мере высыхания газета расширяется, расправляя обувь изнутри. Метод проверен десятилетиями.</p>
  </div>
</section>

<section id="steam">
  <h2>Восстановление формы паром</h2>
  <p>Пар — мощный инструмент для восстановления формы кожаной обуви. Под воздействием тепла и влаги кожа становится пластичной и легко принимает нужную форму.</p>
  <h3>Пошаговая инструкция</h3>
  <ol>
    <li>Вскипятите чайник или используйте отпариватель для одежды.</li>
    <li>Подержите обувь над паром на расстоянии 15–20 см в течение 30–60 секунд. Материал должен стать тёплым и влажным.</li>
    <li>Набейте обувь бумагой или вставьте колодку. Сформируйте правильную форму руками.</li>
    <li>Оставьте сушиться при комнатной температуре на 12–24 часа.</li>
    <li>После высыхания обработайте кремом для восстановления жирового баланса кожи.</li>
  </ol>
  <div data-type="box" data-color="red">
    <h4>Предупреждение</h4>
    <p>Метод с паром НЕ подходит для: лаковой кожи (лак треснет), замши и нубука (потеряют ворс), мембранных материалов (повредится мембрана). Только для гладкой натуральной кожи.</p>
  </div>
  <p>Пар помогает восстановить заломы на голенищах сапог, расправить смятые задники и вернуть форму носкам туфель.</p>
</section>

<section id="freeze">
  <h2>Метод заморозки для восстановления формы</h2>
  <p>Необычный, но эффективный метод, основанный на расширении воды при замерзании. Вода, превращаясь в лёд, расширяется на 9%, аккуратно расправляя материал изнутри.</p>
  <h3>Как это работает</h3>
  <ol>
    <li>Наполните плотный полиэтиленовый пакет водой (около 200–300 мл, в зависимости от размера обуви).</li>
    <li>Убедитесь, что пакет герметично закрыт — вода не должна просочиться.</li>
    <li>Вставьте пакет с водой в обувь, равномерно распределив по проблемной зоне.</li>
    <li>Поместите обувь в морозильную камеру на 6–8 часов (на ночь).</li>
    <li>Достаньте обувь, дайте льду растаять 15–20 минут, извлеките пакет.</li>
    <li>Обувь примет форму, заданную расширяющимся льдом.</li>
  </ol>
  <div data-type="box" data-color="dark">
    <h4>Для каких материалов подходит</h4>
    <p>Метод работает с натуральной кожей, экокожей и плотным текстилем. Не подходит для замши, нубука и обуви с мембраной. Также не стоит замораживать обувь с гелевыми вставками или сложной амортизацией.</p>
  </div>
  <p>Метод заморозки особенно эффективен, если нужно: растянуть обувь вширь, увеличить подъём, расправить смятый носок. После заморозки обувь сохраняет новую форму 2–4 недели. Для закрепления повторите 2–3 раза.</p>
</section>

<section id="backpart">
  <h2>Восстановление задника обуви</h2>
  <p>Смятый или разбитый задник — одна из самых частых проблем, особенно у кроссовок и мокасин. Задник перестаёт фиксировать пятку, и обувь начинает «хлябать».</p>
  <h3>Методы восстановления задника</h3>
  <ul>
    <li><strong>Установка жёсткого задника.</strong> Мастер вклеивает внутрь обуви дополнительный задник из термопластичного материала (ТПУ), который восстанавливает жёсткость. Профессиональный метод — результат держится годами.</li>
    <li><strong>Использование силиконовых накладок.</strong> Для кроссовок — силиконовые пяточные накладки, которые фиксируют пятку и компенсируют деформацию задника.</li>
    <li><strong>Утолщение материала.</strong> Внутрь задника вклеивается слой кожи или замши, который восстанавливает объём и фиксацию пятки.</li>
