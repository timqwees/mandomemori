<section id="prevention">
  <h2>Профилактика потери формы</h2>
  <p>Лучшее лечение — профилактика. Вот как сохранить форму обуви надолго:</p>
  <ul>
    <li><strong>Храните на колодках.</strong> Деревянные колодки — лучшая инвестиция в сохранность обуви. Особенно для дорогих туфель и сапог.</li>
    <li><strong>Не снимайте обувь, наступая на задник.</strong> Самая частая причина деформации. Используйте рожок для обуви.</li>
    <li><strong>Чередуйте обувь.</strong> Каждой паре нужен отдых 24 часа для восстановления формы.</li>
    <li><strong>Сушите правильно.</strong> Комнатная температура, газеты или колодки, никаких батарей и фенов.</li>
    <li><strong>Используйте влагозащитные пропитки.</strong> Вода размягчает материалы и ускоряет деформацию. Пропитка защищает и от влаги, и от потери формы.</li>
  </ul>
  <p>Соблюдая эти простые правила, вы продлите идеальную форму обуви в 2–3 раза. Дорогие туфли могут выглядеть как новые 5–7 лет при правильном уходе.</p>
  <div data-type="box" data-color="green">
    <h4>Совет по хранению</h4>
    <p>Для сапог используйте специальные распорки для голенища — они предотвращают заломы и складки. Храните сапоги вертикально, а не в сложенном виде. Простые картонные втулки от рулонов тоже работают как бюджетная альтернатива.</p>
  </div>
