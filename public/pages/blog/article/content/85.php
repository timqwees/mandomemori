<section id="inspect">
  <h2>Осмотр после хранения</h2>
  <p>Достаньте кроссовки из мест хранения и внимательно осмотрите. За зиму даже с подготовленной обувью могли произойти изменения:</p>
  <ul>
    <li><strong>Клей.</strong> Проверьте, не отошла ли подошва по краям. Старый клей мог высохнуть.</li>
    <li><strong>Материал верха.</strong> Ищите трещины, заломы и следы плесени. Особое внимание — местам сгиба.</li>
    <li><strong>Внутренняя поверхность.</strong> Проверьте стельки и задник — там может завестись грибок.</li>
    <li><strong>Запах.</strong> Неприятный запах говорит о том, что перед хранением обувь была недостаточно высушена.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Если обувь «не проснулась»</h4>
    <p>После зимнего хранения кожа может быть жёсткой и сухой. Нанесите питательный крем и оставьте на ночь. Для замши используйте кондиционер-восстановитель.</p>
  </div>
</section>

<section id="clean">
  <h2>Весенняя чистка</h2>
  <p>Даже если кроссовки выглядят чистыми, после хранения на них есть пыль и микроорганизмы. Проведите генеральную чистку:</p>
  <ol>
    <li>Вытряхните пыль и песок из обуви.</li>
    <li>Пройдитесь сухой щёткой по всей поверхности.</li>
    <li>Для кроссовок из кожи — пена для чистки + влажная салфетка.</li>
    <li>Для замши — резиновая щётка, затем паровая обработка для поднятия ворса.</li>
    <li>Для текстиля — аккуратная чистка пеной или стирка вручную (не в машинке!).</li>
    <li>Подошву вымойте с мылом и щёткой — в протекторе могла застрять грязь.</li>
  </ol>
</section>

<section id="waterproof">
  <h2>Водоотталкивающая обработка</h2>
  <p>Весенняя погода полна сюрпризов. Без качественной пропитки кроссовки быстро промокнут и потеряют вид. Весенняя защита имеет особенности:</p>
  <ul>
    <li><strong>Выбирайте спрей, а не воск.</strong> Воски и кремы хороши для зимы, а для весенней влажной погоды лучше подходят спреи — они не блокируют поры материалов.</li>
    <li><strong>Наносите в 2–3 слоя.</strong> Каждый слой сохнет 15–20 минут. Двухслойное покрытие держится 3–4 недели.</li>
    <li><strong>Не забудьте про швы.</strong> Самое уязвимое место — швы между подошвой и верхом. Обработайте их особенно тщательно.</li>
    <li><strong>Обновляйте защиту раз в месяц.</strong> Весной осадки частые, поэтому стандартная периодичность — каждые 3–4 недели.</li>
  </ul>
  <div data-type="box" data-color="green">
    <h4>Совет профессионала</h4>
    <p>Весной носите кроссовки из более практичных материалов. Кожа с гладкой поверхностью легче отмывается от грязи и дольше держит пропитку, чем текстиль или замша.</p>
  </div>
</section>

<section id="color">
  <h2>Обновление цвета</h2>
  <p>После зимы и хранения кроссовки могли потерять яркость цвета. Весна — время обновить цвет:</p>
  <ul>
    <li><strong>Белые кроссовки.</strong> Используйте отбеливающую пасту для подошвы и белый крем для верха.</li>
    <li><strong>Тёмные кроссовки.</strong> Освежите цвет кремом-красителем соответствующего оттенка.</li>
    <li><strong>Цветная замша.</strong> Спреи-красители для замши восстановят яркость и защитят от влаги.</li>
    <li><strong>Потёртости.</strong> Замаскируйте мелкие потёртости специальным маркером для обуви.</li>
  </ul>
  <p>После нанесения краски обязательно закрепите результат водоотталкивающим спреем.</p>
</section>

