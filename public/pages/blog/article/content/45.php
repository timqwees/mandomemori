<section id="types">
  <h2>Типы трещин на кроссовках</h2>
  <p>Не все трещины одинаковы. От типа трещины зависит возможность и метод ремонта:</p>
  <ul>
    <li><strong>Поверхностные трещины на резиновой подошве.</strong> Затрагивают только протекторный слой. Самый благоприятный случай — ремонт возможен и эффективен.</li>
    <li><strong>Глубокие трещины подошвы на сгибе.</strong> Проходят через всю толщину резины. Могут пропускать воду. Требуют серьёзного ремонта.</li>
    <li><strong>Трещина межподошвы (мидсоль).</strong> Разрыв пенного материала между подошвой и верхом. Критический дефект — амортизация нарушена.</li>
    <li><strong>Трещина на коже или замше.</strong> Появляется из-за пересыхания материала. Можно восстановить жидкой кожей или воском.</li>
    <li><strong>Разрыв текстильной сетки.</strong> Часто в области сгиба стопы. Ремонтируется заплаткой с внутренней стороны.</li>
  </ul>
  <div data-type="box" data-color="gray">
    <h4>Как оценить глубину</h4>
    <p>Залейте в трещину воду. Если вода просачивается внутрь кроссовка — трещина сквозная, нужен срочный ремонт. Если вода остаётся на поверхности — трещина косметическая, можно не спешить.</p>
  </div>
</section>

<section id="solecrack">
  <h2>Трещины на подошве</h2>
  <p>Подошва кроссовок изнашивается под воздействием циклов сжатия-растяжения при беге и ходьбе. Со временем резина теряет эластичность и трескается.</p>
  <h3>Причины трещин на подошве</h3>
  <ul>
    <li>Естественное старение резины (окисление).</li>
    <li>Воздействие реагентов и уличной химии.</li>
    <li>Перепады температур (мороз-жара).</li>
    <li>Неправильное хранение (на солнце, у батареи).</li>
    <li>Критический износ — резина истончилась до 1–2 мм.</li>
  </ul>
  <h3>Методы ремонта</h3>
  <p>Для трещин на подошве используется два основных подхода:</p>
  <ol>
    <li><strong>Заливка полиуретановым герметиком.</strong> Трещина расшивается, зачищается, обезжиривается и заполняется специальным составом. После застывания излишки срезаются. Метод эффективен для трещин глубиной до 3–4 мм.</li>
    <li><strong>Заплатка.</strong> С внутренней стороны подошвы накладывается армирующая заплатка из прочной резины. Снаружи трещина герметизируется. Подходит для сквозных трещин.</li>
  </ol>
  <div data-type="box" data-color="green">
    <h4>Когда ремонт работает</h4>
    <p>Если трещина одиночная и локализована на участке без сильной нагрузки — ремонт надёжный. Если трещина проходит по всей ширине подошвы на сгибе — даже после ремонта она может открыться снова.</p>
  </div>
</section>

<section id="midsole">
  <h2>Трещины в межподошве — самый сложный случай</h2>
  <p>Межподошва (мидсоль) — это слой пены EVA, полиуретана или Boost, который отвечает за амортизацию. Когда трескается он — проблемы серьёзные.</p>
  <p>Почему трескается мидсоль:</p>
  <ul>
    <li>Пена теряет эластичность после 500–800 км пробега.</li>
    <li>Материал разрушается изнутри из-за циклов сжатия.</li>
    <li>Воздействие влаги и ультрафиолета ускоряет деградацию.</li>
  </ul>
  <p>К сожалению, трещины в межподошве не подлежат качественному ремонту. Заполнение трещины клеем или герметиком даёт временный эффект — при нагрузке пена продолжает разрушаться. Амортизация не восстанавливается.</p>
  <div data-type="box" data-color="red">
    <h4>Вывод по мидсолю</h4>
    <p>Если треснула межподошва — кроссовки пора менять. Дальнейшая носка некомфортна и опасна для суставов. Используйте их как повседневные, но для бега и спорта они уже непригодны.</p>
  </div>
</section>

<section id="upper">
  <h2>Трещины на материале верха</h2>
  <p>Верх кроссовок трескается реже, но такие случаи бывают. Особенно страдают кожаные и замшевые элементы.</p>
  <h3>Трещины на коже</h3>
  <p>Кожа трескается из-за пересыхания и недостатка ухода. Мелкие трещины восстанавливаются жидкой кожей или восковым наполнителем. Глубокие — требуют более серьёзного подхода.</p>
  <p>Методы восстановления:</p>
  <ul>
    <li><strong>Жидкая кожа.</strong> Состав наносится на трещину, заполняет её и после высыхания образует эластичную плёнку. Требуется подбор цвета.</li>
    <li><strong>Восковой наполнитель.</strong> Разогретый воск втирается в трещину, после застывания шлифуется. Подходит для мелких дефектов.</li>
    <li><strong>Заплатка.</strong> Если кожа разорвана, ставится заплатка с внутренней стороны на клей.</li>
  </ul>
  <h3>Трещины на замше</h3>
  <p>Замша трескается при пересыхании. Восстановление — только профессиональная пропитка и окрашивание. Полностью скрыть глубокую трещину на замше невозможно.</p>
  <div data-type="box" data-color="blue">
    <h4>Профилактика трещин верха</h4>
    <p>Регулярно увлажняйте и питайте кожу кремами. Замшу обрабатывайте водоотталкивающими спреями. Не сушите обувь у источников тепла. В MANDO MEMORI мы проводим полный уход за обувью, который предотвращает появление трещин.</p>
  </div>
</section>

