<section id="cantfix">
  <h2>Когда ремонт трещин бессмыслен</h2>
  <p>Бывают случаи, когда ремонт трещины — пустая трата денег. Вот признаки:</p>
  <ul>
    <li><strong>Множественные трещины.</strong> Если трещин больше 3–4 на одной подошве — материал разрушен по всей структуре.</li>
    <li><strong>Трещина в пенной межподошве.</strong> Как мы уже говорили — пена не восстанавливается.</li>
    <li><strong>Кроссовкам больше 3–4 лет.</strong> Независимо от пробега, материалы стареют. Через 4 года резина и пена теряют эластичность.</li>
    <li><strong>Пробег более 800 км.</strong> Даже если трещина заделана, амортизация уже не работает.</li>
    <li><strong>Трещина на карбоновой пластине.</strong> Карбон не склеивается без потери свойств.</li>
  </ul>
  <div data-type="box" data-color="red">
    <h4>Честный совет</h4>
    <p>Сравните стоимость ремонта с остаточной стоимостью кроссовок. Если ремонт стоит 2000–3000 ₽, а кроссовкам уже 3 года — лучше добавить до 5000–7000 ₽ и купить новую пару. В MANDO MEMORI мы всегда честно говорим, стоит ли ремонтировать ваши кроссовки.</p>
